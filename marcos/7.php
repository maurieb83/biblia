<h3>EVANGELIO SEGUN SAN MARCOS</h3>
<p>Capítulo 7
</p><p>
1 Los fariseos con algunos escribas llegados de Jerusalén se acercaron a Jesús,
</p><p>
2 y vieron que algunos de sus discípulos comían con las manos impuras, es decir, sin lavar.
</p><p>
3 Los fariseos, en efecto, y los judíos en general, no comen sin lavarse antes cuidadosamente las manos, siguiendo la tradición de sus antepasados;
</p><p>
4 y al volver del mercado, no comen sin hacer primero las abluciones. Además, hay muchas otras prácticas, a las que están aferrados por tradición, como el lavado de los vasos, de las jarras y de la vajilla de bronce.
</p><p>
5 Entonces los fariseos y los escribas preguntaron a Jesús: «¿Por qué tus discípulos no proceden de acuerdo con la tradición de nuestros antepasados, sino que comen con las manos impuras?».
</p><p>
6 El les respondió: «¡Hipócritas! Bien profetizó de ustedes Isaías, en el pasaje de la Escritura que dice: Este pueblo me honra con los labios, pero su corazón está lejos de mí.
</p><p>
7 En vano me rinde culto: las doctrinas que enseñan no son sino preceptos humanos.
</p><p>
8 Ustedes dejan de lado el mandamiento de Dios, por seguir la tradición de los hombres».
</p><p>
9 Y les decía: «Por mantenerse fieles a su tradición, ustedes descartan tranquilamente el mandamiento de Dios.
</p><p>
10 Porque Moisés dijo: Honra a tu padre y a tu madre, y además: El que maldice a su padre y a su madre será condenado a muerte.
</p><p>
11 En cambio, ustedes afirman: «Si alguien dice a su padre o a su madre: Declaro "corbán" –es decir, ofrenda sagrada– todo aquello con lo que podría ayudarte...»
</p><p>
12 En ese caso, le permiten no hacer más nada por su padre o por su madre.
</p><p>
13 Así anulan la palabra de Dios por la tradición que ustedes mismos se han transmitido. ¡Y como estas, hacen muchas otras cosas!».
</p><p>
14 Y Jesús, llamando otra vez a la gente, les dijo: «Escúchenme todos y entiéndanlo bien.
</p><p>
15 Ninguna cosa externa que entra en el hombre puede mancharlo; lo que lo hace impuro es aquello que sale del hombre.
</p><p>
16 ¡Si alguien tiene oídos para oír, que oiga!».
</p><p>
17 Cuando se apartó de la multitud y entró en la casa, sus discípulos le preguntaron por el sentido de esa parábola.
</p><p>
18 El les dijo: «¿Ni siquiera ustedes son capaces de comprender? ¿No saben que nada de lo que entra de afuera en el hombre puede mancharlo,
</p><p>
19 porque eso no va al corazón sino al vientre, y después se elimina en lugares retirados?». Así Jesús declaraba que eran puros todos los alimentos.
</p><p>
20 Luego agregó: «Lo que sale del hombre es lo que lo hace impuro.
</p><p>
21 Porque es del interior, del corazón de los hombres, de donde provienen las malas intenciones, las fornicaciones, los robos, los homicidios,
</p><p>
22 los adulterios, la avaricia, la maldad, los engaños, las deshonestidades, la envidia, la difamación, el orgullo, el desatino.
</p><p>
23 Todas estas cosas malas proceden del interior y son las que manchan al hombre».
</p><p>
24 Después Jesús partió de allí y fue a la región de Tiro. Entró en una casa y no quiso que nadie lo supiera, pero no pudo permanecer oculto.
</p><p>
25 En seguida una mujer cuya hija estaba poseída por un espíritu impuro, oyó hablar de él y fue a postrarse a sus pies.
</p><p>
26 Esta mujer, que era pagana y de origen sirofenicio, le pidió que expulsara de su hija al demonio.
</p><p>
27 El le respondió: «Deja que antes se sacien los hijos; no está bien tomar el pan de los hijos para tirárselo a los cachorros».
</p><p>
28 Pero ella le respondió: «Es verdad, Señor, pero los cachorros, debajo de la mesa, comen las migajas que dejan caer los hijos».
</p><p>
29 Entonces él le dijo: «A causa de lo que has dicho, puedes irte: el demonio ha salido de tu hija».
</p><p>
30 Ella regresó a su casa y encontró a la niña acostada en la cama y liberada del demonio.
</p><p>
31 Cuando Jesús volvía de al región de Tiro, pasó por Sidón y fue hacia el mar de Galilea, atravesando el territorio de la Decápolis.
</p><p>
32 Entonces le presentaron a un sordomudo y le pidieron que le impusiera las manos.
</p><p>
33 Jesús lo separó de la multitud y, llevándolo aparte, le puso los dedos en las orejas y con su saliva le tocó la lengua.
</p><p>
34 Después, levantando los ojos al cielo, suspiró y dijo: «Efatá», que significa: «Abrete».
</p><p>
35 Y enseguida se abrieron sus oídos, se le soltó la lengua y comenzó a hablar normalmente.
</p><p>
36 Jesús les mandó insistentemente que no dijeran nada a nadie, pero cuanto más insistía, ellos más lo proclamaban
</p><p>
37 y, en el colmo de la admiración, decían: «Todo lo ha hecho bien: hace oír a los sordos y hablar a los mudos».
</p>
