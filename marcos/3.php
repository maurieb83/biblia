<h3>EVANGELIO SEGUN SAN MARCOS</h3>
<p>Capítulo 3
</p><p>
1 Jesús entró nuevamente en una sinagoga, y había allí un hombre que tenía una mano paralizada.
</p><p>
2 Los fariseos observaban atentamente a Jesús para ver si lo curaba en sábado, con el fin de acusarlo.
</p><p>
3 Jesús dijo al hombre de la mano paralizada: «Ven y colócate aquí delante».
</p><p>
4 Y les dijo: «¿Está permitido en sábado hacer el bien o el mal, salvar una vida o perderla?». Pero ellos callaron.
</p><p>
5 Entonces, dirigiendo sobre ellos una mirada llena de indignación y apenado por la dureza de sus corazones, dijo al hombre: «Extiende tu mano». El la extendió y su mano quedó curada.
</p><p>
6 Los fariseos salieron y se confabularon con los herodianos para buscar la forma de acabar con él.
</p><p>
7 Jesús se retiró con sus discípulos a la orilla del mar, y lo siguió mucha gente de Galilea.
</p><p>
8 Al enterarse de lo que hacía, también fue a su encuentro una gran multitud de Judea, de Jerusalén, de Idumea, de la Transjordania y de la región de Tiro y Sidón.
</p><p>
9 Entonces mandó a sus discípulos que le prepararan una barca, para que la muchedumbre no lo apretujara.
</p><p>
10 Porque, como curaba a muchos, todos los que padecían algún mal se arrojaban sobre él para tocarlo.
</p><p>
11 Y los espíritus impuros, apenas lo veían, se tiraban a sus pies, gritando: «¡Tú eres el Hijo de Dios!».
</p><p>
12 Pero Jesús les ordenaba terminantemente que no lo pusieran de manifiesto.
</p><p>
13 Después subió a la montaña y llamó a su lado a los que quiso. Ellos fueron hacia él,
</p><p>
14 y Jesús instituyó a doce para que estuvieran con él, y para enviarlos a predicar
</p><p>
15 con el poder de expulsar a los demonios.
</p><p>
16 Así instituyó a los Doce: Simón, al que puso el sobrenombre de Pedro;
</p><p>
17 Santiago, hijo de Zebedeo, y Juan, hermano de Santiago, a los que dio el nombre de Boanerges, es decir, hijos del trueno;
</p><p>
18 luego, Andrés, Felipe, Bartolomé, Mateo, Tomás, Santiago, hijo de Alfeo, Tadeo, Simón, el Cananeo,
</p><p>
19 y Judas Iscariote, el mismo que lo entregó.
</p><p>
20 Jesús regresó a la casa, y de nuevo se juntó tanta gente que ni siquiera podían comer.
</p><p>
21 Cuando sus parientes se enteraron, salieron para llevárselo, porque decían: «Es un exaltado».
</p><p>
22 Los escribas que habían venido de Jerusalén decían: «Está poseído por Belzebul y expulsa a los demonios por el poder del Príncipe de los Demonios».
</p><p>
23 Jesús los llamó y por medio de comparaciones les explicó: «¿Cómo Satanás va a expulsar a Satanás?
</p><p>
24 Un reino donde hay luchas internas no puede subsistir,
</p><p>
25 Y una familia dividida tampoco puede subsistir.
</p><p>
26 Por lo tanto, si Satanás se dividió, levantándose contra sí mismo, ya no puede subsistir, sino que ha llega a su fin.
</p><p>
27 Pero nadie puede entrar en la casa de un hombre fuerte y saquear sus bienes, si primero no lo ata. Sólo así podrá saquear la casa.
</p><p>
28 Les aseguro que todo será perdonado a los hombres: todos los pecados y cualquier blasfemia que profieran.
</p><p>
29 Pero el que blasfeme contra el Espíritu Santo, no tendrá perdón jamás: es culpable de pecado para siempre».
</p><p>
30 Jesús dijo esto porque ellos decían: «Está poseído por un espíritu impuro».
</p><p>
31 Entonces llegaron su madre y sus hermanos y, quedándose afuera, lo mandaron llamar.
</p><p>
32 La multitud estaba sentada alrededor de Jesús, y le dijeron: «Tu madre y tus hermanos te buscan ahí fuera».
</p><p>
33 El les respondió: «¿Quién es mi madre y quiénes son mis hermanos?».
</p><p>
34 Y dirigiendo su mirada sobre los que estaban sentados alrededor de él, dijo: «Estos son mi madre y mis hermanos.
</p><p>
35 Porque el que hace la voluntad de Dios, ese es mi hermano, mi hermana y mi madre».
</p>
