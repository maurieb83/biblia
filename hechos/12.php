<h3>HECHOS DE LOS APOSTOLES</h3>
<p>Capítulo 12
</p><p>
1 Por aquel entonces, el rey Herodes hizo arrestar a algunos miembros de la Iglesia para maltratarlos.
</p><p>
2 Mandó ejecutar a Santiago, hermano de Juan,
</p><p>
3 y al ver que esto agradaba a los judíos, también hizo arrestar a Pedro. Eran los días de «los panes Acimos».
</p><p>
4 Después de arrestarlo, lo hizo encarcelar, poniéndolo bajo la custodia de cuatro relevos de guardia, de cuatro soldados cada uno. Su intención era hacerlo comparecer ante el pueblo después de la Pascua.
</p><p>
5 Mientras Pedro estaba bajo custodia en la prisión, la Iglesia no cesaba de orar a Dios por él.
</p><p>
6 La noche anterior al día en que Herodes pensaba hacerlo comparecer, Pedro dormía entre los soldados, atado con dos cadenas, y los otros centinelas vigilaban la puerta de la prisión.
</p><p>
7 De pronto, apareció el Angel del Señor y una luz resplandeció en el calabozo. El Angel sacudió a Pedro y lo hizo levantar, diciéndole: «¡Levántate rápido!». Entonces las cadenas se le cayeron de las manos.
</p><p>
8 El Angel le dijo: «Tienes que ponerte el cinturón y las sandalias» y Pedro lo hizo. Después de dijo: «Cúbrete con el manto y sígueme».
</p><p>
9 Pedro salió y lo seguía; no se daba cuenta de que era cierto lo que estaba sucediendo por intervención del Angel, sino que creía tener una visión.
</p><p>
10 Pasaron así el primero y el segundo puesto de guardia, y llegaron a la puerta de hierro que daba a la ciudad. La puerta se abrió sola delante de ellos. Salieron y anduvieron hasta el extremo de una calle, y en seguida el Angel se alejó de él.
</p><p>
11 Pedro, volviendo en sí, dijo: «Ahora sé que realmente el Señor envió a su Angel y me libró de las manos de Herodes y de todo cuanto esperaba el pueblo judío».
</p><p>
12 Y al advertir lo que le había sucedido, se dirigió a la casa de María, la madre de Juan, llamado Marcos, donde un grupo numeroso se hallaba reunido en oración.
</p><p>
13 Cuando golpeó a la puerta de calle, acudió una sirvienta llamada Rosa;
</p><p>
14 esta, al reconocer su voz, se alegró tanto, que en lugar de abrir, entró corriendo a anunciar que Pedro estaba en la puerta.
</p><p>
15 «Estás loca», le respondieron. Pero ella insistía que era verdad. Ellos le dijeron: «Será su ángel».
</p><p>
16 Mientras tanto, Pedro seguía llamando. Cuando abrieron y vieron que era él, no salían de su asombro.
</p><p>
17 Pedro le hizo señas con la mano para que se callaran, y les relató cómo el Señor lo había sacado de la cárcel, añadiendo: «Hagan saber esto a Santiago y a los hermanos». Y saliendo de allí, se fue a otro lugar.
</p><p>
18 Cuando amaneció, se produjo un gran alboroto entre los soldados, porque no podían explicarse qué había pasado con Pedro.
</p><p>
19 Herodes lo hizo buscar, pero como no lo encontraron, después de haber interrogado a los guardias, dio orden de ejecutarlos. Luego descendió de Judea a Cesarea, y permaneció allí.
</p><p>
20 Herodes estaba en grave conflicto con los habitantes de Tiro y Sidón. Estos se pusieron de acuerdo para ir a verlo, y después de haberse conquistado a Blasto, el camarero del rey, solicitaron la reconciliación, ya que importaban sus víveres del territorio del rey.
</p><p>
21 El día fijado, Herodes se sentó en su trono con la vestidura real y les dirigió la palabra.
</p><p>
22 El pueblo comenzó a gritar: «¡Es un dios el que habla, no un hombre!».
</p><p>
23 Pero en ese mismo instante, el Angel del Señor lo hirió, por no haber dado gloria a Dios, y Herodes murió carcomido por los gusanos.
</p><p>
24 Mientras tanto, la Palabra de Dios se difundía incesantemente.
</p><p>
25 Bernabé y Saulo, una vez cumplida su misión, volvieron de Jerusalén a Antioquía, llevando consigo a Juan, llamado Marcos.
</p>
