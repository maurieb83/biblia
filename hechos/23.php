<h3>HECHOS DE LOS APOSTOLES</h3>
<p>Capítulo 23
</p><p>
1 Con los ojos fijos en el Sanedrín, Pablo dijo: «Hermanos, hasta hoy yo he obrado con rectitud de conciencia delante de Dios».
</p><p>
2 Pero el Sumo Sacerdote Ananías ordenó a sus asistentes que le pegaran en la boca.
</p><p>
3 Entonces Pablo replicó: «A ti te golpeará Dios, hipócrita. ¡Tú te sientas allí para juzgarme según la Ley y, violando la Ley, me haces golpear!».
</p><p>
4 Los asistentes le advirtieron: «Estás insultando al Sumo Sacerdote de Dios».
</p><p>
5 «Yo no sabía, hermanos, que era el Sumo Sacerdote, respondió Pablo, porque está escrito: No maldecirás al jefe de tu pueblo».
</p><p>
6 Pablo, sabiendo que había dos partidos, el de los saduceos y el de los fariseos, exclamó en medio del Sanedrín: «Hermanos, yo soy fariseo, hijo de fariseos, y ahora me están juzgando a causa de nuestra esperanza en la resurrección de los muertos».
</p><p>
7 Apenas pronunció estas palabras, surgió una disputa entre fariseos y saduceos, y la asamblea se dividió.
</p><p>
8 Porque los saduceos niegan la resurrección y la existencia de los ángeles y de los espíritus; los fariseos, por el contrario, admiten una y otra cosas.
</p><p>
9 Se produjo un griterío, y algunos escribas del partido de los fariseos se pusieron de pie y protestaron enérgicamente: «Nosotros no encontramos nada de malo en este hombre. ¿Y si le hubiera hablado algún espíritu o un ángel...?».
</p><p>
10 Como la disputa se hacía cada vez más violenta, el tribuno, temiendo por la integridad de Pablo, mandó descender a los soldados para que lo sacaran de allí y lo llevaran de nuevo a la fortaleza.
</p><p>
11 A la mañana siguiente, el Señor se apareció a Pablo y le dijo: «Animo, así como has dado testimonio de mí en Jerusalén, también tendrás que darlo en Roma».
</p><p>
12 Al amanecer, los judíos se confabularon y se comprometieron bajo juramente a no comer ni beber, hasta no haber matado a Pablo.
</p><p>
13 Los comprometidos en la conjuración eran más de cuarenta.
</p><p>
14 Fueron al encuentro de los sumos sacerdotes y los ancianos, y les dijeron: «Nosotros nos hemos comprometido bajo juramento a no probar nada antes de haber matado a Pablo.
</p><p>
15 Pónganse de acuerdo con el Sanedrín, y propongan al tribuno que lo haga comparecer delante de ustedes con el pretexto de examinar más exactamente su causa; nosotros, por nuestra parte, estaremos preparados para matarlo en el camino».
</p><p>
16 Pero un sobrino de Pablo, al enterarse de la emboscada, se dirigió a la fortaleza y entró para prevenir a Pablo.
</p><p>
17 Este, llamando a uno de los centuriones, le dijo: «Acompaña a este muchacho hasta donde está el tribuno, porque tiene algo que comunicarle».
</p><p>
18 El centurión lo llevó y dijo al tribuno: «El prisionero Pablo me pidió que te trajera a este muchacho, porque tiene algo que decirte».
</p><p>
19 El tribuno, tomándolo de la mano, lo llevó aparte y le preguntó: «¿Qué tienes que comunicarme?».
</p><p>
20 El muchacho le respondió: «Los judíos, bajo pretexto de examinar más a fondo la causa, se han puesto de acuerdo para pedirte que mañana presentes a Pablo ante el Sanedrín.
</p><p>
21 No lo creas. Es una emboscada que le preparan más de cuarenta de ellos, comprometidos bajo juramente a no comer ni beber hasta haberlo matado. Ya están dispuestos y sólo esperan tu consentimiento».
</p><p>
22 El tribuno despidió al muchacho, haciéndole esta recomendación: «No digas a nadie que me has contado esto».
</p><p>
23 Después llamó a dos centuriones y les dijo: «Preparen doscientos soldados, setenta jinetes y doscientos lanceros, para que salgan en dirección a Cesarea a las nueve de la noche.
</p><p>
24 Preparen también caballos para Pablo, y llévenlo sano y salvo hasta el gobernador Félix».
</p><p>
25 Y escribió una carta que decía:
</p><p>
26 «Claudio Lisias saluda al excelentísimo gobernador Félix.
</p><p>
27 Aquí te envío a un hombre que fue detenido por los judíos, y cuando ya lo iban a matar, enterándome de que era ciudadano romano, intervine con mis soldados y pude rescatarlo.
</p><p>
28 Queriendo saber exactamente de qué lo acusaban, lo hice comparecer delante del Tribunal judío,
</p><p>
29 pero comprobé que se lo acusaba por cuestiones relativas a la Ley de los judíos, y que no había ningún cargo por el que mereciera la muerte o la prisión.
</p><p>
30 Informado de que se tramaba una conspiración contra este hombre, he querido enviarlo allí en seguida, ordenando también a sus acusadores que te expongan los cargos que tengan contra él. Adiós».
</p><p>
31 De acuerdo con la orden recibida, los soldados tomaron a Pablo y lo condujeron de noche a Antipátride.
</p><p>
32 Al día siguiente, dejaron que los jinetes partieran con él, y ellos se volvieron a la fortaleza.
</p><p>
33 Llegados a Cesarea, los jinetes entregaron la carta al gobernador y le presentaron a Pablo.
</p><p>
34 El gobernador leyó la carta y preguntó de qué provincia era. Al saber que era de Cilicia,
</p><p>
35 dijo: «Te oiré cuando lleguen tus acusadores». Y lo hizo poner bajo custodia en el pretorio de Herodes.
</p>
