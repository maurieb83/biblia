<h3>HECHOS DE LOS APOSTOLES</h3>
<p>Capítulo 25
</p><p>
1 Tres días después de haberse hecho cargo de su provincia, Festo subió de Cesarea a Jerusalén.
</p><p>
2 Los sumos sacerdotes y los judíos más importantes acusaron entonces a Pablo en su presencia,
</p><p>
3 y le pidieron la gracia de que lo hiciera trasladar a Jerusalén. En realidad preparaban una emboscada para matarlo en el camino.
</p><p>
4 Pero Festo respondió que Pablo debía quedar bajo custodia en Cesarea, y que él mismo iría allí inmediatamente.
</p><p>
5 «Que los de más autoridad entre ustedes, añadió, vengan conmigo y presenten su acusación, si tienen algo contra él».
</p><p>
6 Festo permaneció en Jerusalén unos ocho o diez días, y luego bajó a Cesarea. Al día siguiente, se sentó en el tribunal e hizo comparecer a Pablo.
</p><p>
7 En cuanto llegó, los judíos venidos de Jerusalén lo rodearon, y presentaron contra él numerosas y graves acusaciones que no podían probar.
</p><p>
8 Pablo se defendía diciendo: «Yo no he cometido ninguna falta contra la Ley de los judíos, ni contra el Templo, ni contra el Emperador».
</p><p>
9 Festo, queriendo congraciarse con los judíos, se dirigió a Pablo y le dijo: «¿Quieres subir a Jerusalén para ser juzgado allí en mi presencia?».
</p><p>
10 Pablo respondió: «Estoy delante del tribunal del Emperador, y es aquí donde debo ser juzgado. Yo no hice ningún mal a los judíos, como tú lo sabes perfectamente.
</p><p>
11 Si soy culpable y he cometido algún delito que merezca la muerte, no me niego a morir, pero si las acusaciones que hacen los judíos contra mí carecen de fundamento, nadie tiene el derecho de entregarme a ellos. Apelo al Emperador».
</p><p>
12 Festo, después de haber consultado con su Consejo, respondió: «Ya que apelaste al Emperador, comparecerás ante él».
</p><p>
13 Algunos días más tarde, el rey Agripa y Berenice llegaron a Cesarea y fueron a saludar a Festo.
</p><p>
14 Como ellos permanecieron varios días, Festo expuso al rey el caso de Pablo, diciéndole: «Félix ha dejado a un prisionero,
</p><p>
15 y durante mi estadía en Jerusalén, los sumos sacerdotes y los ancianos de los judíos, presentaron quejas pidiendo su condena.
</p><p>
16 Yo les respondí que los romanos no tienen la costumbre de entregar a un hombre antes de enfrentarlo con sus acusadores y darle la oportunidad de defenderse.
</p><p>
17 Ellos vinieron aquí, y sin ninguna demora, me senté en el tribunal e hice comparecer a ese hombre al día siguiente.
</p><p>
18 Pero cuando se presentaron los acusadores, estos no alegaron contra él ninguno de los cargos que yo sospechaba.
</p><p>
19 Lo que había entre ellos eran no sé qué discusiones sobre su religión, y sobre un tal Jesús que murió y que Pablo asegura que vive.
</p><p>
20 No sabiendo bien qué partido tomar en un asunto de esta índole le pregunté a Pablo si quería ir a Jerusalén para ser juzgado allí.
</p><p>
21 Pero como este apeló al juicio de Su Majestad imperial, yo ordené que lo dejaran bajo custodia hasta que lo enviara al Emperador».
</p><p>
22 Agripa dijo entonces a Festo: «A mí también me gustaría escuchar a ese hombre». «Mañana lo escucharás», respondió Festo.
</p><p>
23 Al día siguiente, Agripa y Berenice llegaron con gran pompa y entraron en la sala de audiencias, rodeados de los tribunos y de los hombres más importantes de la ciudad. A una orden de Festo, trajeron a Pablo.
</p><p>
24 Festo tomó la palabra, diciendo: «Rey Agripa y todos los que están aquí presentes, ustedes ven a este hombre, por quien toda la comunidad judía ha venido a verme, tanto aquí como en Jerusalén, insistiendo a gritos que no había que dejarlo vivir más.
</p><p>
25 Yo no he encontrado en él nada que merezca la muerte; pero ya que él mismo ha apelado al Emperador, he decidido enviárselo.
</p><p>
26 Como no tengo nada preciso que escribir sobre él al Soberano, lo hice comparecer ante ustedes, especialmente ante ti, rey Agripa; así, después de este interrogatorio, yo tendré algo para informar.
</p><p>
27 Porque me parece absurdo enviar a un prisionero, sin indicar al mismo tiempo los cargos que se le imputan».
</p>
