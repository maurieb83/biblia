<h3>HECHOS DE LOS APOSTOLES</h3>
<p>Capítulo 28
</p><p>
1 Cuando estuvimos a salvo, nos enteramos de que la misma se llamaba Malta. 2 Sus habitantes nos demostraron una cordialidad nada común y nos recibieron a todos alrededor de un gran fuego que habían encendido a causa de la lluvia y del frío.
</p><p>
3 Pablo recogió unas ramas secas y las echó al fuego. El calor hizo salir una serpiente que se enroscó en su mano.
</p><p>
4 Cuando los habitantes del lugar vieron el reptil enroscado en su mano, comenzaron a decir entre sí: «Este hombre es seguramente un asesino: se ha salvado del mar, y ahora la justicia divina no le permite sobrevivir».
</p><p>
5 Pero él tiró la serpiente al fuego y no sufrió ningún mal.
</p><p>
6 Ellos esperaban que se hinchara o cayera muerto. Después de un largo rato, viendo que no le pasaba nada, cambiaron de opinión y decían: «Es un dios».
</p><p>
7 Había en los alrededores una propiedad perteneciente al principal de la isla, llamado Publio. Este nos recibió y nos brindó cordial hospitalidad durante tres días.
</p><p>
8 El padre de Publio estaba en cama con fiebre y disentería. Pablo fue a verlo, oró, le impuso las manos y lo curó.
</p><p>
9 A raíz de esto, se presentaron los otros enfermos de la isla y fueron curados.
</p><p>
10 Nos colmaron luego de toda clase de atenciones y cuando nos embarcamos, nos proveyeron de lo necesario.
</p><p>
11 Al cabo de tres meses nos embarcamos en un navío que había permanecido en la isla durante el invierno; era un barco alejandrino que tenía la insignia de Cástor y Pólux.
</p><p>
12 Hicimos escala en Siracusa, donde permanecimos tres días.
</p><p>
13 De allí, bordeando la costa llegamos a Regio. Al día siguiente, se levantó un viento del sur, y en dos días llegamos a Pozzuoli,
</p><p>
14 donde encontramos a unos hermanos que nos invitaron a permanecer una semana con ellos. Luego llegamos a Roma.
</p><p>
15 Los hermanos de esta ciudad, informamos de nuestra llegada, nos salieron al encuentro y nos alcanzaron a la altura del «Foro de Apio» y en las «Tres Tabernas». Pablo, al verlos, dio gracias a Dios y se sintió reconfortado.
</p><p>
16 Cuando llegamos a Roma, recibió autorización para alojarse en una casa particular con un soldado que lo custodiara.
</p><p>
17 Tres días después convocó a los judíos principales, y cuando se reunieron les dijo: «Hermanos, sin haber hecho nada contra el pueblo ni contra las costumbres de nuestros padres, fui arrestado en Jerusalén y puesto en manos de los romanos.
</p><p>
18 Después de interrogarme, quisieron dejarme en libertad, porque no encontraban en mí nada que mereciera la muerte;
</p><p>
19 pero ante la oposición de los judíos, me vi obligado a apelar al Emperador, sin querer por esto acusar en nada a mi pueblo.
</p><p>
20 Por eso he querido verlos y hablarles, ya que a causa de la esperanza de Israel llevo estas cadenas».
</p><p>
21 Ellos le respondieron: «Nosotros no hemos recibido de Judea ninguna carta referente a ti, y ninguno de los hermanos que vinieron nos han contado nada que te sea desfavorable.
</p><p>
22 Pero ahora quisiéramos oírte exponer lo que piensas, porque sabemos que esta secta encuentra oposición en todas partes».
</p><p>
23 Entonces fijaron un día para encontrarse con él, y fueron a verlo en mayor número al lugar donde se alojaba. Pablo les habló durante todo el día sobre el Reino de Dios, dándoles toda clase de testimonio y tratando de persuadirlos para que creyeran en Jesucristo, a partir de la Ley de Moisés y de los Profetas.
</p><p>
24 Unos se convencían con sus palabras, pero otros se resistían a creer,
</p><p>
25 y mientras ellos se retiraban sin haberse puesto de acuerdo, Pablo dijo esta sola frase: «Son muy ciertas las palabras que el Espíritu Santo dijo a los padres de ustedes, por medio del profeta Isaías:
</p><p>
26 "Ve a decir a este pueblo: Por más que oigan no comprenderán, por más que vean, no conocerán,
</p><p>
27 Porque el corazón de este pueblo se ha endurecido, se taparon los oídos y cerraron los ojos, por temor de que sus ojos vean, que sus oídos oigan, que su corazón comprenda, que se conviertan, y que yo los cure".
</p><p>
28 Sepan entonces que esa salvación de Dios va a ser anunciada a los paganos. Ellos sí que la escucharán».
</p><p>
29 [Al oír estas palabras los judíos se retiraron discutiendo acaloradamente.]
</p><p>
30 Pablo vivió dos años enteros por sus propios medios, recibiendo a todos los que querían verlo,
</p><p>
31 proclamando el Reino de Dios, y enseñando con toda libertad y sin encontrar ningún obstáculo, lo concerniente al Señor Jesucristo.
</p>
