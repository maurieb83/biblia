<h3>HECHOS DE LOS APOSTOLES</h3>
<p>Capítulo 15
</p><p>
1 Algunas personas venidas de Judea enseñaban a los hermanos que si no se hacían circuncidar según el rito establecido por Moisés, no podían salvarse.
</p><p>
2 A raíz de esto, se produjo una agitación: Pablo y Bernabé discutieron vivamente con ellos, y por fin, se decidió que ambos, junto con algunos otros, subieran a Jerusalén para tratar esta cuestión con los Apóstoles y los presbíteros.
</p><p>
3 Los que habían sido enviados por la Iglesia partieron y atravesaron Fenicia y Samaría, contando detalladamente la conversión de los paganos. Esto causó una gran alegría a todos los hermanos.
</p><p>
4 Cuando llegaron a Jerusalén, fueron bien recibidos por la Iglesia, por los Apóstoles y los presbíteros, y relataron todo lo que Dios había hecho con ellos.
</p><p>
5 Pero se levantaron algunos miembros de la secta de los fariseos que habían abrazado la fe, y dijeron que era necesario circuncidar a los paganos convertidos y obligarlos a observar la Ley de Moisés.
</p><p>
6 Los Apóstoles y los presbíteros se reunieron para deliberar sobre este asunto.
</p><p>
7 Al cabo de una prolongada discusión, Pedro se levantó y dijo: «Hermanos, ustedes saben que Dios, desde los primeros días, me eligió entre todos ustedes para anunciar a los paganos la Palabra del Evangelio, a fin de que ellos abracen la fe.
</p><p>
8 Y Dios, que conoce los corazones, dio testimonio en favor de ellos, enviándoles el Espíritu Santo, lo mismo que a nosotros.
</p><p>
9 El no hizo ninguna distinción entre ellos y nosotros, y los purificó por medio de la fe.
</p><p>
10 ¿Por qué ahora ustedes tientan a Dios, pretendiendo imponer a los discípulos un yugo que ni nuestros padres ni nosotros pudimos soportar?
</p><p>
11 Por el contrario, creemos que tanto ellos como nosotros somos salvados por la gracia del Señor Jesús».
</p><p>
12 Después, toda la asamblea hizo silencio para oír a Bernabé y a Pablo, que comenzaron a relatar los signos y prodigios que Dios había realizado entre los paganos por intermedio de ellos.
</p><p>
13 Cuando dejaron de hablar, Santiago tomó la palabra, diciendo: «Hermano, les ruego que me escuchen:
</p><p>
14 Simón les ha expuesto cómo Dios dispuso desde el principio elegir entre las naciones paganas, un Pueblo consagrado a su Nombre.
</p><p>
15 Con esto concuerdan las palabras de los profetas que dicen:
</p><p>
16 "Después de esto, yo volveré y levantaré la choza derruida de David; restauraré sus ruinas y la reconstruiré,
</p><p>
17 para que el resto de los hombres busque al Señor, lo mismo que todas las naciones que llevan mi Nombre. Así dice el Señor, que da
</p><p>
18 a conocer estas cosas desde la eternidad".
</p><p>
19 Por eso considero que no se debe inquietar a los paganos que se convierten a Dios,
</p><p>
20 sino que solamente se les debe escribir, pidiéndoles que se abstengan de lo que está contaminado por los ídolos, de las uniones ilegales, de la carne de animales muertos sin desangrar y de la sangre.
</p><p>
21 Desde hace muchísimo tiempo, en efecto, Moisés tiene en cada ciudad sus predicadores que leen la Ley en la sinagoga todos los sábados».
</p><p>
22 Entonces los Apóstoles, los presbíteros y la Iglesia entera, decidieron elegir a algunos de ellos y enviarlos a Antioquía con Pablo y Bernabé. Eligieron a Judas, llamado Barsabás, y a Silas, hombres eminentes entre los hermanos
</p><p>
23 y les encomendaron llevar la siguiente carta: «Los Apóstoles y los presbíteros saludamos fraternalmente a los hermanos de origen pagano, que están en Antioquía, en Siria y en Cilicia.
</p><p>
24 Habiéndonos enterado de que algunos de los nuestros, sin mandato de nuestra parte, han sembrado entre ustedes la inquietud y provocado el desconcierto,
</p><p>
25 hemos decidido de común acuerdo elegir a unos delegados y enviárselos junto con nuestros queridos Bernabé y Pablo,
</p><p>
26 los cuales han consagrado su vida al nombre de nuestro Señor Jesucristo.
</p><p>
27 Por eso les enviamos a Judas y a Silas, quienes les transmitirán de viva voz este mismo mensaje.
</p><p>
28 El Espíritu Santo, y nosotros mismos, hemos decidido no imponerles ninguna carga más que las indispensables, a saber:
</p><p>
29 que se abstengan de la carne inmolada a los ídolos, de la sangre, de la carne de animales muertos sin desangrar y de las uniones ilegales. Harán bien en cumplir todo esto. Adiós».
</p><p>
30 Los delegados, después de ser despedidos, descendieron a Antioquía donde convocaron a la asamblea y le entregaron la carta.
</p><p>
31 Esta fue leída y todos se alegraron por el aliento que les daba.
</p><p>
32 Judas y Silas, que eran profetas, exhortaron a sus hermanos y los confirmaron, hablándoles largamente.
</p><p>
33 Al cabo de un tiempo, los hermanos los enviaron nuevamente a la comunidad que los había elegido, despidiéndolos en paz.
</p><p>
34 [Como Silas creyó que debía quedarse, Judas partió solo.]
</p><p>
35 Pablo y Bernabé permanecieron en Antioquía, enseñando y anunciando la Buena Noticia de la Palabra del Señor, junto con muchos otros.
</p><p>
36 Algún tiempo después, Pablo dijo a Bernabé: «Volvamos a visitar a los hermanos que están en las ciudades donde ya hemos anunciado la palabra del Señor, para ver cómo se encuentran».
</p><p>
37 Bernabé quería llevar consigo también a Juan, llamado Marcos.
</p><p>
38 Pero Pablo consideraba que no debía llevar a quien los había abandonado cuando estaban en Panfilia y no había trabajado con ellos.
</p><p>
39 La discusión fue tan viva que terminaron por separarse; Bernabé, llevando consigo a Marcos, se embarcó rumbo a Chipre.
</p><p>
40 Pablo, por su parte, eligió por compañero a Silas y partió, encomendado por sus hermanos a la gracia del Señor.
</p><p>
41 Así atravesó la Siria y la Cilicia, confirmando a las comunidades.
</p>
