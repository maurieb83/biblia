<h3>HECHOS DE LOS APOSTOLES</h3>
<p>Capítulo 6
</p><p>
1 En aquellos días, como el número de discípulos aumentaba, los helenistas comenzaron a murmurar contra los hebreos porque se desatendían a sus viudas en la distribución diaria de los alimentos.
</p><p>
2 Entonces los Doce convocaron a todos los discípulos y les dijeron: «No es justo que descuidemos el ministerio de la Palabra de Dios para ocuparnos de servir las mesas.
</p><p>
3 Es preferible, hermanos, que busquen entre ustedes a siete hombres de buena fama, llenos del Espíritu Santo y de sabiduría, y nosotros les encargaremos esta tarea.
</p><p>
4 De esa manera, podremos dedicarnos a la oración y al ministerio de la Palabra».
</p><p>
5 La asamblea aprobó esta propuesta y eligieron a Esteban, hombre lleno de fe y del Espíritu Santo, a Felipe y a Prócoro, a Nicanor y a Timón, a Pármenas y a Nicolás, prosélito de Antioquía.
</p><p>
6 Los presentaron a los Apóstoles, y estos, después de orar, les impusieron las manos.
</p><p>
7 Así la Palabra de Dios se extendía cada vez más, el número de discípulos aumentaba considerablemente en Jerusalén y muchos sacerdotes abrazaban la fe.
</p><p>
8 Esteban, lleno de gracia y de poder, hacía grandes prodigios y signos en el pueblo.
</p><p>
9 Algunos miembros de la sinagoga llamada «de los Libertos», como también otros, originarios de Cirene, de Alejandría, de Cilicia y de la provincia de Asia, se presentaron para discutir con él.
</p><p>
10 Pero como no encontraban argumentos, frente a la sabiduría y al espíritu que se manifestaba en su palabra,
</p><p>
11 sobornaron a unos hombres para que dijeran que le habían oído blasfemar contra Moisés y contra Dios.
</p><p>
12 Así consiguieron excitar al pueblo, a los ancianos y a los escribas, y llegando de improviso, lo arrestaron y lo llevaron ante el Sanedrín.
</p><p>
13 Entonces presentaron falsos testigos, que declararon: «Este hombre no hace otra cosa que hablar contra el Lugar santo y contra la Ley.
</p><p>
14 Nosotros le hemos oído decir que Jesús de Nazaret destruirá este Lugar y cambiará las costumbres que nos ha transmitido Moisés».
</p><p>
15 En ese momento, los que estaban sentados en el Sanedrín tenían los ojos clavados en él y vieron que el rostro de Esteban parecía el de un ángel.
</p>
