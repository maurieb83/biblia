<h3>SEGUNDO LIBRO DE SAMUEL</h3>
<p>Capítulo 16
</p><p>
La adhesión de Sibá a David
</p><p>
1 David acababa de pasar la cumbre, cuando le salió al encuentro Sibá, el servidor de Meribaal, con un par de asnos ensillados y cargados con doscientos panes, cien racimos de pasas de uva, cien frutas frescas y un odre de vino.
</p><p>
2 El rey dijo a Sibá: «¿Qué vas a hacer con eso?». Sibá respondió: «Los asnos servirán de cabalgadura a la familia del rey; el pan y la fruta son para que coman los jóvenes, y el vino, para que beban los que desfallezcan en el desierto».
</p><p>
3 El rey le preguntó: «¿Dónde está el hijo de tu señor?». Sibá respondió al rey: «Se ha quedado en Jerusalén, diciendo: «Hoy la casa de Israel me devolverá el reino de mi padre».
</p><p>
4 El rey dijo a Sibá: «Desde ahora te pertenecen todos los bienes de Meribaal». Sibá respondió: «¡A tus pies! ¡Quiera mi señor, el rey, dispensarme siempre su favor!».
</p><p>
David maldecido por Simei
</p><p>
5 Cuando el rey llegaba a Bajurím salió de allí un hombre del mismo clan que la casa de Saúl, llamado Simei, hijo de Guerá. Mientras salía, iba lanzando maldiciones,
</p><p>
6 y arrojaba piedras contra David y contra sus servidores, a pesar de que todo el pueblo y todos los guerreros marchaban a la derecha y a la izquierda del rey.
</p><p>
7 Y al maldecirlo, decía: «¡Fuera, fuera, hombre sanguinario y canalla!
</p><p>
8 El Señor hace recaer sobre ti toda la sangre de la casa de Saúl, a quien tú has usurpado el reino. ¡El Señor ha puesto la realeza en manos de tu hijo Absalón, mientras que tú has caído en desgracia, porque eres un sanguinario!».
</p><p>
9 Abisai, hijo de Seruiá, dijo al rey: «¿Cómo ese perro muerto va a maldecir a mi señor, el rey? ¡Deja que me cruce y le cortaré la cabeza!».
</p><p>
10 Pero el rey replicó: «¿Qué tengo que ver yo con ustedes, hijos de Seruiá? Si él maldice, es porque el Señor le ha dicho: «¡Maldice a David!». ¿Quién podrá entonces reprochárselo?».
</p><p>
11 Luego David dijo a Abisai y a todos sus servidores: «Si un hijo mío, nacido de mis entrañas, quiere quitarme la vida, ¡cuánto más este benjaminita! Déjenlo que maldiga, si así se lo ha dicho el Señor.
</p><p>
12 Quizá el Señor mire con humillación y me devuelve la felicidad, a cambio de esta maldición que hoy recibo de él».
</p><p>
13 David siguió con sus hombres por el camino, mientras Simei iba por la ladera de la montaña, al costo de él; y a medida que avanzaba, profería maldiciones, arrojaba piedras y levantaba polvo.
</p><p>
14 David y su gente llegaron rendidos, y allí retomaron aliento.
</p><p>
Absalón en Jerusalén
</p><p>
15 Mientras tanto, Absalón había entrado en Jerusalén con todos los hombres de Israel, y Ajitófel lo acompañaba.
</p><p>
16 Cuando Jusai, el arquita, el amigo de David, llegó a donde estaba Absalón, le dijo: «¡Viva el rey! ¡Viva el rey!».
</p><p>
17 Pero Absalón replicó a Jusai: «¿Esa es tu lealtad hacia tu amigo? ¿Por qué no te has ido con él?».
</p><p>
18 Entonces Jusai dijo a Absalón» «¡No, de ninguna manera! Yo estoy con aquel a quien ha elegido el Señor, y también esta gente y todos los hombres de Israel. ¡Con él me quedaré!
</p><p>
19 Después de todo, ¿a quién voy a servir? ¿No es acaso a su hijo? Como estuve al servicio de tu padre, así te servirá a ti».
</p><p>
20 Luego Absalón dijo a Ajitófel: «¡Deliberen a ver qué nos conviene hacer!».
</p><p>
21 Ajitófel dijo a Absalón: «Unete a las concubinas que dejó tu padre al cuidado de su casa. Así todo Israel sabrá que has roto con tu padre, y tus partidarios se sentirán fortalecidos».
</p><p>
22 Entonces le instalaron a Absalón una carpa en la azotea, y él se unió a las concubinas de su padre, a la vista de todo Israel.
</p><p>
23 En aquella época, se buscaba el consejo de Ajitófel como un oráculo divino: tal era la estima que tenían por sus consejos tanto David como Absalón.
</p>
