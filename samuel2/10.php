<h3>SEGUNDO LIBRO DE SAMUEL</h3>
<p>Capítulo 10
</p><p>
La afrenta de los amonitas a los enviados de David
</p><p>
1 Después de esto, murió el rey de los amonitas, y su hijo Janún reinó en lugar de él.
</p><p>
2 David dijo: «Voy a retribuirle a Janún, hijo de Najás, las pruebas de lealtad que me ha dado su padre». Y por intermedio de sus servidores, le envió las condolencias por la muerte de su padre. Pero cuando los servidores de David llegaron al país de los amonitas,
</p><p>
3 los jefes amonitas dijeron a Janún, su señor: «¿Crees que David te hace llegar sus condolencias para honrar a tu padre? ¿No será que ha enviado a sus servidores como espías, para explorar la ciudad y sembrar la agitación?».
</p><p>
4 Entonces Janún hizo detener a los servidores de David, les afeitó la mitad de la barba, les cortó la ropa a la altura de las nalgas y los despidió.
</p><p>
5 Apenas lo pusieron al tanto de lo sucedido, David ordenó que fueran a recibir a aquellos hombres, porque estaban muy avergonzados. Y el rey les mandó decir: «Quédense en Jericó hasta que les crezca la barba, y después vengan».
</p><p>
Primera campaña de Israel contra los amonitas
</p><p>
6 Cuando los amonitas advirtieron que se habían atraído el odio de David, mandaron a contratar veinte mil soldados de los arameos de Bet Rejob y de los arameos de Sobá, al rey de Maacá con mil hombres, y a veinte mil hombres de la gente de Tob.
</p><p>
7 David, al enterarse, envió a Joab con todo el ejército y con sus guerreros.
</p><p>
8 Los amonitas salieron y formaron en orden de batalla a la entrada de la Puerta, pero los arameos de Sobá y de Rejob, y la gente de Tob y de Maacá se mantuvieron aparte, en campo abierto.
</p><p>
9 Cuando Joab vio que había dos frentes de batalla, uno delante de él y otro detrás, seleccionó a lo más escogido de Israel y los alineó frente a los arameos,
</p><p>
10 dejando el resto de la tropa a las órdenes de su hermano Abisai. Luego les hizo tomar posiciones frente a los amonitas,
</p><p>
11 y dijo: «Si los arameos son más fuertes que yo, tú vendrás en mi ayuda; y si los amonitas son más fuertes que tú, yo iré a auxiliarte.
</p><p>
12 ¡Animo! ¡Luchemos valerosamente por nuestro pueblo y por las ciudades de nuestro Dios! ¡Y que el Señor haga lo que te parezca bien!».
</p><p>
13 Entonces Joab avanzó con sus tropas para enfrentarse con Aram, y estos huyeron delante de él.
</p><p>
14 Cuando los amonitas vieron que los arameos habían huido, también ellos huyeron delante de Abisai y entraron en la ciudad. Joab, por su parte, suspendió su campaña contra los amonitas y volvió a Jerusalén.
</p><p>
Nueva victoria de David sobre los arameos
</p><p>
15 Los arameos, al ver que habían sido vencidos, concentraron sus tropas,
</p><p>
16 y Hadadézer envió mensajeros para movilizar a los arameos del otro lado del Río. Estos llegaron a Helam a las órdenes de Sobac, el jefe del ejército de Hadadézer.
</p><p>
17 Cuando informaron de esto a David, él concentró a todo Israel, cruzó el Jordán y llegó a Helam. Los arameos tomaron posiciones frente a David y le libraron batalla.
</p><p>
18 Pero tuvieron que huir delante de Israel, y David les mató a los arameos setecientos caballos y cuarenta mil hombres de caballería. También hirió a Sobac, el jefe del ejército, el cual murió allí mismo.
</p><p>
19 Cuando todos los reyes que estaban a las órdenes de Hadadézer vieron que habían caído derrotados ante Israel, hicieron las paces con los israelitas y les quedaron sometidos. En adelante, los arameos no quisieron prestar más ayuda a los amonitas.
</p>
