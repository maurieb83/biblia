<h3>SEGUNDO LIBRO DE SAMUEL</h3>
<p>Capítulo 9
</p><p>
David y Meribaal, hijo de Jonatán
</p><p>
1 David preguntó: «¿Queda algún sobreviviente de la casa de Saúl, a quien yo pueda darle una prueba de lealtad, por amor a Jonatán?».
</p><p>
2 Y como la casa de Saúl había tenido un servidor llamado Sibá, se lo presentaron a David. El rey le dijo: «¿Tú eres Sibá?». El respondió: «Sí, para servirte».
</p><p>
3 Entonces el rey le preguntó: «¿Queda todavía alguien de la casa de Saúl, para que yo pueda cumplir con él el compromiso de fidelidad contraído ante Dios?». Sibá respondió al rey: «Queda todavía un hijo de Jonatán, que es lisiado de ambos pies».
</p><p>
4 ¿Dónde está?», le dijo el rey. Sibá le respondió: «Está en la casa de Maquir, hijo de Amiel, en Lo Dabar».
</p><p>
5 Y el rey David mandó a buscarlo a la casa de Maquir, hijo de Amiel, en Lo Dabar.
</p><p>
6 Cuando Meribaal, hijo de Jonatán, se presentó ante David, cayó con el rostro en tierra y se postró. David le dijo: «¡Meribaal!». «Aquí estoy para servirte», respondió él.
</p><p>
7 Luego David añadió: «No tengas miedo. Quiero darte una prueba de fidelidad, por amor a tu padre Jonatán. Voy a devolverte todas las tierras de tu antepasado Saúl, y tú compartirás siempre la mesa conmigo».
</p><p>
8 Meribaal se postró y dijo: «¿Quién es tu servidor, para que te fijes en un perro muerto como yo?».
</p><p>
9 Después el rey llamó a Sibá, el servidor de Saúl, y le dijo: «Todo lo que pertenecía a Saúl y a su familia, se lo doy al hijo de tu señor.
</p><p>
10 Tú trabajarás la tierra para él, y lo mismo harán tus hijos y tus esclavos. Lo que tú aportes, servirá de alimento para la casa de tu señor. En cuanto a Meribaal, compartirá siempre la mesa conmigo». Sibá, que tenía quince hijos y veinte esclavos,
</p><p>
11 respondió al rey: «Tu servidor obrará en todo conforme a lo que ha mandado el rey, mi señor». Meribaal comía en la mesa de David, como uno de los hijos del rey.
</p><p>
12 El tenía un hijo pequeño, llamado Micá. Todos los que vivían en la casa de Sibá estaban al servicio de Meribaal,
</p><p>
13 y este habitaba en Jerusalén, porque compartía siempre la mesa del rey. Meribaal rengueaba de ambos pies.
</p>
