<h3>SEGUNDO LIBRO DE SAMUEL</h3>
<p>Capítulo 18
</p><p>
El enfrentamiento de David y Absalón
</p><p>
1 David pasó revista a sus tropas y puso al frente de ellas jefes de mil y cien hombres.
</p><p>
2 Luego dio a la tropa la señal de partida: un tercio iba a las órdenes de Joab, un tercio a las órdenes de Abisai, hijo de Seruiá y hermano de Joab, y el otro tercio a las órdenes de Itai, el de Gat. El rey dijo a la tropa: «Yo también saldré con ustedes».
</p><p>
3 Pero la tropa respondió: «Tú no vendrás con nosotros. Porque si tenemos que huir, eso no le importaría a nadie; y aunque muriera la mitad de nosotros, tampoco nos tendrían en cuenta. Tú, en cambio, vales ahora por diez mil de nosotros. Es mejor que estés pronto a socorrernos desde la ciudad».
</p><p>
4 El rey les dijo: «Haré lo que les parezca bien». Y permaneció al lado de la Puerta, mientras toda la tropa salía en grupos de cien y mil hombres.
</p><p>
5 El rey hizo esta recomendación a Joab, Abisai e Itai: «Trátenme con cuidado al joven Absalón».
</p><p>
6 La tropa salió al campo abierto para enfrentarse con Israel, y se entabló batalla en el bosque de Efraím.
</p><p>
7 Allí el ejército de Israel cayó derrotado ante los servidores de David, y aquel día el desastre fue tan grande, que hubo veinte mil bajas.
</p><p>
8 Desde allí el combate se extendió a toda la región, y el bosque devoró aquel día más gente que la espada.
</p><p>
La muerte de Absalón
</p><p>
9 De pronto, Absalón se encontró frente a los servidores de David. Iba montado en un mulo, y este se metió bajo el tupido ramaje de una gran encina, de manera que la cabeza de Absalón quedó enganchada en la encina. Así él quedó colgado entre el cielo y la tierra, mientras el mulo seguía de largo por debajo de él.
</p><p>
10 Al verlo, un hombre avisó a Joab: «¡Acabo de ver a Absalón colgado de una encina!».
</p><p>
11 Joab replicó al hombre que le dio la noticia: «Y si lo viste, ¿por qué no lo dejaste tendido allí mismo? ¡Yo ahora te hubiera dado diez siclos de plata y un cinturón!».
</p><p>
12 Pero el hombre dijo a Joab: «Aunque pudiera pesar en la palma de mi mano mil siclos de plata, no atentaría contra el hijo del rey. Porque en presencia nuestra el rey les impartió esta orden, a ti, a Abisai y a Itai: «¡Cuídenme bien al joven Absalón!».
</p><p>
13 Y si yo hubiera atentado alevosamente contra su vida, como al rey no se le oculta nada, tú te habrías puesto contra mí».
</p><p>
14 Entonces Joab replicó: «No voy a perder más tiempo contigo». Y tomando en su mano tres dardos, los clavó en el corazón de Absalón, que estaba todavía vivo en medio de la encina.
</p><p>
15 Luego diez jóvenes, los escuderos de Joab, rodearon a Absalón y lo acabaron de matar.
</p><p>
16 Joab hizo sonar el cuerno y la tropa dejó de perseguir a Israel, porque Joab la retuvo.
</p><p>
17 Luego tomaron a Absalón, lo arrojaron en un gran pozo, en plena foresta, y pusieron encima un enorme montón de piedras. Mientras tanto, todo Israel huyó, cada uno a su carpa.
</p><p>
El monumento de Absalón
</p><p>
18 Absalón se había erigido en vida una piedra conmemorativa, que está en el valle del Rey. Porque él decía: «Yo no tengo un hijo para perpetuar mi nombre». A esa estela la había llamado con su nombre, y se la llama «Monumento de Absalón» hasta el día de hoy.
</p><p>
El anuncio de la muerte de Absalón
</p><p>
19 Ajimáas, hijo de Sadoc, dijo: «¡Iré corriendo a llevar al rey la buena noticia de que el Señor le ha hecho justicia, librándolo de sus enemigos!».
</p><p>
20 Joab le respondió: «Hoy no serás portador de buenas noticias. Otro día sí lo serás, pero hoy no vas a llevar una buena noticia, porque ha muerto el hijo del rey».
</p><p>
21 Luego Joab dijo a un cusita: «Ve a informar al rey de lo que has visto». El cusita se postró delante de Joab y salió corriendo.
</p><p>
22 Ajimáas volvió a decir a Joab: «Pase lo que pase, yo también iré corriendo detrás del cusita». Joab replicó: «¿Para qué vas a correr, hijo mío? Esa buena noticia no te reportará nada bueno».
</p><p>
23 Pero él insistió: «¡Pase lo que pase, iré corriendo!». Entonces Joab le dijo: «Está bien, corre». Ajimáas fue corriendo por el camino del Distrito y se adelantó al cusita.
</p><p>
24 David estaba sentado entre las dos puertas. El centinela, que había subido a la azotea de la Puerta, encima de la muralla, alzó los ojos y vio a un hombre que corría solo.
</p><p>
25 El centinela lanzó un grito y avisó al rey. El rey dijo: «Si está solo, trae una buena noticia». Mientras el hombre se iba acercando,
</p><p>
26 el centinela divisó a otro que venía corriendo y gritó al portero: «¡Otro hombre viene corriendo solo!». El rey comentó: «Ese también trae una buena noticia».
</p><p>
27 Luego el centinela dijo: «Por la manera de correr, me parece que el primero es Ajimáas, hijo de Sadoc». Entonces el rey dijo: «Es una buena persona: seguro que viene con buenas noticias».
</p><p>
28 Cuando Ajimáas se acercó, dijo al rey: «¡Paz!». Y postrándose ante el rey con el rostro en tierra, añadió: «¡Bendito sea el Señor, tu Dios, que ha reprimido a los hombres que alzaron su mano contra el rey, mi señor!».
</p><p>
29 El rey preguntó: «¿Está bien él joven Absalón?». Ajimaás respondió: «Cuando me envió Joab, el servidor del rey, vi un gran tumulto, pero no sé de qué se trataba».
</p><p>
30 El rey le ordenó: «Retírate y quédate allí». El se retiró y se quedó de pie.
</p><p>
31 En seguida llegó el cusita y dijo: «¡Que mi señor, el rey, se entere de la buena noticia! El Señor hoy te ha hecho justicia, librándote de todos los que se sublevaron contra ti».
</p><p>
32 El rey preguntó al cusita: «¿Está bien el joven Absalón?». El cusita respondió: «¡Que tengan suerte de ese joven los enemigos de mi señor, el rey, y todos los rebeldes que buscan tu desgracia!».
</p>
