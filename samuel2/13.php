<h3>SEGUNDO LIBRO DE SAMUEL</h3>
<p>Capítulo 13
</p><p>
El ultraje de Amón a su hermana Tamar
</p><p>
1 Un tiempo después, sucedió lo siguiente. Absalón, hijo de David, tenía una hermana muy hermosa, llamada Tamar, y Amnón, hijo de David, se enamoró de ella.
</p><p>
2 Era tal su ansiedad, que llegó a enfermarse a causa de su hermanastra Tamar, porque como la joven era virgen, a Amnón le parecía imposible llevar a cabo algo con ella.
</p><p>
3 Amnón tenía un amigo llamado Jonadab, hijo de Simeá, hermano de David. Este hombre era muy perspicaz,
</p><p>
4 y dijo a Amnón: «¿Qué te pasa, príncipe, que cada día estás más deprimido? ¿No me lo vas a contar?». Amnón le respondió: «Es por Tamar, la hermana de mi hermano Absalón. Estoy enamorado de ella».
</p><p>
5 Entonces Jonadab le dijo: «Acuéstate como si estuvieras enfermo, y cuando tu padre venga a verte, tú le dirás: «Deja que mi hermana Tamar venga a darme de comer; que prepare la comida en mi presencia, de manera que yo pueda ver, y que me la sirva ella misma».
</p><p>
6 Amnón se acostó, fingiendo estar enfermo y cuando el rey fue a verlo, Amnón le dijo: «Por favor, que venga mi hermana Tamar a cocinar aquí mismo un par de buñuelos, y que me los sirva con sus propias manos».
</p><p>
7 David mandó a decir a Tamar: «Ve a la casa de tu hermano Amnón y prepárale la comida».
</p><p>
8 Tamar fue a la casa de hermano Amnón, que estaba acostado. Tomó la harina, la amasó, preparó los buñuelos a la vista de él, y los hizo cocer.
</p><p>
9 Luego retiró la sartén y la vació delante de él, pero él se negó a comer, y ordenó: «¡Hagan salir a todos de mi presencia!». Cuando salieron todos,
</p><p>
10 Amnón dijo a Tamar: «Tráeme la comida a la habitación y dame tú misma de comer». Tamar tomó los buñuelos que había preparado y los llevó a la habitación donde estaba su hermano Amnón.
</p><p>
11 Pero cuando se los acercó para que comiera, él la agarró y le dijo: «¡Ven, acuéstate conmigo, hermana!».
</p><p>
12 «¡No, hermano, replicó Tamar, no trates de forzarme, porque eso no se hace en Israel! ¡No cometas esa infamia!
</p><p>
13 ¿A dónde iría yo con mi deshonra? En cuanto a ti, ¡quedarías como un infame en Israel! Por favor, habla con el rey, y él no se opondrá a que seas mi esposo».
</p><p>
14 Pero Amnón no quiso escucharla, sino que la tomó por la fuerza y se acostó con ella.
</p><p>
15 En seguida, Amnón sintió hacia ella un odio terrible, más fuerte aún que el amor con que la había amado. Entonces le dijo: «¡Levántate y vete!».
</p><p>
16 Ella le respondió: «No, hermano; echarme ahora sería una maldad peor que la otra que has hecho conmigo». Pero él no quiso hacerle caso;
</p><p>
17 llamó al joven que lo servía y ordenó: «¡Echenme a esta a la calle, y atranca la puerta detrás de ella!».
</p><p>
18 Tamar llevaba una túnica de mangas largas, porque así vestían entonces las hijas del rey, cuando eran vírgenes. El sirviente la sacó afuera y atrancó la puerta detrás de ella.
</p><p>
19 Tamar se cubrió la cabeza con ceniza, desgarró su túnica de mangas largas y poniéndose las manos sobre la cabeza, se fue gritando.
</p><p>
20 Su hermano Absalón le dijo: «¿Fue tu hermano Amnón el que estuvo contigo? Ahora, hermana, no hables más de esto. El es tu hermano, no tomes la cosa tan a pecho». Y Tamar se quedó desolada en casa de su hermano Absalón.
</p><p>
21 Cuando el rey David se enteró de lo sucedido, se indignó profundamente.
</p><p>
22 Absalón, por su parte, no le dirigió más la palabra a Amnón, debido al rencor que le tenía por haber violado a su hermana Tamar.
</p><p>
Asesinato de Amón y huida de Absalón
</p><p>
23 Dos años más tarde, se hacía la esquila para Absalón en Baal Jasor, que está cerca de Efraím, y él invitó a todos los hijos del rey.
</p><p>
24 Absalón se presentó al rey y le dijo: «Tu servidor está esquilando las ovejas. Háganme el honor de venir conmigo el rey y sus servidores».
</p><p>
25 El rey respondió a Absalón: «No, hijo mío, no vamos a ir todos a ponerte en gastos». El le insistió, pero David no quiso ir y lo despidió con su bendición.
</p><p>
26 Entonces Absalón dijo: «Permite al menos que venga con nosotros mi hermano Amnón». «¿Para qué va a ir contigo?», repuso David.
</p><p>
27 Pero Absalón le insistió tanto, que David dejó partir con él a Amnón y a todos los hijos del rey. Absalón preparó un regio banquete,
</p><p>
28 y dio esta orden a sus servidores: «¡Fíjense bien! Cuando Amnón se haya puesto alegre con el vino y yo les diga: «Hieran a Amnón», ustedes lo matarán. No tengan miedo, porque soy yo el que lo ordeno. ¡Tengan ánimo y sean valientes!».
</p><p>
29 Los servidores hicieron a Amnón lo que Absalón les había ordenado. Entonces todos los hijos de rey se levantaron, montaron cada uno en su mula y huyeron.
</p><p>
30 Cuando todavía estaban en camino, David recibió esta noticia: «Absalón ha matado a todos los hijos del rey; no ha quedado ni uso solo».
</p><p>
31 El rey se levantó, rasgó sus vestiduras y se acostó en el suelo, mientras todos sus servidores permanecían de pie, con las ropas desgarradas.
</p><p>
32 Pero Jonadab, hijo de Simeá, hermano de David, tomó la palabra y dijo: «Que mi señor no diga que han matado a todos los jóvenes hijos del rey. Sólo ha muerto Amnón, porque Absalón ya había pronunciado la sentencia desde el día en que aquel violó a su hermana Tamar.
</p><p>
33 Que mi señor el rey no se preocupe ahora, pensando que han muerto todos los hijos del rey. No, sólo Amnón ha muerto,
</p><p>
34 y Absalón ha huido». El joven que estaba de centinela alzó los ojos, y vio avanzar un gran gentío por el camino que estaba detrás de él, sobre la ladera de la montaña.
</p><p>
35 Entonces Jonadab dijo al rey: «Ahí llegan los hijos del rey, tal como tu servidor lo había dicho».
</p><p>
36 Apenas terminó de hablar, entraron los hijos del rey y se pusieron a llorar a gritos. También el rey y todos sus servidores derramaron abundantes lágrimas.
</p><p>
37 En cuanto a Absalón, fue a refugiarse junto a Talmai, hijo de Amijur, rey de Guesur. Y el rey estuvo de duelo por su hijo todo aquel tiempo.
</p><p>
38 Absalón, que había ido a refugiarse en Guesur, estuvo allí tres años.
</p><p>
39 Mientras tanto, a David se le pasó su enojo contra Absalón, porque se había consolado de la muerte de Amnón.
</p>
