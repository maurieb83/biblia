<h3>SEGUNDO LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 13
</p><p>
1 El año ciento cuarenta y nueve, los hombres de Judas se enteraron de que Antíoco Eupátor avanzaba contra Judea con un ejército numeroso,
</p><p>
2 y que con él venía Lisias, su tutor y encargado de los asuntos de Estado. Ambos conducían un ejército griego de ciento diez mil soldados, cinco mil trescientos jinetes, veintidós elefantes y trescientos carros armados de cuchillas.
</p><p>
3 También se unió a ellos Menelao, el cual, con toda mala intención, trataba de ganarse a Antíoco, no para salvar a su patria, sino para ser restablecido en su cargo.
</p><p>
4 Pero el Rey de los reyes provocó la indignación de Antíoco contra aquel criminal, porque Lisias le hizo ver que aquel hombre era el causante de todos los males. Entonces Antíoco ordenó que lo llevaran a Berea para que lo mataran según las costumbres del lugar.
</p><p>
5 Allí hay una torre de veinticinco metros, llena de ceniza, provista de una máquina giratoria inclinada por todas partes hacia la ceniza.
</p><p>
6 Es costumbre arrojar en ese lugar a los reos de robo sacrílego o de algún otro crimen enorme, a fin de hacerlos morir.
</p><p>
7 Con este suplicio murió el impío Menelao, sin ser ni siquiera sepultado en la tierra.
</p><p>
8 Esto le sucedió con toda justicia; en efecto, aquel que había perpetrado tantos crímenes contra el altar, cuyo fuego y ceniza son sagrados, encontró él mismo la muerte en la ceniza.
</p><p>
9 El rey avanzaba, animado de brutales sentimientos, dispuesto a tratar a los judíos con más crueldad que su mismo padre.
</p><p>
10 Al enterarse de esto, Judas ordenó a sus tropas que invocaran al Señor día y noche, para que también esa vez, como en otras ocasiones, acudiera en auxilio de los que estaban en peligro de ser despojados de la Ley, de la patria y del Santuario,
</p><p>
11 y no permitiera que el pueblo, que apenas comenzaba a reanimarse, cayera en manos de paganos blasfemos.
</p><p>
12 Todos unánimemente cumplieron la orden y suplicaron al Señor misericordioso con lamentaciones, ayunos y postraciones, durante tres días seguidos. Entonces Judas los arengó, animándolos a estar preparados.
</p><p>
13 Después de reunirse en particular con los Ancianos, resolvió no esperar que el ejército del rey invadiera Judea y se adueñara de la ciudad, sino ponerse en marcha para decidir la situación con la ayuda de Dios.
</p><p>
14 Judas, dejándolo todo en manos del Creador del universo, exhortó a sus compañeros a luchar valerosamente hasta la muerte por las leyes, el Santuario, la ciudad, la patria y las instituciones. Luego acampó en las inmediaciones de Modín.
</p><p>
15 Y dando a sus hombres este santo y seña: «Victoria de Dios», atacó durante la noche con los jóvenes más aguerridos el campamento del rey. Así dio muerte a unos dos mil hombres del ejército y al más grande de los elefantes con su conductor.
</p><p>
16 Finalmente, sembrando el pánico y la confusión en el ejército, se retiraron victoriosos.
</p><p>
17 Al amanecer, ya todo había terminado, gracias a la protección que el Señor había brindado a Judas.
</p><p>
18 El rey, que había sufrido en carne propia la audacia de los judíos, intentó apoderarse de sus fortalezas de manera sistemática.
</p><p>
19 Se acercó a Betsur, plaza fuerte de los judíos, pero fue rechazado, puesto en fuga y vencido,
</p><p>
20 Judas envió a los sitiados todo lo que necesitaban,
</p><p>
21 pero Rodoco, un hombre del ejército judío, revelaba los secretos al enemigo. Por eso fue buscado, detenido y ejecutado.
</p><p>
22 El rey volvió a tratar con los habitantes de Betsur, hizo las paces con ellos y se retiró. Luego atacó a las tropas de Judas, pero fue derrotado.
</p><p>
23 En ese momento supo que Filipo, a quien había dejado en Antioquía al frente del gobierno, se había sublevado. Muy consternado, mandó llamar a los judíos y juró someterse a sus legítimas demandas. Después de esta reconciliación, ofreció un sacrificio, honró al Santuario y se mostró generoso con el Lugar santo.
</p><p>
24 El rey prestó una buena acogida al Macabeo y dejó a Hegemónidas como gobernador desde Tolemaida hasta la región de los guerraínos.
</p><p>
25 Luego se trasladó a Tolemaida, pero sus habitantes descontentos por ese tratado, se indignaron y querían anular lo convenido.
</p><p>
26 Entonces Lisias subió a la tribuna y defendió el asunto lo mejor que pudo. Así los persuadió, logró calmarlos y los dispuso favorablemente. Después partió para Antioquía. Esto es lo que sucedió con la expedición y la retirada del rey.
</p>
