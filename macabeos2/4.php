<h3>SEGUNDO LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 4
</p><p>
1 El susodicho Simón, delator del Tesoro del Templo y traidor de la patria, calumniaba a Onías, como si fuera este el que había maltratado a Heliodoro y el causante de sus desgracias.
</p><p>
2 Al bienhechor de la ciudad, al defensor de sus compatriotas, al ferviente cumplidor de las leyes, se atrevía a calificarlo de conspirador contra el Estado.
</p><p>
3 La hostilidad llegó a tal punto que uno de los partidarios de Simón cometió varios asesinatos.
</p><p>
4 Entonces Onías, considerando que aquella rivalidad era peligrosa y que Apolonio, hijo de Menesteo, gobernador de Celesiria y de Fenicia, fomentaba la maldad de Simón,
</p><p>
5 se hizo presentar delante del rey, no para acusar a sus conciudadanos, sino por el bien general de todo su pueblo y de cada uno en particular.
</p><p>
6 El veía, efectivamente, que sin una intervención real, era imposible lograr la pacificación y contener los desatinos de Simón.
</p><p>
7 Después que murió Seleuco y le sucedió en el trono Antíoco, llamado Epífanes, Jasón, hermano de Onías, usurpó fraudulentamente el sumo sacerdocio,
</p><p>
8 prometiendo al rey en una entrevista trescientos sesenta talentos de plata, y ochenta de otras rentas.
</p><p>
9 Se comprometió, además, por escrito a pagar otros ciento cincuenta talentos, si se le concedía la facultad de instalar por su propia cuenta un gimnasio y un ateneo juvenil y de inscribir en un registro a los antioquenos residentes en Jerusalén.
</p><p>
10 Con el asentimiento del rey y teniendo los poderes en su mano, comenzó rápidamente a introducir entre sus compatriotas el estilo de vida de los griegos.
</p><p>
11 Suprimió los humanitarios privilegios que los reyes habían concedido a los judíos, por intermedio de Juan, padre de Eupólemo, el mismo Eupólemo que fue enviado como embajador para hacer una alianza de amistad con los romanos; derogó las instituciones legales e introdujo nuevas costumbres contrarias a la Ley;
</p><p>
12 así se dio el gusto de fundar un gimnasio al pie mismo de la Acrópolis e indujo a lo mejor de la juventud a los ejercicios atléticos.
</p><p>
13 Era tal el auge del helenismo y el avance de la moda extranjera, debido a la enorme perversidad de Jasón –el cual tenía más de impío que de Sumo Sacerdote –
</p><p>
14 que ya los sacerdotes no tenían ningún celo por el servicio del altar, sino que despreciaban el Templo. Apenas se daba la señal de lanzar el disco, dejaban de lado los sacrificios y se apresuraban a participar en los ejercicios de la palestra, que eran contrarios a la Ley.
</p><p>
15 Sin mostrar ningún aprecio por los valores nacionales, juzgaban las glorias de los griegos como las mejores.
</p><p>
16 Pero esto mismo los puso en grave aprieto, porque después tuvieron como enemigos y opresores a aquellos mismos cuya conducta emulaban y a los cuales querían imitar en todo.
</p><p>
17 Porque no se violan en vano las leyes divinas: así lo va a demostrar la etapa siguiente.
</p><p>
18 Cuando se celebraron en Tiro los juegos quinquenales con la asistencia del rey,
</p><p>
19 el infame Jasón envió como representantes de Jerusalén a algunos antioquenos, en calidad de observadores, con un presente de trescientas dracmas de oro para el sacrificio de Hércules. Pero ellos consideraron que era un inconveniente emplearlas para el sacrificio y que debían aplicarlas a otra clase de gastos.
</p><p>
20 De esta manera, el dinero asignado por el donante al sacrificio de Hércules fue destinado, por voluntad de los portadores, a la construcción de trirremes.
</p><p>
21 Apolonio, hijo de Menesteo, fue enviado a Egipto con motivo de la entronización del rey Filométor. Cuando Antíoco supo que aquel se había convertido en su adversario político, se preocupó por su propia seguridad. Por eso, al pasar por Jope, se desvió hacia Jerusalén.
</p><p>
22 Allí fue solemnemente recibido por Jasón y por la ciudad, e hizo su entrada en medio de antorchas y aclamaciones. Después de esto, fue a acampar con sus tropas a Fenicia.
</p><p>
23 Tres años más tarde, Jasón envió a Menelao, hermano del ya mencionado Simón, para llevar el dinero al rey y también para gestionar algunos asuntos importantes.
</p><p>
24 Pero Menelao, una vez presentado ante el rey, lo impresionó con su aire majestuoso y logró hacerse investir del sumo sacerdocio, ofreciéndole trescientos talentos de plata más que Jasón.
</p><p>
25 Así regresó provisto del mandato real, pero sin llevar consigo nada digno del sumo sacerdocio, sino más bien la furia de un cruel tirano y la violencia de una fiera salvaje.
</p><p>
26 De esta manera Jasón, que había suplantado a su vez por otro, y se vio forzado a huir a la región de Amán.
</p><p>
27 Pero Menelao, una vez adueñado del poder, no se preocupaba de pagar las sumas prometidas al rey,
</p><p>
28 a pesar de las reclamaciones de Sóstrates, el prefecto de la Acrópolis, ya que a él le correspondía percibir los impuestos. Por esto motivo, ambos fueron convocados por el rey.
</p><p>
29 Menelao dejó como sustituto en el sumo sacerdocio a su hermano Lisímaco, y Sóstrates dejó a Crates, jefe de los chipriotas.
</p><p>
30 Mientras tanto, se sublevaron los habitantes de Tarso y de Malos, porque sus ciudades habían sido regaladas a Antióquida, la concubina del rey.
</p><p>
31 El rey partió apresuradamente para poner las cosas en orden, dejando en su lugar a Andrónico, uno de los grandes dignatarios.
</p><p>
32 Menelao, pensando que se le había presentado una ocasión favorable, se apropió de unos objetos de oro del Templo y se los regaló a Andrónico, y también vendió otros en Tiro y en las ciudades vecinas.
</p><p>
33 Cuando Onías tuvo la evidencia de lo sucedido, se lo reprochó, después de haberse retirado da Dafne, ciudad que estaba cerca de Antioquía y gozaba de inmunidad.
</p><p>
34 Por eso Menelao, en conversaciones secretas con Andrónico, lo instigaba a matar a Onías. Entonces Andrónico se presentó ante Onías, y se ganó astutamente su confianza, estrechándole la mano derecha con un juramento. Así lo persuadió a que saliera de su refugio –aun sin disipar toda sospecha –y lo mató inmediatamente, conculcando toda justicia.
</p><p>
35 Frente a esto, no sólo los judíos, sino también mucha gente de las otras naciones se indignaron y se afligieron por el injusto asesinato de aquel hombre.
</p><p>
36 Apenas el rey regresó de las regiones de Cilicia, los judíos de la ciudad y los griegos que reprochaban tan mala acción, acudieron a él para quejarse por la injusta muerte de Onías.
</p><p>
37 Antíoco se entristeció profundamente y, movido a compasión, lloró recordando la prudencia y la gran moderación del difunto.
</p><p>
38 Luego, lleno de indignación, despojó a Andrónico de la púrpura, desgarró sus vestiduras y lo hizo conducir por toda la ciudad hasta el sitio donde había tratado tan impíamente a Onías. Allí hizo ajusticiar al homicida, y así el Señor le infligió el castigo que había merecido.
</p><p>
39 Lisímaco había cometido muchos robos sacrílegos en la ciudad con el consentimiento de Menelao, y la noticia se había divulgado entre la gente. Por eso el pueblo se amotinó contra Lisímaco, cuando ya muchos objetos de oro habían desaparecido.
</p><p>
40 Como la multitud estaba muy excitada y había llegado al colmo de su furor, Lisímaco armó cerca de tres mil hombres e inició una violenta represión, poniendo al frente a un tal Arauno, hombre avanzado en edad no menos que en falta de juicio.
</p><p>
41 Cuando advirtieron que Lisímaco los atacaba, unos se armaron de piedras, otros de palos, y otros, tomando puñados de la ceniza que había allí, los arrojaban violentamente contra las tropas.
</p><p>
42 De este modo hirieron a muchos de ellos y mataron a otros; a todos los demás los obligaron a huir y dieron muerte al sacrílego junto al Tesoro del Templo.
</p><p>
43 Con motivo de estos sucesos, se entabló un proceso contra Menelao.
</p><p>
44 Cuando el rey llegó a Tiro, tres hombres enviados por el Consejo de los ancianos presentaron una acusación contra él.
</p><p>
45 Al verse perdido, Menelao prometió una importante suma a Tolomeo, hijo de Dorimeno, para que tratara de persuadir al rey.
</p><p>
46 Tolomeo llevó al rey a una galería, como quien va a tomar un poco de aire, y allí lo hizo cambiar de parecer.
</p><p>
47 Así absolvió de las acusaciones a Menelao, que era el causante de todos esos males. En cambio, condenó a muerte a aquellos desdichados que hubieran sido absueltos como inocentes, incluso por un tribunal de bárbaros.
</p><p>
48 De esta manera fueron inmediatamente sometidos a un castigo injusto los que habían defendido la ciudad, el pueblo y los objetos sagrados.
</p><p>
49 Por eso algunos tirios, indignados por aquella maldad, se encargaron de darles una espléndida sepultura.
</p><p>
50 Mientras tanto, Menelao se mantenía en el poder, gracias a la avaricia de aquellos gobernantes. Su maldad crecía cada vez más, convirtiéndolo en el principal adversario de sus compatriotas.
</p>
