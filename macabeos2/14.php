<h3>SEGUNDO LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 14
</p><p>
1 Al cabo de tres años, Judas y sus hombres recibieron la noticia de que Demetrio, el hijo de Seleuco, había atracado en el puerto de Trípoli con un poderoso ejército y una flota,
</p><p>
2 adueñándose del país y haciendo ejecutar a Antíoco y a su tutor Lisias.
</p><p>
3 Un cierto Alcimo, que antes había sido Sumo Sacerdote, pero que se había contaminado voluntariamente en el tiempo de la ruptura con el paganismo, comprendiendo que él ya no podía rehabilitarse ni acceder al altar sagrado,
</p><p>
4 fue a ver al rey Demetrio hacia el año ciento cincuenta y uno, y le obsequió una corona de oro y una palma, además de los acostumbrados ramos de olivo del templo. Y aquel día, se contentó con eso.
</p><p>
5 Pero él encontró una ocasión propicia para sus perversos designios, cuando Demetrio lo convocó ante el Consejo, y lo consultó acerca de las actitudes y planes de los judíos. Alcimo respondió:
</p><p>
6 «Los judíos llamados asideos, capitaneados por Judas Macabeo, fomentan la guerra y las sediciones, y no dejan que el reino viva en paz.
</p><p>
7 Debido a eso, aunque he sido despojado de la dignidad heredada de mis antepasados –me refiero al sumo sacerdocio– he venido aquí,
</p><p>
8 en primer lugar, sinceramente preocupado por los intereses del rey y, en segundo lugar, por consideración hacia mis compatriotas. A causa de la conducta irreflexiva de los hombres ya mencionados, toda nuestra nación padece no pocos infortunios.
</p><p>
9 Por eso, ya que tú eres el rey, una vez que te informes detalladamente de todo esto, vela por nuestro país y nuestra nación amenazados por todas partes, de acuerdo con los sentimientos humanitarios que demuestras hacia todos.
</p><p>
10 Ciertamente, mientras Judas viva, es imposible que el Estado goce de paz».
</p><p>
11 Apenas Alcimo pronunció estas palabras, los demás Amigos del rey que eran hostiles a Judas, se apresuraron a incitar aún más a Demetrio.
</p><p>
12 Este designó inmediatamente a Nicanor, que dirigía la tropa de los elefantes, lo nombró gobernador de Judea, y lo envió
</p><p>
13 con la orden de matar a Judas y dispersar a sus partidarios, restableciendo a Alcimo como Sumo Sacerdote del Templo más excelso.
</p><p>
14 Los paganos que habían huido de Judea por temor a Judas, se unieron en masa a Nicanor, pensando que los infortunios y las derrotas de los judíos serían sus propias victorias.
</p><p>
15 Cuando los judíos se enteraron de la expedición de Nicanor y de la invasión de los paganos, se cubrieron de polvo y suplicaron a Aquel que había establecido a su pueblo para siempre y nunca dejaba de proteger a su propia herencia en forma bien visible.
</p><p>
16 Bajo la orden de su jefe, partieron inmediatamente de allí y se trabaron en lucha con ellos junto al poblado de Desau.
</p><p>
17 Simón, el hermano de Judas, había entrado en combate con Nicanor, pero sufrió un ligero revés a causa del ataque sorpresivo de los enemigos.
</p><p>
18 Nicanor, enterado de la audacia de los hombres de Judas y del valor con que defendían a su patria, temió definir la situación de una manera sangrienta.
</p><p>
19 Por eso envió a Posidonio, Teodoto y Matatías para concertar la paz.
</p><p>
20 Después de un detenido examen de la propuesta, el jefe la comunicó a las tropas, y por decisión unánime aceptaron el tratado.
</p><p>
21 Se fijó un día en que los jefes se reunirían a solas en un lugar determinado; se puso una tarima de cada lado y se prepararon asientos.
</p><p>
22 Judas distribuyó algunos hombres armados en puntos estratégicos, por si se producía inesperadamente algún ataque a traición por parte de los enemigos. Sin embargo, la entrevista se realizó normalmente.
</p><p>
23 Nicanor vivía en Jerusalén sin molestar a nadie: incluso, licenció a las tropas que se le habían incorporado en masa.
</p><p>
24 Se veía constantemente, con Judas y sentía por él un sincero aprecio;
</p><p>
25 le aconsejó que se casara y que tuviera hijos. Judas se casó y vivió tranquilamente, disfrutando de la vida.
</p><p>
26 Cuando Alcimo vio la comprensión que reinaba entre ellos, consiguió una copia del pacto celebrado y se presentó ante Demetrio, diciéndole que Nicanor abrigaba sentimientos contrarios a los intereses del Estado, ya que había nombrado lugarteniente suyo a Judas, el rival de su reino.
</p><p>
27 El rey se puso fuera de sí y, excitado por las calumnias de ese miserable, escribió a Nicanor, manifestándole su disgusto por el acuerdo y ordenándole que le mandara inmediatamente preso a Antioquía al Macabeo.
</p><p>
28 Cuando Nicanor recibió la noticia, quedó desconcertado, porque le indignaba romper el acuerdo sin que aquel hombre hubiera cometido ninguna injusticia.
</p><p>
29 Pero como no era posible oponerse al rey, buscaba la ocasión favorable para cumplir la orden valiéndose de alguna estratagema.
</p><p>
30 El Macabeo, por su parte, viendo que Nicanor lo trataba más secamente y que le demostraba una gran frialdad en sus relaciones habituales, pensó que esa actitud no presagiaba nada bueno. Entonces reunió a un buen número de sus compañeros y se ocultó de Nicanor.
</p><p>
31 Cuando este advirtió que Judas se había burlado de él tan hábilmente, se presentó en el augusto y santo Templo, mientras los sacerdotes ofrecían los sacrificios rituales y les exigió que le entregaran a aquel hombre.
</p><p>
32 Como ellos juraron que no sabían dónde se encontraba el hombre que buscaba,
</p><p>
33 él, extendiendo la mano derecha hacia el Santuario, pronunció este juramento: «Si no me entregan prisionero a Judas, arrasaré este recinto consagrado a Dios, derribaré el altar y levantaré aquí mismo un espléndido templo a Dionisos».
</p><p>
34 Dicho esto, se fue. Los sacerdotes, con las manos extendidas hacia el cielo, invocaron a Aquel que había combatido incesantemente en favor de nuestra nación, diciendo:
</p><p>
35 «Tú, Señor de todas las cosas, que no necesitas de nada, has querido que el Santuario donde tú habitas estuviera en medio de nosotros.
</p><p>
36 Por eso ahora, Señor santo, lleno de toda santidad, preserva para siempre de toda profanación esta Casa recién purificada».
</p><p>
37 Un tal Razís, uno de los ancianos de Jerusalén, buen patriota, sumamente estimado y llamado por su bondad «Padre de los judíos», fue denunciado a Nicanor.
</p><p>
38 El, en los primeros tiempos de la ruptura con el paganismo, había abrazado la causa del Judaísmo, entregándose a ella en cuerpo y alma, con una gran entereza.
</p><p>
39 Nicanor, queriendo poner en evidencia la hostilidad que profesaba contra los judíos, envió más de quinientos soldados para detenerlo,
</p><p>
40 porque le parecía que, tomándolo prisionero, ocasionaba un gran perjuicio a los judíos.
</p><p>
41 Cuando las tropas estaban a punto de ocupar la torre, forzando la puerta de entrada y dando órdenes de traer fuego e incendiar las puertas. Razís, acorralado por todas partes, se arrojó sobre su espada.
</p><p>
42 El prefirió morir noblemente, antes que caer en manos de aquellos desalmados y soportar ultrajes indignos de su nobleza.
</p><p>
43 Pero, por lo precipitado del ataque, no acertó el golpe, y como las tropas ya se abrían paso a través de las puertas, subió valerosamente a lo alto del muro y se arrojó con intrepidez sobre la multitud.
</p><p>
44 Esta retrocedió a una cierta distancia y él cayó en medio del espacio vacío.
</p><p>
45 Estando aún con vida, lleno de ardor, se incorporó sangrando copiosamente, y a pesar de sus graves heridas, pasó corriendo por entre las tropas y se paró sobre una roca escarpada.
</p><p>
46 Cuando ya estaba completamente exangüe, se arrancó las entrañas y, tomándolas con ambas manos, las arrojó contra aquella gente. Así, invocando al Señor de la vida y del espíritu para que un día se las devolviera, murió aquel hombre.
</p>
