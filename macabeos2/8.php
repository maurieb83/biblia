<h3>SEGUNDO LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 8
</p><p>
1 Mientras tanto, Judas Macabeo y sus compañeros, entraban clandestinamente en los pueblos, convocaban a sus familiares y, atrayendo a los que se mantenían fieles al Judaísmo, lograron reunir seis mil hombres.
</p><p>
2 Ellos suplicaban al Señor que se dignara mirar a aquel pueblo pisoteado por todos, y se compadeciera del Templo profanado por los impíos.
</p><p>
3 Le rogaban que se apiadara de la Ciudad, devastada y a punto de ser arrasada, y escuchara la voz de la sangre que clamaba hacia él;
</p><p>
4 que se acordara de la inicua masacre de los niños inocentes y se vengara de las blasfemias proferidas contra su Nombre.
</p><p>
5 Una vez que se puso al frente de sus tropas, el Macabeo resultó invencible ante los paganos, porque la ira del Señor se había convertido en misericordia.
</p><p>
6 Atacando por sorpresa, incendiaba ciudades y poblados; ocupaba posiciones estratégicas y derrotaba a numerosos enemigos.
</p><p>
7 Aprovechaba sobre todo la noche como aliada para tales incursiones, y por todas partes se extendía la fama de su valor.
</p><p>
8 Al ver Filipo que Judas progresaba cada vez más y sus victorias eran cada día más frecuentes, escribió a Tolomeo, gobernador de Celesiria y Fenicia, para que prestara apoyo a la causa del rey.
</p><p>
9 Este designó inmediatamente a Nicanor, hijo de Patroclo, uno de sus principales Amigos, y lo envió al frente de no menos de veinte mil hombres de todas las naciones para acabar con toda la población de Judea. A su lado puso a Gorgias, general experimentado en la estrategia militar.
</p><p>
10 Nicanor calculaba que, con la venta de los judíos prisioneros, podría pagar el tributo del rey a los romanos, que ascendía a dos mil talentos.
</p><p>
11 Por eso envió en seguida un aviso a las ciudades de la costa, invitando a comprar esclavos judíos y prometiendo entregar noventa esclavos por talento, sin imaginarse el castigo que pronto le infligiría el Todopoderoso.
</p><p>
12 El anuncio de la expedición de Nicanor llegó a oídos de Judas. Cuando este comunicó a sus acompañantes que se acercaba al ejército enemigo,
</p><p>
13 los cobardes y los que desconfiaban de la justicia de Dios se dispersaron y buscaron refugio en otra parte.
</p><p>
14 Otros, vendían todo lo que les quedaba, y al mismo tiempo suplicaban al Señor que librara a los que el impío Nicanor ya tenía vendidos antes que comenzara la lucha.
</p><p>
15 Rogaban al Señor que hiciera esto, si no por ellos mismos, al menos por las Alianzas concedidas a sus padres y porque ellos llevaban su Nombre augusto y lleno de majestad.
</p><p>
16 Cuando el Macabeo reunió a sus seguidores, unos seis mil en total, los exhortó a que no se dejaran acobardar por los enemigos ni se amedrentaran ante la inmensa multitud de gente que venía a atacarlos injustamente. Los animó asimismo a que lucharan con entusiasmo,
</p><p>
17 teniendo bien presente los ultrajes perpetrados contra el Santuario, las violencias contra la Ciudad humillada y la supresión de las costumbres de sus antepasados.
</p><p>
18 «Ellos, les dijo, confían en sus armas y en su audacia, pero nosotros confiamos en el Dios todopoderoso que puede deshacer con un solo gesto no sólo a los que nos atacan, sino también al mundo entero».
</p><p>
19 Luego les enumeró todas las ayudas con que habían sido favorecidos sus antepasados, especialmente en tiempos de Senaquerib, cuando murieron ciento ochenta y cinco mil hombres.
</p><p>
20 Les recordó la batalla librada en Babilonia contra los gálatas, cuando ocho mil judíos entraron en acción junto con cuatro mil macedonios. En esa oportunidad, los macedonios se encontraban sin salida y los ocho mil judíos, gracias al exilio recibido del Cielo, derrotaron a ciento veinte mil enemigos y se apoderaron de un gran botín.
</p><p>
21 Con estas palabras, los enardeció para la lucha, y los animó a morir por las leyes y por la patria. Luego dividió el ejército en cuatro cuerpos,
</p><p>
22 y puso al frente de cada unidad a sus hermanos Simón, José y Jonatán, con mil quinientos hombres a las órdenes de cada uno.
</p><p>
23 También mandó a Eleazar que leyera en alta voz el Libro sagrado. Y finalmente, dándoles como santo y seña el grito «Auxilio de Dios», se lanzó él mismo a combatir contra Nicanor, al frente del primer cuerpo.
</p><p>
24 Teniendo como aliado al Todopoderoso, mataron a más de nueve mil enemigos, hirieron y dejaron fuera de combate a la mayor parte del ejército de Nicanor y obligaron a huir a todos los demás.
</p><p>
25 También se apoderaron del dinero de los que habían venido a comprarlos, y después de haberlos perseguido bastante tiempo, tuvieron que regresar, apremiados por la hora:
</p><p>
26 como era víspera de sábado, no pudieron continuar la persecución.
</p><p>
27 Una vez que recogieron las armas y se llevaron los despojos del enemigo, se pusieron a celebrar el sábado, bendiciendo y alabando una y otra vez al Señor, que los había salvado aquel día, concediéndoles así las primicias de su misericordia.
</p><p>
28 Pasado el sábado, distribuyeron parte del botín entre los damnificados, las viudas y los huérfanos, y se repartieron el resto entre ellos y sus hijos.
</p><p>
29 Después organizaron rogativas, pidiendo al Señor misericordioso que se reconciliara definitivamente con sus servidores.
</p><p>
30 En un combate contra las tropas de Timoteo y de Báquides, les causaron más de veinte mil bajas y en seguida se apoderaron de fortalezas muy importantes. Luego distribuyeron un cuantioso botín por partes iguales, entre ellos, los damnificados, los huérfanos, las viudas y también los ancianos.
</p><p>
31 Recogieron cuidadosamente las armas de los enemigos y las depositaron en lugares estratégicos, llevando a Jerusalén el resto del botín.
</p><p>
32 También mataron al jefe de la escolta de Timoteo, un hombre muy impío que había hecho mucho daño a los judíos.
</p><p>
33 Mientras celebraban la victoria en su patria, quemaron a los que habían incendiado las puertas sagradas, incluido Calístenes, que se había refugiado en una choza. Así él recibió el castigo merecido por su impiedad.
</p><p>
34 En cuanto al perversísimo Nicanor, que había traído miles de mercaderes para la venta de los judíos,
</p><p>
35 quedó humillado con el auxilio del Señor por los mismos que él despreciaba como los más viles. Despojado de sus lujosas vestiduras, solo y errante por los campos como un fugitivo, llegó a Antioquía con mucha más suerte que su ejército, que había sido destruido.
</p><p>
36 Y el que había pretendido pagar el tributo a los romanos con la venta de prisioneros de Jerusalén, pregonaba que los judíos tenían un Defensor y que eran invulnerables porque seguían las leyes prescritas por él.
</p>
