<h3>SEGUNDO LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 9
</p><p>
1 Por ese tiempo, Antíoco tuvo que retirarse precipitadamente de las regiones de Persia.
</p><p>
2 En efecto, después de haber entrado en la ciudad llamada Persépolis, intentó saquear el templo y apoderarse de la ciudad. Pero el pueblo se amotinó y se defendió con las armas. Antíoco, derrotado por la gente del país, tuvo que emprender una vergonzosa retirada.
</p><p>
3 Cuando estaba en Ecbátana, recibió la noticia de lo que le había sucedido a Nicanor y a las tropas de Timoteo.
</p><p>
4 Enfurecido a causa de esto, pensaba desquitarse con los judíos de la afrenta que le habían inferido los que le obligaron a emprender la retirada. Entonces ordenó al auriga que condujera el carro sin parar hasta el fin del trayecto. Pero, en realidad, ya era inminente el juicio del Cielo porque él había dicho lleno de arrogancia: «Al llegar allí, haré de Jerusalén un cementerio de judíos».
</p><p>
5 El Señor, que todo lo ve, el Dios de Israel, lo castigó con un mal incurable e invisible. Apenas pronunciadas estas palabras, sintió un intenso dolor intestinal con agudos retorcijones internos.
</p><p>
6 Todo esto era muy justo, porque él había atormentado las entrañas de los demás con tantos y tan refinados suplicios.
</p><p>
7 A pesar de esto, no cedía en lo más mínimo su arrogancia; por el contrario, siempre lleno de soberbia, y exhalando contra los judíos el fuego de su furor, mandaba acelerar la marcha. Pero mientras avanzaba velozmente, se cayó del carro y todos los miembros de su cuerpo quedaron lesionados por la violencia de la caída.
</p><p>
8 Aquel que poco antes, llevado de una jactancia sobrehumana, creía dictar órdenes a las olas del mar y pensaba pesar en la balanza las cimas de los montes, era llevado en camilla, después de haber caído en tierra. Así ponía de manifiesto a los ojos de todos el poder de Dios.
</p><p>
9 Su estado era tal que del cuerpo del impío brotaban los gusanos; estando vivo aún, la carne se le caía a pedazos, en medio de dolores y sufrimientos, y el ejército apenas podía soportar el hedor que emanaba de él.
</p><p>
10 A causa de ese olor insoportable, nadie podía llevar ahora al que poco antes se creía capaz de tocar los astros del cielo.
</p><p>
11 Sólo entonces, en aquel estado de postración, comenzó a ceder en su desmedida soberbia y a entrar en razón, por los dolores que se hacían cada vez más intensos a causa del castigo divino.
</p><p>
12 Como ni él mismo podía soportar su propio hedor, exclamó: «Es justo someterse a Dios y no creerse igual a él, siendo un simple mortal».
</p><p>
13 Aquel malvado rogaba al Soberano, de quien ya no alcanzaría misericordia, prometiendo
</p><p>
14 que declararía libre a la Ciudad santa, a la que antes se había dirigido rápidamente para arrasarla y convertirla en un cementerio;
</p><p>
15 que equipararía con los atenienses a todos aquellos judíos que había considerado dignos, no de una sepultura, sino de ser arrojados, junto con sus hijos, como pasto de las fieras y de las aves de rapiña;
</p><p>
16 que adornaría con los más hermosos presente el Templo santo que antes había saqueado; que devolvería con creces los objetos sagrados y que proveería con sus propios ingresos los fondos necesarios para los sacrificios,
</p><p>
17 y finalmente, que se haría judío y recorrería todos los lugares habitados, proclamando el poder de Dios.
</p><p>
18 Como sus dolores no se calmaban de ninguna forma, porque el justo juicio de Dios se había abatido sobre él, y desesperando de su salud, escribió a los judíos, en tono de súplica, la carta que se transcribe a continuación:
</p><p>
19 «Antíoco, rey y general, saluda a los judíos, ciudadanos respetables, deseándoles felicidad, salud y prosperidad.
</p><p>
20 Si ustedes y sus hijos se encuentran bien y sus asuntos marchan conforme a sus deseos, damos inmensas gracias por eso.
</p><p>
21 En cuanto a mí, que estoy postrado sin fuerzas en mi lecho, conservo un afectuoso recuerdo de ustedes y de sus buenos sentimientos. Cuando regresaba de las regiones de Persia, contraje una penosa enfermedad, y he creído conveniente preocuparme por la seguridad de todos.
</p><p>
22 No es que desespere de mi salud: al contrario, tengo mucha confianza de que podré recuperarme de mi enfermedad.
</p><p>
23 Sin embargo he tenido en cuenta que cuando mi padre emprendió una campaña a las regiones de la meseta, designó a su futuro sucesor.
</p><p>
24 Así, si sucedía algo imprevisto o llegaba una noticia desagradable, los habitantes de las provincias no se perturbarían, sabiendo de antemano a quién quedaba confiado el gobierno.
</p><p>
25 He tenido en cuenta, además, que los soberanos de los países vecinos a mi reino están al acecho, esperando cualquier ocasión favorable. Por eso, he decidido designar rey a mi hijo Antíoco, a quien muchas veces, durante mis campañas a las provincias de la meseta, ya he presentado y recomendado a la mayor parte de ustedes.
</p><p>
26 También a él le he escrito la carta que aquí se adjunta. Y ahora les pido encarecidamente que recuerden mis beneficios públicos y privados, y perseveren en sus buenos sentimientos hacia mí y hacia mi hijo.
</p><p>
27 Porque estoy persuadido de que él seguirá con moderación y humanidad el programa que yo me he trazado, y así se entenderá bien con ustedes».
</p><p>
28 Así murió aquel criminal y blasfemo. Padeciendo los peores sufrimientos, como los había hecho padecer a otros, terminó su vida en un país extranjero, en medio de las montañas y en el más lamentable infortunio.
</p><p>
29 Filipo, su compañero de infancia, conducía el cadáver, pero no fiándose del hijo de Antíoco, se dirigió a Egipto, donde reinaba Tolomeo Filométor.
</p>
