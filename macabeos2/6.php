<h3>SEGUNDO LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 6
</p><p>
1 Poco tiempo después, el rey envió a un consejero ateniense para que obligara a los judíos a abandonar las costumbres de sus padres y a no vivir conforme a las leyes de Dios;
</p><p>
2 a profanar el Templo de Jerusalén, dedicándolo a Júpiter Olímpico, y a dedicar el del monte Garizim a Júpiter Hospitalario, conforme a la idiosincrasia de los habitantes de aquel lugar.
</p><p>
3 Este recrudecimiento del mal se hacía penoso e insoportable para todos.
</p><p>
4 El Templo se llenó del desenfreno y las orgías de los paganos, que se divertían con prostitutas y tenían relaciones con mujeres en los atrios sagrados, e incluso, introducían allí objetos prohibidos.
</p><p>
5 El altar estaba repleto de ofrendas ilegítimas, proscritas por la Ley.
</p><p>
6 No se podía observar el sábado, y ni siquiera declararse judío.
</p><p>
7 Por el contrario, todos se veían penosamente forzados a participar del banquete ritual con que se conmemoraba cada mes el nacimiento del rey; y cuando llegaban las fiestas dionisíacas, se los obligaba a seguir el cortejo de Dionisios, coronados de guirnaldas.
</p><p>
8 Por instigación de Tolomeo, se publicó un decreto dirigido a las ciudades griegas de los alrededores, obligándolas a que procedieran de la misma manera contra los judíos y los hicieran participar en los banquetes rituales.
</p><p>
9 Además, se ordenaba degollar a los que rehusaran adoptar las costumbres griegas. Todo esto hacía prever la inminente calamidad.
</p><p>
10 Dos mujeres fueron delatadas por haber circuncidado a sus hijos, y después de hacerlas pasear públicamente por la ciudad con sus niños colgados del pecho, las precipitaron desde lo alto de la muralla.
</p><p>
11 Otros, que se habían reunido en las cavernas cercanas para celebrar ocultamente el día sábado, fueron denunciados a Filipo y quemados todos juntos, ya que no se habían atrevido a defenderse por respeto a la santidad de aquel día.
</p><p>
12 Ruego a los lectores de este libro que no se dejen impresionar por estas calamidades. Piensen más bien que estos castigos no han sucedido para la ruina, sino para la educación de nuestro pueblo.
</p><p>
13 Porque es una señal de gran benevolencia no tolerar por mucho tiempo a los impíos, sino infligirles rápidamente un castigo.
</p><p>
14 Antes de castigar a las otras naciones, el Soberano espera pacientemente que colmen la medida de sus pecados; pero con nosotros ha decidido obrar de otra manera,
</p><p>
15 para no tener que castigarnos más tarde, cuando nuestros pecados hayan llegado al colmo.
</p><p>
16 Por eso nunca retira de nosotros su misericordia, y aunque corrige a su pueblo por medio de la adversidad, no lo abandona.
</p><p>
17 Que esto sirva solamente para recordar ciertas verdades. Y después de estas consideraciones, prosigamos la narración.
</p><p>
18 Eleazar, uno de los principales maestros de la Ley, de edad muy avanzada y de noble aspecto, fue forzado a abrir la boca para comer carne de cerdo.
</p><p>
19 Pero él, prefiriendo una muerte honrosa a una vida infame, marchó voluntariamente al suplicio,
</p><p>
20 después de haber escupido la carne, como deben hacerlo los que tienen el valor de rechazar lo que no está permitido comer, ni siquiera por amor a la vida.
</p><p>
21 Los que presidían este banquete ritual contrario a la Ley, como lo conocían desde hacía mucho tiempo, lo llevaron aparte y le rogaron que hiciera traer carne preparada expresamente para él y que le estuviera permitido comer. Asimismo le dijeron que fingiera comer la carne del sacrificio, conforme a la orden del rey.
</p><p>
22 Obrando de esa manera, se libraría de la muerte y sería tratado humanitariamente por su antigua amistad con ellos.
</p><p>
23 Pero él, tomando una noble resolución, digna de su edad, del prestigio de su vejez, de sus veneradas canas, de la vida ejemplar que había llevado desde su infancia y, sobre todo, de la santa legislación establecida por Dios, se mostró consecuentemente consigo mismo, pidiendo que lo enviaran de inmediato a la morada de los muertos.
</p><p>
24 «A nuestra edad, decía, no está bien fingir. De lo contrario, muchos jóvenes creerán que Eleazar, a los noventa años, se ha pasado a las costumbres paganas.
</p><p>
25 Entonces también ellos, a causa de mi simulación y de mi apego a lo poco que me resta de vida, se desviarán por culpa mía, y yo atraeré sobre mi vejez la infamia y el deshonor.
</p><p>
26 Porque, aunque ahora me librara del castigo de los hombres, no podría escapar, ni vivo ni muerto, de las manos del Todopoderoso.
</p><p>
27 Por eso, me mostraré digno de mi vejez entregando mi vida valientemente.
</p><p>
28 Así dejaré a los jóvenes un noble ejemplo, al morir con entusiasmo y generosidad por las venerables y santas leyes». Dicho esto, se encaminó resueltamente al suplicio.
</p><p>
29 Al oír estas palabras, que consideraban una verdadera locura, los que lo conducían cambiaron en crueldad la benevolencia que antes le habían demostrado.
</p><p>
30 Pero él, a punto ya de morir bajo los golpes, dijo entre gemidos: «El Señor, que posee el santo conocimiento, sabe muy bien que, pudiendo librarme de la muerte, soporto crueles dolores en mi cuerpo azotado; pero mi alma los padece gustosamente por temor a él».
</p><p>
31 De este modo, Eleazar dejó al morir, no sólo a los jóvenes, sino a la nación entera, su propia muerte como ejemplo de generosidad y como recuerdo de virtud.
</p>
