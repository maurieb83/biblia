<h3>SEGUNDO LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 3
</p><p>
1 Cuando la Ciudad santa se encontraba en completa paz y las leyes se observaban a la perfección, gracias a la piedad y a la rectitud del Sumo Sacerdote Onías,
</p><p>
2 solía suceder que hasta los mismos reyes honraban el Santuario y lo enriquecían con espléndidos regalos,
</p><p>
3 hasta tal punto que Seleuco, rey de Asia, mantenía con sus propios recursos todas las expensas para la celebración de los sacrificios.
</p><p>
4 Pero un tal Simón, de la familia de Bilgá, que había sido designado administrador del Templo, tuvo diferencias con el Sumo Sacerdote en lo relativo al control de los mercados de la ciudad.
</p><p>
5 Como no lograba imponerse a Onías, acudió a Apolonio de Tarso, que era entonces gobernador de Celesiria y de Fenicia,
</p><p>
6 y le comunicó que el tesoro de Jerusalén estaba repleto de incontables riquezas, tanto que la cantidad de dinero era incalculable y muy superior al presupuesto de los sacrificios, y nada impedía que fuera puesto a disposición del rey.
</p><p>
7 En la audiencia con el rey, Apolonio lo puso al tanto de las riquezas que la habían sido denunciadas, y el rey designó a Heliodoro, su encargado de negocios, y lo envió con la orden de incautarse de aquellos tesoros.
</p><p>
8 Heliodoro emprendió inmediatamente el viaje, fingiendo que inspeccionaba las ciudades de Celesiria y Fenicia, aunque su intención era cumplir los planes del rey.
</p><p>
9 Al llegar a Jerusalén, fue recibido amistosamente por el Sumo Sacerdote de la ciudad, al que informó sobre la denuncia que se había hecho y le manifestó el motivo de su presencia, preguntándole si todo eso era verdad.
</p><p>
10 El Sumo Sacerdote le explicó que se trataba de unos depósitos pertenecientes a las viudas y a los huérfanos,
</p><p>
11 y que una parte pertenecía a Hircano, hijo de Tobías, que era un personaje de posición muy elevada. Contrariamente a la calumniosa denuncia de Simón, el total ascendía a cuatrocientos talentos de plata y doscientos de oro.
</p><p>
12 Y no se podía defraudar a los que habían depositado su confianza en la santidad de ese Lugar y en la inviolable majestad de aquel Templo venerado en todo el mundo.
</p><p>
13 Pero Heliodoro, siguiendo las órdenes del rey, sostenía inflexiblemente que aquellas riquezas debían ser confiscadas en beneficio del tesoro real.
</p><p>
14 En la fecha fijada, Heliodoro procedió a realizar el inventario de los bienes, con gran consternación de toda la ciudad:
</p><p>
15 los sacerdotes, postrados ante el altar con sus ornamentos sagrados, suplicaban al Cielo, que había dictado la ley sobre los bienes en depósito, rogándole que los conservara intactos para quienes los había depositado.
</p><p>
16 A uno se le partía el alma con solo mirar el rostro del Sumo Sacerdote, porque su aspecto y su palidez revelaban la angustia de su alma.
</p><p>
17 El miedo y el temblor estremecían todo su cuerpo, descubriendo a quienes lo observaban el sufrimiento de su corazón.
</p><p>
18 Además, algunos salían de sus casas en grupos para hacer rogativas públicas, a causa del inminente ultraje a que se vería expuesto el Santuario;
</p><p>
19 las mujeres, ceñidas de cilicio debajo de los senos, se aglomeraban en las calles; las más jóvenes, habitualmente recluidas, corrían, unas a las puertas, otras a los muros, y otras, se asomaban por las ventanas.
</p><p>
20 Todas elevaban sus plegarias con los brazos extendidos hacia el Cielo.
</p><p>
21 Daba pena ver a la muchedumbre postrada desordenadamente, y al Sumo Sacerdote lleno de ansiedad y de angustia.
</p><p>
22 Mientras ellos rogaban al Señor todopoderoso que guardara intactos los bienes depositados, dando plena seguridad a sus dueños.
</p><p>
23 Heliodoro, por su parte, comenzó a ejecutar lo que se había propuesto.
</p><p>
24 Pero cuando ya se encontraba con su escolta junto al Tesoro, el Soberano de los espíritus y de toda Potestad se manifestó tan esplendorosamente que todos los que se habían atrevido a venir con él, heridos por el poder de Dios, quedaron sin fuerzas y acobardados.
</p><p>
25 Porque se les apareció un caballo montado por un temible jinete y ricamente enjaezado, el cual, arrojándose con ímpetu, levantó contra Heliodoro sus cascos delanteros. El jinete aparecía cubierto con una armadura de oro.
</p><p>
26 También se le aparecieron otros dos jóvenes de extraordinario vigor, resplandecientes por su hermosura y vestidos espléndidamente: ellos se pusieron uno a cada lado y lo azotaban sin cesar, moliéndolo a golpes.
</p><p>
27 Heliodoro cayó en tierra, envuelto en una densa oscuridad, y en seguida lo recogieron y lo sacaron en una camilla.
</p><p>
28 Así llevaban ahora, incapaz de valerse por sí mismo, al que poco antes había entrado al Tesoro, acompañado de numeroso séquito y de toda su escolta. Y todos reconocieron claramente la soberanía de Dios.
</p><p>
29 Mientras él yacía derribado por la fuerza divina, sin habla y sin esperanza de salvación,
</p><p>
30 los judíos bendecían al Señor, que había glorificado su propio Lugar. El Templo, que poco antes había estado lleno de miedo y consternación, desbordaba ahora de alegría y de júbilo por la manifestación del Señor todopoderoso.
</p><p>
31 En seguida, algunos de los acompañantes de Heliodoro rogaron a Onías que invocara al Altísimo a fin de que perdonara la vida al que ya estaba a punto de expirar.
</p><p>
32 El Sumo Sacerdote, temiendo que el rey sospechara que los judíos habían atentado contra Heliodoro, ofreció un sacrificio por su curación.
</p><p>
33 Mientras el Sumo Sacerdote ofrecía el sacrificio de expiación, se aparecieron otra vez a Heliodoro los mismos jóvenes, cubiertos con las mismas vestiduras y, puestos de pie, le dijeron: «Da muchas gracias al Sumo Sacerdote Onías, porque por su intercesión el Señor te concede la vida.
</p><p>
34 Y ahora tú, que has sido castigado por el Cielo, anuncia a todos la grandeza del poder de Dios». Dicho esto, desaparecieron.
</p><p>
35 Heliodoro, después de ofrecer un sacrificio al Señor y de orar largamente al que le había concedido la vida, se despidió de Onías y volvió con sus tropas adonde estaba el rey.
</p><p>
36 Y daba testimonio delante de todos de las obras del gran Dios, que él había contemplado con sus propios ojos.
</p><p>
37 Cuando el rey preguntó a Heliodoro a quién convendría enviar otra vez a Jerusalén, él respondió:
</p><p>
38 «Si tienes algún enemigo o alguien que conspira contra el gobierno, envíalo allá y volverá molido a golpes, si es que logra salvar su vida. Porque te aseguro que una fuerza divina rodea aquel lugar;
</p><p>
39 el que tiene su morada en el cielo vela por él y lo protege, y a todos los que se acercan con malas intenciones los castiga con la muerte».
</p><p>
40 Así terminaron los hechos referentes a Heliodoro y a la preservación del Tesoro.
</p>
