<h3>SEGUNDO LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 5
</p><p>
1 Alrededor de ese tiempo, Antíoco preparaba su segunda expedición contra Egipto.
</p><p>
2 Y sucedió que por espacio de unos cuarenta días aparecieron en toda la ciudad, corriendo por los aires, jinetes vestidos de oro, tropas armadas divididas en escuadrones, espadas desenvainadas,
</p><p>
3 regimientos de caballería en orden de batalla, ataques e incursiones de una y otra parte, movimientos de escudos, nubes de lanzas, disparos de flechas, destellos de guarniciones de oro y corazas de toda clase.
</p><p>
4 Ante esto, todos rogaban que aquella aparición fuera señal de buen augurio.
</p><p>
5 Al difundirse el falso rumor de que Antíoco había muerto, Jasón lanzó un ataque imprevisto contra la ciudad con no menos de mil hombres. Como los que estaban en la muralla fueron rechazados y la ciudad al fin fue tomada, Menelao se refugió en la Acrópolis.
</p><p>
6 Jasón masacró sin piedad a sus propios conciudadanos, sin caer en la cuenta de que una victoria sobre ellos era el mayor de los desastres: ¡él se imaginaba que ganaba trofeos de sus enemigos y no a sus propios compatriotas!
</p><p>
7 Sin embargo, no logró adueñarse del poder y finalmente, sin haber conseguido otra cosa que su propio fracaso, tuvo que huir de nuevo al país de Amán.
</p><p>
8 Su conducta perversa tuvo un final desastroso. Acusado ante Aretas, soberano de los árabes, huyó de ciudad en ciudad; perseguido por todos, aborrecido como transgresor de las leyes y abominado como verdugo de su patria y de sus conciudadanos, fue a parar a Egipto.
</p><p>
9 El que había desterrado a muchos de su patria murió en el destierro, mientras se dirigía a Lacedemonia con la esperanza de encontrar un refugio, apelando a su origen común.
</p><p>
10 El que había dejado a muchos sin sepultura, no tuvo quien lo llorara; nadie le tributó honras fúnebres y no encontró sitio en el sepulcro de sus antepasados.
</p><p>
11 Cuando el rey se enteró de lo ocurrido, llegó a la conclusión de que Judea tramaba su independencia. Entonces, volvió de Egipto, enfurecido como una fiera, tomó la ciudad por las armas,
</p><p>
12 y mandó a los soldados que hirieran sin compasión a todos los que cayeran en sus manos y degollaran a los que intentaran refugiarse en las casas.
</p><p>
13 Fue una verdadera matanza de jóvenes y ancianos, una masacre de muchachos, mujeres y niños, una carnicería de muchachas y niños de pecho.
</p><p>
14 En sólo tres días hubo ochenta mil víctimas: cuarenta mil fueron muertos y otros tantos vendidos como esclavos.
</p><p>
15 No contento con esto, Antíoco tuvo la osadía de entrar en el Templo más santo de toda la tierra, llevando como guía a Menelao, el traidor de las leyes y de la patria.
</p><p>
16 Con sus manos impuras tomó los objetos sagrados, y arrebató con manos sacrílegas los presentes hechos por otros reyes para realzar la gloria y el honor de ese Lugar.
</p><p>
17 El se engreía porque no tenía en cuenta que el Señor se había irritado por poco tiempo a causa de los pecados cometidos por los habitantes de la ciudad, y por eso había apartado su mirada del Lugar.
</p><p>
18 Si ellos no se hubieran dejado dominar por tantos pecados, también Antíoco habría sido golpeado y hecho desistir de su atrevimiento apenas ingresó en el Santuario, como lo había sido Heliodoro cuando fue enviado por el rey Seleuco para inspeccionar el Tesoro.
</p><p>
19 Pero el Señor no eligió al pueblo a causa del pueblo.
</p><p>
20 Por eso, el mismo Lugar, después de haber participado de las desgracias del pueblo, también participó de su restauración y, habiendo sido abandonado en el tiempo de la ira del Todopoderoso, fue de nuevo restaurado con toda su gloria, cuando el gran Soberano se reconcilió con él.
</p><p>
21 Antíoco, después de haber sacado del Templo mil ochocientos talentos, partió en seguida para Antioquía, creyendo presuntuosamente que era capaz de navegar por la tierra y caminar por el mar: tal era la arrogancia de su corazón.
</p><p>
22 Pero antes, dejó prefectos para que hicieran daño al pueblo. En Jerusalén, dejó a Filipo, de origen frigio, un hombre de costumbres más bárbaras que el que lo había designado;
</p><p>
23 en el monte Garizim, dejó a Andrónico, y además de estos, a Menelao, que superaba a todos los otros en maldad, por el odio que tenía a sus compatriotas judíos.
</p><p>
24 Antíoco envió a Apolonio, jefe de los mercenarios de Misia, con un ejército de veintidós mil soldados, dándole la orden de degollar a todos los hombres adultos y de vender a las mujeres y a los niños.
</p><p>
25 Una vez que Apolonio llegó a Jerusalén, fingiendo que venía en sonde paz, esperó hasta el santo día del sábado. Y mientras los judíos observaban el descanso, mandó a sus tropas que hicieran un desfile militar.
</p><p>
26 Entonces hizo pasar al filo de la espada a todos los que habían salido a ver el espectáculo. Luego de una batida por la ciudad con los soldados armados y mató a una gran muchedumbre.
</p><p>
27 Mientras tanto, Judas, llamado el Macabeo, formó un grupo de unos diez hombres y se retiró al desierto. Allí vivía entre las montañas con sus compañeros, como las fieras salvajes, sin comer nada más que hierbas, para no incurrir en ninguna impureza.
</p>
