<h3>SEGUNDO LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 2
</p><p>
1 Consta en los archivos que el profeta Jeremías ordenó a los deportados que tomaran fuego, como ya se ha indicado,
</p><p>
2 y que el profeta, después de entregarles la Ley, les mandó que no olvidaran los preceptos del Señor, ni se desviaran al ver los ídolos de oro y plata y la pompa que los rodeaba.
</p><p>
3 Entre otras recomendaciones similares, los exhortaba una y otra vea a que no apartaran la Ley de sus corazones.
</p><p>
4 Se decía en el escrito cómo el profeta, advertido por un oráculo, mandó llevar con él la Carpa y el Arca, y cómo partió hacia la montaña donde Moisés había subido para contemplar la herencia de Dios.
</p><p>
5 Al llegar, Jeremías encontró una caverna: allí introdujo la Carpa, el Arca y el altar del incienso y clausuró la entrada.
</p><p>
6 Algunos de sus acompañantes volvieron para poner señales en el camino, pero no pudieron encontrarlo.
</p><p>
7 Y cuando Jeremías se enteró de esto, los reprendió, diciéndoles: «Ese lugar quedará ignorado hasta que Dios tenga misericordia de su pueblo y lo reúna.
</p><p>
8 Entonces el Señor pondrá todo de manifiesto, y aparecerá la gloria del Señor y la nube, como apareció en tiempos de Moisés y cuando Salomón oró para que el Santuario fuera solemnemente consagrado».
</p><p>
9 Además, se hacía constar que Salomón, lleno del espíritu de sabiduría, ofreció el sacrificio de la dedicación y la terminación del Templo.
</p><p>
10 Así como Moisés oró al Señor y bajó fuego del Cielo, que devoró las ofrendas del sacrificio, así también cuando oró Salomón, bajó fuego y consumió la víctima.
</p><p>
11 Moisés había dicho: «Por no haber sido comida, la oblación ofrecida por el pecado ha sido destruida».
</p><p>
12 De la misma manera, Salomón celebró los ocho días de fiesta.
</p><p>
13 Los mismos hechos se narraban en los archivos y en las Memorias de Nehemías, donde se relataba, además, cómo este fundó una biblioteca, en la que reunió los libros que tratan de los reyes, los libros de los profetas y los de David, así como también las cartas de los reyes sobre las ofrendas.
</p><p>
14 Del mismo modo, Judas reunió todos los escritos dispersos a causa de las guerras que hemos padecido, los cuales están ahora en poder nuestro.
</p><p>
15 Si ustedes necesitan alguno de estos escritos, manden a alguien que los venga a buscar.
</p><p>
16 Les escribimos esto, próximos a celebrar la purificación del Templo; también ustedes hagan lo posible por celebrar estos días.
</p><p>
17 El Dios que salvó a todo su pueblo y concedió a todos la herencia, el reino, el sacerdocio y la santificación,
</p><p>
18 como lo había prometido por medio de la Ley, ese mismo Dios –así lo esperamos– tendrá compasión de nosotros y nos reunirá en el Santuario, desde todas las partes de la tierra. Porque él nos ha librado de graves males y ha purificado el Lugar santo.
</p><p>
19 La historia de Judas Macabeo y sus hermanos, de la purificación del gran Templo y de la dedicación del altar,
</p><p>
20 así como las guerras contra Antíoco Epífanes y su hijo Eupátor,
</p><p>
21 y las manifestaciones celestiales a los que combatieron valerosamente en favor del Judaísmo –los cuales, siendo tan pocos, saquearon todo el país, expulsaron las hordas extranjeras,
</p><p>
22 recuperaron el Santuario célebre en todo el mundo, liberaron la ciudad y restablecieron las leyes que estaban en peligro de ser abolidas, porque el Señor, en su gran benignidad, se mostró propicio con ellos –
</p><p>
23 todo esto ha sido expuesto en cinco libros por Jasón de Cirene, y nosotros intentaremos resumirlo en uno solo.
</p><p>
24 En efecto, teniendo en cuenta la enorme cantidad de cifras y la dificultad que encuentran, por la amplitud de la materia, los que desean sumergirse en los relatos de la historia,
</p><p>
25 hemos procurado ofrecer un relato ameno para los aficionados a la lectura, práctico para los que quieren grabar los hechos en su memoria y útil para todos indistintamente.
</p><p>
26 Para nosotros, que hemos asumido la penosa tarea de hacer este resumen, la obra no ha sido fácil, sino que nos ha costado muchos sudores y desvelos,
</p><p>
27 como no es cosa fácil preparar un banquete, tratando de complacer a todos. Sin embargo, soportamos con gusto esta molestia para utilidad de muchos,
</p><p>
28 dejando al autor el examen detallado de cada hecho, para esforzarnos nosotros por seguir las reglas de un resumen detallado de cada hecho, para esforzarnos nosotros por seguir las reglas de un resumen.
</p><p>
29 Porque así como al arquitecto de una casa nueva, le corresponde preocuparse de toda la construcción, en tanto que los decoradores y pintores sólo se ocupan de la ornamentación, pienso que lo mismo sucede con nosotros:
</p><p>
30 al historiador le compete profundizar y analizar las ideas y examinar cada cosa en detalle:
</p><p>
31 pero al que se propone resumir los hechos, se le permite hacer una síntesis de la obra, omitiendo tratar el tema en forma exhaustiva.
</p><p>
32 Comencemos, entonces, la narración sin alargar tanto los preliminares, porque sería absurdo extenderse en la introducción y ser breve en la historia misma.
</p>
