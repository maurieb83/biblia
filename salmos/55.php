</p><p>
mi amigo y confidente,
</p><p>
15 con quien vivía en dulce intimidad:
</p><p>
juntos íbamos entre la multitud
</p><p>
a la Casa de Dios!
</p><p>
16 Que la muerte los sorprenda,
</p><p>
que bajen vivos al Abismo,
</p><p>
