<h3>JUECES</h3>
<p>Capítulo 13
</p><p>
El anuncio del nacimiento de Sansón
</p><p>
1 los israelitas volvieron a hacer lo que es malo a los ojos del Señor, y el Señor los entregó en manos de los filisteos durante cuarenta años.
</p><p>
2 Había un hombre de Sorá, del clan de los danitas, que se llamaba Manóaj. Su mujer era estéril y no tenía hijos.
</p><p>
3 El Angel del Señor se apareció a la mujer y le dijo: «Tú eres estéril y no has tenido hijos, pero vas a concebir y a dar a luz un hijo.
</p><p>
4 Ahora, deja de beber vino o cualquier bebida fermentada, y no comas nada impuro.
</p><p>
5 Porque concebirás y darás a luz un hijo. La navaja nunca pasará por su cabeza, porque el niño estará consagrado a Dios desde el seno materno. El comenzará a salvar a Israel del poder de los filisteos».
</p><p>
6 La mujer fue a decir a su marido: «Un hombre de Dios ha venido a verme. Su aspecto eran tan imponente, que parecía un ángel de Dios. Yo no le pregunté de dónde era, ni él me dio a conocer su nombre.
</p><p>
7 Peor me dijo: «Concebirás y darás a luz un hijo. En adelante, no bebas vino, ni comas nada impuro, porque el niño estará consagrado a Dios desde el seno de su madres hasta el día de su muerte».
</p><p>
8 Entonces Manóaj oró a Dios, diciendo: «Te ruego, Señor, que vuelva el hombre que tú nos has enviado y nos indique qué debemos hacer con el niño, cuando haya nacido».
</p><p>
9 Dios escuchó la voz de Manóaj, y el Angel de Dios se presentó otra vez a la mujer, mientras se encontraba en el campo y su marido no estaba con ella.
</p><p>
10 La mujer corrió en seguida a avisarle a su marido: «Se me apareció el hombre que vino a verme el otro día».
</p><p>
11 Manóaj se levantó y fue detrás de su mujer. Y al llegar adonde estaba el hombre, le dijo: «¿Eres tú el que le ha hablado a esta mujer?». «Sí, soy yo», respondió él.
</p><p>
12 Entonces Manóaj le preguntó: «Cuando suceda lo que tú has dicho, ¿qué forma de vida tendrá que llevar el niño y cuál será su conducta?».
</p><p>
13 El Angel del Señor dijo a Manóaj: «El tendrá que abstenerse de todo lo que le dije a esta mujer:
</p><p>
14 no probará el fruto de la vid, ni beberá vino ni bebida fermentada; no comerá nada impuro, y observará todo lo que le he mandado a esta mujer».
</p><p>
15 Manóaj dijo al Angel del Señor: «Quédate aquí y te prepararemos un cabrito».
</p><p>
16 Pero el Angel del Señor le respondió: «Aunque me obligues a quedarme, no probaré tu comida. Si quieres hacer un holocausto, ofrécelo al Señor. Manóaj no se había dado cuente de que aquel hombre era el Angel del Señor.
</p><p>
17 Entonces Manóaj le preguntó: «¿Cuál es tu nombre, para que podamos honrarte cuando suceda lo que tú has dicho?».
</p><p>
18 Peor el Angel del Señor dijo: «¿Por qué me preguntas mi nombre? El es misterioso».
</p><p>
19 Manóaj tomó el cabrito y la oblación, y los ofreció sobre la roca en holocausto al Señor, que es misterioso en sus acciones. Manóaj y su mujer estaban mirando.
</p><p>
20 Y cuando la llama subía del altar hacia el cielo, el Angel del Señor subía en la llama del altar, a la vista de Manóaj y de su mujer, que cayeron con el rostro en tierra.
</p><p>
21 El Angel del Señor ya no se volvió a aparecer a Manóaj ni a su mujer. Entonces Manóaj reconoció que aquel hombre era el Angel del Señor,
</p><p>
22 y dijo a su mujer: «¡Vamos a morir, porque hemos visto a Dios!»
</p><p>
23 Pero su mujer le respondió: «Si el Señor quisiera hacernos morir no habría aceptado de nuestras manos el holocausto y la oblación; tampoco nos habría mostrado todo esto, no nos habría comunicado una cosa así».
</p><p>
24 La mujer dio a luz un hijo y lo llamó Sansón. El niño creció y el Señor lo bendijo.
</p><p>
25 Y el espíritu del Señor comenzó a actuar sobre él en el Campamento de Dan, entre Sorá y Estaol.
</p>
