<h3>PRIMER LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 5
</p><p>
1 Cuando las naciones vecinas supieron que había sido reconstruido el altar y restaurado como antes el Santuario, se irritaron profundamente
</p><p>
2 y decidieron acabar con los descendientes de Jacob que vivían entre ellos. Por eso comenzaron a matar y exterminar a mucha gente del pueblo.
</p><p>
3 Judas hizo la guerra contra los descendientes de Esaú que habitaban en Idumea, en la región de Acrabatena, porque tenían asediados a los israelitas. Les infligió una gran derrota, cometiéndolos y apoderándose de sus despojos.
</p><p>
4 Luego se acordó de la maldad de los descendientes de Beán; que eran una trampa y un obstáculo para el pueblo por las emboscadas que le tendían en los caminos.
</p><p>
5 Los obligó a encerrarse en sus torres, los asedió y los consagró al exterminio total, prendiendo fuego a esas torres con todos los que estaban dentro.
</p><p>
6 Luego atacó a los amonitas, y allí encontró un fuerte ejército y una población numerosa cuyo jefe era Timoteo.
</p><p>
7 Después de muchos combates, los desbarató y los deshizo.
</p><p>
8 También ocupó Iazer y sus poblados, y regresó a Judea.
</p><p>
9 Los pueblos de Galaad se coaligaron contra los israelitas que vivían en su territorio, para exterminarlos. Pero ellos se refugiaron en la fortaleza de Datemá,
</p><p>
10 desde donde enviaron una carta a Judas y a sus hermanos, diciéndoles: «Los pueblos que nos rodean se coaligaron para exterminarnos;
</p><p>
11 ahora se preparan para venir a tomar la fortaleza donde nos hemos refugiado, y Timoteo está al frente de su ejército.
</p><p>
12 Ven en seguida a librarnos de sus manos, porque muchos de entre nosotros ya han caído;
</p><p>
13 todos nuestros hermanos que vivían en el país de Tobías han sido matados, sus mujeres y sus hijos fueron llevados cautivos y sus bienes han sido robados. Allí han muerto unos mil hombres».
</p><p>
14 Cuando todavía estaban leyendo la carta, llegaron otros mensajeros de Galilea, con las vestiduras rasgadas, trayendo esta noticia:
</p><p>
15 «Los habitantes de Tolemaida, de Tiro, de Sidón y de toda la Galilea de los extranjeros se han coaligado para acabar con nosotros».
</p><p>
16 Apenas Judas y el pueblo oyeron estas noticias, reunieron una gran asamblea para deliberar sobre lo que debían hacer en favor de sus hermanos que se encontraban en un aprieto, amenazados por sus enemigos.
</p><p>
17 Judas dijo a su hermano Simón: «Elige algunos hombres y ve a librar a tus hermanos de Galilea; mi hermano Jonatán y yo iremos a la región de Galaad».
</p><p>
18 Dejó para defender a Judea a José, hijo de Zacarías, y a Azarías, jefe del pueblo, con el resto del ejército,
</p><p>
19 dándoles esta orden: «Tomen el mando de estas tropas, pero no entren en batalla con los paganos hasta que nosotros volvamos».
</p><p>
20 Se le asignaron tres mil hombres a Simón para la campaña de Galilea y ocho mil a Judas para la de Galaad.
</p><p>
21 Simón partió para Galilea y luego de librar muchos combates con los paganos, los derrotó, los obligó a huir
</p><p>
22 y los persiguió hasta las puertas de Tolemaida. Allí sucumbieron unos tres mil hombres y Judas se apoderó del botín.
</p><p>
23 Luego tomó consigo a los judíos de Galilea y de Arbatá, con sus mujeres, sus hijos y todos sus bienes, y en medio de una gran alegría los llevó a Judea.
</p><p>
24 Judas Macabeo y su hermano Jonatán, por su parte, atravesaron el Jordán y caminaron tres días por el desierto.
</p><p>
25 Allí se encontraron con los nabateos, que los recibieron amistosamente y los pusieron al tanto de lo que les ocurría a sus hermanos de la región de Galaad;
</p><p>
26 muchos de ellos se encontraban prisioneros en Bosorá y Bosor, en Alemá, Casfó, Maqued y Carnain, que eran ciudades fuertes e importantes;
</p><p>
27 también había prisioneros en las demás ciudades de Galaad, y sus enemigos tomaban posiciones para atacar las fortalezas al día siguiente, a fin de apoderarse de ellos y exterminarlos a todos de una sola vez.
</p><p>
28 Inmediatamente, Judas dio vuelta con su ejército y se dirigió por el desierto hacia Bosorá, ocupó la ciudad y después de pasar al filo de la espada a todos los varones, la saqueó por completo y la incendió.
</p><p>
29 Partió de allí por la noche y avanzó hasta la fortaleza.
</p><p>
30 Al llegar el día, los judíos divisaron una muchedumbre innumerable que levantaba escaleras y empalizadas para tomar la fortaleza y había pasado a la ofensiva.
</p><p>
31 Al ver que el ataque ya había comenzado y que el griterío de la ciudad y el sonido de las trompetas subía hasta el cielo,
</p><p>
32 Judas dijo a sus hombres: «¡Luchen hoy por nuestros hermanos!».
</p><p>
33 Luego los ordenó en tres columnas y los hizo avanzar por detrás del enemigo, tocando las trompetas y orando a gritos.
</p><p>
34 Las tropas de Timoteo, apenas se enteraron que era el Macabeo, huyeron ante él. Judas les infligió una gran derrota, y ese día dejaron tendidos unos ocho mil hombres.
</p><p>
35 Luego se volvió contra Alemá; la atacó, la ocupó y, después de matar a todos los varones, la saqueó y la incendió.
</p><p>
36 Partiendo de allí, se apoderó de Casfó, Maqued, Bosor y de las demás ciudades de Galaad.
</p><p>
37 Después de estos acontecimientos, Timoteo reunió un nuevo ejército y acampó frente a Rafón, al otro lado del torrente.
</p><p>
38 Judas mandó a explorar el campamento y le dieron este informe: «Todas las naciones vecinas se han unido a Timoteo y forman un ejército muy numeroso.
</p><p>
39 Además, tienen como auxiliares a mercenarios árabes. Ahora están acampados al otro lado del torrente, preparados para atacarle». Entonces Judas salió a su encuentro,
</p><p>
40 y mientras él se acercaba al torrente con su ejército, Timoteo dijo a sus capitanes: «Si él lo pasa primero y viene sobre nosotros, no podremos resistir, y nos vencerá seguramente;
</p><p>
41 pero si se atemoriza y acampa al otro lado del río, lo atravesaremos nosotros, caeremos sobre él y lo venceremos».
</p><p>
42 Cuando Judas llegó al borde del torrente, ubicó a los escribas del pueblo a la orilla y les dio esta orden: «No dejen que ningún hombre quede en el campamento, sino que todos vayan al combate».
</p><p>
43 El fue el primero en cruzar el río en dirección al enemigo, y toda su gente lo siguió. Todos los paganos quedaron derrotados ante ellos, arrojaron sus armas y corrieron a refugiarse en el templo de Carnain.
</p><p>
44 Pero los judíos se apoderaron de la ciudad y quemaron el templo con todos los que había adentro. Carnain fue sometida, y ya nadie pudo resistir a Judas.
</p><p>
45 Judas reunió a todos los israelitas de la región de Galaad, del más pequeño al más grande, con sus mujeres, sus hijos y sus equipajes, para llevarlos al país de Judá: era una inmensa muchedumbre.
</p><p>
46 Llegaron a Efrón, ciudad importante y muy fortificada, que estaba sobre el camino, por la que tenían que pasar necesariamente, ya que no era posible desviarse ni a la derecha ni a la izquierda.
</p><p>
47 Pero los habitantes de la ciudad les negaron el paso y bloquearon las entradas con piedras.
</p><p>
48 Judas les envió un mensaje en son de paz, diciéndoles: «Permítannos pasar por el territorio de ustedes, para ir a nuestro país; nadie les hará ningún mal, sólo queremos pasar». Como ellos se negaron a abrirle,
</p><p>
49 Judas hizo anunciar en el campamento que cada uno tomara posición donde se encontraba.
</p><p>
50 Los soldados ocuparon sus posiciones, y Judas atacó la ciudad todo aquel día y toda la noche, hasta que cayó en sus manos.
</p><p>
51 Hizo pasar al filo de la espada a todos los varones, arrasó la ciudad, la saqueó y la atravesó por encima de los cadáveres.
</p><p>
52 Después pasaron el Jordán en dirección a la gran llanura que está frente a Betsán.
</p><p>
53 Durante todo el trayecto, Judas fue recogiendo a los rezagados y animando al pueblo hasta llegar a la tierra de Judá.
</p><p>
54 Todos subieron al monte Sión con júbilo y alegría, y ofrecieron holocaustos por haber regresado sanos y salvos sin perder a ninguno de los suyos.
</p><p>
55 Cuando Judas y Jonatán estaban en el país de Galaad, y su hermano Simón en Galilea, frente Tolemaida,
</p><p>
56 José, hijo de Zacarías, y Azarías, jefes del ejército, al oír las proezas y combates que aquellos habían llevado a cabo,
</p><p>
57 dijeron: «Hagámonos célebres también nosotros, luchando contra los paganos que nos rodean».
</p><p>
58 Entonces ordenaron a las tropas que estaban bajo su mando que avanzaran sobre Iamnia.
</p><p>
59 Gorgias salió de la ciudad con su ejército para luchar contra ellos.
</p><p>
60 José y Azarías fueron derrotados y perseguidos hasta la frontera de Judea. Aquel día cayeron alrededor de dos mil israelitas.
</p><p>
61 Este fue un grave desastre para el pueblo por no haber obedecido a Judas y a sus hermanos, creyéndose capaces de grandes hazañas.
</p><p>
62 Pero ellos no pertenecían a la estirpe de aquellos hombres a quienes estaba confiada la salvación de Israel.
</p><p>
63 El valiente Judas y sus hermanos alcanzaron gran celebridad en todo Israel en todas las naciones donde se oía hablar de ellos.
</p><p>
64 La gente se agolpaba a su alrededor para aclamarlos.
</p><p>
65 Judas salió con sus hermanos para hacer la guerra a los descendientes de Esaú, en la región meridional. Se apoderó de Hebrón y de sus poblados, destruyó sus fortificaciones e incendió las torres de su alrededor.
</p><p>
66 Luego partió en dirección al país de los filisteos y atravesó Marisá.
</p><p>
67 Aquel día, algunos sacerdotes que querían mostrar su valentía, cayeron en el combate por salir a luchar imprudentemente.
</p><p>
68 En seguida Judas se desvió hacia Azoto, en territorio filisteo: allí derribó sus altares, incendió las estatuas de sus dioses, saqueó sus ciudades, y finalmente, regresó al país de Judea.
</p>
