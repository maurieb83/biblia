<h3>PRIMER LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 11
</p><p>
1 El rey de Egipto reunió un ejército tan numeroso como la arena que hay a orillas del mar y una gran flota, porque pretendía apoderarse con astucia del reino de Alejandro y unirlo al suyo.
</p><p>
2 Entonces se dirigió a Siria con pretextos pacíficos, y los habitantes de las ciudades le abrían las puertas y salían a su encuentro, porque Alejandro había dado orden de recibirlo, ya que era su suegro.
</p><p>
3 A medida que Tolomeo entraba en las ciudades, dejaba una guarnición en cada una de ellas.
</p><p>
4 Cuando estuvo cerca de Azoto le mostraron el templo de Dagón incendiado, la ciudad y sus alrededores en ruinas, los cadáveres esparcidos y los restos calcinados de los que habían sido quemados en la batalla, porque los habían amontonado por donde iba a pasar el rey.
</p><p>
5 Entonces contaron al rey todo lo que había hecho Jonatán, esperando que lo desaprobara, pero el rey guardó silencio.
</p><p>
6 Jonatán, por su parte, fue a encontrarse con el rey en Jope con gran pompa: ambos se saludaron y pasaron la noche allí.
</p><p>
7 Después Jonatán acompañó al rey hasta el río llamado Eléuteros, y de allí regresó a Jerusalén.
</p><p>
8 El rey Tolomeo se adueñó de las ciudades del litoral hasta Seleucia Marítima. Mientras tanto, maquinaba sus planes contra Alejandro.
</p><p>
9 A tal efecto, Tolomeo mandó a una embajada al rey Demetrio, con este mensaje: «Hagamos una alianza entre nosotros. Yo te dará a mi hija, la que ahora tiene Alejandro, y tú serás rey en el reino de tu padre.
</p><p>
10 Estoy arrepentido de habérsela entregado, ya que él trató de asesinarme».
</p><p>
11 En realidad, le hacía estos cargos porque ambicionaba su reino.
</p><p>
12 Entonces quitó su hija a Alejandro y se la dio a Demetrio: así rompió con Alejandro y se puso en evidencia su enemistad.
</p><p>
13 Después entró en Antioquía y se ciñó la corona de Asia, poniendo así sobre su frente dos coronas, la de Egipto y la de Asia.
</p><p>
14 En ese momento el rey Alejandro se encontraba en Cilicia, porque la gente de aquella región se había rebelado.
</p><p>
15 Apenas se enteró, salió a combatirlo, pero Tolomeo se movilizó con un poderoso ejército y lo derrotó.
</p><p>
16 Alejandro huyó a Arabia en busca de refugio, y el rey Tolomeo quedó dueño de la situación.
</p><p>
17 El árabe Zabdiel le cortó la cabeza a Alejandro y se la envió a Tolomeo.
</p><p>
18 Pero tres días después murió también Tolomeo, y los habitantes de las plazas fuertes mataron a los egipcios acantonados en ellas.
</p><p>
19 Demetrio comenzó a reinar el año ciento sesenta y siete.
</p><p>
20 En ese tiempo, Jonatán reunió a los habitantes de Judea para atacar la Ciudadela de Jerusalén y con ese fin levantó numerosas máquinas de guerra.
</p><p>
21 Algunos renegados, enemigos de su propia nación, acudieron al rey y le anunciaron que Jonatán tenía sitiada a la Ciudadela.
</p><p>
22 Esta noticia lo enfureció y en seguida se puso en marcha y fue a Tolemaida. Al mismo tiempo, escribió a Jonatán, ordenándole suspender el asedio e ir lo antes posible a Tolemaida para entrevistarse con él.
</p><p>
23 Cuando Jonatán se enteró de esto, ordenó continuar el asedio y decidió enfrentar él mismo el peligro: eligió un grupo de ancianos y sacerdotes de Israel,
</p><p>
24 y fue a Tolemaida a entrevistarse con el rey, llevando consigo plata, oro, vestiduras y muchos otros regalos. De esta manera, se ganó el favor del rey
</p><p>
25 Algunos renegados de su nación lo acusaron,
</p><p>
26 pero el rey lo trató como lo habían hecho sus predecesores y lo honró en presencia de todos sus Amigos.
</p><p>
27 Lo confirmó en el sumo sacerdocio y en todos los altos cargos que había tenido antes, y le dio un lugar preeminente entre sus principales Amigos.
</p><p>
28 Jonatán pidió al rey que eximiera de impuestos a Judea y a los tres distritos de Samaría, prometiéndole en cambio trescientos talentos.
</p><p>
29 El rey lo aprobó y extendió a Jonatán un documento acerca de lo conversado, en los siguientes términos:
</p><p>
30 «El rey Demetrio saluda a su hermano Jonatán y al Pueblo judío.
</p><p>
31 A título de información, les adjuntamos una copia de la carta que hemos escrito acerca de ustedes a nuestro pariente Lástenes.
</p><p>
32 El rey Demetrio saluda a su padre Lástenes.
</p><p>
33 Por sus buenos sentimientos hacia nosotros, hemos decidido favorecer al Pueblo judío, que es Amigo nuestro y respeta nuestros derechos.
</p><p>
34 Les confirmamos los límites territoriales de Judea, con los tres distritos de Aferema, Lida y Ramataim. Estos, con todas sus adyacencias, fueron separados de Samaría y anexados a Judea, para beneficio de los que ofrecen sacrificios en Jerusalén, en compensación por los impuestos reales que el rey percibía de ellos cada año, sobre los productos de la tierra y los frutos de los árboles.
</p><p>
35 En lo que respecta a nuestros otros derechos –los diezmos, los impuestos que nos corresponden de las salinas, y las coronas de oro– a partir de ahora, los declaramos exentos de toda obligación.
</p><p>
36 Ninguna de estas concesiones será derogada de ahora en adelante.
</p><p>
37 Manden hacer una copia de este documento, para entregarla a Jonatán y exponerla en la Montaña santa, en lugar bien visible».
</p><p>
38 El rey Demetrio vio que todo el país estaba en calma bajo su mando y que no encontraba ningún obstáculo. Entonces licenció a su ejército, enviando a cada uno a su casa, excepto a los extranjeros que había reclutado en las islas de las naciones. Por ese motivo, se atrajo la hostilidad de todas las tropas de sus antepasados.
</p><p>
39 Trifón, antiguo partidario de Alejandro, al ver que todas esas tropas protestaban contra Demetrio, fue a ver al árabe Imalcué, preceptor de Antíoco, el hijo de Alejandro.
</p><p>
40 Lo presionó para que se lo entregara, a fin de que reinara en lugar de su padre; lo puso al corriente de todo lo que había hecho Demetrio y del odio que le tenían sus tropas, y permaneció allí mucho tiempo.
</p><p>
41 Entre tanto, Jonatán pidió al rey Demetrio que retirara las guarniciones de la Ciudadela de Jerusalén y de las plazas fuertes, porque hostigaban continuamente a Israel.
</p><p>
42 Demetrio mandó decir a Jonatán: «No sólo haré por ti y por tu nación lo que me pides, sino que te colmaré de honores a ti y a tu nación apenas se me presente la ocasión favorable.
</p><p>
43 Pero ahora harías bien en enviarme algunos hombres en mi auxilio, porque todas mis tropas han desertado».
</p><p>
44 Jonatán le envió a Antioquía tres mil soldados aguerridos, y cuando se presentaron al rey, este se alegró de su llegada.
</p><p>
45 Pero los habitantes de la ciudad, unos ciento veinte mil hombres, se amotinaron en las calles con la intención de matar al rey.
</p><p>
46 Este se refugió en su palacio, mientras la gente ocupaba las calles y comenzaba el ataque.
</p><p>
47 Entonces el rey pidió auxilio a los judíos, y ellos se agruparon todos juntos alrededor de él. Luego se dispersaron por la ciudad, y ese día mataron a unas cien mil personas.
</p><p>
48 Después incendiaron la ciudad y recogieron ese mismo día un cuantioso botín, salvando así al rey.
</p><p>
49 Cuando la gente vio que los judíos dominaban completamente la ciudad, se desanimaron y comenzaron a suplicar al rey:
</p><p>
50 «¡Hagamos las paces! ¡Que esos judíos dejen de atacarnos a nosotros y a la ciudad!».
</p><p>
51 Y deponiendo las armas, hicieron la paz. Los judíos se cubrieron de gloria delante del rey y de todos sus vasallos, y regresaron a Jerusalén con un abundante botín.
</p><p>
52 Así el rey Demetrio se afianzó en su trono real, y el país quedó pacificado bajo su mando.
</p><p>
53 Pero luego faltó a sus promesas y se distanció de Jonatán, no correspondiendo a los servicios que le había prestado y ocasionándole grandes sufrimientos.
</p><p>
54 Después de un tiempo, regresó Trifón, acompañado de Antíoco, que todavía era muy joven, y este ocupó el trono, ciñéndose la corona.
</p><p>
55 Todas las tropas dadas de baja por Demetrio se pusieron de su parte y lucharon contra Demetrio, lo derrotaron y lo obligaron a huir.
</p><p>
56 Trifón se apoderó de los elefantes y ocupó Antioquía.
</p><p>
57 Entonces el joven Antíoco escribió a Jonatán, en estos términos: «Te confirmo en el sumo sacerdocio, te pongo al frente de los cuatro distritos y quiero que te cuentes entre los Amigos del rey».
</p><p>
58 Al mismo tiempo, le envió una vajilla de oro y un juego completo de mesa, autorizándolo a beber en copas de oro, a vestirse de púrpura y a llevar un prendedor de oro.
</p><p>
59 A su hermano Simón lo designó comandante desde la Escalera de Tiro hasta la frontera de Egipto.
</p><p>
60 Jonatán salió a hacer un recorrido por la región y las ciudades de este lado del Eufrates, donde se le incorporaron todas las tropas sirias como aliados de guerra. Cuando llegó a Ascalón, sus habitantes salieron a recibirlo con muchos honores.
</p><p>
61 De allí pasó a Gaza, pero los habitantes le cerraron las puertas. Entonces sitió la ciudad y saqueó e incendió sus alrededores.
</p><p>
62 Los habitantes de Gaza fueron a pedirle clemencia y Jonatán hizo las paces con ellos, pero tomó como rehenes a los hijos de los jefes y los envió a Jerusalén. Luego atravesó el país en dirección a Damasco.
</p><p>
63 Jonatán se enteró de que los generales de Demetrio se encontraban cerca de Quedes de Galilea con un ejército numeroso, para hacerlo desistir de su proyecto.
</p><p>
64 Entonces dejó en el país a su hermano Simón y salió al encuentro de ellos.
</p><p>
65 Simón acampó frente a Betsur, la atacó durante muchos días y la sitió.
</p><p>
66 Sus habitantes le hicieron una propuesta de paz y él aceptó, pero los obligó a evacuar la ciudad, y se apoderó de ella, poniendo allí una guarnición.
</p><p>
67 Jonatán y su ejército acamparon junto al lago de Genesaret y, muy de madrugada, llegaron a la llanura de Asor.
</p><p>
68 El ejército extranjero les salió al encuentro en la llanura, dejando algunos hombres emboscados en las montañas. Mientras el ejército avanzaba de frente,
</p><p>
69 los que estaban emboscados salieron de sus puestos y entraron en combate.
</p><p>
70 Los hombres de Jonatán huyeron y no quedó ni uno solo, a excepción de Matatías, hijo de Absalón, y de Judas, hijo de Calfí, generales del ejército,
</p><p>
71 Jonatán rasgó sus vestiduras, se cubrió de polvo la cabeza y oró.
</p><p>
72 Luego reanudó el combate, derrotó al enemigo y lo puso en fuga.
</p><p>
73 Al ver esto, los hombres de Jonatán que huían se unieron de nuevo a él, persiguieron juntos al enemigo hasta su campamento en Quedes, y acamparon allí.
</p><p>
74 Aquel día cayeron unos tres mil hombres del ejército extranjero. Después Jonatán regresó a Jerusalén.
</p>
