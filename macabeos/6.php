<h3>PRIMER LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 6
</p><p>
1 Mientras tanto, el rey Antíoco recorría las provincias de la meseta. Allí se enteró de que en Persia había una ciudad llamada Elimaida, célebre por sus riquezas, su plata y su oro.
</p><p>
2 Ella tenía un templo muy rico, donde se guardaban armaduras de oro, corazas y armas dejadas allí por Alejandro, hijo de Filipo y rey de Macedonia, el primero que reinó sobre los griegos.
</p><p>
3 Antíoco se dirigió a esa ciudad para apoderarse de ella y saquearla, pero no lo consiguió, porque los habitantes de la ciudad, al conocer sus planes,
</p><p>
4 le opusieron resistencia. El tuvo que huir y se retiró de allí muy amargado para volver a Babilonia.
</p><p>
5 Cuando todavía estaba en Persia, le anunciaron que la expedición contra el país de Judá había fracasado.
</p><p>
6 Le comunicaron que Lisias había ido al frente de un poderoso ejército, pero había tenido que retroceder ante los judíos, y que estos habían acrecentado su poder, gracias a las armas y al cuantioso botín tomado a los ejércitos vencidos.
</p><p>
7 Además, habían destruido la Abominación que él había erigido sobre el altar de Jerusalén y habían rodeado el Santuario de altas murallas como antes, haciendo lo mismo con Betsur, que era una de las ciudades del rey.
</p><p>
8 Al oír tales noticias, el rey quedó consternado, presa de una violenta agitación, y cayó en cama enfermo de tristeza, porque las cosas no le habían salido como él deseaba.
</p><p>
9 Así pasó muchos días, sin poder librarse de su melancolía, hasta que sintió que se iba a morir.
</p><p>
10 Entonces hizo venir a todos sus amigos y les dijo: «No puedo conciliar el sueño y me siento desfallecer.
</p><p>
11 Yo me pregunto cómo he llegado al estado de aflicción y de amargura en que ahora me encuentro, yo que era generoso y amado mientras ejercía el poder.
</p><p>
12 Pero ahora caigo en la cuenta de los males que causé en Jerusalén, cuando robé los objetos de plata y oro que había allí y mandé exterminar sin motivo a los habitantes de Judá.
</p><p>
13 Reconozco que por eso me suceden todos estos males y muero de pesadumbre en tierra extranjera».
</p><p>
14 Luego, llamó a Filipo, uno de sus Amigos, y lo puso al frente de todo su reino.
</p><p>
15 Le entregó su diadema, su manto y su anillo, encargándole que dirigiera a su hijo Antíoco y lo educara para que fuera rey.
</p><p>
16 El rey Antíoco murió en aquel lugar, el año ciento cuarenta y nueve.
</p><p>
17 Cuando Lisias se enteró de la muerte del rey, puso en le trono a su hijo Antíoco, que él había educado desde niño, dándole el sobrenombre de Eupátor.
</p><p>
18 La gente de la Ciudadela tenía confinados a los israelitas alrededor del Santuario, y no perdía ocasión de hacerles mal y de apoyar a los paganos.
</p><p>
19 Judas resolvió acabar con ellos y convocó a todo el ejército para sitiarlos.
</p><p>
20 El año ciento cincuenta, se reunieron todos y sitiaron la Ciudadela, construyendo torres de asalto y empalizadas.
</p><p>
21 Pero varios de los sitiados rompieron el cerco y se les unieron algunos renegados de Israel,
</p><p>
22 que acudieron al rey para decirle: «¿Hasta cuándo vas a estar sin hacernos justicia y sin vengar a nuestros hermanos?
</p><p>
23 Nosotros aceptamos de buen grado servir a tu padre, cumplir sus órdenes y obedecer sus decretos.
</p><p>
24 Por eso, nuestros compatriotas han sitiado la Ciudadela y nos tratan como extraños. Más aún, han matado a los nuestros que caían en sus manos y han confiscado nuestros bienes.
</p><p>
25 Y no sólo han levantado su mano contra nosotros, sino que también sobre todos los países limítrofes,
</p><p>
26 Ahora mismo tienen sitiada la Ciudadela de Jerusalén para apoderarse de ella y han fortificado el Santuario y la ciudad de Betsur.
</p><p>
27 Si no te adelantas rápidamente, harán cosas mayores todavía y ya no podrás detenerlos».
</p><p>
28 El rey, al oír esto, se enfureció y convocó a todos sus Amigos, a los capitanes del ejército y a los comandantes de caballería.
</p><p>
29 Además, le llegaron tropas mercenarias de otros reinos y de las islas del mar.
</p><p>
30 El número de sus fuerzas era de cien mil soldados, veinte mil jinetes y treinta y dos elefantes adiestrados para la guerra.
</p><p>
31 Entraron por Idumea y acamparon cerca de Betsur, atacándola durante mucho tiempo con máquinas de guerra. Pero los sitiados, en una salida sorpresiva, se las quemaron y combatieron valerosamente.
</p><p>
32 Entonces Judas levantó el sitio de la Ciudades y acampó en Betzacaría, frente al campamento del rey.
</p><p>
33 A la mañana siguiente, el rey se levantó de madrugada y condujo apresuradamente al ejército por el camino de Betzacaría. Las tropas se dispusieron para el ataque y se tocaron las trompetas.
</p><p>
34 A los elefantes les mostraron mosto de uva y de moras para excitarlos al combate.
</p><p>
35 Los animales estaban repartidos entre los batallones. Al lado de cada elefante se alineaban mil hombres con cota de malla y cascos de bronce, además de quinientos jinetes escogidos.
</p><p>
36 Estos estaban pendientes de los movimientos del animal, de manera que adonde iba él, iban también ellos, sin apartarse de su lado.
</p><p>
37 Cada elefante llevaba encima, sujeta con cinchas, una sólida torre de madera que servía de defensa, y en cada una de ellas iban tres guerreros que combatían desde allí, además del conductor.
</p><p>
38 En cuanto al resto de la caballería, el rey la ubicó a un lado y a otro, sobre los flancos del ejército, con la misión de hostigar al enemigo y cubrir a los batallones.
</p><p>
39 Cuando el sol brilló sobre el oro y el bronce de los escudos, sus reflejos iluminaron las montañas que relucían como antorchas.
</p><p>
40 Una parte del ejército real se había alineado en lo alto de la montaña, y la otra en el valle. Todos avanzaban con paso seguro y en perfecto orden.
</p><p>
41 Los israelitas se estremecían al oír el rumor de aquella multitud, el ruido de su marcha y el estrépito de sus armas, porque era un ejército inmenso y poderoso.
</p><p>
42 Entonces Judas se adelantó con sus tropas para entrar en batalla, y cayeron seiscientos hombres del ejército real.
</p><p>
43 Mientras tanto, Eleazar, llamado Avarán, vio a un elefante pertrechado con una cota real, que sobresalía entre todos los demás, y pensó que en él iba el rey.
</p><p>
44 Entonces sacrificó su propia vida para salvar a su pueblo y adquirir una fama imperecedera.
</p><p>
45 Corrió resueltamente hacia él, al través del batallón, matando a derecha e izquierda. Así se abrió paso a un lado y a otro
</p><p>
46 y se deslizó por debajo del elefante, clavándole su espada. Al desplomarse por tierra el animal, cayó sobre él y lo mató.
</p><p>
47 Pero los judíos, al ver el poderío del rey y el empuje de sus tropas, emprendieron la retirada.
</p><p>
48 El ejército real subió a Jerusalén, al encuentro de los judíos, y el rey acampó frente a Judea y al monte Sión.
</p><p>
49 El hizo la paz con los habitantes de Betsur, que abandonaron la ciudad por carecer de víveres para resistir el asedio, ya que aquel era un año sabático para la tierra.
</p><p>
50 El rey ocupó Betsur y dejó allí una guarnición para su defensa.
</p><p>
51 Durante mucho tiempo estuvo sitiando el Santuario. Levantó contra él ballestas y torres de asalto, lanzallamas y catapultas, lanza flechas y hondas.
</p><p>
52 Los sitiados, por su parte, construyeron armas similares para el contraataque, y así resistieron mucho tiempo.
</p><p>
53 Pero, al fin, se agotaron los víveres almacenados, porque era el séptimo año y, además, porque los refugiados en Judea, provenientes de las naciones, habían consumido las últimas reservas.
</p><p>
54 Así no quedaron en el Santuario más que unos pocos hombres, porque se hacía sentir el hambre. Los demás se dispersaron, cada uno por su lado.
</p><p>
55 Mientras tanto, Lisias se enteró de que Filipo –a quien el rey Antíoco había encargado antes de morir que educara a su hijo Antíoco, para que fuera rey –
</p><p>
56 había vuelto de Persia y de Media con las tropas que acompañaron al rey, y trataba de tomar el poder.
</p><p>
57 Por eso pensó que era necesario partir en seguida y dijo al rey, a los capitanes del ejército y a los soldados: «Cada día estamos peor y escasean los víveres; el lugar que asediamos está bien fortificado y nos urgen los asuntos del reino.
</p><p>
58 Tendamos la mano a estos hombres, y hagamos la paz con ellos y con toda su nación.
</p><p>
59 Dejemos que vivan según sus costumbres tradicionales, ya que se han irritado y han hecho todas estas cosas, porque nosotros hemos tratado de abolirlas».
</p><p>
60 El rey y los capitanes aprobaron la propuesta, y el rey mandó ofrecer la paz a los sitiados. Estos la aceptaron,
</p><p>
61 y el rey y los capitanes se comprometieron con un juramento.
</p><p>
62 Con esta garantía salieron de la fortaleza y el rey subió al monte Sión. Pero al ver las fortificaciones de aquel lugar, violó el juramento que había hecho y ordenó destruir la muralla que lo rodeaba.
</p><p>
63 Luego partió rápidamente y volvió a Antioquía, donde encontró a Filipo dueño ya de la ciudad: lo atacó y ocupó la ciudad por la fuerza.
</p>
