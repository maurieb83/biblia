<h3>PRIMER LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 10
</p><p>
1 El año ciento sesenta, Alejandro, hijo de Antíoco, por sobrenombre Epífanes, desembarcó y ocupó Tolemaida, donde fue bien recibido y comenzó a reinar.
</p><p>
2 Enterado de esto, el rey Demetrio reclutó un ejército muy numeroso y salió a su encuentro para combatirlo.
</p><p>
3 Además, Demetrio envió a Jonatán una carta amistosa, dándole mayores poderes,
</p><p>
4 haciéndose esta reflexión: «Anticipémonos a negociar la paz con él antes que él la haga con Alejandro en detrimento nuestro,
</p><p>
5 acordándose de los males que le causamos a él, a sus hermanos y a su nación».
</p><p>
6 Demetrio le dio autorización para reclutar tropas, fabricar armamentos y ser su aliado. También ordenó que le entregaran los rehenes detenidos en la Ciudadela.
</p><p>
7 Jonatán fue a Jerusalén y leyó la carta en presencia de todo el pueblo y de los que ocupaban la Ciudadela.
</p><p>
8 Estos últimos quedaron muy atemorizados cuando supieron que el rey lo había autorizado para reclutar tropas, y
</p><p>
9 los de la Ciudadela entregaron los rehenes a Jonatán, el cual los devolvió a sus familias.
</p><p>
10 Jonatán fijó su residencia en Jerusalén y comenzó a reconstruir y restaurar la ciudad.
</p><p>
11 Ordenó a los constructores que reconstruyeran las murallas y que rodearan el monte Sión con un muro de piedras talladas, y así lo hicieron.
</p><p>
12 Los extranjeros que ocupaban las fortalezas levantadas por Báquides, huyeron,
</p><p>
13 abandonando cada uno su puesto para regresar a su país.
</p><p>
14 Sólo en Betsur quedaron algunos de los que habían renegado de la Ley y de los mandamientos, porque esa era una ciudad de refugio.
</p><p>
15 El rey Alejandro se enteró de los ofrecimientos que Demetrio había hecho a Jonatán. También le contaron las guerras y las proezas que él y sus hermanos habían realizado y las contrariedades que habían soportado.
</p><p>
16 Entonces exclamó: «¿Podremos hallar otro hombre como este? ¡Hagámoslo ahora mismo nuestro amigo y nuestro aliado!».
</p><p>
17 Y en seguida le envió una carta redactada en los siguientes términos:
</p><p>
18 «El rey Alejandro saluda a su hermano Jonatán.
</p><p>
19 Hemos oído que eres un guerrero valiente y digno de nuestra amistad.
</p><p>
20 Por eso te nombramos hoy Sumo Sacerdote de tu nación y te concedemos el título de Amigo del rey para que apoyes nuestra causa y nos asegures tu amistad». Al mismo tiempo, le enviaba una capa de púrpura y una corona de oro.
</p><p>
21 Jonatán se revistió de los ornamentos sagrados el séptimo mes del año ciento sesenta, en la fiesta de las Chozas; reclutó tropas y fabricó una gran cantidad de armas.
</p><p>
22 Apenas supo esto, Demetrio se disgustó mucho y dijo:
</p><p>
23 «¿Qué hemos hecho? Alejandro se nos ha adelantado, ganándose la amistad y el apoyo de los judíos.
</p><p>
24 También yo voy a escribirles en términos persuasivos, ofreciéndoles dignidades y regalos, para que se comprometan a ayudarme».
</p><p>
25 Y les escribió en estos términos:
</p><p>
26 «El rey Demetrio saluda a la nación de los judíos. Nos hemos enterado con satisfacción de que ustedes han observado los pactos hechos con nosotros y han perseverado en nuestra amistad, sin pasarse al enemigo.
</p><p>
27 Continúen guardándonos la misma fidelidad y nosotros los recompensaremos a cambio de la colaboración que nos prestan.
</p><p>
28 Los eximiremos de muchas obligaciones y les haremos regalos.
</p><p>
29 Y desde ahora, los libero a ustedes, y eximo a todos los judíos, de las contribuciones, del impuesto a la sal y de la entrega de las coronas de oro.
</p><p>
30 Renuncio también, a partir de hoy y para siempre, a percibir el tercio de los granos y la mitad de los frutos de los árboles que me corresponden, tanto de Judá como de los tres distritos anexos de Samaría y Galilea.
</p><p>
31 Jerusalén, con su territorio, sus diezmos y derechos, será sagrada y estará exenta de impuestos.
</p><p>
32 Renuncio asimismo a toda autoridad sobre la Ciudadela de Jerusalén y se la cedo al Sumo Sacerdote, a fin de que establezca en ella a todos los hombres que él mismo elija para su defensa.
</p><p>
33 A todo judío llevado cautivo de Judá a cualquier parte de mi reino, le concedo la libertad gratuitamente, y ninguno estará obligado a pagar impuestos, ni siquiera los del ganado.
</p><p>
34 Todas las fiestas, los sábados, los novilunios y los días fijados para las solemnidades –con los tres días que preceden y siguen a cada fiesta –serán días de inmunidad y exención para todos los judíos residentes en mi reino:
</p><p>
35 nadie tendrá derecho a demandar o inquietar a ninguno de ellos por ningún motivo.
</p><p>
36 En los ejércitos del rey se alistarán hasta treinta mil judíos que percibirán el mismo sueldo que las demás tropas del rey.
</p><p>
37 Algunos de ellos serán apostados en las principales fortalezas del rey y otros ocuparán cargos de confianza en el reino. Sus jefes y oficiales serán elegidos entre ellos y todos podrán vivir conforme a sus leyes, tal como lo ha dispuesto el rey para el país de Judá.
</p><p>
38 Los tres distritos de la provincia de Samaría, incorporados a Judea, quedarán anexados definitivamente a ella y considerados como parte suya, de manera que dependan de un solo jefe y no estén sometidos a otra autoridad que la del Sumo Sacerdote.
</p><p>
39 Doy como presente al Templo de Jerusalén la ciudad de Tolemaida y sus alrededores, para cubrir las expensas del Santuario.
</p><p>
40 Por mi parte, daré cada año quince mil siclos de plata, que se tomarán de los ingresos del rey en los lugares apropiados.
</p><p>
41 Toda la cantidad que los agentes del fisco han dejado de pagar, como se hacía en los años precedentes, será entregada desde ahora para las obras del Templo.
</p><p>
42 Además, los cinco mil siclos de plata que se solían recaudar cada año de los ingresos del Santuario quedarán condonados en beneficio de los sacerdotes que ejercen el culto.
</p><p>
43 Todos aquellos que por una deuda al Tesoro real y por cualquier otra causa se refugien en el Templo de Jerusalén o en alguna de sus dependencias, quedarán absueltos, ellos con las posesiones que tengan en mi reino.
</p><p>
44 Los gastos para las obras de construcción y reparación del Santuario, correrán por cuenta del rey.
</p><p>
45 También estarán a cargo del rey la construcción de las murallas de Jerusalén y la fortificación de su recinto, lo mismo que la reconstrucción de las murallas en las ciudades de Judea».
</p><p>
46 Cuando Jonatán y el pueblo oyeron estas palabras, no les dieron crédito ni las aceptaron, porque se acordaban del enorme daño que Demetrio había causado a Israel y de la opresión a que los había sometido.
</p><p>
47 Entonces se decidieron por Alejandro porque, a su parecer, les hacía mejores propuestas de paz, y fueron siempre sus aliados.
</p><p>
48 El rey Alejandro reunió un gran ejército y tomó posiciones contra Demetrio.
</p><p>
49 Ambos reyes entablaron batalla, y el ejército de Alejandro emprendió la retirada. Demetrio los persiguió y se impuso sobre ellos.
</p><p>
50 Y aunque combatió encarnizadamente hasta la puesta del sol, Demetrio sucumbió aquel día.
</p><p>
51 Alejandro mandó una embajada a Tolomeo, rey de Egipto, con este mensaje.
</p><p>
52 «Yo he vuelto a mi reino, y me he sentado en el trono de mis padres, adueñándome del poder. Después de derrotar a Demetrio, he tomado posesión de mi país,
</p><p>
53 porque combatí con él, lo derroté, a él y a su ejército, y ocupé su trono real.
</p><p>
54 Establezcamos ahora vínculos de amistad entre nosotros: dame a tu hija por esposa, y yo seré tu yerno, y a ti y a ella les haré regalos dignos de ti».
</p><p>
55 El rey Tolomeo respondió en estos términos: «¡Feliz el día en que regresaste al país de tus padres, para sentarte en su trono real!
</p><p>
56 Voy a cumplir ahora mismo lo que tú has escrito. Ven hasta Tolemaida para que nos entrevistemos, y yo seré tu suegro como tú lo has dicho».
</p><p>
57 Tolomeo partió de Egipto con su hija Cleopatra y llegó a Tolemaida, el año ciento sesenta y dos.
</p><p>
58 El rey Alejandro fue a su encuentro, y Tolomeo le entregó a su hija Cleopatra, celebrándose la boda en Tolemaida con la magnificencia propia de los reyes.
</p><p>
59 Luego Alejandro escribió a Jonatán para que fuera a entrevistarse con él.
</p><p>
60 Este fue a Tolemaida con gran pompa; allí se entrevistó con los dos reyes y los obsequió con plata y oro, ofreciendo además numerosos presentes a sus Amigos. De esa manera se ganó el favor de ellos.
</p><p>
61 Entonces un grupo de prevaricadores, la gente más indeseable de Israel, se confabularon contra él y lo acusaron ante el rey. Pero este, en lugar de hacerles caso,
</p><p>
62 ordenó que quitaran a Jonatán la ropa que tenía puesta y lo vistieran de púrpura. Así lo hicieron.
</p><p>
63 El rey lo hizo sentar a su lado y dijo a sus dignatarios: «Recorran con él la ciudad y proclamen que nadie se atreva a levantar ninguna acusación contra él ni a molestarlo por ningún motivo».
</p><p>
64 Apenas sus detractores vieron los honores que le tributaban, los términos de la proclama y la púrpura con que estaba revestido, se dieron a la fuga.
</p><p>
65 El rey lo honró inscribiéndolo entre sus principales Amigos, y lo constituyó general y gobernador.
</p><p>
66 Así Jonatán regresó a Jerusalén en paz y lleno de alegría.
</p><p>
67 El año ciento sesenta y cinco, Demetrio, hijo de Demetrio, llegó al país de sus padres, procedente de Creta,
</p><p>
68 y Alejandro, muy contrariado por esta noticia regresó a Antioquía.
</p><p>
69 Demetrio designó general a Apolonio, el gobernador de la Celesiria, y este reclutó un numeroso ejército y acampó en Iamnia, enviando a decir al Sumo Sacerdote Jonatán:
</p><p>
70 «Tú eres el único que te rebelas contra nosotros, y a causa de ti, y ho soy objeto de burla y de desprecio. ¿Por qué usas de tu autoridad contra nosotros en las montañas?
</p><p>
71 Si realmente confías en tus tropas, baja ahora a medirte con nosotros en la llanura, porque yo cuento con las tropas de las ciudades.
</p><p>
72 Averigua, y sabrás quién soy yo y quiénes son los que nos ayudan: ellos dicen que ustedes no pueden resistirnos, ya que dos veces fueron derrotados tus padres en su propio país.
</p><p>
73 Ahora no podrás enfrentar a la caballería y a un ejército tan grande en esta llanura, donde no hay una piedra, ni una roca, ni un sitio donde refugiarse».
</p><p>
74 Cuando Jonatán escuchó el mensaje de Apolonio, se turbó profundamente. Entonces eligió a diez mil hombres y salió de Jerusalén. Su hermano Simón se unió a él para ayudarlo.
</p><p>
75 Luego acampó frente a Jope, pero los habitantes de la ciudad le cerraron las puertas porque allí había una guarnición de Apolonio. Apenas comenzó el ataque,
</p><p>
76 los habitantes de la ciudad, aterrorizados, le abrieron las puertas, y así Jonatán se adueñó de Jope.
</p><p>
77 Al enterarse de esto, Apolonio puso en pie de guerra tres mil jinetes y una numerosa infantería, y partió en dirección a Azoto, como si fuera de paso; pero al mismo tiempo se iba adentrando en la llanura, confiado en su numerosa caballería.
</p><p>
78 Jonatán lo persiguió en dirección a Azoto, y los dos ejércitos entablaron batalla.
</p><p>
79 Apolonio había dejado mil jinetes ocultos a espaldas de ellos.
</p><p>
80 Pero Jonatán se dio cuenta de que estaban emboscados detrás de él. Los enemigos rodearon a su ejército, arrojándole flechas durante todo el día.
</p><p>
81 Las tropas se mantuvieron firmes, como lo había ordenado Jonatán, mientras que los caballos de los enemigos se cansaron.
</p><p>
82 Entonces Simón hizo avanzar sus escuadrones y atacó a la infantería, porque la caballería estaba extenuada: así los derrotó y los obligó a huir.
</p><p>
83 La caballería se desbandó por la llanura, y los fugitivos huyeron a Azoto y entraron en la Casa de Dagón, el templo de su ídolo, para ponerse a salvo.
</p><p>
84 Jonatán incendió a Azoto y a las ciudades vecinas y se apoderó del botín. También incendió el templo de Dagón, con todos los que se habían refugiado en él.
</p><p>
85 Los que perecieron por la espada o por el fuego fueron unos ocho mil hombres.
</p><p>
86 Luego Jonatán partió de allí y acampó frente a Ascalón, cuyos habitantes salieron a recibirlo con grandes honores.
</p><p>
87 Después regresó con su gente a Jerusalén, llevando consigo un gran botín.
</p><p>
88 Cuando el rey Alejandro se enteró de todo esto, concedió nuevos honores a Jonatán;
</p><p>
89 le envió un prendedor de oro, como se acostumbra conceder a los parientes de los reyes, y le dio en propiedad Acarón con todo su reino.
</p>
