<h3>PRIMER LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 9
</p><p>
1 Cuando Demetrio se enteró de que Nicanor y su ejército habían sucumbido en el combate, envió por segunda vez al país de Judá a Báquides y Alcimo, con el ala derecha de su ejército.
</p><p>
2 Estos tomaron el camino de Guilgal y sitiaron a Mesalot en el territorio de Arbela; se apoderaron de ella y mataron a mucha gente.
</p><p>
3 El primer mes del año ciento cincuenta y dos acamparon frente a Jerusalén,
</p><p>
4 de donde partieron con veinte mil hombres y dos mil jinetes en dirección a Berzet.
</p><p>
5 Judas tenía puesto su campamento en Elasá y había con él tres mil hombres elegidos.
</p><p>
6 Pero al ver la multitud de los enemigos, se atemorizaron y muchos desertaron del campamento, de manera que no quedaron más que ochocientos hombres.
</p><p>
7 Judas advirtió que su ejército se había desbandado, precisamente cuando la batalla era inminente y quedó descorazonado, porque no había tiempo de volverlos a reunir.
</p><p>
8 A pesar de su desaliento, dijo a los que habían quedado: «Ataquemos lo mismo a nuestros enemigos; tal vez podamos hacerles frente».
</p><p>
9 Pero ellos trataban de disuadirlo, diciéndole: «¡Imposible! Salvemos primero nuestras vidas; después volveremos con nuestros hermanos para continuar luchando, ya que ahora somos muy pocos».
</p><p>
10 Judas les respondió: «¡Eso nunca! No podemos huir ante ellos. Si es que ha llegado nuestra hora, muramos valientemente por nuestros hermanos, sin que nuestra gloria sufra menoscabo».
</p><p>
11 El ejército enemigo salió del campamento y se aprestó para enfrentarlos. La caballería se había dividido en dos escuadrones; en primera línea, avanzaban los más aguerridos, precedidos por los arqueros y los honderos.
</p><p>
12 Báquides estaba en el ala derecha. Las tropas avanzaron por ambos lados, al sonido de las trompetas.
</p><p>
13 Los hombres de Judas también tocaron las trompetas y la tierra tembló por el estruendo de los ejércitos. La lucha se inició al amanecer y duró hasta la tarde.
</p><p>
14 Judas vio que Báquides y el grueso de su ejército estaban a la derecha. Entonces los israelitas más decididos se reunieron a él
</p><p>
15 y derrotaron el ala derecha, persiguiéndola hasta las últimas estribaciones de la montaña.
</p><p>
16 Pero los del ala izquierda, al ver derrotada el ala derecha, se volvieron contra Judas y los suyos, tomándolos por la espalda.
</p><p>
17 La lucha se hizo más encarnizada, y hubo muchas víctimas de uno y otro bando.
</p><p>
18 También cayó Judas y los demás huyeron.
</p><p>
19 Jonatán y Simón tomaron a su hermano Judas y lo sepultaron en el sepulcro de sus padres en Modín.
</p><p>
20 Todo Israel lloró e hizo un gran duelo por él, y muchos días repitieron esta lamentación:
</p><p>
21 «¡Cómo ha caído el héroe que salvaba a Israel!».
</p><p>
22 El resto de las acciones de Judas, de sus guerras, de las proezas que realizó y de sus títulos de gloria no ha sido escrito, porque fueron innumerables.
</p><p>
23 Después de la muerte de Judas, reaparecieron los renegados en todo el territorio de Israel y se envalentonaron los impíos.
</p><p>
24 En aquellos días, el hambre asoló el país y la gente se puso de parte de ellos.
</p><p>
25 Báquides eligió a unos hombres impíos y los hizo dueños del país.
</p><p>
26 Ellos buscaban a los amigos de Judas, siguiéndoles las pistas, y se los llevaban a Báquides, que los castigaba y escarnecía.
</p><p>
27 Esta fue una gran tribulación para Israel, como no se había visto desde que dejaron de manifestarse los profetas.
</p><p>
28 Entonces todos los amigos de Judas se reunieron y dijeron a Jonatán:
</p><p>
29 «Desde la muerte de tu hermano Judas no tenemos un hombre como él, capaz de enfrentar a nuestros enemigos, a Báquides y a los que odian a nuestra nación.
</p><p>
30 Por eso, hoy te elegimos a ti para que ocupes el lugar de tu hermano, y seas nuestro jefe y nuestro guía en la lucha que sostenemos».
</p><p>
31 En ese momento Jonatán tomó el mando como sucesor de su hermano Judas.
</p><p>
32 Cuando Báquides lo supo, trató de matarlo.
</p><p>
33 Pero Jonatán, su hermano Simón y todos sus partidarios, al enterarse de esto, huyeron al desierto de Técoa y acamparon junto a las aguas de la cisterna de Asfar.
</p><p>
34 Báquides se enteró el día sábado, y atravesó el Jordán con todo su ejército.
</p><p>
35 Jonatán envió a su hermano Juan, el encargado de conducir la caravana, a pedir autorización a los nabateos, sus amigos, para dejarles en depósito su equipaje, que era muy grande.
</p><p>
36 Pero los jambritas, que habitaban en Madabá, capturaron a Juan con todo lo que llevaba, y se fueron con el botín.
</p><p>
37 Poco tiempo después, Jonatán y su hermano Simón se enteraron de que los jambritas celebraban una gran boda y traían de Nabatá, con mucha pompa, a la novia, hija de uno de los grandes magnates de Canaán.
</p><p>
38 Entonces se acordaron del sangriento fin de su hermano Juan y fueron a esconderse en un repliegue de la montaña.
</p><p>
39 Al alzar los ojos, divisaron una numerosa caravana que avanzaba en medio de un gran tumulto, y vieron que el novio iba a su encuentro, acompañado de sus amigos y hermanos, al son de tambores e instrumentos musicales y con mucha gente armada.
</p><p>
40 Inmediatamente, salieron de su escondite, se precipitaron sobre ellos y los masacraron, dejando muchas víctimas. Mientras los sobrevivientes huían a la montaña, ello se apoderaron de todo el botín.
</p><p>
41 Así la boda terminó en duelo y la música en lamentaciones.
</p><p>
42 De esta manera vengaron la sangre de su hermano y volvieron a las regiones pantanosas del Jordán.
</p><p>
43 Cuando Báquides se enteró, fue un día sábado a las riberas del Jordán con un ejército numeroso.
</p><p>
44 Entonces Jonatán arengó a sus hombres, diciendo: «¡Animo! Luchemos por defender nuestras vidas, porque ahora no estamos como antes.
</p><p>
45 El enemigo nos asedia por delante y por detrás, de un lado están las aguas del Jordán y del otro, los pantanos y las malezas; no hay escapatoria posible.
</p><p>
46 Clamen al Cielo, para que nos salve de nuestros enemigos».
</p><p>
47 Una vez iniciado el combate, Jonatán extendió su brazo para descargar un golpe sobre Báquides, pero este lo esquivó, echándose atrás.
</p><p>
48 Entonces Jonatán y los suyos se tiraron al Jordán y lo atravesaron a nado, pero sus enemigos no nos persiguieron.
</p><p>
49 Aquel día murieron unos mil hombres del ejército de Báquides.
</p><p>
50 Al volver a Jerusalén, Báquides comenzó a fortificar algunas ciudades en Judea: las fortalezas de Jericó, Emaús, Betjorón, Betel, Tamnatá, Faratón y Tefón, protegiéndolas con altas murallas, puertas y cerrojos.
</p><p>
51 En cada una de ellas puso una guarnición para hostigar a Israel.
</p><p>
52 También fortificó la ciudad de Betsur, Guéser y la Ciudadela, dejando en ellas tropas y depósitos de víveres.
</p><p>
53 Después tomó como rehenes a los hijos de las principales familias del país y los puso bajo custodia en la Ciudadela de Jerusalén.
</p><p>
54 En el segundo mes del año ciento cincuenta y tres, Alcimo mandó derribar las murallas de la parte interior del Santuario, destruyendo así la obra de los profetas. Pero al comenzar la demolición,
</p><p>
55 sufrió un ataque y la obra se detuvo. El perdió el habla y la boca le quedó paralizada, de manera que no pudo hablar más ni dar ninguna orden en lo referente a su casa.
</p><p>
56 Alcimo murió en esa época en medio de grandes tormentos.
</p><p>
57 Al ver que Alcimo había muerto, Báquides regresó adonde estaba el rey, y así Judá quedó en paz durante dos años.
</p><p>
58 Todos los renegados se confabularon diciendo: «Jonatán y los suyos viven tranquilos y confiados. Hagamos volver a Báquides, para que los arreste a todos en una sola noche».
</p><p>
59 Ellos fueron a comunicarle su plan,
</p><p>
60 y Báquides partió con un gran ejército. Mientras tanto, envió instrucciones secretas a todos sus aliados de Judea para que se apoderaran de Jonatán y de sus amigos, pero aquellos no pudieron hacerlo porque sus planes fueron descubiertos.
</p><p>
61 En represalia, Jonatán y sus amigos apresaron a unos cincuenta hombres entre los cabecillas de la conspiración, y los mataron.
</p><p>
62 Jonatán y Simón se retiraron con sus compañeros a Betbasí, en el desierto, y la fortificaron, restaurando sus ruinas.
</p><p>
63 Al saber esto, Báquides reunió a toda su gente y convocó a sus partidarios de Judea.
</p><p>
64 Luego acampó frente a Betbasí y la atacó durante varios días, emplazando máquinas de guerra.
</p><p>
65 Pero Jonatán, dejando en la ciudad a su hermano Simón, hizo una incursión por el país con algunos hombres.
</p><p>
66 Derrotó a Odomerá y a sus hermanos, y también a los hijos de Fasirón en sus propios campamentos. Una vez asestados estos primeros golpes, volvieron con más fuerzas.
</p><p>
67 Simón y los suyos salieron de la ciudad e incendiaron las máquinas de guerra.
</p><p>
68 Lucharon contra Báquides y lo derrotaron, dejándolo muy abatido porque sus planes y su campaña habían fracasado.
</p><p>
69 Por eso se enfureció contra los renegados que le habían aconsejado regresar al país y mandó ejecutar a muchos de ellos. Después decidió volver a su país.
</p><p>
70 Al enterarse de esto, Jonatán envió mensajeros a Báquides para concertar con él la paz y para que les devolviera los prisioneros.
</p><p>
71 Báquides aceptó la propuesta y le juró no hacerle ningún daño durante toda su vida;
</p><p>
72 le devolvió los prisioneros capturados anteriormente en Judá y regresó a su país. Y nunca más volvió al territorio de Judea.
</p><p>
73 Hubo así paz en Israel y Jonatán se estableció en Micmás, donde comenzó a gobernar al pueblo y a exterminar a los impíos de en medio de Israel.
</p>
