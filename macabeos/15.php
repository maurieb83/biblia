<h3>PRIMER LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 15
</p><p>
1 Antíoco, hijo del rey Demetrio, envió desde las islas del mar una carta a Simón, sacerdote y etnarca de los judíos, y a toda la nación,
</p><p>
2 redactada en los siguientes términos: «El rey Antíoco saluda a Simón, Sumo Sacerdote y etnarca, y a la nación de los judíos.
</p><p>
3 Puesto que gente indeseable ha usurpado el trono de mis padres, yo estoy dispuesto a hacer valer mis derechos sobre el reino, a fin de restablecerlo como estaba antes. A tal efecto, he reclutado un ejército numeroso y equipado barcos de guerra,
</p><p>
4 con la intención de desembarcar en el país para perseguir a los que lo han arruinado y han devastado muchas ciudades de mi reino.
</p><p>
5 Por eso, ahora ratifico todas las exenciones de tributos que te concedieron mis predecesores, y las otras dispensas de contribuciones que ellos te otorgaron.
</p><p>
6 Te autorizo, además, a acuñar moneda propia, de curso legal en tu país.
</p><p>
7 Jerusalén y el Santuario serán libres. Las armas que has fabricado y las fortalezas que has construido y ocupas, quedarán en tu poder.
</p><p>
8 A partir de este momento, se te condona todo lo que adeudas al tesoro real y todo lo que adeudarás en el futuro.
</p><p>
9 Y cuando hayamos reconquistado nuestro reino, te colmaremos a ti, a tu pueblo y al Santuario de tales honores, que tu gloria será conocida en toda la tierra».
</p><p>
10 El año ciento setenta y cuatro Antíoco partió para el país de sus padres; todas las tropas se pusieron de su parte, de manera que sólo unos pocos quedaron con Trifón.
</p><p>
11 Antíoco lo persiguió y Trifón se refugió en Dora, a orillas del mar,
</p><p>
12 porque veía que había caído en desgracia y que las tropas lo habían abandonado.
</p><p>
13 Antíoco acampó frente a Dora con ciento veinte mil soldados de infantería y ocho mil jinetes.
</p><p>
14 Luego sitió la ciudad, mientras la escuadra se aproximaba por el mar. De esa manera bloqueó la ciudad por tierra y por mar, sin dejar que nadie entrara o saliera.
</p><p>
15 Mientras tanto, Numenio y su comitiva regresaron de Roma con cartas para los reyes de los diversos países, en las que se decía:
</p><p>
16 «Lucio, cónsul de los romanos, saluda al rey Tolomeo.
</p><p>
17 Los embajadores judíos, enviados por el Sumo Sacerdote Simón y por el Pueblo judío, se han presentado a nosotros como amigos y aliados, para renovar el antiguo pacto de amistad,
</p><p>
18 trayéndonos un escudo de oro de mil minas.
</p><p>
19 En consecuencia, nos ha parecido bien escribir a los reyes de los diversos países que no les hagan ningún daño ni los ataquen, ni a ellos ni a sus ciudades ni a su país, y que no presten apoyo a sus enemigos.
</p><p>
20 También hemos decidido aceptar de ellos el escudo.
</p><p>
21 Por lo tanto, si se encuentra entre ustedes algún hombre indeseable que haya huido del país de los judíos, entréguenlo al Sumo Sacerdote Simón, para que lo castigue de acuerdo con su ley».
</p><p>
22 Cartas iguales fueron remitidas al rey Demetrio, a Atalo, a Ariartes, a Arsaces
</p><p>
23 y a todos los países, a saber: Sámpsamo, Esparta, Delos, Mindos, Sición, Caria, Samos, Panfilia, Licia, Halicarnaso, Rodas, Fasélida, Cos, Side, Arados, Gortina, Cnido, Chipre y Cirene.
</p><p>
24 Redactaron, además, una copia de esta carta para el Sumo Sacerdote Simón.
</p><p>
25 Mientras tanto, el rey Antíoco continuaba el sitio de Dora, acampando en los suburbios de la ciudad, alzando incesantemente sus tropas contra ella y construyendo máquinas de guerra. Tenía bloqueado a Trifón y nadie podía entrar ni salir.
</p><p>
26 Simón le envió dos mil hombres elegidos para ayudarlo en la lucha, además de plata, oro y abundante material.
</p><p>
27 Pero él no queso aceptar el envío; más aún, anuló las concesiones que le había hecho antes y se mostró hostil con él.
</p><p>
28 Además, le envió a Atenobio, uno de sus Amigos, para transmitirle el siguiente mensaje: «Ustedes ocupan Jope, Gázara y la Ciudadela de Jerusalén, que son ciudades de mi reino.
</p><p>
29 Han devastado su territorio, causando graves daños al país, y se han adueñado de muchos lugares de mi reino.
</p><p>
30 Devuélvanme ahora mismo las ciudades que han tomado y los impuestos de los lugares ocupados fuera de las fronteras de Judea.
</p><p>
31 De lo contrario, paguen en compensación quinientos talentos de plata, y otros quinientos talentos como indemnización por los daños causados y por los tributos de las ciudades. Si no, iremos a atacarlos».
</p><p>
32 Cuando Atenobio, el Amigo del rey, llegó a Jerusalén quedó asombrado al ver la magnificencia de Simón, su aparador con vajilla de oro y plata y toda la fastuosidad que lo rodeaba. Entonces le transmitió el mensaje del rey,
</p><p>
33 y Simón respondió: «Nosotros no nos hemos apoderado de tierras ajenas ni nos hemos apropiado de los bienes de otros, sino de la herencia de nuestros padres. Nuestros enemigos la retuvieron injustamente en un momento dado,
</p><p>
34 pero nosotros, al presentarse la ocasión favorable la hemos recuperado.
</p><p>
35 En cuanto a Jope y a Gázara, las ciudades que tú reclamas, eran ellas, precisamente las que causaban graves daños al pueblo y asolaban el país. A pesar de todo, te daremos por ellas cien talentos». Atenobio no le respondió nada.
</p><p>
36 sino que regresó muy indignado y transmitió la respuesta al rey, informándolo acerca de la magnificencia de Simón y de todo lo que había visto. Y el rey se enojó muchísimo.
</p><p>
37 A todo esto, Trifón había huido a Ortosia en un barco.
</p><p>
38 El rey designó a Cendebeo comandante en jefe de la zona marítima y le entregó tropas de infantería y caballería.
</p><p>
39 Le mandó acampar frente a Judea, reconstruir Cedrón, reforzar sus puertas y hacer la guerra al pueblo, mientras el rey trataba de alcanzar a Trifón.
</p><p>
40 Cuando Cendebeo llegó a Iamnia, comenzó a hostigar al pueblo, haciendo incursiones por Judea, tomando prisioneros y dando muerte a gente del pueblo.
</p><p>
41 También reconstruyó Cedrón, y puso en ella tropas de caballería e infantería para incursionar por los caminos de Judea, como el rey se lo había ordenado.
</p>
