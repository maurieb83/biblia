<h3>PRIMER LIBRO DE LOS MACABEOS</h3>
<p>Capítulo 16
</p><p>
1 Juan subió desde Gázara a contar a su padre Simón lo que estaba haciendo Cendebeo.
</p><p>
2 Simón llamó entonces a sus dos hijos mayores, Judas y Juan, y les dijo: «Yo, mis hermanos y la familia de mi padre hemos combatido a los enemigos de Israel desde nuestra juventud hasta el día de hoy, y gracias a nosotros se logró más de una vez la liberación de Israel.
</p><p>
3 Pero ahora estoy viejo, mientras que ustedes, por la misericordia del Cielo, están en la mejor edad. Ocupen mi puesto y el de mi hermano, salgan a combatir por nuestra nación y que la ayuda del Cielo esté con ustedes».
</p><p>
4 Después seleccionó veinte mil combatientes y jinetes del país, y estos partieron para atacar a Cendebeo. Pasaron la noche en Modín
</p><p>
5 y, al amanecer, se levantaron y avanzaron hacia la llanura. De pronto divisaron un numeroso ejército, compuesto de soldados y jinetes, que venía a su encuentro. Entre ellos se interponía un torrente.
</p><p>
6 Juan tomó posiciones con sus tropas frente al enemigo y, advirtiendo que sus hombres tenían miedo de pasar el torrente, lo pasó el primero. Al verlo, todos los demás lo siguieron.
</p><p>
7 El había dividido su ejército en dos cuerpos, poniendo la caballería en medio de la infantería, porque la caballería del enemigo era muy numerosa.
</p><p>
8 En seguida tocaron las trompetas, y Cendebeo fue derrotado con todo su ejército. Muchos de ellos murieron y los restantes huyeron en dirección a la fortaleza.
</p><p>
9 Entonces fue herido Judas, el hermano de Juan. Este los persiguió hasta que Cendebeo entró en Cedrón, la fortaleza que había reconstruido.
</p><p>
10 Algunos huyeron también a las torres de los campos de Azoto. Juan las incendió, y murieron unos dos mil enemigos. Luego Juan regresó a Judea sano y salvo.
</p><p>
11 Tolomeo, hijo de Abubos, había sido designado comandante de la llanura de Jericó y poseía mucha plata y oro.
</p><p>
12 por ser yerno del Sumo Sacerdote.
</p><p>
13 Su corazón se ensoberbeció tanto que aspiró a adueñarse del país, y por eso maquinaba pérfidamente la manera de eliminar a Simón y a sus hijos.
</p><p>
14 Una vez, mientras Simón inspeccionaba las ciudades del país y se ocupaba de su administración, bajó a Jericó, con sus hijos Matatías y Judas. Era en el undécimo mes, el mes de Sabat, del año ciento setenta y siete.
</p><p>
15 El hijo de Abubos los recibió dolorosamente en la pequeña fortaleza llamada Doc, que él había hecho construir. Allí les sirvió un gran banquete, a la vez que les preparaba una emboscada.
</p><p>
16 Cuando Simón y sus hijos estuvieron ebrios, Tolomeo y sus hombres se levantaron, empuñaron sus armas, se precipitaron sobre Simón en la sal del banquete y lo mataron, junto con sus dos hijos y algunos de sus servidores.
</p><p>
17 Así cometió una grave traición, devolviendo mal por bien.
</p><p>
18 Luego Tolomeo escribió un informe al rey acerca de lo sucedido, pidiéndole que le enviara tropas en su auxilio y que le cediera las ciudades y el país.
</p><p>
19 Además, mandó otros emisarios de Gázara con el encargo de matar a Juan y envió cartas a los oficiales del ejército, invitándolos a reunirse con él para obsequiarles oro, plata y otros regalos.
</p><p>
20 Finalmente, envió algunos hombres para que ocuparan Jerusalén y la montaña del Templo.
</p><p>
21 Pero alguien se adelantó y anunció a Juan en Gázara que su padre y sus hermanos habían muerto, y añadió: «Ha enviado gente para matarte también a ti».
</p><p>
22 Esta noticia lo dejó consternado. Entonces mandó arrestar a los hombres que venían a matarlo y los hizo ejecutar, porque sabía que pretendían asesinarlo.
</p><p>
23 El resto de las acciones de Juan, sus guerras y las hazañas que llevó a cabo, las murallas que construyó, sus hechos y sus gestas,
</p><p>
24 todo esto está escrito en los Anales de su pontificado, a partir del día en que sucedió a su padre como Sumo Sacerdote.
</p>
