<h3>SEGUNDO LIBRO DE LOS REYES</h3>
<p>Capítulo 8
</p><p>
La devolución de los bienes de la sunamita
</p><p>
1 Eliseo dijo a la madre del niño que él había hecho revivir: «Parte ahora mismo con toda tu familia, y emigra adonde puedas, porque el Señor ha llamado al hambre, y ya viene al país por siete años».
</p><p>
2 La mujer partió e hizo lo que le había dicho el hombre de Dios: emigró con su familia al país de los filisteos y se quedó allí siete años.
</p><p>
3 Al cabo de siete años, la mujer volvió del país de los filisteos y fue a reclamar al rey su casa y su campo.
</p><p>
4 El rey estaba hablando con Guejazí, el servidor del hombre de Dios: «Cuéntame, le decía, las maravillas que ha hecho Eliseo».
</p><p>
5 Y mientras le estaba contando al rey cómo Eliseo había hecho revivir a un muerto, la madre del niño que él había hecho revivir vino a reclamar al rey su casa y su campo. Guejazí dijo entonces: «¡Rey, señor mío, esta es la mujer y aquí está el hijo que Eliseo hizo revivir!».
</p><p>
6 El rey interrogó a la mujer, y ella le contó todo. Luego puso a su disposición un eunuco, al que ordenó: «Que se le restituya todo lo que le pertenece, con todas las rentas del campo, desde el día en que dejó el país hasta ahora».
</p><p>
La predicción de Eliseo acerca de Ben Hadad y Jazael
</p><p>
7 Eliseo se dirigió a Damasco. Ben Hadad, rey de Aram, estaba enfermo. Cuando le avisaron: «El hombre de Dios ha venido hasta aquí»,
</p><p>
8 el rey dijo a Jazael: «Toma contigo un presente, ve al encuentro del hombre de Dios, y consulta al Señor por medio de él, a ver si me restableceré de esta enfermedad».
</p><p>
9 Jazael fue al encuentro de Eliseo llevando como presente cuarenta camellos cargados con lo mejor que había en Damasco. Al llegar, se presentó ante él y le dijo: «Tu hijo Ben Hadad, rey de Aram, me ha enviado a preguntarte: ¿Me restableceré de esta enfermedad?».
</p><p>
10 Eliseo respondió: «Ve a decirle: «Sí, te restablecerás»; pero el Señor me ha hecho ver que morirá».
</p><p>
11 Después fijó la mirada y permaneció así largo rato. Y el hombre de Dios lloró.
</p><p>
12 Jazael le preguntó: «¿Por qué llora mi señor?». «Porque sé el mal que harás a los israelitas, respondió Eliseo; tú incendiarás sus plazas fuertes, matarás a sus jóvenes con la espada, estrellarás a sus niños y abrirás el vientre de sus mujeres embarazadas».
</p><p>
13 Jazael replicó: «Tu servidor no es más que un perro. ¿Cómo va a hacer tales hazañas?». Eliseo respondió: «El Señor me ha hecho ver que tú reinarás sobre Aram».
</p><p>
14 El se alejó de Eliseo y fue a ver a su señor, el cual le dijo: «¿Qué te ha dicho Eliseo?». El respondió: «Me ha dicho que te restablecerás».
</p><p>
15 Pero a la mañana siguiente tomó una manta, la empapó en agua y la extendió sobre el rostro del rey, hasta que murió. Jazael reinó en lugar de él.
</p><p>
El reinado de Jorám en Judá (848-841)
</p><p>
2 Cro 21.5-10.20
</p><p>
16 El quinto año de Joram, hijo de Ajab, rey de Israel, inició su reinado Joram, hijo de Josafat, rey de Judá.
</p><p>
17 Tenía treinta y dos años cuando comenzó a reinar, y reinó ocho años en Jerusalén.
</p><p>
18 Siguió el camino de los reyes de Israel, conforme a lo que había hecho la casa de Ajab, porque se había casado con una hija de Ajab; e hizo lo que es malo a los ojos del Señor.
</p><p>
19 Pero el Señor no quiso destruir a Judá, a causa de su servidor David, según la promesa que le había hecho de darles a él y a sus hijos una lámpara para siempre.
</p><p>
20 En ese tiempo, Edom se rebeló contra el poder de Judá y se estableció un rey.
</p><p>
21 Joram partió entonces para Saír con todos sus carros de guerra. Por la noche, se levantó y derrotó a los edomitas, que lo tenían cercado a él y a los jefes de los carros; pero las tropas huyeron a la desbandada.
</p><p>
22 Así se independizó Edom del poder de Judá, hasta el día de hoy. En aquel tiempo, también se rebeló Libná.
</p><p>
23 El resto de los hechos de Joram y todo lo que él hizo, ¿no está escrito en el libro de los Anales de los reyes de Judá?
</p><p>
24 Joram se fue a descansar con sus padres, y fue sepultado con ellos en la Ciudad de David. Su hijo Ocozías reinó en lugar de él.
</p><p>
El reinado de Ocozías en Judá (841)
</p><p>
2 Cro 22.1-6
</p><p>
25 El duodécimo año de Joram, hijo de Ajab, rey de Israel, inició su reinado Ocozías, hijo de Joram, rey de Judá.
</p><p>
26 Ocozías tenía veintidós años cuando comenzó a reinar, y reinó un año en Jerusalén. Su madre se llamaba Atalía, y era hija de Omrí, rey de Israel.
</p><p>
27 Siguió el camino de la casa de Ajab e hizo lo que es malo a los ojos del Señor, como la casa de Ajab, porque estaba emparentado con ella.
</p><p>
28 El fue con Joram, hijo de Ajab, a combatir contra Jazael, rey de Aram, a Ramot de Galaad; pero los arameos hirieron a Joram.
</p><p>
29 El rey Joram volvió a Izreel, para hacerse curar de las heridas que le habían infligido los arameos en Ramá, cuando combatía contra Jazael, rey de Aram. Entonces Ocozías, hijo de Joram, rey de Judá, bajó a Izreel para visitar a Joram, hijo de Ajab, que estaba herido.
</p><p>
