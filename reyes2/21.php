<h3>SEGUNDO LIBRO DE LOS REYES</h3>
<p>Capítulo 21
</p><p>
El reinado de Manasés en Judá (687-642)
</p><p>
2 Cro 33.1-10.18-20
</p><p>
1 Manasés tenía doce años cuando comenzó a reinar, y reinó cincuenta y cinco años en Jerusalén. Su madre se llamaba Jefsibá.
</p><p>
2 El hizo lo que es malo a los ojos del Señor, siguiendo las costumbres abominables de las naciones que el Señor había desposeído delante de los israelitas.
</p><p>
3 Reedificó los lugares altos que había hecho desaparecer su padre. Ezequías; erigió altares a Baal, hizo un poste sagrado como lo había hecho Ajab, rey de Israel, y se postró delante de todo el Ejército de los cielos y lo sirvió.
</p><p>
4 Edificó altares en la Casa del Señor, de la que el Señor había dicho: «En Jerusalén pondré mi Nombre».
</p><p>
5 Edificó altares a todo el Ejército de los cielos en los dos atrios de la Casa del Señor.
</p><p>
6 Inmoló a su hijo en el fuego, practicó la astrología y la magia, e instituyó nigromantes y adivinos. Persistió en hacer lo que es malo a los ojos del Señor, provocando su indignación.
</p><p>
7 La estatua de Aserá que había hecho, la instaló en la Casa de la que el Señor había dicho a David y a su hijo Salomón: «En esta Casa y en Jerusalén, que yo elegí entre todas las tribus de Israel, pondré mi Nombre para siempre.
</p><p>
8 Ya no permitiré que Israel ande errante lejos del suelo que di a sus padres, con tal que se empeñen en practicar todo lo que les he mandado, conforme a toda la Ley que les prescribió mi servidor Moisés».
</p><p>
9 Pero ellos no escucharon, y Manasés los extravió, de manera que obraron peor que las naciones que el Señor había exterminado delante de los israelitas.
</p><p>
10 Entonces el Señor habló por medio de sus servidores los profetas, en estos términos:
</p><p>
11 «Porque Manasés, rey de Judá, cometió estas abominaciones, porque superó en maldad a todo lo que habían hecho los amorreos antes que él, y también hizo pecar a Judá con sus ídolos,
</p><p>
12 por eso, así habla el Señor, el Dios de Israel: Yo haré venir sobre Jerusalén y Judá una desgracia tal, que le zumbarán los dos oídos al que oiga hablar de ella.
</p><p>
13 Tenderé sobre Jerusalén la cuerda de Samaría y el nivel de la casa de Ajab, y limpiaré a Jerusalén como se limpia un plato, y una vez limpio, se lo vuelve boca abajo.
</p><p>
14 Rechazaré al resto de mi herencia, los entregaré en manos de sus enemigos, y serán el botín y la presa de todos sus enemigos,
</p><p>
15 porque han hecho lo que es malo a mis ojos y no han cesado de provocar mi indignación, desde el día en que sus padres salieron de Egipto hasta el día de hoy».
</p><p>
16 Manasés derramó también sangre inocente, en tal cantidad que inundó a Jerusalén de un extremo a otro, aparte del pecado que hizo cometer a Judá, haciendo lo que es malo a los ojos del Señor.
</p><p>
17 El resto de los hechos de Manasés y todo lo que él hizo, así como el pecado que cometió, ¿no está escrito todo eso en el libro de los Anales de los reyes de Judá?
</p><p>
18 Manasés se fue a descansar con sus padres, y fue sepultado en el jardín de su casa, en el Jardín de Uzá. Su hijo Amón reinó en lugar de él.
</p><p>
El reinado de Amón en Judá (642-640)
</p><p>
2 Cro 33.21-25
</p><p>
19 Amón tenía veintidós años cuando comenzó a reinar, y reinó dos años en Jerusalén. Su madre se llamaba Mesulémet, hija de Jarús, y era de Jotbá.
</p><p>
20 El hizo lo que es malo a los ojos del Señor, como lo había hecho su padre Manasés.
</p><p>
21 Siguió en todo el camino que había seguido su padre; sirvió a los ídolos que había servido su padre y se postró delante de ellos.
</p><p>
22 Abandonó al Señor, el Dios de sus padres, y no siguió el camino del Señor.
</p><p>
23 Los servidores de Amón conspiraron contra el rey y lo mataron en su casa.
</p><p>
24 Pero el pueblo del país mató a todos los que habían conspirado contra el rey Amón, y proclamó rey en lugar de él a su hijo Josías.
</p><p>
25 El resto de los hechos de Amón, todo lo que él hizo, ¿no está escrito en el libro de los Anales de los reyes de Judá?
</p><p>
26 Lo sepultaron en su tumba, en el Jardín de Uzá. Su hijo Josías reinó en lugar de él.
</p><p>
La reforma religiosa de Josías
