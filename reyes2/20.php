<h3>SEGUNDO LIBRO DE LOS REYES</h3>
<p>Capítulo 20
</p><p>
Enfermedad y curación de Ezequías
</p><p>
2 Cro 32.24; Is 38.1-8
</p><p>
1 En aquellos días, Ezequías cayó gravemente enfermo. El profeta Isaías, hijo de Amós, fue a verlo y le dijo: «Así habla el Señor: Ordena todos los asuntos de tu casa, porque vas a morir. Ya no vivirás más».
</p><p>
2 Ezequías volvió su rostro hacia la pared y oró al Señor, diciendo:
</p><p>
3 «¡Ah, Señor! Recuerda que yo he caminado delante de ti con fidelidad e integridad de corazón, y que hice lo que es bueno a tus ojos». Y Ezequías se deshizo en llanto.
</p><p>
4 Isaías no había salido aún del patio central, cuando le llegó la palabra del Señor:
</p><p>
5 «Vuelve y dile a Ezequías, el jefe de mi pueblo: Así habla el Señor, el Dios de tu padre David: He oído tu súplica, he visto tus lágrimas. Yo te voy a curar: dentro de tres días subirás a la Casa del Señor.
</p><p>
6 Añadiré otros quince años a tu vida; te libraré, a ti y a esta ciudad, de manos del rey de Asiria, y defenderé a esta ciudad por mi honor y el de mi servidor David».
</p><p>
7 Luego dijo Isaías: «Traigan un emplasto de higos». Lo trajeron, lo aplicaron sobre la úlcera, y el rey se curó.
</p><p>
8 Entonces Ezequías dijo a Isaías: «¿Cuál es la señal de que el Señor me sanará y que dentro de tres días podré subir a la Casa del Señor?».
</p><p>
9 Isaías respondió: «Esta es la señal que te da el Señor para confirmar la palabra que ha pronunciado: ¿La sombra debe avanzar diez grados o retroceder diez grados?
</p><p>
10 Ezequías respondió: «Es fácil para la sombra adelantarse diez grados, pero no que los retroceda».
</p><p>
11 El profeta invocó al Señor, y él hizo que la sombra retrocediera los diez grados que había descendido, en el reloj de sol de Ajaz.
</p><p>
Los emisarios del rey de Babilonia
</p><p>
2 Cro 32.27-29; Is 39.2
</p><p>
12 En aquel tiempo, Merodac Baladán, hijo de Baladán, rey de Babilonia, envió una carta y un presente a Ezequías, al enterarse de que había estado enfermo.
</p><p>
13 Ezequías se alegró de esto, y mostró a los emisarios la sala del tesoro, la plata, el oro, los perfumes, el aceite precioso, su arsenal y todo lo que se encontraba en sus depósitos. De todo lo que había en su palacio y en sus dominios, no quedó nada que Ezequías no les hiciera ver.
</p><p>
14 Entonces el profeta Isaías se presentó al rey Ezequías y le preguntó: «¿Qué te ha dicho esa gente y de dónde ha venido?». Ezequías respondió: «Vinieron de un país lejano, de Babilonia».
</p><p>
15 Isaías preguntó: «¿Qué han visto en tu casa?». Han visto todo lo que hay en mi casa, respondió Ezequías. No hay nada en mis depósitos que no les haya mostrado».
</p><p>
16 Entonces Isaías dijo a Ezequías: «Escucha la palabra del Señor:
</p><p>
17 Llegarán los días en que todo lo que hay en tu casa, todo lo que han atesorado tus padres hasta el día de hoy, será llevado a Babilonia. No quedará nada, dice el Señor.
</p><p>
18 Y algunos de tus hijos, de los que han nacido de ti, que tú mismo habrás engendrado, serán tomados para que sirvan como eunucos en el palacio del rey de Babilonia».
</p><p>
19 Ezequías respondió a Isaías: «Es auspiciosa la palabra del Señor que has pronunciado». Porque se decía a sí mismo: «Mientras yo viva, habrá paz y seguridad».
</p><p>
Fin del reinado de Ezequías
</p><p>
2 Cro 32.32-33
</p><p>
20 El resto de los hechos de Ezequías, sus proezas, todo lo que él hizo, la cisterna y el canal que construyó para llevar el agua a la ciudad, ¿no está escrito en el libro de los Anales de los reyes de Judá?
</p><p>
21 Ezequías fue a descansar con sus padres, y su hijo Manasés reinó en lugar de él.
</p><p>
