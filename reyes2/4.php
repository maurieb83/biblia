<h3>SEGUNDO LIBRO DE LOS REYES</h3>
<p>Capítulo 4
</p><p>
El aceite de la viuda
</p><p>
1 La mujer de uno de la comunidad de profetas imploró a Eliseo, diciendo: «Tu servidor, mi marido, ha muerto, y tú sabes que era un hombre temeroso del Señor. Pero ahora ha venido un acreedor para llevarse a mis dos hijos como esclavos».
</p><p>
2 Eliseo le dijo: «¿Qué puedo hacer por ti? Dime qué tienes en tu casa». Ella le respondió: «Tu servidora no tiene en su casa nada más que un frasco de aceite».
</p><p>
3 Eliseo le dijo: «Ve y pide prestados a tos tus vecinos unos recipientes vacíos; cuántos más sean, mejor.
</p><p>
4 Luego entra y enciérrate con tus hijos; echa el aceite en todos esos recipientes, y cuando estén llenos, colócalos aparte».
</p><p>
5 Ella se fue y se encerró con sus hijos; estos le presentaban los recipientes, y ella los iba llenando.
</p><p>
6 Cuando todos estuvieron llenos, ella dijo a su hijo: «Alcánzame otro recipiente». Pero él respondió: «Ya no quedan más». Entonces dejó de correr el aceite.
</p><p>
7 Ella fue a informar al hombre de Dios, y este le dijo: «Ve a vender el aceite y paga la deuda; después, tú y tus hijos podrán vivir con el resto».
</p><p>
El hijo de la mujer de Sunám
</p><p>
8 Un día, Eliseo pasó por Sunam. Había allí una mujer pudiente, que le insistió para que se quedara a comer. Desde entonces, cada vez que pasaba, él iba a comer allí.
</p><p>
9 Ella dijo a su marido: «Mira, me he dado cuenta de que ese que pasa siempre por nuestra casa es un santo hombre de Dios.
</p><p>
10 Vamos a construirle una pequeña habitación en la terraza; le pondremos allí una cama, una mesa, una silla y una lámpara, y así, cuando él venga, tendrá donde alojarse».
</p><p>
11 Un día Eliseo llegó por allí, se retiró a la habitación de arriba y se acostó.
</p><p>
12 Después dijo a Guejazí, su servidor: «Llama a esa buena sunamita». El servidor la llamó, y ella se presentó ante él.
</p><p>
13 Eliseo dijo entonces a Guejazí: «Dile: Realmente tú te has desvivido por nosotros: ¿qué se puede hacer por ti? ¿Necesitas una recomendación para el rey o el jefe del ejército?». Ella respondió: «Me siento muy bien donde estoy, en medio de mi gente».
</p><p>
14 Pero Eliseo insistió: «Entonces, ¿qué se puede hacer por ella?». Guejazí respondió: «Lamentablemente, no tiene un hijo y su marido es viejo».
</p><p>
15 «Llámala», dijo Eliseo. Cuando la llamó, ella se quedó junto a la puerta,
</p><p>
16 y Eliseo le dijo: El año próximo, para esta misma época, tendrás un hijo en tus brazos». Ella exclamó: «No, señor, por favor; tú eres un hombre de Dios, no engañes a tu servidora».
</p><p>
17 Pero la mujer concibió, y dio a luz un hijo al año siguiente, para esa misma época, como se lo había dicho Eliseo.
</p><p>
18 El niño creció. Y un día en que había ido a ver a su padre, que estaba con los segadores,
</p><p>
19 le dijo: «¡Ay, mi cabeza! ¡Ay, mi cabeza!». El padre dijo al servidor: «Llévaselo a su madre».
</p><p>
20 El lo tomó y se lo llevó a su madre. El niño estuvo en la falda de su madre hasta el mediodía y luego murió.
</p><p>
21 Entonces ella subió, lo acostó en la cama del hombre de Dios, cerró la puerta y salió.
</p><p>
22 Después mandó llamar a su marido y le dijo: «Envíame, por favor, a uno de los servidores con un asna; voy corriendo a ver al hombre de Dios, y en seguida vuelvo».
</p><p>
23 El le preguntó: «¿Por qué vas a verlo hoy, si no es día de luna nueva ni sábado?». Pero ella le dijo: «No te preocupes».
</p><p>
24 Luego hizo ensillar el asna y dijo a su servidor: «Toma la rienda y camina. No me detengas por el camino, a no ser que yo te lo diga».
</p><p>
25 Así partió y llegó adonde estaba el hombre de Dios, en el monte Carmelo. Cuando el hombre de Dios la divisó a lo lejos, dijo a Guejazí, su servidor: «Ahí viene nuestra sunamita.
</p><p>
26 Corre a su encuentro y dile: ¿Cómo estás? ¿Cómo están tu marido y tu hijo?». Ella respondió: «Todos bien».
</p><p>
27 Y al llegar junto al hombre de Dios, en la montaña, se abrazó a sus pies. Guejazí se acercó para apartarla, pero el hombre de Dios dijo: «Déjala, porque está muy apenada, y el Señor me lo tuvo oculto, no me manifestó nada».
</p><p>
28 Entonces ella dijo: «¿Le pedí yo un hijo a mi señor? ¿No te dije que no me ilusionaras?».
</p><p>
29 Eliseo dijo a Guejazí: «Cíñete el cinturón, toma mi bastón y vete. Si encuentras a alguien por el camino no lo saludes, y si alguien te saluda no le respondas. Coloca mi bastón sobre el rostro del muchacho».
</p><p>
30 Pero la madre replicó: «Juro por la vida del Señor y por tu propia vida que no te dejaré». Entonces Eliseo se levantó y fue detrás de ella.
</p><p>
31 Mientras tanto, Guejazí se les había adelantado y había puesto el bastón sobre el rostro del muchacho, pero este no dio señales de vida. Volvió entonces a presentarse ante Eliseo y le comunicó: «El muchacho no se ha despertado».
</p><p>
32 Cuando Eliseo llegó a la casa, vio que el muchacho estaba muerto, tendido sobre su lecho.
</p><p>
33 En seguida entró, se encerró sólo con el muchacho y oró al Señor.
</p><p>
34 Luego subió a la cama, se acostó sobre el niño y puso su boca, sus ojos y sus manos sobre la boca, los ojos y las manos del niño; permaneció recostado sobre él y la carne del niño entró en calor.
</p><p>
35 Se puso a caminar por la casa de un lado a otro, se levantó y se recostó sobre él hasta siete veces. Entonces el muchacho estornudó y abrió los ojos.
</p><p>
36 Eliseo llamó a Guejazí y le ordenó: «Llama a la sunamita». Cuando la llamó, ella vino y Eliseo le dijo: «Toma a tu hijo».
</p><p>
37 Ella entró y cayó a los pies de Eliseo con el rostro en tierra. Después levantó a su hijo y salió.
</p><p>
El caldo envenenado
</p><p>
38 Eliseo volvió a Guilgal, cuando el hambre se hacía sentir en la región. Mientras la comunidad de profetas estaba sentada delante de él, dijo a su servidor: «Coloca sobre el fuego la olla grande y prepara un caldo para la comunidad de profetas».
</p><p>
39 Uno de ellos salió al campo para recoger algunas hierbas. Encontró una especie de viña silvestre, de la que recogió los frutos salvajes hasta llenar su manto. Al volver, los cortó en pedazos y los echó a la olla del caldo, porque nadie sabía lo que eran.
</p><p>
40 Luego sirvieron la comida a los hombres, pero apenas probaron el caldo, se pusieron a gritar: «¡La muerte está en esa olla, hombre de Dios!» Y no pudieron comer.
</p><p>
41 Eliseo dijo: «Traigan harina». El la arrojó en la olla y agregó: «Sírvele a esta gente, para que coman». Y ya no había nada malo en la olla.
</p><p>
La multiplicación de los panes
</p><p>
42 Llegó un hombre de Baal Salisá, trayendo al hombre de Dios pan de los primeros frutos: veinte panes de cebada y grano recién cortado, en una alforja. Eliseo dijo: «Dáselo a la gente para que coman».
</p><p>
43 Pero su servidor respondió: «¿Cómo voy a servir esto a cien personas?». «Dáselo a la gente para que coman, replicó él, porque así habla el Señor: Comerán y sobrará».
</p><p>
44 El servidor se lo sirvió: todos comieron y sobró, conforme a la palabra del Señor.
</p>
