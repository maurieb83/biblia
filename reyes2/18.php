<h3>SEGUNDO LIBRO DE LOS REYES</h3>
<p>Capítulo 18
</p><p>
El reinado de Ezequías en Judá (716-687)
</p><p>
2 Cro 29.1-2
</p><p>
1 El tercer año de Oseas, hijo de Elá, rey de Israel, inició su reinado Ezequías, hijo de Ajaz, rey de Judá.
</p><p>
2 Tenía veinticinco años cuando comenzó a reinar, y reinó veintinueve años en Jerusalén. Su madre se llamaba Abí, hija de Zacarías.
</p><p>
3 El hizo lo que es recto a los ojos del Señor, tal como lo había hecho su padre David.
</p><p>
4 Hizo desaparecer los lugares altos, rompió las piedras conmemorativas, taló el poste sagrado e hizo pedazos la serpiente de bronce que había hecho Moisés, porque hasta esos días los israelitas le quemaban incienso; se la llamaba Nejustán.
</p><p>
5 Ezequías puso su confianza en el Señor, el Dios de Israel, y no hubo después de él ninguno igual entre todos los reyes de Judá, como tampoco lo hubo antes que él.
</p><p>
6 Se mantuvo fiel al Señor sin apartarse de él, y observó los mandamientos que el Señor había dado a Moisés.
</p><p>
7 Tuvo éxito en todas sus empresas, porque el Señor estaba con él. Se rebeló contra el rey de Asiria y no fue más su vasallo.
</p><p>
8 Derrotó a los filisteos hasta Gaza y devastó su territorio, desde las torres de guardia hasta las plazas fuertes.
</p><p>
Evocación de la caída de Samaría
</p><p>
9 El cuarto año del rey Ezequías, que era el séptimo año de Oseas, hijo de Elá, rey de Israel, Salmanasar, rey de Asiria, subió contra Samaría y la sitió.
</p><p>
10 Al cabo de tres años la conquistaron: en el sexto año de Ezequías, que era el noveno año de Oseas, rey de Israel, fue tomada Samaría.
</p><p>
11 El rey de Asiria deportó a los israelitas a Asiria y los estableció en Jalaj, y también junto al Jabor, río de Gozán, y en las ciudades de los medos.
</p><p>
12 Esto sucedió porque no habían escuchado la voz del Señor, su Dios, y habían transgredido su alianza; todo lo que había mandado Moisés, el servidor del Señor, ellos no lo habían escuchado ni practicado.
</p><p>
La invasión de Senaquerib y el tributo impuesto a Ezequías
</p><p>
2 Cro 32.1; Is 36.1
</p><p>
13 El decimocuarto año del rey Ezequías, Senaquerib, rey de Asiria, subió contra todas las ciudades fortificadas de Judá y se apoderó de ellas.
</p><p>
14 Ezequías, rey de Judá, mandó a decir al rey de Asiria, que estaba en Laquis: «He cometido un error; retírate y aceptaré lo que me impongas». El rey de Asiria exigió al rey Ezequías, rey de Judá, trescientos talentos de plata y trescientos talentos de oro.
</p><p>
15 Ezequías entregó entonces toda la plata que se encontraba en la Casa del Señor y en los tesoros de la casa del rey.
</p><p>
16 Fue en aquel tiempo cuando Ezequías desmanteló las puertas del Templo del Señor y los soportes que el mismo Ezequías, rey de Judá, había recubierto de metal, para entregarlos al rey de Asiria.
</p><p>
Amenazas de Senaquerib contra Jerusalén
</p><p>
2 Cro 32.9-19; Is 36.2-22
</p><p>
17 Desde Laquis, el rey de Asiria envió a Jerusalén, donde estaba Ezequías, al general en jefe, al jefe de los eunucos y al copero mayor, acompañados de una fuerte escolta. Ellos subieron y, al llegar a Jerusalén, se apostaron junto al canal de la piscina superior, sobre la seda del campo del Tintorero.
</p><p>
18 Llamaron al rey, y Eliaquim, hijo de Jilquías, el mayordomo de palacio, salió a su encuentro, junto con Sebná, el secretario, y Joaj, hijo de Asaf, el archivista.
</p><p>
19 El copero mayor les dijo: «Digan a Ezequías: Así habla el gran rey, el rey de Asiria: ¿Qué motivo tienes para estar tan confiado?
</p><p>
20 ¿Piensas que la estrategia y el valor para el combate son cuestión de palabras? ¿En quien confías para rebelarte contra mí
</p><p>
21 ¡Ah, sí! Tú confías en el apoyo de esa caña quebrada, en Egipto, que perfora y atraviesa la mano de todo el que se apoya en él. Eso es el Faraón, rey de Egipto, para todos los que confían en él.
</p><p>
22 Seguramente, tú me dirás: «Nosotros confiamos en el Señor, nuestro Dios». Pero ¿no fue acaso Ezequías el que suprimió todos los lugares altos y los altares dedicados a él, diciendo a la gente de Judá y de Jerusalén: «Sólo delante de este altar, en Jerusalén, ustedes deberán postrarse»?
</p><p>
23 ¡Y bien! Haz una apuesta con mi señor, el rey de Asiria: ¡Yo te daré dos mil caballos, si puedes conseguir bastantes hombres para montarlos!
</p><p>
24 ¿Cómo harías retroceder a uno solo de los más insignificantes servidores de mi señor? ¡Pero tú confías en Egipto para tener carros de guerra y soldados!
</p><p>
25 ¿Acaso he venido a arrasar este país sin el consentimiento del Señor? Fue el Señor quien me dijo: ¡Sube contra ese país y arrásalo!».
</p><p>
26 Eliaquim, hijo de Jilquías, Sebná y Joaj dijeron al copero mayor: «Por favor, háblanos en arameo, porque nosotros lo entendemos. No nos hables en hebreo, a oídos del pueblo que está sobre la muralla».
</p><p>
27 Pero el copero mayor les replicó» «¿Acaso mi señor me envió a decir estas cosas a tu señor y a ti? ¿No están dirigidas a esos hombres apostados sobre la muralla, que tendrán que comer sus excrementos y beber su orina, igual que ustedes?».
</p><p>
28 Entonces el copero mayor, puesto de pie, gritó bien fuerte en hebreo: «Escuchen la palabra del gran rey, el rey de Asiria:
</p><p>
29 Así habla el rey: Que Ezequías no los engañe, porque él no podrá librarnos de mi mano.
</p><p>
30 Y que Ezequías no los induzca a confiar en el Señor, diciendo: Seguramente el Señor nos librará, y esta ciudad no caerá en manos del rey de Asiria.
</p><p>
31 No le hagan caso a Ezequías, porque así habla el rey de Asiria: Hagan las paces conmigo y ríndanse. Así cada uno de ustedes comerá los frutos de su viña y de su higuera, y beberá el agua de su pozo,
</p><p>
32 hasta que venga yo y los lleve a un país como el de ustedes, un país de trigo y vino nuevo, un país de pan y viñedos, un país de olivares, de aceite fresco y de miel; así ustedes vivirán y no morirán. Pero no escuchen a Ezequías, porque él los seduce, diciendo: «El Señor nos librará».
</p><p>
33 ¿Acaso los dioses de las naciones han librado a sus países de la mano del rey de Asiria?
</p><p>
34 ¿Dónde están los dioses de Sefarvaim, de Hená y de Ivá? ¿Dónde los dioses del país de Samaría?
</p><p>
35 Entre todos los dioses de esos países, ¿hubo alguno que librara de mi mano a su propio país, para que el Señor libre de mi mano a Jerusalén?».
</p><p>
36 El pueblo guardó silencio y no le respondió ni una sola palabra, porque esta era la orden del rey: «No le respondan nada».
</p><p>
37 Eliaquim, hijo de Jilquías, el mayordomo de palacio, Sebná, el secretario, y Joaj, hijo de Asaf, el archivista, se presentaron ante Ezequías con sus vestiduras desgarradas, y le informaron de las palabras del copero mayor.
</p>
