<h3>CARTA A LOS ROMANOS</h3>
<p>Capítulo 3
</p><p>
1 ¿Cuál es entonces la superioridad del judío, y qué utilidad tiene la circuncisión?
</p><p>
2 Las ventajas con muchas desde todo punto de vista. Ante todo, Dios confió su Palabra a los judíos.
</p><p>
3 ¿Y que importa que algunos no hayan creído? ¿Acaso su incredulidad anulará la fidelidad de Dios?
</p><p>
4 De ninguna manera: Dios es veraz, y todo hombre, mentiroso, porque como dice la Escritura: "Serás reconocido como justo por lo que dices y triunfarás cuando seas juzgado".
</p><p>
5 Ahora bien, si nuestra injusticia hace resaltar la justicia de Dios, ¿qué conclusión sacaremos? ¿Dios será injusto –me expreso en términos humanos– al dar libre curso a su ira?
</p><p>
6 De ningún modo. De lo contrario, ¿cómo podría Dios juzgar al mundo?
</p><p>
7 Pero si con mi mentira, la verdad de Dios sale ganando, para gloria suya, ¿por qué todavía voy a ser condenado como pecador?
</p><p>
8 ¿O debemos hacer el mal para que resulte el bien, como algunos calumniadores nos hacen decir? ¡Estos sí merecen ser condenados!
</p><p>
9 En definitiva, entonces, ¿somos o no superiores a los paganos? De ninguna manera.
</p><p>
10 Porque acabamos de probar que todos están sometidos al pecado, tanto los judíos como los que no lo son. Así lo afirma la Escritura: "No hay ningún justo, ni siquiera uno;
</p><p>
11 no hay nadie que comprenda, nadie que busque a Dios.
</p><p>
12 Todos están extraviados, igualmente corrompidos; nadie practica el bien, ni siquiera uno solo.
</p><p>
13 Su garganta es un sepulcro abierto; engañan con su lengua, sus labios destilan veneno de víboras,
</p><p>
14 su boca está llena de maldición y amargura.
</p><p>
15 Sus pies son rápidos para derramar sangre,
</p><p>
16 en sus caminos hay ruina y miseria,
</p><p>
17 no conocen la senda de la paz.
</p><p>
18 El temor de Dios no está ante sus ojos".
</p><p>
19 Ahora bien, nosotros sabemos que todo lo que dice la Ley es válido solamente para los que están bajo la Ley, a fin de que nadie pueda alegar inocencia y todo el mundo sea reconocido culpable delante de Dios.
</p><p>
20 Porque a los ojos de Dios, nadie será justificado por las obras de la Ley, ya que la Ley se limita a hacernos conocer el pecado.
</p><p>
21 Pero ahora, sin la Ley, se ha manifestado la justicia de Dios atestiguada por la Ley y los Profetas:
</p><p>
22 la justicia de Dios, por la fe en Jesucristo, para todos los que creen. Porque no hay ninguna distinción:
</p><p>
23 todos han pecado y están privados de la gloria de Dios,
</p><p>
24 pero son injustificados gratuitamente por su gracia, en virtud de la redención cumplida en Cristo Jesús.
</p><p>
25 El fue puesto por Dios como instrumento de propiciación por su propia sangre, gracias a la fe. De esa manera, Dios ha querido mostrar su justicia:
</p><p>
26 en el tiempo de la paciencia divina, pasando por alto los pecados cometidos anteriormente, y en el tiempo presente, siendo justo y justificado a los que creen en Jesús.
</p><p>
27 ¿Qué derecho hay entonces para gloriarse? Ninguna. Pero, ¿en virtud de qué ley se excluye ese derecho? ¿Por la ley de las obras? No, sino por la ley de la fe.
</p><p>
28 Porque nosotros estimamos que le hombre es justificado por al fe, sin las obras de la Ley.
</p><p>
29 ¿Acaso Dios es solamente el Dios de los judíos? ¿No lo es también de los paganos? Evidentemente que sí,
</p><p>
30 porque no hay más que un solo Dios, que justifica por medio de la fe tanto a los judíos circuncidados como a los paganos incircuncisos.
</p><p>
31 Entonces, ¿por medio de la fe, anulamos la Ley? ¡Ni pensarlo! Por el contrario, la confirmamos.
</p>
