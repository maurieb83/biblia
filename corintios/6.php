<h3>PRIMERA CARTA A LOS CORINTIOS</h3>
<p>Capítulo 6
</p><p>
1 ¡Cómo es posible que cuando uno de ustedes tienen algún conflicto con otro, se atreve a reclamar justicia a los injustos, en lugar de someterse al juicio de los santos?
</p><p>
2 ¿No saben ustedes que los santos juzgarán al mundo? Y si el mundo va ser juzgado por ustedes, ¿cómo no van a ser capaces de juzgar asuntos de mínima importancia?
</p><p>
3 ¿Ignoran que vamos a juzgar a los mismos ángeles? Con mayor razón entonces, los asuntos de esta vida.
</p><p>
4 ¡Y pensar que cuando ustedes tienen litigios, buscan como jueces a los que no son nadie para la Iglesia!
</p><p>
5 Lo digo para avergonzarlos: ¡por lo visto, no hay entre ustedes ni siquiera un hombre sensato, que sea capaz de servir de árbitro entre sus hermanos!
</p><p>
6 ¡Un hermano pleitea con otro, y esto, delante de los que no creen!
</p><p>
7 Ya está mal que haya litigios entre ustedes: ¿acaso no es preferible sufrir la injusticia o ser despojado?
</p><p>
8 Pero no, ustedes mismos son los que cometen injusticias y defraudan a los demás, ¡y esto entre hermanos!
</p><p>
9 ¿Ignoran que los injustos no heredarán el Reino de Dios? No se hagan ilusiones: ni los inmorales, ni los idólatras, ni los adúlteros, ni los afeminados, ni los pervertidos,
</p><p>
10 ni los ladrones, ni los avaros, ni los bebedores, ni los difamadores, ni los usurpadores heredarán el Reino de Dios.
</p><p>
11 Algunos de ustedes fueron así, pero ahora han sido purificados, santificados y justiciados en el nombre de nuestro Señor Jesucristo y por Espíritu de nuestro Dios.
</p><p>
12 «Todo me está permitido», pero no todo es conveniente. Todo me está permitido», pero no me dejaré dominar por nada.
</p><p>
13 Los alimentos son para el estómago y el estómago para los alimentos, y Dios destruirá a ambos. Pero el cuerpo no es para la fornicación, sino para el Señor, y el Señor es para el cuerpo.
</p><p>
14 Y Dios que resucitó al Señor, nos resucitará también a nosotros con su poder.
</p><p>
15 ¿No saben acaso que sus cuerpos son miembros de Cristo? ¿Cómo voy a tomar los miembros de Cristo para convertirlos en miembros de una prostituta? De ninguna manera.
</p><p>
16 ¿No saben que el que se une a una prostituta, se hace un solo cuerpo con ella? Porque dice la Escritura: Los dos serán una sola carne.
</p><p>
17 En cambio, el que se une al Señor se hace un solo espíritu con él.
</p><p>
18 Eviten la fornicación. Cualquier otro pecado cometido por el hombre es exterior a su cuerpo, pero el que fornica peca contra su propio cuerpo.
</p><p>
19 ¿O no saben que sus cuerpos son templo del espíritu Santo, que habita en ustedes y que han recibido de Dios? Por lo tanto, ustedes no se pertenecen,
</p><p>
20 sino que han sido comprados, ¡y a qué precio! Glorifiquen entonces a Dios en sus cuerpos.
</p>
