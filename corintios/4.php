<h3>PRIMERA CARTA A LOS CORINTIOS</h3>
<p>Capítulo 4
</p><p>
1 Los hombres deben considerarnos simplemente como servidores de Cristo y administradores de los misterios de Dios.
</p><p>
2 Ahora bien, lo que se pide a un administrador es que sea fiel.
</p><p>
3 En cuanto a mí, poco me importa que me juzguen ustedes o un tribunal humano; ni siquiera yo mismo me juzgo.
</p><p>
4 Es verdad que mi conciencia nada me reprocha, pero no por eso estoy justificado: mi juez es el Señor.
</p><p>
5 Por eso, no hagan juicios prematuros. Dejen que venga el Señor: él sacará a la luz lo que está oculto en las tinieblas y manifestará las intenciones secretas de los corazones. Entonces, cada uno recibirá de Dios la alabanza que le corresponda.
</p><p>
6 En todo esto, hermanos, les puse mi ejemplo y el de Apolo, a fin de que aprendan de nosotros el refrán: «No vayamos más allá de lo que está escrito», y así nadie tome partido orgullosamente en favor de uno contra otro.
</p><p>
7 En efecto, ¿con qué derecho te distingues de los demás? ¿Y qué tienes que no hayas recibido? Y si lo has recibido, ¿por qué te glorías como si no lo hubieras recibido?
</p><p>
8 ¡Será que ustedes ya están satisfechos! ¡Será que se han enriquecido o que se han convertido en reyes, sin necesidad de nosotros! ¡Ojalá que así fuera, para que nosotros pudiéramos reinar con ustedes!
</p><p>
9 Pienso que a nosotros, los Apóstoles, Dios nos ha puesto en el último lugar, como condenados a muerte, ya que hemos llegado a ser un espectáculo para el mundo, para los ángeles y los hombres.
</p><p>
10 Nosotros somos tenidos por necios, a causa de Cristo, y en cambio, ustedes son sensatos en Cristo. Nosotros somos débiles, y ustedes, fuertes. Ustedes gozan de prestigio, y nosotros somos despreciados.
</p><p>
11 Hasta ahora sufrimos hambre, sed y frío. Somos maltratados y vivimos errantes.
</p><p>
12 Nos agotamos, trabajando con nuestras manos.
</p><p>
13 Nos insultan y deseamos el bien. Padecemos persecución y la soportamos. Nos calumnian y consolamos a los demás. Hemos llegado a ser como la basura del mundo, objeto de desprecio para todos hasta el día de hoy.
</p><p>
14 No les escribo estas cosas para avergonzarlos, sino para reprenderlos como a hijos muy queridos.
</p><p>
15 Porque, aunque tengan diez mil preceptores en Cristo, no tienen muchos padres: soy yo el que los ha engendrado en Cristo Jesús, mediante la predicación de la Buena Noticia.
</p><p>
16 Les ruego, por lo tanto, que sigan mi ejemplo.
</p><p>
17 Por esta misma razón les envié a Timoteo, mi hijo muy querido y fiel en el Señor; él les recordará mis normas de conducta, que son las de Cristo, y que yo enseño siempre en todas las Iglesias.
</p><p>
18 Algunos de ustedes, pensando que yo no regresaría, se han llenado de orgullo.
</p><p>
19 Pero pronto iré a verlos –si así lo quiere el Señor– y entonces los juzgaré, no por sus palabras, sino por el poder que tienen.
</p><p>
20 ¡Porque el Reino de Dios no es cuestión de palabras sino de poder!
</p><p>
21 ¿Qué prefieren? ¿Qué vaya a verlos con la vara en la mano, o con amor y espíritu de mansedumbre?
</p>
