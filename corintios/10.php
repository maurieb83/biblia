<h3>PRIMERA CARTA A LOS CORINTIOS</h3>
<p>Capítulo 10
</p><p>
1 Porque no deben ignorar, hermanos, que todos nuestros padres fueron guiados por la nube y todos atravesaron el mar;
</p><p>
2 y para todos, la marcha bajo la nube y el paso del mar, fue un bautismo que los unió a Moisés.
</p><p>
3 También todos comieron la misma comida y bebieron la misma bebida espiritual.
</p><p>
4 En efecto, bebían el agua de una roca espiritual que los acompañaba, y esa roca era Cristo.
</p><p>
5 A pesar de esto, muy pocos de ellos fueron agradables a Dios, porque sus cuerpos quedaron tendidos en el desierto.
</p><p>
6 Todo esto aconteció simbólicamente para ejemplo nuestro, a fin de que no nos dejemos arrastrar por los malos deseos, como lo hicieron nuestros padres.
</p><p>
7 No adoren a falsos dioses, como hicieron algunos de ellos, según leemos en la Escritura: El pueblo se sentó a comer y a beber, y luego se levantó para divertirse.
</p><p>
8 No forniquemos, como algunos de ellos, y por eso, en castigo, murieron veintitrés mil en un solo día.
</p><p>
9 No provoquemos al Señor, como hicieron algunos de ellos, y perecieron víctimas de las serpientes.
</p><p>
10 No nos rebelemos contra Dios, como algunos de ellos, por lo cual murieron víctimas del Angel exterminador.
</p><p>
11 Todo esto les sucedió simbólicamente, y está escrito para que nos sirva de lección a los que vivimos en el tiempo final.
</p><p>
12 Por eso, el que se cree muy seguro, ¡cuídese de no caer!
</p><p>
13 Hasta ahora, ustedes no tuvieron tentaciones que superen sus fuerzas humanas. Dios es fiel, y él no permitirá que sean tentados más allá de sus fuerzas. Al contrario, en el momento de la tentación, les dará el medio de librarse de ella, y los ayudará a soportarla.
</p><p>
14 Por esto, queridos míos, eviten la idolatría.
</p><p>
15 Les hablo como a gente sensata; juzguen ustedes mismos lo que voy a decirles.
</p><p>
16 La copa de bendición que bendecimos, ¿no es acaso comunión con la Sangre de Cristo? Y el pan que partimos, ¿no es comunión con el Cuerpo de Cristo?
</p><p>
17 Ya que hay un solo pan, todos nosotros, aunque somos muchos, formamos un solo Cuerpo, porque participamos de ese único pan.
</p><p>
18 Pensemos en Israel según la carne: aquellos que comen las víctimas, ¿no están acaso en comunión con el altar?
</p><p>
19 ¿Quiero decir con esto que la carne sacrificada a los ídolos tiene algún valor, o que el ídolo es algo?
</p><p>
20 No, afirmo sencillamente que los paganos ofrecen sus sacrificios a los demonios y no a Dios. Ahora bien, yo no quiero que ustedes entren en comunión con los demonios.
</p><p>
21 Ustedes no pueden beber de la copa del Señor y de la copa de los demonios; tampoco pueden sentarse a la mesa del Señor y a la mesa de los demonios.
</p><p>
22 ¿O es que queremos provocar los celos del Señor? ¿Pretendemos ser más fuertes que él?
</p><p>
23 «Todo está permitido», pero no todo es conveniente. «Todo está permitido», pero no todo es edificante.
</p><p>
24 Que nadie busque su propio interés, sino el de los demás.
</p><p>
25 Coman de todo lo que se vende en el mercado, sin hacer averiguaciones por escrúpulos de conciencia.
</p><p>
26 Porque del Señor es la tierra y todo lo que hay en ella.
</p><p>
27 Si un pagano los invita a comer y ustedes aceptan, coman de todo aquello que les sirvan, sin preguntar nada por motivos de conciencia.
</p><p>
28 Pero si alguien les dice: «Esto ha sido sacrificado a los ídolos», entonces no lo coman, en consideración del que los previno y por motivos de conciencia.
</p><p>
29 Me refiero a la conciencia de ellos, no a la de ustedes: ¿acaso mi libertad va ser juzgada por la conciencia de otro?
</p><p>
30 Si yo participo de la comida habiendo dado gracias, ¿seré reprendido por aquello mismo de lo que he dado gracias?
</p><p>
31 En resumen, sea que ustedes coman, sea que beban, o cualquier cosa que hagan, háganlo todo para la gloria de Dios.
</p><p>
32 No sean motivo de escándalo ni para los judíos ni para los paganos ni tampoco para la Iglesia de Dios.
</p><p>
33 Hagan como yo, que me esfuerzo por complacer a todos en todas las cosas, no buscando mi interés personal, sino el del mayor número, para que puedan salvarse.
</p>
