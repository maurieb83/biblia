<h3>JEREMIAS</h3>
<p>Capítulo 31
</p><p>
1 En aquel tiempo –oráculo del Señor– yo seré el Dios de todas las familias de Israel y ellos serán mi Pueblo.
</p><p>
2 Así habla el Señor: Halló gracia en el desierto el pueblo que escapó de la espada; Israel camina hacia su descanso.
</p><p>
3 De lejos se le apareció el Señor: Yo te amé con un amor eterno, por eso te atraje con fidelidad.
</p><p>
4 De nuevo te edificaré y serás reedificada, virgen de Israel; de nuevo te adornarás con tus tamboriles y saldrás danzando alegremente;
</p><p>
5 de nuevo plantarás viñas sobre los montes de Samaría: los que las planten tendrán los primeros frutos.
</p><p>
6 Porque llega el día en que la los vigías gritarán sobre la montaña de Efraím: «¡De pie, subamos a Sión, hacia el Señor, nuestro Dios!».
</p><p>
7 Porque así habla el Señor: ¡Griten jubilosos por Jacob, aclamen a la primera de las naciones! Háganse oír, alaben y digan: «¡El Señor ha salvado a su pueblo, al resto de Israel!».
</p><p>
8 Yo los hago venir del país del Norte y los reúno desde los extremos de la tierra; hay entre ellos ciegos y lisiados, mujeres embarazadas y parturientas: ¡es una gran asamblea la que vuelve aquí!
</p><p>
9 Habían partido llorando, pero yo los traigo llenos de consuelo; los conduciré a los torrentes de agua por un camino llano, donde ellos no tropezarán. Porque yo soy un padre para Israel y Efraím es mi primogénito.
</p><p>
10 ¡Escuchen, naciones, la palabra del Señor, anúncienla en las costas más lejanas! Digan: «El que dispersó a Israel lo reunirá, y lo cuidará como un pastor a su rebaño».
</p><p>
11 Porque el Señor ha rescatado a Jacob, lo redimió de una mano más fuerte que él.
</p><p>
12 Llegarán gritando de alegría a la altura de Sión, afluirán hacia los bienes del Señor, hacia el trigo, el vino nuevo y el aceite, hacia las crías de ovejas y de vacas. Sus almas serán como un jardín bien regado y no volverán a desfallecer.
</p><p>
13 Entonces la joven danzará alegremente, los jóvenes y los viejos se regocijarán; yo cambiaré su duelo en alegría, los alegraré y los consolaré de su aflicción.
</p><p>
14 Colmaré a los sacerdotes con la grasa de las víctimas, y mi pueblo se saciará de mis bienes –oráculo del Señor–.
</p><p>
15 Así habla el Señor: ¡Escuchen! En Ramá se oyen lamentos, llantos de amargura: es Raquel que llora a sus hijos; ella no quiere ser consolada, porque ya no existen.
</p><p>
16 Así habla el Señor: Reprime tus sollozos, ahoga tus lágrimas, porque tu obra recibirá su recompensa –oráculo del Señor– y ellos volverán del país enemigo.
</p><p>
17 Sí, hay esperanza para tu futuro –oráculo del Señor–: los hijos regresarán a su patria.
</p><p>
18 Oigo muy bien a Efraím que se estremece de pesar: «Me has corregido, y yo acepté la corrección como un ternero no domado. Conviérteme y yo me convertiré, porque tú, Señor, eres mi Dios.
</p><p>
19 Sí, después de apartarme, me arrepentí, y al darme cuenta, me he golpeado el pecho. Estoy avergonzado y confundido, porque cargo con el oprobio de mi juventud».
</p><p>
20 ¿Es para mí Efraím un hijo querido o un niño mimado, para que cada vez que hablo de él, todavía lo recuerde vivamente? Por eso mis entrañas se estremecen por él, no puedo menos que compadecerme de él –oráculo del Señor–.
</p><p>
21 Levanta para ti mojones, colócate señales, fíjate bien en el sendero, en el camino que has recorrido. ¡Vuelve, virgen de Israel, vuelve a estas tus ciudades!
</p><p>
22 ¿Hasta cuándo irás de aquí para allá, hija apóstata? Porque el Señor crea algo nuevo en el país: la mujer rodea al varón.
</p><p>
23 Así habla el Señor de los ejércitos, el Dios de Israel: De nuevo se dirá esta palabra en el país de Judá y en sus ciudades, cuando yo haya cambiado su suerte: ¡Que el Señor te bendiga, morada de justicia, Montaña santa!
</p><p>
24 Allí se establecerán Judá y todas sus ciudades, los agricultores y los que se desplazan con los rebaños.
</p><p>
25 Porque yo abrevaré a los que están agotados y colmaré a los que están exhaustos.
</p><p>
26 De pronto me desperté y abrí los ojos, y mi sueño había sido agradable.
</p><p>
27 Llegarán los días –oráculo del Señor– en que yo sembraré la casa de Israel y la casa de Judá con semilla de hombres y semilla de animales.
</p><p>
28 Y así como yo he velado sobre ellos para arrancar y derribar, para demoler, perder y hacer el mal, así también velaré sobre ellos para edificar y para plantar –oráculo del Señor–.
</p><p>
29 En aquellos días, no se dirá más: Los padres comieron uva verde y los hijos sufren la dentera.
</p><p>
30 No, cada uno morirá por su propia iniquidad: todo el que coma uva verde sufrirá la dentera.
</p><p>
31 Llegarán los días –oráculo del Señor– en que estableceré una nueva Alianza con la casa de Israel y la casa de Judá.
</p><p>
32 No será como la Alianza que establecí con sus padres el día en que los tomé de la mano para hacerlos salir del país de Egipto, mi Alianza que ellos rompieron, aunque yo era su dueño –oráculo del Señor–.
</p><p>
33 Esta es la Alianza que estableceré con la casa de Israel, después de aquellos días –oráculo del Señor–: pondré mi Ley dentro de ellos, y la escribiré en sus corazones; yo seré su Dios y ellos serán mi Pueblo.
</p><p>
34 Y ya no tendrán que enseñarse mutuamente, diciéndose el uno al otro: «Conozcan al Señor». Porque todos me conocerán, del más pequeño al más grande –oráculo del Señor–. Porque yo habré perdonado su iniquidad y no me acordaré más de su pecado.
</p><p>
35 Así habla el Señor, el que puso el sol para alumbrar el día, la luna y las estrella para iluminar la noche, el que agita el mar y rugen sus olas; su nombre es Señor de los ejércitos:
</p><p>
36 Si este ordenamiento dejara de regir delante de mi –oráculo del Señor– entonces, también la descendencia de Israel dejaría de ser para siempre una nación delante de mí.
</p><p>
37 Así habla el Señor: Si se pudieran medir los cielos en lo alto y explorar aquí abajo los cimientos de la tierra, también yo rechazaría a la descendencia de Israel a causa de todo lo que hicieron –oráculo del Señor–.
</p><p>
38 Llegarán los días –oráculo del Señor– en que la ciudad será reconstruida para el Señor, desde la torre de Jananel hasta la puerta del Angulo.
</p><p>
39 La cuerda de medir se extenderá en línea recta hasta la colina de Gareb, y luego girará hacia Goa.
</p><p>
40 Todo el valle de los cadáveres y de la ceniza de los sacrificios, y todos los campos hasta el torrente Cedrón, hasta el ángulo de la puerta de los Caballos, al oriente, estarán consagrados al Señor: ¡nunca más se arrancará, nunca más se demolerá!
</p>
