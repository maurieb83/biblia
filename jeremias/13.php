<h3>JEREMIAS</h3>
<p>Capítulo 13
</p><p>
1 Así me habló el Señor: «Ve a comprarte una faja de lino; te la ajustarás a la cintura, pero no la meterás en el agua».
</p><p>
2 Yo compré la faja, conforme a la palabra del Señor, y me la ajusté a la cintura.
</p><p>
3 La palabra del Señor me llegó por segunda vez, en estos términos:
</p><p>
4 «Toma la faja que habías comprado y que llevas puesta a la cintura. Ve en seguida a Perat y escóndela allí en la hendidura de una roca».
</p><p>
5 Yo fui a esconderla en Perat, como el Señor me lo había ordenado.
</p><p>
6 Al cabo de muchos días, el Señor me dijo: «Ve enseguida a Perat y recoge la faja que yo te mandé esconder allí».
</p><p>
7 Yo fui a Perat, cavé y recogí la faja del lugar donde la había escondido: la faja estaba estropeada, no servía para nada.
</p><p>
8 Entonces la palabra del Señor me llegó en estos términos:
</p><p>
9 Así habla el Señor: De esa misma manera destruiré el orgullo de Judá y el gran orgullo de Jerusalén.
</p><p>
10 Este pueblo malvado, que se niega a escuchar mis palabras, que sigue los impulsos de su corazón obstinado, que va detrás de otros dioses para servirlos y postrarse delante de ellos, será como esta faja que ya no sirve para nada.
</p><p>
11 Porque así como la faja se adhiere a la cintura del hombre, así yo me había adherido a toda la casa de Israel y a toda la casa de Judá –oráculo del Señor– para que ellos fueran mi pueblo, ni renombre, mi honor y mi gloria. ¡Pero no han escuchado!
</p><p>
12 Tú les dirás esta palabra: Así habla el Señor, el Dios de Israel: «Todo cántaro debe llenarse de vino». Y si ellos te dicen: «¿No sabemos acaso que todo cántaro debe llenarse de vino?»,
</p><p>
13 tú les responderás: «Así habla el Señor: Yo voy a llenar de bebida embriagante a todos los habitantes de este país: a los reyes descendientes de David que se sientan en su trono, a los sacerdotes, a los profetas y a todos los habitantes de Jerusalén.
</p><p>
14 Los haré pedazos unos contra otros, a los padres y a los hijos juntamente –oráculo del Señor–: no me dejaré llevar de la piedad, ni de la clemencia, ni de la compasión, sino que los destruiré».
</p><p>
15 ¡Escuchen y presten atención, no sean altaneros, porque ha hablado el Señor!
</p><p>
16 ¡Den gloria al Señor, su Dios, antes que él haga oscurecer, antes que los pies de ustedes tropiecen contra las montañas del crepúsculo! Ustedes aguardan la luz, y él la cambiará en tinieblas, la convertirá en densa oscuridad.
</p><p>
17 Si ustedes no escuchan esto, mi alma llorará en secreto, por el orgullo de ustedes; lloraré a lágrima viva, mis ojos se disolverán en lágrimas, porque el rebaño del Señor irá al cautiverio.
</p><p>
18 Digan al rey y a la reina madre: Siéntense en el suelo, porque se les ha caído de la cabeza la corona de gloria.
</p><p>
19 Las ciudades del Négueb están bloqueadas y nadie abre paso. Todo Judá ha sido deportado, deportado masivamente.
</p><p>
20 ¡Levanta los ojos, Jerusalén, y mira a los que llegan del Norte! ¿Dónde está el rebaño que se te había confiado, las ovejas que eran tu gloria?
</p><p>
21 ¿Qué dirás cuando te impongan como jefes a esos mismos que tú habías acostumbrado a ser tus amigos íntimos? ¿No serás acaso presa de los dolores como una parturienta?
</p><p>
22 Tal vez te digas entonces: «¿Por qué me pasa esto?». Por tu gran iniquidad te han levantado las faldas, han sido violados tus talones.
</p><p>
23 ¿Puede un Etíope cambiar de piel o un leopardo de pelaje? Así ustedes, ¿podrían hacer el bien, habituados como están a hacer el mal?
</p><p>
24 Pero yo los dispersaré como paja al viento del desierto.
</p><p>
25 Esta es tu suerte, la parte que yo he medido para ti –oráculo del Señor– Porque tú me has olvidado y has confiado en la mentira,
</p><p>
26 yo, a mi vez, te alzaré las faldas hasta el rostro para que se vea tu vergüenza.
</p><p>
27 ¡Tus adulterios y tus relinchos, tu infame prostitución! Sobre las colinas, en los campos, he visto tus Inmundicias. ¡Ay de ti, Jerusalén, que no te purificas! ¿Hasta cuándo seguirás así?
</p>
