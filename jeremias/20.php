<h3>JEREMIAS</h3>
<p>Capítulo 20
</p><p>
1 El sacerdote Pasjur, hijo de Imer, inspector principal de la Casa del Señor, oyó a Jeremías profetizar estas cosas.
</p><p>
2 Entonces mandó golpear a Jeremías, el profeta, y lo hizo poner en el cepo que está en la puerta Alta de Benjamín, en la Casa del Señor.
</p><p>
3 Pero a la mañana siguiente, cuando Pasjur sacó a Jeremías del cepo, este le dijo: «El nombre que dio el Señor no es Pasjur, sino «Terror por todas partes».
</p><p>
4 Porque así habla el Señor: Yo haré que seas presa del terror, tú y todos tus amigos; ellos caerán bajo la espada de sus enemigos, y tú lo verás con tus propios ojos. Y yo entregaré a todo Judá en manos del rey de Babilonia, que los deportará a Babilonia y los herirá con la espada.
</p><p>
5 Todas las riquezas de esta ciudad, todas sus ganancias y todo lo que hay de precioso en ella, junto con todos los tesoros de los reyes de Judá, los entregaré en manos de sus enemigos: ellos los saquearán, los tomarán y se los llevarán a Babilonia.
</p><p>
6 En cuanto a ti, Pasjur, tú y todos los que habitan en tu casa irán al cautiverio: llegarás a Babilonia y allí morirás, y allí serás enterrado, tú y todos tus amigos a quienes les has profetizado falsamente».
</p><p>
7 ¡Tú me has seducido, Señor, y yo me dejé seducir! ¡Me has forzado y has prevalecido! Soy motivo de risa todo el día, todos se burlan de mí.
</p><p>
8 Cada vez que hablo, es para gritar, para clamar: «Violencia, devastación!». Porque la palabra del Señor es para mí oprobio y afrenta todo el día.
</p><p>
9 Entonces dije: «No lo voy a mencionar, ni hablaré más en su Nombre». Pero había en mi corazón como un fuego abrasador, encerrado en mis huesos: me esforzaba por contenerlo, pero no podía.
</p><p>
10 Oía los rumores de la gente: «¡Terror por todas partes! ¡Denúncienlo! ¡Sí, lo denunciaremos!». Hasta mis amigos más íntimos acechaban mi caída: «Tal vez se lo pueda seducir; prevaleceremos sobre él y nos tomaremos nuestra venganza».
</p><p>
11 Pero el Señor está conmigo como un guerrero temible: por eso mis perseguidores tropezarán y no podrán prevalecer; se avergonzarán de su fracaso, será una confusión eterna, inolvidable.
</p><p>
12 Señor de los ejércitos, que examinas al justo, que ves las entrañas y el corazón, ¡que yo vea tu venganza sobre ellos!, porque a ti he encomendado mi causa.
</p><p>
13 ¡Canten al Señor, alaben al Señor, porque él libró la vida del indigente del poder de los malhechores!
</p><p>
14 ¡Maldito el día en que nací! ¡El día en que mi madre me dio a luz jamás sea bendecido!
</p><p>
15 ¡Maldito el hombre que dio a mi padre la noticia: «Te ha nacido un hijo varón», llenándolo de alegría!
</p><p>
16 Que ese día sea como las ciudades que el Señor destruyó sin arrepentirse, que oiga gritos por la mañana y un alarido al tiempo de mediodía,
</p><p>
17 porque no me hizo morir desde el seno materno: ¡así mi madre hubiera sido mi tumba y su gravidez hubiera durado para siempre!
</p><p>
18 ¿Por qué salí del vientre materno para no ver más que pena y aflicción, y acabar mis días avergonzado?
</p>
