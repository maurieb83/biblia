<h3>JEREMIAS</h3>
<p>Capítulo 12
</p><p>
1 Tú eres demasiado justo, Señor, para que yo te recrimine; sin embargo, quiero tratar contigo una cuestión de justicia. ¿Por qué prospera el camino de los malvados y están en paz todos los traidores?
</p><p>
2 Tú los plantas y ellos echan raíces, crecen y producen fruto. Tú estás cerca de sus labios y lejos de sus sentimientos.
</p><p>
3 Pero tú me conoces, Señor, tú me ves, has sondeado mi actitud hacia ti. Arrástralos como ovejas al matadero, resérvalos para el día de la masacre.
</p><p>
4 ¿Hasta cuándo el país estará de duelo y se secará toda la hierba del campo? Por la maldad de los que habitan en él perecen las bestias y los pájaros. Porque ellos dicen: «¡El no ve nuestros senderos!».
</p><p>
5 Si ya te fatiga una carrera de a pie, ¿cómo competirás con los caballos? Si en una tierra de paz no te sientes seguro, ¿qué harás en la espesura del Jordán?
</p><p>
6 Porque hasta tus hermanos y la casa de tu padre, hasta ellos mismos te traicionan, ellos mismos gritan a voz en cuello detrás de ti. No te fíes de ellos, cuando te dirigen hermosas palabras.
</p><p>
7 He abandonado mi casa, he rechazado mi herencia, he entregado lo que más quería al poder de sus enemigos.
</p><p>
8 Mi herencia ha sido para mí como un león en la selva; ella lanzó rugidos contra mí, por eso la detesto.
</p><p>
9 Mi herencia es un pájaro multicolor, asediado por las aves de rapiña. ¡Vayan, re únanse, todas las fieras del campo, vengan a devorar!
</p><p>
10 Muchos pastores han arrasado mi viña, han pisoteado mi parcela, han hecho de mi parcela deliciosa un desierto desolado;
</p><p>
11 la han convertido en una desolación, está de duelo, desolada delante de mi; todo el país está devastado, sin que nadie se lo tome a pecho.
</p><p>
12 Por todos los montes del desierto llegaron devastadores, porque el Señor tiene una espada que devora de un extremo al otro del país; ¡no hay paz para ningún ser viviente!
</p><p>
13 Sembraron trigo y cosecharon espinas, se han agotado sin ningún provecho: ¡avergüéncense de sus cosechas, por el ardor de la ira del Señor!
</p><p>
14 Así habla el Señor: A todos mi malos vecinos que tocan la herencia que hice heredar a mi pueblo Israel, yo los voy a arrancar de su suelo, y a la casa de Judá la arrancaré de en medio de ellos.
</p><p>
15 Pero, después de haberlos arrancado, me compadeceré nuevamente de ellos y los haré volver, cada uno a su herencia y cada uno a su país.
</p><p>
16 Y si ellos aprenden los caminos de mi pueblo, jurando por mi Nombre, por la vida del Señor, como ellos enseñaron a mi pueblo a jurar por Baal, entonces serán edificados en medio de mi pueblo.
</p><p>
17 Pero si no escucha, yo arrancaré a esa gente: la arrancaré y la haré desaparecer –oráculo del Señor–.
</p>
