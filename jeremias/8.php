<h3>JEREMIAS</h3>
<p>Capítulo 8
</p><p>
1 En aquel tiempo –oráculo del Señor– sacarán de sus tumbas los huesos de los reyes de Judá, los huesos de sus príncipes, los huesos de los sacerdotes, los huesos de los profetas y los huesos de los habitantes de Jerusalén.
</p><p>
2 Los expondrán ante el sol y la luna, y ante todo el Ejército de los cielos, a los que ellos amaron y sirvieron, a los que ellos amaron y sirvieron, a los que ellos siguieron y consultaron, y ante los cuales se postraron, Y no serán recogidos ni enterrados, sino que se convertirán en estiércol sobre la superficie del suelo.
</p><p>
3 La muerte será preferible a la vida para todos los sobrevivientes que hayan quedado de esa familia perversa, en todos los lugares adonde yo los expulsaré –oráculo del Señor de los ejércitos–.
</p><p>
4 Tú les dirás: Así habla el Señor: ¿No se levanta el que cae? ¿Y no vuelve el que se desvía?
</p><p>
5 ¿Por qué entonces ha defeccionado este pueblo y Jerusalén es una apostasía sin fin? Ellos se aferran a sus ilusiones, se niegan a volver.
</p><p>
6 Yo escuché con la mayor atención: ellos no hablan como es debido, ni uno solo se arrepiente de su maldad, diciendo: «¿Qué es lo que hice?». Todos vuelven a sus andanzas, como un caballo que se lanza al combate.
</p><p>
7 Hasta la cigüeña, en el cielo, conoce sus estaciones; la tórtola, la golondrina y la grulla tienen en cuenta el tiempo de sus migraciones ¡Pero mi pueblo no conoce el derecho del Señor!
</p><p>
8 ¿Cómo ustedes se atreven a decir: «Somos sabios y la Ley del Señor está con nosotros», siendo así que la ha falsificado la pluma engañosa de los escribas?
</p><p>
9 Los sabios se cubrirán de vergüenza, quedarán espantados, atrapados. Ellos han despreciado la palabra del Señor: ¿qué sabiduría es entonces la de ellos?
</p><p>
10 Por eso, yo entregaré sus mujeres a otros, y sus campos a usurpadores. Porque del más pequeño al más grande, todos están ávidos de ganancias y desde el profeta hasta el sacerdote, no hacen otra cosa que engañar.
</p><p>
11 Ellos curan a la ligera el quebranto de la hija de mi pueblo, diciendo: «¡Paz, paz!», pero no hay paz.
</p><p>
12 ¿Se avergüenzan de la abominación que cometieron? ¡No, no sienten la menor vergüenza, no saben los que es sonrojarse! Por eso, ellos caerán con los que caen, sucumbirán cuando tengan que dar cuenta, dice el Señor.
</p><p>
13 Cuando quiero cosechar entre ellos –oráculo del Señor– no hay uvas en la viña, no hay higos en la higuera, y el follaje está marchito.
</p><p>
14 ¿Por qué nos quedamos quietos? Reúnanse y entremos en las plazas fuertes para perecer allí, porque el Señor, nuestro Dios, nos hace perecer y nos da de beber agua envenenada, porque hemos pecado contra el Señor.
</p><p>
15 Se esperaba la paz ¡y no hay nada bueno...! el tiempo de la curación, ¡y sobrevino el espanto!
</p><p>
16 Desde Dan se escucha el resuello de sus caballos; por el ruido de los relinchos de sus corceles tiembla toda la tierra. Ellos llegan y devoran el país y todo lo que hay en él, la ciudad y a los que habitan en ella.
</p><p>
17 Porque yo envío contra ustedes serpientes venenosas, contra las que no hay encantamientos: ellas los morderán –oráculo del Señor –
</p><p>
18 y no habrá remedio. Me invade la aflicción, mi corazón está dolorido.
</p><p>
19 El grito de alarma de la hija de mi pueblo se eleva a lo largo de todo el país: «¿No está el Señor en Sión, no está en ella su Rey?». ¿Por qué me han indignado con sus ídolos, con las Vanidades del extranjero?
</p><p>
20 «Pasó la cosecha, terminó el verano, ¡y nosotros no hemos sido salvados! :.
</p><p>
21 Estoy abrumado por el desastre de la hija de mi pueblo, estoy ensombrecido, la consternación se apoderó de mí.
</p><p>
22 ¿No hay más bálsamo en Galaad? ¿No hay allí ningún médico? ¿Por qué entonces no cicatriza la llaga de la hija de mi pueblo?
</p><p>
23 ¡Ojalá mi cabeza se convirtiera en llanto y mis ojos en fuente de lágrimas, para llorar de día y de noche por las víctimas de la hija de mi pueblo!
</p>
