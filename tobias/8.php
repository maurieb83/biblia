<h3>TOBIAS</h3>
<p>Capítulo 8
</p><p>
1 Cuando terminaron de comer y beber, decidieron ir a costarse. Acompañaron al joven y lo hicieron entrar en la habitación.
</p><p>
2 Entonces Tobías se acordó de los consejos de Rafael, sacó de su bolsa el hígado y el corazón del pez y los colocó sobre el brasero de los perfumes.
</p><p>
3 El olor del pez alejó al demonio, y este huyó por el aire hacia las regiones de Egipto. Rafael lo persiguió, lo sujetó y lo encadenó al instante.
</p><p>
4 Mientras tanto, los padres habían salido de la habitación y cerraron la puerta. Tobías se levantó de la cama y dijo a Sara: «Levántate, hermana, y oremos para pedir al Señor que nos manifieste su misericordia y su salvación».
</p><p>
5 Ella se levantó, y los dos se pusieron a orar para alcanzar la salvación. El comenzó así: «¡Bendito seas, Dios de nuestros padres, y bendito sea tu Nombre por todos los siglos de los siglos! ¡Que te bendigan los cielos y todas tus criaturas por todos los siglos!
</p><p>
6 Tú creaste a Adán e hiciste a Eva, su mujer, para que le sirviera de ayuda y de apoyo, y de ellos dos nació el género humano. Tú mismo dijiste: «No conviene que el hombre esté solo. Hagámosle una ayuda semejante a él».
</p><p>
7 Yo ahora tomo por esposa a esta hermana mía, no para satisfacer una pasión desordenada, sino para constituir un verdadero matrimonio. ¡Ten misericordia de ella y de mí, y concédenos llegar juntos a la vejez!».
</p><p>
8 Ambos dijeron: «¡Amén, amén!»,
</p><p>
9 y se acostaron a dormir. Cuando Ragüel se levantó, llamó sus servidores y fue con ellos a cavar una fosa.
</p><p>
10 Porque había pensado: «No sea que Tobías haya muerto y nos expongamos a caer en el ridículo».
</p><p>
11 Apenas terminaron de cavar la fosa, Ragüel volvió a la casa, llamó a su mujer
</p><p>
12 y le dijo: «Manda a una de las sirvientas a la habitación, para ver si él está vivo. Así, si está muerto, lo enterraremos sin que nadie se entere».
</p><p>
13 Mandaron adelante a la sirvienta, encendieron la lámpara y abrieron la puerta. Ella entró y los encontró a los dos juntos, profundamente dormidos.
</p><p>
14 Luego salió y les avisó: «Está vivo; no ha pasado nada malo».
</p><p>
15 Entonces Ragüel bendijo al Dios del cielo, diciendo: «¡Bendito seas, Señor, con la más pura bendición! ¡Que te bendigan por todos los siglos!
</p><p>
16 ¡Bendito seas por la alegría que me has dado! No ha sucedido lo que yo temía, sino que nos has tratado según tu gran misericordia.
</p><p>
17 ¡Bendito seas por haberte compadecido de estos dos hijos únicos! ¡Manifiéstales, Señor, tu misericordia y tu salvación, y concédeles una vida llena de alegría y de gracia!».
</p><p>
18 Después Ragüel ordenó a sus servidores que rellenaran la fosa, antes que amaneciera.
</p><p>
19 Luego dijo a su mujer que hiciera una hornada de pan, y él fue al establo, tomó dos bueyes y cuatro carneros, mandó cocinarlos y comenzaron los preparativos.
</p><p>
20 Hizo llamar a Tobías y le dijo: «Durante catorce días no te moverás de este lugar. Te quedarás, aquí, comiendo y bebiendo conmigo, y alegrando a mi hija que ha sufrido tanto.
</p><p>
21 Después tomarás la mitad de mis bienes y volverás sano y salvo a la casa de tu padre. Cuando mi mujer y yo hayamos muerto, también recibirás la otra mitad. ¡Animo, hijo mío! Yo soy tu padre y Edna es tu madre. Desde ahora y para siempre, estamos unidos a ti lo mismo que a tu hermano. ¡Animo, hijo mío!».
</p>
