<h3>TOBIAS</h3>
<p>Capítulo 2
</p><p>
1 Durante el reinado de Asaradón regresé a mi casa y me devolvieron a mi mujer Ana y a mi hijo Tobías. En nuestra fiesta de Pentecostés, que es la santa fiesta de las siete Semanas, me prepararon una buena comida y yo me dispuse a comer.
</p><p>
2 Cuando me encontré con la mesa llena de manjares, le dije a mi hijo Tobías: «Hijo mío, ve a buscar entre nuestros hermanos deportados en Nínive a algún pobre que se acuerde de todo corazón del Señor, y tráelo para que comparta mi comida. Yo esperaré hasta que tú vuelvas».
</p><p>
3 Tobías salió a buscar a un pobre entre nuestros hermanos, pero regresó, diciéndome: «¡Padre!». Yo le pregunté: «¿Qué te pasa, hijo?». Y él agregó: «Padre, uno de nuestro pueblo ha sido asesinado: lo acaban de estrangular en la plaza del mercado, y su cadáver está tirado allí».
</p><p>
4 Entonces me levanté rápidamente, y, sin probar la comida, fue a retirar el cadáver de la plaza, y lo deposité en una habitación para enterrarlo al atardecer.
</p><p>
5 Al volver, me lavé y me puse a comer muy apenado,
</p><p>
6 recordando las palabras del profeta Amós contra Betel: «Sus fiestas se convertirán en duelo y todos sus cantos en lamentaciones».
</p><p>
7 Y me puse a llorar. A la caída del sol, cavé una fosa y enterré el cadáver.
</p><p>
8 Mis vecinos se burlaban de mí, diciendo: «¡Todavía no ha escarmentado! Por este mismo motivo ya lo buscaron para matarlo. ¡Apenas pudo escapar, y ahora vuelve a enterrar a los muertos!».
</p><p>
9 Aquella misma noche, después de bañarme, salí al patio y me acosté a dormir junto a la pared, con la cara descubierta a causa del calor.
</p><p>
10 Yo no sabía que arriba, en la pared, había unos gorriones; de pronto, su estiércol caliente cayó sobre mis ojos, produciéndome unas manchas blancas. Me hice atender por los médicos, pero cuantos más remedios me aplicaban, menos veías a causa de las manchas, hasta que me quedé completamente ciego. Así estuve cuatro años privado de la vista, y todos mis parientes estaban afligidos. Ajicar me proveyó de lo necesario durante dos años, hasta que partió para Elimaida.
</p><p>
11 Desde ese momento, mi esposa Ana empezó a trabajar en labores femeninas: hilaba lana,
</p><p>
12 enviaba el tejido a sus clientes y recibía el pago correspondiente. Una vez, el siete del mes de Distros, terminó un tejido y lo entregó a sus clientes. Estos le pagaron lo que correspondía y, además, le regalaron un cabrito para comer.
</p><p>
13 Cuando entró en mi casa, el cabrito comenzó a balar. Yo llamé a mi mujer y le pregunté: «¿De dónde salió este cabrito? ¿No habría sido robado? Devuélvelo a sus dueños, porque no podemos comer nada robado»,
</p><p>
14 Ella me respondió: «¡Pero si es un regalo que me han hecho, además del pago!». Yo no le creí e insistía en que lo devolviera a sus dueños, llegando a enojarme con ella por este asunto. Entonces ella me replicó: «¿Para qué te sirvieron tus limosnas y tus obras de justicia? ¡Ahora se ve bien claro!».
</p>
