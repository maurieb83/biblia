<h3>PRIMER LIBRO DE LAS CRONICAS</h3>
<p>Capítulo 28
</p><p>
1 David reunió en Jerusalén a todos los jefes de Israel, a los jefes de las tribus, a los jefes de las divisiones que estaban al servicio del rey, a los jefes de mil y de cien hombres, a los administradores de todos los bienes y del ganado del rey y de sus hijos, a los funcionarios, a los guerreros y a todos los hombres de valor.
</p><p>
2 El rey David se puso de pie y dijo: «Oiganme, hermanos y pueblo mío. Yo me había propuesto construir una Casa donde descansara el Arca de la Alianza del Señor, que es la tarima de los pies de nuestro Dios, y había hecho preparativos para su construcción.
</p><p>
3 Pero Dios me dijo: «Tú no edificarás la Casa para mi Nombre, porque eres hombre de guerra y has derramado sangre».
</p><p>
4 Sin embargo, el Señor, el Dios de Israel, me eligió entre toda mi familia, a fin de que yo fuera rey de Israel para siempre. Porque él eligió como guía a la tribu de Judá, y entre las familias de Judá, a la casa de mi padre; y entre los hijos de mi padre se complació en mí para hacerme rey sobre todo Israel.
</p><p>
5 Y entre todos mis hijos –porque el Señor me ha dado muchos– eligió a mi hijo Salomón para que se sentara en el trono de la realeza del Señor sobre Israel.
</p><p>
6 Y él me dijo: «Tu hijo Salomón edificará mi Casa y mis atrios; porque lo he elegido como hijo y yo seré para él un padre.
</p><p>
7 Afianzaré su reino para siempre, si se mantiene firme en el cumplimiento de mis mandamientos y mis leyes como en el día de hoy».
</p><p>
8 Y ahora, a la vista de todo Israel, que es la asamblea del Señor, y delante de nuestro Dios que nos escucha, yo les digo: «cumplan y observen todos los mandamientos del Señor, nuestro Dios, para que puedan poseer esta hermosa tierra y puedan dejarla como herencia a sus hijos después de ustedes para siempre».
</p><p>
9 Y tú, Salomón, hijo mío, reconoce al Dios de tu padre, y sírvelo con un corazón íntegro y con ánimo generoso, porque el Señor sondea todos los corazones y penetra hasta el fondo de todos los pensamientos. Si lo buscas, él se dejará encontrar; pero si lo abandonas, él te rechazará para siempre.
</p><p>
10 Ten presente que el Señor te ha elegido para que le edifiques una Casa como Santuario. ¡Sé fuerte, y manos a la obra!».
</p><p>
11 David dio a su hijo Salomón el diseño del vestíbulo y de los demás edificios, de los almacenes, de las habitaciones superiores, de las salas interiores y del lugar del Propiciatorio.
</p><p>
12 Le entregó también el diseño de todo lo que había proyectado para los atrios de la Casa del Señor, para las habitaciones de alrededor, para los tesoros de la Casa de Dios, para los depósitos de las cosas sagradas,
</p><p>
13 para las clases de los sacerdotes y de los levitas, para la manera de ejercer el servicio de la Casa del Señor y para los utensilios destinados al culto de la Casa del Señor.
</p><p>
14 Determinó el peso del oro y la plata para todos los objetos de oro y plata que debían utilizarse en cada servicio;
</p><p>
15 fijó asimismo el peso del oro y la plata para los candelabros de oro y plata con sus lámparas, según el uso a que estaban destinados;
</p><p>
16 también determinó el peso del oro para cada una de las mesas de los panes de la ofrenda, y el peso de la plata destinada a las mesas de plata;
</p><p>
17 el oro puro para los tenedores, los aspersorios y las jarras; el peso del oro y de la plata para cada una de las copas de oro y plata;
</p><p>
18 y el peso del oro refinado para el altar del incienso; finalmente, entregó el modelo de la carroza y de los querubines que cubren con sus alas extendidas el Arca de la Alianza del Señor.
</p><p>
19 Todo esto había sido escrito por la mano del Señor, para dar a conocer los detalles del diseño.
</p><p>
20 David dijo a su hijo Salomón: «¡Sé fuerte y valeroso, y manos a la obra! No temas ni te acobardes, porque el Señor Dios, mi Dios, estará contigo: él no te dejará ni te abandonará hasta que hayas terminado toda la obra necesaria para el servicio de la Casa del Señor.
</p><p>
21 Aquí están las clases de los sacerdotes y levitas para todo el servicio de la Casa de Dios. En todo este trabajo contarás con la ayuda de obreros adiestrados para cualquier tipo de tareas. También los jefes y todo el pueblo estarán a tus órdenes».
</p>
