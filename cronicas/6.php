<h3>PRIMER LIBRO DE LAS CRONICAS</h3>
<p>Capítulo 6
</p><p>
1 Los descendientes de Leví fueron Gersón, Quehat y Merarí.
</p><p>
2 Los nombres de los hijos de Gersón son los siguientes: Libní y Simí.
</p><p>
3 Los hijos de Quehat fueron Amram, Ishar, Hebrón y Uziel.
</p><p>
4 Los hijos de Merarí fueron Majlí y Musí. Estas son las familias de los levitas, agrupadas según sus padres.
</p><p>
5 El hijo de Gersón fue Libní; Iájat; el hijo de Iájat, Zimá;
</p><p>
6 el hijo de Zimá, Ioaj; el hijo de Ioaj, Idó; el hijo de Idó, Zéraj; el hijo de Zéraj, Ieotrai.
</p><p>
7 El hijo de Quehat fue Aminadab; el hijo de Aminadab, Coré; el hijo de Coré, Asir;
</p><p>
8 el hijo de Asir, Elcaná; el hijo de Elcaná, Ebiasaf; el hijo de Ebiasaf, Asir;
</p><p>
9 el hijo de Asir, Tájat; el hijo de Tájat, Uriel; el hijo de Uriel, Ozías; el hijo de Ozías, Saúl.
</p><p>
10 Los hijos de Elcaná fueron Amasai y Ajimot.
</p><p>
11 El hijo de Ajimot fue Elcaná; el hijo de Elcaná, Sofai; el hijo de Sofai, Nájat;
</p><p>
12 el hijo de Nájat, Eliab; el hijo de Eliab, Ierojam; el hijo de Ierojam, Elcaná; el hijo de Elcaná, Samuel.
</p><p>
13 Los hijos de Samuel fueron Joel, el primogénito, y Abías, el segundo.
</p><p>
14 El hijo de Merarí fue Majlí; el hijo de Majlí, Libní; el hijo de Libní, Simei; el hijo de Simei, Uzá,
</p><p>
15 El hijo de Uzá, Simá; el hijo de Zimá, Jaguías; el hijo de Jaguías, Asaías.
</p><p>
16 Estos son los cantores que puso David para dirigir el canto en la Casa del Señor, desde que el Arca descansó en ella.
</p><p>
17 Ellos servían como cantores ante la Morada –la Carpa del Encuentro– hasta que Salomón edificó el Templo del Señor en Jerusalén, y prestaban servicio conforme a su reglamento.
</p><p>
18 Los que ejercían ese ministerio y sus hijos son los siguientes: De los descendientes de Quehat: Hemán el cantor, hijo de Joel, hijo de Samuel,
</p><p>
19 hijo de Elcaná, hijo de Ierojam, hijo de Eliel, hijo de Tóaj,
</p><p>
20 hijo de Sud, hijo de Elcaná, hijo de Májat, hijo de Amasai,
</p><p>
21 hijo de Elcaná, hijo de Joel, hijo de Azarías, hijo de Sefanías,
</p><p>
22 hijo de Tájat, hijo de Asir, hijo de Ebiasaf, hijo de Coré,
</p><p>
23 hijo de Ishar, hijo de Quehat, hijo de Leví, hijo de Israel.
</p><p>
24 Además, su hermano Asaf, que asistía a su derecha. Asaf era hijo de Berequías, hijo de Simá,
</p><p>
25 hijo de Micael, hijo de Baasías, hijo de Malquías,
</p><p>
26 hijo de Etní, hijo de Zéraj, hijo de Adaías,
</p><p>
27 hijo de Etán, hijo de Zimá, hijo de Simei,
</p><p>
28 hijo de Iájat, hijo de Gersón, hijo de Leví.
</p><p>
29 Los hijos de Merarí, sus hermanos, que asistían a su izquierda, eran Etán, hijo de Quisí, hijo de Abdí, hijo de Maluc,
</p><p>
30 Hijo de Jasabías, hijo de Amasías, hijo de Jilquías,
</p><p>
31 hijo de Amsí, hijo de Baní, hijo de Sémer,
</p><p>
32 hijo de Majlí, hijo de Musí, hijo de Merarí, hijo de Leví.
</p><p>
33 Sus hermanos, los levitas, estaban encargados de todo el servicio de la Morada de la Casa de Dios.
</p><p>
34 Pero Aarón y sus hijos eran los que quemaban las ofrendas en el altar de los holocaustos y en el altar de los perfumes: ellos se ocupaban de todo lo concernientes a las cosas santísimas y del rito de expiación en favor de Israel, según lo que había ordenado Moisés, el servidor de Dios.
</p><p>
35 El hijo de Aarón fue Eleazar; el hijo de Eleazar, Pinjás; el hijo de Pinjás, Abisúa;
</p><p>
36 el hijo de Abisúa, Buquí; el hijo de Buquí, Uzí; el hijo de Uzí, Zerajías;
</p><p>
37 el hijo de Zerajías, Meraiot; el hijo de Meraiot, Amarías; el hijo de Amarías, Ajitub;
</p><p>
38 el hijo de Ajitub, Sadoc; el hijo de Sadoc, Ajimáas.
</p><p>
39 Estos son los lugares de residencia de los descendientes de Aarón, según los límites de sus campamentos: A los descendientes de Aarón, de la familia de los quehatitas –porque la suerte cayó primero sobre ellos –
</p><p>
40 se les dio Hebrón en el país de Judá, con sus campos de pastoreo vecinos;
</p><p>
41 pero el campo de la ciudad y sus poblados fueron dados a Caleb, hijo de Iefuné.
</p><p>
42 Las ciudades de refugio concedidas a los hijos de Aarón fueron Hebrón, Libná con sus campos de pastoreo; Iatir y Estemoa con sus campos de pastoreo;
</p><p>
43 Jilaz, Debir,
</p><p>
44 Asán y Bet Semes, con sus respectivos campos de pastoreo.
</p><p>
45 Y de la tribu de Benjamín, se les dio Gueba, Alémet y Anatot, con sus respectivos campos de pastoreo. En total, sus ciudades fueron trece, distribuidas según sus familias.
</p><p>
46 A los otros hijos de Quehat les dieron por sorteo, conforme a sus familias, diez ciudades de la tribu de Efraím, de la tribu de Dan y de media tribu de Manasés.
</p><p>
47 A los hijos de Gersón, según sus familias, les correspondieron trece ciudades de la tribu de Isacar, de la tribu de Aser, de la tribu de Neftalí y de la tribu de Manasés en el Basán.
</p><p>
48 A los hijos de Merarí, según sus familias, les tocaron en suerte doce ciudades de la tribu de Rubén, de la tribu de Gad y de la tribu de Zabulón.
</p><p>
49 Los israelitas dieron a los levitas estas ciudades con sus campos de pastoreo.
</p><p>
50 Ellos les entregaron, mediante un sorteo, las ciudades de las tribus de Judá, de Simeón y de Benjamín antes mencionadas.
</p><p>
51 A las otras familias de los descendientes de Quehat, les tocaron en suerte ciudades de la tribu de Efraím.
</p><p>
52 Les fueron asignadas las siguientes ciudades de refugio: Siquém, en la montaña de Efraím, Guézer,
</p><p>
53 Iocmeam, Bet Jorón,
</p><p>
54 Aialón y Gat Rimón, con sus respectivos campos de pastoreo.
</p><p>
55 Y de la mitad de la tribu de Manasés les fueron asignadas Aner y Bilán, con sus respectivos campos de pastoreo. Esto es lo que se dio a las familias de los otros hijos de Quehat.
</p><p>
56 A los hijos de Gersón, se les dio: de la mitad de la tribu de Manasés, Golán, en Basán, y Astarot, con sus respectivos campos de pastoreo.
</p><p>
57 De la tribu de Isacar, Cadés, Dobrat,
</p><p>
58 Ramot y Aném, con sus respectivos campos de pastoreo.
</p><p>
59 De la tribu de Aser, Masal, Abdón,
</p><p>
60 Jucoc y Rejob, con sus respectivos campos de pastoreo.
</p><p>
61 De la tribu de Neftalí, Quedes, en Galilea, Jamón y Quiriataim, con sus respectivos campos de pastoreo.
</p><p>
62 A los demás hijos de Merarí, se les dio: de la tribu de Zabulón, Rimón y Tabor, con sus respectivos campos de pastoreo.
</p><p>
63 De la tribu de Rubén, en la otra parte del Jordán, frente a Jericó, al este del Jordán: Béser en el desierto, Iahsa,
</p><p>
64 Quedemot y Mefaat, con sus respectivos campos de pastoreo.
</p><p>
65 De la tribu de Gad, Ramot, en Galaad, Majanaim,
</p><p>
66 Jesbón y Iazer, con sus respectivos campos de pastoreo.
</p>
