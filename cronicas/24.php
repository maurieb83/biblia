</p><p>
27 Hijos de Merarí por la línea de su hijo Iaazías: Sóham, Zacur e Ibrí.
</p><p>
28 Por parte de Majlí, Eleazar, que no tuvo hijos.
</p><p>
29 Por parte de Quis, su hijo Ierajmel.
</p><p>
