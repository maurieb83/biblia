<h3>PRIMER LIBRO DE LOS REYES</h3>
<p>Capítulo 21
</p><p>
La viña de Nabot
</p><p>
1 Después de esto, sucedió lo siguiente: Nabot, el izreelita, tenía una viña en Izreel, al lado del palacio de Ajab, rey de Samaría.
</p><p>
2 Ajab dijo a Nabot: «Dame tu viña para hacerme una huerta, ya que está justo al lado de mi casa. Yo te daré a cambio una viña mejor o, si prefieres, te pagaré su valor en dinero».
</p><p>
3 Pero Nabot respondió a Ajab: «¡El Señor me libre de cederte la herencia de mis padres!».
</p><p>
4 Ajab se fue a su casa malhumorado y muy irritado por lo que le había dicho Nabot, el izreelita: «No te daré la herencia de mis padres». Se tiró en su lecho, dio vuelta la cara y no quiso probar bocado.
</p><p>
5 Entonces fue a verlo su esposa Jezabel y le preguntó: «¿Por qué estás tan malhumorado y no comes nada?».
</p><p>
6 El le dijo: «Porque le hablé a Nabot, el izreelita, y le propuse: «Véndeme tu viña o, si quieres, te daré otra a cambio». Pero él respondió: «No te daré mi viña».
</p><p>
7 Su esposa Jezabel le dijo: «¿Así ejerces tú la realeza sobre Israel? ¡Levántate, come y alégrate! ¡Yo te daré la viña de Nabot, el izreelita!».
</p><p>
8 En seguida escribió una carta en nombre de Ajab, la selló con el sello del rey y la envió a los ancianos y a los notables de la ciudad, conciudadanos de Nabot.
</p><p>
9 En esa carta escribió: «Proclamen un ayuno y en la asamblea del pueblo hagan sentar a Nabot en primera fila.
</p><p>
10 Hagan sentar enfrente a dos malvados, que atestigüen contra él, diciendo: «Tú has maldecido a Dios y al rey». Luego sáquenlo afuera y mátenlo a pedradas».
</p><p>
11 Los hombres de la ciudad, los ancianos y notables, conciudadanos de Nabot, obraron de acuerdo con lo que les había mandado Jezabel, según lo que estaba escrito en la carta que les había enviado.
</p><p>
12 Proclamaron un ayuno e hicieron sentar a Nabot en primera fila.
</p><p>
13 En seguida llegaron dos malvados que se le sentaron enfrente y atestiguaron contra él diciendo: «Nabot ha maldecido a Dios y al rey». Entonces lo sacaron fuera de la ciudad y lo mataron a pedradas.
</p><p>
14 Y mandaron decir a Jezabel: «Nabot fue apedreado y murió».
</p><p>
15 Cuando Jezabel se enteró de que Nabot había sido matado a pedradas, dijo a Ajab: «Ya puedes tomar posesión de la viña de Nabot, esa que él se negaba a venderte, porque Nabot ya no vive: está muerto».
</p><p>
16 Apenas oyó Ajab que Nabot estaba muerto, bajó a la viña de Nabot, el izreelita, para tomar posesión de ella.
</p><p>
La intervención profética de Elías
</p><p>
17 Entonces la palabra del Señor llegó a Elías, el tisbita, en estos términos:
</p><p>
18 «Baja al encuentro de Ajab, rey de Israel en Samaría. Ahora está en la viña de Nabot: ha bajado allí para tomar posesión de ella.
</p><p>
19 Tú le dirás: Así habla el Señor: ¡Has cometido un homicidio, y encima te apropias de lo ajeno! Por eso, así habla el Señor: En el mismo sitio donde los perros lamieron la sangre de Nabot, allí lamerán tu sangre».
</p><p>
20 Ajab respondió a Elías: «¡Me has sorprendido, enemigo mío!». «Sí, repuso Elías, te he sorprendido, porque te has prestado a hacer lo que es malo a los ojos de Señor.
</p><p>
21 Yo voy a atraer la desgracia sobre ti: barreré hasta tus últimos restos y extirparé a todos los varones de la familia de Ajab, esclavos o libres en Israel.
</p><p>
22 Dejaré tu casa como la de Jeroboam, hijo de Nebat, y como la de Basá, hijo de Ajías, porque has provocado mi indignación y has hecho pecar a Israel.
</p><p>
23 Y el Señor también ha hablado contra Jezabel, diciendo: Los perros devorarán la carne de Jezabel en la parcela de Izreel.
</p><p>
24 Al de la familia de Ajab que muera en la ciudad, se lo comerán los perros, y al que muera en despoblado, se lo comerán los pájaros del cielo».
</p><p>
25 No hubo realmente nadie que se haya prestado como Ajab para hacer lo que es malo a los ojos del Señor, instigado por su esposa Jezabel.
</p><p>
26 El cometió las peores abominaciones, yendo detrás de los ídolos, como lo habían hecho los amorreos que el Señor había desposeído delante de los israelitas.
</p><p>
27 Cuando Ajab oyó aquellas palabras, rasgó sus vestiduras, se puso un sayal sobre su carne, y ayunó. Se acostaba con el sayal y andaba taciturno.
</p><p>
28 Entonces la palabra del Señor llegó a Elías, el tisbita, en estos términos:
</p><p>
29 «¿Has visto cómo Ajab se ha humillado delante de mí, no atraeré la desgracia mientras él viva, sino que la haré venir sobre su casa en tiempos de su hijo».
</p>
