<h3>PRIMER LIBRO DE LOS REYES</h3>
<p>Capítulo 13
</p><p>
El altar de Betel reprobado por un profeta
</p><p>
1 Un hombre de Dios vino a Judá a Betel, por orden del Señor, mientras Jeroboam estaba de pie junto al altar para quemar incienso.
</p><p>
2 Y gritó contra el altar, por orden del Señor: «¡Altar! ¡Altar! A la casa de David le nacerá un hijo –su nombre será Josías– y él inmolará sobre ti a los sacerdotes de los lugares altos que queman incienso sobre ti, y hará arder sobre ti huesos humanos».
</p><p>
3 Ese mismo día, el hombre de Dios dio una señal, diciendo: «Esta es la señal de que ha hablado el Señor: el altar se va a resquebrajar, y se desparramará la ceniza grasienta que hay sobre él».
</p><p>
4 Al oír la palabra que el hombre de Dios proclamaba contra el altar de Betel, Jeroboam extendió su brazo desde encima del altar, diciendo: «¡Deténganlo!». Pero el brazo que había extendido hacia el hombre de Dios le quedó paralizado, y no pudo volverlo atrás.
</p><p>
5 El altar se resquebrajó y se desparramó la ceniza grasienta que había en él, conforme a la señal que había dado el hombre de Dios por orden del Señor.
</p><p>
6 Entonces el rey tomó la palabra y dijo al hombre de Dios: «Aplaca, por favor, el rostro del Señor, tu Dios, y ruega por mí, para que pueda doblar mi brazo». El hombre de Dios aplacó el rostro del Señor, y el rey pudo doblar el brazo como antes.
</p><p>
7 El rey dijo entonces al hombre de Dios: «Entra conmigo en la casa para reconfortarte, y te dará un regalo».
</p><p>
8 Pero el hombre de Dios respondió al rey: «Aunque me des la mitad de tu casa, no iré contigo. No comeré pan ni beberé agua en este lugar,
</p><p>
9 porque esto es lo que se me ha mandado por orden del Señor: No comerás pan ni beberás agua, ni regresarás por el mismo camino».
</p><p>
10 Y se fue por otro camino, sin retomar el que había recorrido para venir a Betel.
</p><p>
El hombre de Dios y el profeta de Betel
</p><p>
11 Había un viejo profeta que vivía en Betel. Sus hijos fueron a contarle todo lo que el hombre de Dios había hecho aquel día en Betel, y también le contaron a su padre las palabras que había dicho al rey.
</p><p>
12 Su padre les preguntó: «¿Por qué camino se fue?». Los hijos le indicaron el camino que había tomado el hombre de Dios venido de Judá,
</p><p>
13 y él les dijo: «Ensíllenme el asno» Le ensillaron el asno y él se montó.
</p><p>
14 Luego se fue detrás del hombre de Dios y lo encontró sentado bajo el terebinto. «¿Eres tú el hombre de Dios que vino de Judá?», le preguntó. «Así es», respondió él.
</p><p>
15 Entonces el profeta le dijo: «Ven conmigo a casa a comer algo».
</p><p>
16 Pero el otro replicó: «No puedo volver contigo ni acompañarte. No comeré pan ni beberé agua contigo en este lugar,
</p><p>
17 porque esta es la orden que recibí del Señor: No comerás pan ni beberás agua cuando estés allí, ni regresarás por el camino que tomaste a la ida:.
</p><p>
18 El otro le dijo: «Yo también soy profeta como tú, y un ángel me dijo, por orden del Señor: Tráelo contigo a tu casa, para que coma pan y beba agua:. Pero en realidad le estaba mintiendo.
</p><p>
19 Así el hombre de Dios regresó con él, y comió y bebió en su casa.
</p><p>
20 Mientras estaban sentados a la mesa, la palabra del Señor llegó al profeta que lo había hecho volver,
</p><p>
21 y este gritó al hombre de Dios venido de Judá: «Así habla el Señor: Porque has sido rebelde a la orden del Señor y no has observado el mandato que te dio el Señor, tu Dios;
</p><p>
22 porque has regresado y has comido pan y bebido agua en el lugar del que te había dicho: «No comas pan ni bebas agua allí», por eso, tu cadáver no entrará en la tumba de tus padres».
</p><p>
23 Después que él comió y bebió, el profeta le ensilló el asno,
</p><p>
24 y el hombre de Dios emprendió el camino de regreso. Pero un león lo encontró en el camino y lo mató. Su cadáver quedó tendido en el camino, y el asno y el león permanecieron de pie al lado de él.
</p><p>
25 Unos hombres que pasaban por ahí vieron el cadáver tendido sobre el camino y al león parado junto a él, y fueron a dar la noticia a la ciudad donde vivía el viejo profeta.
</p><p>
26 Cuando se enteró el profeta que lo había hecho volver atrás, dijo: «¡Es el hombre de Dios que se rebeló contra la orden del Señor! El Señor lo entregó al león, que lo destrozó y lo mató, según la palabra que le había dicho el Señor».
</p><p>
27 Luego dijo a sus hijos: «Ensíllenme el asno». Cuando se lo ensillaron,
</p><p>
28 él partió y encontró el cadáver tendido sobre el camino, mientras que el asno y el león estaban de pie junto al cadáver; el león no había devorado el cadáver ni había despedazado al asno.
</p><p>
29 El viejo profeta recogió el cadáver del hombre de Dios, lo cargó sobre el asno y lo llevó a la ciudad para hacer duelo por él y enterrarlo.
</p><p>
30 Puso el cadáver en su propia tumba, y le entonaron la lamentación: «¡Ay, hermano mío!».
</p><p>
31 Después que lo enterraron, el profeta habló así a sus hijos: «Cuando yo muera, me enterrarán en la tumba donde ha sido sepultado el hombre de Dios; depositen mis huesos junto a los suyos,
</p><p>
32 porque ciertamente se cumplirá la palabra que él proclamó, por orden del Señor, contra el altar de Betel y contra todos los santuarios de los lugares altos que están en las ciudades de Samaría».
</p><p>
Las consecuencias de la apostasía de Jeroboam
</p><p>
33 Después que sucedió esto, Jeroboam no se convirtió de su mala conducta. Volvió a instituir como sacerdotes de los lugares altos a personas tomadas del común de la gente; todo el que lo deseaba era investido por él y se convertía en sacerdote de los lugares altos.
</p><p>
34 Esto fue una ocasión de pecado para la casa de Jeroboam, y provocó su destrucción y su exterminio de la faz de la tierra.
</p><p>
Los reinos de Israel y de Judá<br>
hasta los tiempos de Elías
