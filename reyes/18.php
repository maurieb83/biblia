<h3>PRIMER LIBRO DE LOS REYES</h3>
<p>Capítulo 18
</p><p>
El encuentro de Elías con Abdías
</p><p>
1 Mucho tiempo después, al tercer año, la palabra del Señor llegó a Elías, en estos términos: «Ve a presentarte a Ajab, y yo enviaré lluvia a la superficie del suelo».
</p><p>
2 Entonces Elías partió para presentarse ante Ajab. Como apretaba el hambre en Samaría,
</p><p>
3 Ajab llamó a Abdías, el mayordomo de palacio. –Abdías era muy temeroso del Señor,
</p><p>
4 y cuando Jezabel perseguía a muerte a los profetas del Señor, él había recogido a cien de ellos, los había ocultado en dos cuevas, cincuenta en cada una, y los había provisto de pan y agua–.
</p><p>
5 Ajab dijo a Abdías: «Vamos a recorrer todos los manantiales y torrentes del país. Tal vez encontremos pasto para conservar con vida los caballos y las mulas, y así no tendremos que sacrificar ganado».
</p><p>
6 Se repartieron el país para recorrerlo: Ajab partió solo por un camino y Abdías, también solo, se fue por otro.
</p><p>
7 Mientras Abdías iba por el camino, le salió al encuentro Elías. Apenas lo reconoció, cayó con el rostro en tierra y dijo: «¿Eres tú, Elías, mi señor?».
</p><p>
8 «Soy yo, le respondió él. Ve a decirle a tu señor que Elías está aquí».
</p><p>
9 Pero él replicó: «¿Qué pecado he cometido para que pongas a tu servidor en manos de Ajab y él me haga morir?
</p><p>
10 ¡Por la vida del Señor, tu Dios!, no hay nación ni reino adonde mi señor Ajab no te haga mandado buscar. Y cuando decían: No está aquí, él hacía jurar a ese reino y a esa nación que no te habían encontrado.
</p><p>
11 Y ahora tú dices: «Ve a decirle a tu señor que aquí está Elías».
</p><p>
12 Pero en cuanto yo me aparte de ti, el espíritu del Señor te llevará quién sabe adónde, y cuando vaya a avisarle a Ajab, él no te encontrará y me matará. Sin embargo, tu servidor teme al Señor desde su juventud.
</p><p>
13 ¿Acaso no te han contado lo que hice cuando Jezabel mataba a los profetas del Señor, cómo oculté a cien de ellos en dos cuevas, cincuenta en cada una, y los proveí de pan y agua?
</p><p>
14 Y ahora tú me dices: «Ve a decirle a tu señor que aquí está Elías». ¡Seguro que me matará!».
</p><p>
15 Pero Elías replicó: «¡Por la vida del Señor de los ejércitos, a quien yo sirvo! Hoy mismo me presentaré a él».
</p><p>
El encuentro de Elías con Ajab
</p><p>
16 Abdías fue al encuentro de Ajab; le comunicó el mensaje, y Ajab fue a encontrarse con Elías.
</p><p>
17 Apenas vio a Elías, Ajab le dijo: «¿Así que eres tú, el que trae la desgracia a Israel?».
</p><p>
18 Elías respondió: «No soy yo el que traigo la desgracia a Israel, sino tú y la casa de tu padre, porque han abandonado al Señor y te has ido detrás de los Baales.
</p><p>
19 Y ahora, manda que todo Israel se reúna junto a mí en el monte Carmelo, con los cuatrocientos profetas de Baal y los cuatrocientos profetas de Aserá que comen a la mesa de Jezabel».
</p><p>
El juicio de Dios en el monte Carmelo
</p><p>
20 Ajab mandó buscar a todos los israelitas y reunió a los profetas sobre el monte Carmelo.
</p><p>
21 Elías se acercó a todo el pueblo y dijo: «¿Hasta cuándo van a andar rengueando de las dos piernas? Si el Señor es Dios, síganlo; si es Baal, síganlo a él. Pero el pueblo no le respondió ni una palabra.
</p><p>
22 Luego Elías dijo al pueblo: «Como profeta del Señor, he quedado yo solo, mientras que los profetas de Baal son cuatrocientos cincuenta.
</p><p>
23 Traigamos dos novillos; que ellos se elijan uno, que lo despedacen y lo pongan sobre la leña, pero sin prender fuego. Yo haré lo mismo con el otro novillo: lo pondré sobre la leña y tampoco prenderé fuego.
</p><p>
24 Ustedes invocarán el nombre de su dios y yo invocaré el nombre del Señor: el dios que responda enviándome fuego, ese es Dios». Todo el pueblo respondió diciendo: «¡Está bien!».
</p><p>
25 Elías dijo a los profetas de Baal: «Elíjanse un novillo y prepárenlo ustedes primero, ya que son los más numerosos; luego invoquen el nombre de su dios, pero no prendan fuego».
</p><p>
26 Ellos tomaron el novillo que se les había dado, lo prepararon e invocaron el nombre de Baal desde la mañana hasta el mediodía, diciendo: «¡Respóndenos, Baal!». Pero no se oyó ninguna voz ni nadie que respondiera. Mientras tanto, danzaban junto al altar que habían hecho.
</p><p>
27 Al mediodía, Elías empezó a burlarse de ellos, diciendo: «¡Griten bien fuerte, porque es un dios! Pero estará ocupado, o ausente, o se habrá ido de viaje. A lo mejor está dormido y se despierta».
</p><p>
28 Ellos gritaron a voz en cuello y, según su costumbre, se hacían incisiones con cuchillos y punzones, hasta chorrear sangre.
</p><p>
29 Y una vez pasado el mediodía, se entregaron al delirio profético hasta la hora en que se ofrece la oblación. Pero no se oyó ninguna voz, ni hubo nadie que respondiera o prestara atención.
</p><p>
30 Entonces Elías dijo a todo el pueblo: «¡Acérquense a mí!». Todo el pueblo se acercó a él, y él restauró el altar del Señor que había sido demolido:
</p><p>
31 tomó doce piedras, conforme al número de los hijos de Jacob, a quien el Señor había dirigido su palabra, diciéndole: «Te llamarás Israel»,
</p><p>
32 y con esas piedras erigió un altar al nombre del Señor. Alrededor del altar hizo una zanja, como un surco para dos medidas de semilla.
</p><p>
33 Luego dispuso la leña, despedazó el novillo y lo colocó sobre la leña.
</p><p>
34 Después dijo: «Llenen de agua cuatro cántaros y derrámenla sobre el holocausto y sobre la leña». Así lo hicieron. El añadió: «Otra vez». Lo hicieron por segunda vez, y él insistió: «Una vez más». Lo hicieron por tercera vez.
</p><p>
35 El agua corrió alrededor del altar, y hasta la zanja se llenó de agua.
</p><p>
36 A la hora en que se ofrece la oblación, el profeta Elías se adelantó y dijo: «¡Señor, Dios de Abraham, de Isaac y de Israel! Que hoy se sepa que tú eres Dios en Israel, que yo soy tu servidor y que por orden tuya hice todas estas cosas.
</p><p>
37 Respóndeme, Señor, respóndeme, para que este pueblo reconozca que tú, Señor, eres Dios, y que eres tú el que les ha cambiado el corazón».
</p><p>
38 Entonces cayó el fuego del Señor: Abrazó el holocausto, la leña, las piedras y la tierra, y secó el agua de la zanja.
</p><p>
39 Al ver esto, todo el pueblo cayó con el rostro en tierra y dijo: «¡El Señor es Dios! ¡El Señor es Dios!».
</p><p>
40 Elías les dijo: «¡Agarren a los profetas de Baal! ¡Que no escape ninguno!». Ellos los agarraron: Elías los hizo bajar al torrente Quisón y allí los degolló.
</p><p>
El fin de la sequía
</p><p>
41 Elías dijo a Ajab: «Sube a comer y a beber, porque ya se percibe el ruido de la lluvia».
</p><p>
42 Ajab subió a comer y a beber, mientras Elías subía a la cumbre del Carmelo. Allí se postró en tierra, con el rostro entre las rodillas.
</p><p>
43 Y dijo a su servidor: «Sube y mira hacia el mar». El subió, miró y dijo: «No hay nada». Elías añadió: «Vuelve a hacerlo siete veces».
</p><p>
44 La séptima vez, el servidor dijo: «Se eleva del mar una nube, pequeña como la palma de una mano». Elías dijo: «Ve a decir a Ajab: Engancha el carro y baja, para que la lluvia no te lo impida».
</p><p>
45 El cielo se oscureció cada vez más por las nubes y el viento, y empezó a llover copiosamente. Ajab subió a su carro y partió para Izreel.
</p><p>
46 La mano del Señor se posó sobre Elías; él se ató el cinturón y corrió delante de Ajab hasta la entrada de Izreel.
</p>
