<h3>DANIEL</h3>
<p>Capítulo 5
</p><p>
1 El rey Baltasar ofreció un gran banquete a mil de sus dignatarios, y bebió vino en la presencia de esos mil.
</p><p>
2 Estimulado por el vino, Baltasar mandó traer los vasos de oro y plata que Nabucodonosor, su padre, había sacado del Templo de Jerusalén, para que bebieran en ellos el rey y sus dignatarios, sus mujeres y sus concubinas.
</p><p>
3 Entonces trajeron los vasos de oro que habían sido sacados del Templo, de la Casa de Dios en Jerusalén, y bebieron en ellos el rey y sus dignatarios, sus mujeres y sus concubinas.
</p><p>
4 Mientras bebían vino, glorificaban a los dioses de oro y plata, de bronce, hierro, madera y piedra.
</p><p>
5 De pronto, aparecieron unos dedos de mano humana, que escribían sobre el estuco del muro del palacio real, frente al candelabro, y el rey veía el extremo de esa mano que escribía.
</p><p>
6 Entonces el rey cambió de color y sus pensamientos lo llenaron de espanto; se le aflojaron todos los miembros y se entrechocaban sus rodillas.
</p><p>
7 El rey gritó con fuerza que hicieran venir a los adivinos, a los caldeos y los astrólogos. Y tomando la palabra, dijo a los sabios de Babilonia: «Cualquiera que lea la inscripción y me la interprete, se vestirá de púrpura, llevará un collar de oro en su cuello, y ocupará el tercer puesto en el reino».
</p><p>
8 Pero cuando entraron todos los sabios del rey, no fueron capaces de leer la inscripción ni de hacer conocer al rey su interpretación.
</p><p>
9 El rey Baltasar sintió un gran temor, cambió de color, y sus dignatarios quedaron consternados.
</p><p>
10 La reina, enterada de las palabras del rey y de sus dignatarios, entró en la sala del banquete y, tomando la palabra, dijo: «¡Viva el rey eternamente! Que tus pensamientos no te llenen de espanto y no cambies de color.
</p><p>
11 En tu reino hay un hombre que posee el espíritu de los dioses santos; mientras vivía tu padre, se encontró en él una clarividencia, una perspicacia y una sabiduría igual a la sabiduría de los dioses; el rey Nabucodonosor, tu padre, lo constituyó jefe de los magos, los adivinos, los caldeos y los astrólogos.
</p><p>
12 Ahora bien, ya que en este Daniel, a quien el rey dio el nombre de Beltsasar, se ha encontrado un espíritu superior, así como también ciencia, inteligencia, y el arte de interpretar sueños, resolver enigmas y solucionar problemas, que se llame a Daniel y él expondrá la interpretación».
</p><p>
13 Daniel fue introducido en la presencia del rey, y este, tomando la palabra, le dijo: «¿Así que tú eres Daniel, uno de los deportados judíos que el rey, mi padre, hizo venir de Judá?
</p><p>
14 Yo he oído decir que en ti reside el espíritu de los dioses, y que se han hallado en ti clarividencia, perspicacia y una sabiduría superior.
</p><p>
15 Acaban de ser traídos a mi presencia los sabios y los adivinos, para que lean esta inscripción y me la interpreten, pero ellos no han sido capaces de interpretar la cosa.
</p><p>
16 Yo he oído de ti que puedes dar interpretaciones y resolver problemas. Si tú ahora puedes leer la inscripción y me haces conocer su interpretación, te vestirás de púrpura, llevarás un collar de oro en tu cuello y ocuparás el tercer puesto en el reino».
</p><p>
17 Daniel tomó la palabra y dijo en presencia del rey: «Puedes guardar para ti tus dones y dar a otros tus regalos; de todas maneras, yo leeré al rey la inscripción y le haré conocer su interpretación.
</p><p>
18 Escucha, rey: El Dios Altísimo dio a tu padre Nabucodonosor la realeza, y también magnificencia, gloria y majestad.
</p><p>
19 Y a causa de la magnificencia que le concedió, todos los pueblos, naciones y lenguas temblaban de temor delante de él: él mataba y hacía vivir a quien quería, exaltaba y humillaba a quien quería.
</p><p>
20 Pero cuando se ensoberbeció su corazón y su espíritu se obstinó hasta la arrogancia, fue depuesto de su trono real y le fue retirada la gloria.
</p><p>
21 El fue expulsado de entre los hombres y adquirió instintos de bestia; convivió con los asnos salvajes, se alimentó de hierba como los bueyes y su cuerpo fue empapado por el rocío, hasta que supo que el Dios Altísimo domina sobre la realeza de los hombres y entroniza a quien él quiere.
</p><p>
22 Pero tú, su hijo Baltasar, no has querido humillarte, aunque sabías todo esto.
</p><p>
23 Te has exaltado contra el Señor del cielo: han traído a tu presencia los vasos de su Casa, y han bebido vino en ellos, tú y tus dignatarios, tus mujeres y tus concubinas; has glorificado a los dioses de plata y oro, de bronce, hierro, madera y piedra, que no ven, ni oyen, ni entienden, pero no has celebrado al Dios que tiene en su mano tu aliento y a quien pertenecen todos tus caminos.
</p><p>
24 Por eso ha sido enviada esta mano de parte de él, y ha sido trazada esta inscripción.
</p><p>
25 Esta es la inscripción que ha sido trazada: Mené, Tequel, Parsín.
</p><p>
26 Y esta es la interpretación de las palabras: Mené: Dios ha contado los días de tu reinado y les ha puesto fin;
</p><p>
27 Tequel: tú has sido pesado en la balanza y hallado falto de peso;
</p><p>
28 Parsín: tu reino ha sido dividido y entregado a los medos y a los persas».
</p><p>
29 Entonces Baltasar mandó revestir de púrpura a Daniel e hizo poner en su cuello el collar de oro y proclamar que ocuparía el tercer puesto en el reino.
</p><p>
30 Esa misma noche, mataron a Baltasar, rey de los caldeos.
</p>
