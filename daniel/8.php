<h3>DANIEL</h3>
<p>Capítulo 8
</p><p>
1 El tercer año del reinado del rey Baltasar, se me presentó una visión, a mí, Daniel, después de aquella que se me había presentado anteriormente.
</p><p>
2 En esa visión, yo estaba mirando; y mientras miraba, estaba en Susa, la ciudadela que está en la provincia de Elam. En esa visión, yo estaba mirando, y me encontraba junto al río Ulai.
</p><p>
3 Alcé mis ojos, miré, y vi un carnero que estaba parado ante el río. Tenía dos cuernos; los dos cuernos eran altos, pero uno era más alto que el otro, y el más alto se elevó en último término.
</p><p>
4 Yo vi al carnero que embestía hacia el oeste, hacia el norte y hacia el sur. Ningún animal podía resistir ante él, y nadie podía librar de su poder; él obraba a su arbitrio y se engrandecía.
</p><p>
5 Presté atención, y vi un chivo que venía del occidente, sobre la faz de toda la tierra, sin tocar el suelo; ese chivo tenía un cuerno imponente entre sus ojos.
</p><p>
6 Llegó hasta el carnero de los dos cuernos, que yo había visto parado ante el río, y corrió hacia él con todo el ardor de su fuerza.
</p><p>
7 Lo vi avanzar hacia el carnero, hecho una furia contra él; lo golpeó y le quebró sus dos cuernos, y el carnero no tuvo fuerza para resistir ante él: lo arrojó por tierra y lo pisoteó, y no hubo nadie que librara al carnero de su poder.
</p><p>
8 Así el chivo se engrandeció enormemente; pero una vez que se hizo fuerte, el cuerno grande se quebró, y cuatro cuernos imponentes se elevaron en lugar de él, hacia los cuatro vientos del cielo.
</p><p>
9 De uno de ellos salió otro cuerno pequeño, que se agrandó mucho hacia el sur, hacia el oriente y hacia la Hermosura.
</p><p>
10 Se agrandó hasta el Ejército del cielo, hizo caer por tierra a una parte de este Ejército y de las estrellas, y las pisoteó.
</p><p>
11 Se agrandó hasta llegar al Jefe del Ejército y le suprimió el sacrificio perpetuo; el lugar de su Santuario fue avasallado,
</p><p>
12 lo mismo que su Ejército. Sobre el sacrificio perpetuo fue instalada la iniquidad, y se echó por tierra la verdad. El tuvo éxito en todo lo que emprendió.
</p><p>
13 Entonces oí a un Santo que hablaba, y otro Santo dijo al que hablaba: «¿Hasta cuándo se verá el sacrificio perpetuo suprimido, la iniquidad desoladora instalada, el Lugar santo y el Ejército pisoteados?».
</p><p>
14 El respondió: «Hasta que pasen dos mil trescientas tardes y mañanas: entonces el Lugar santo será reivindicado».
</p><p>
15 Mientras yo, Daniel, miraba la visión y trataba de comprender, vi que estaba de pie frentea mí alguien con aspecto de hombre.
</p><p>
16 Y oí una voz de hombre en medio del río Ulai, que gritaba, diciendo: «Gabriel, explícale la aparición a este hombre».
</p><p>
17 El llegó hasta donde yo estaba, y cuando llegó, sentí un gran temor y caí sobre mi rostro. El me dijo: «Entiende, hijo de hombre, que la visión se refiere al tiempo del Fin».
</p><p>
18 Mientras él me hablaba, yo caí en trance con el rostro en tierra. El me tocó y me hizo poner de pie en el lugar donde estaba.
</p><p>
19 Luego añadió: «Voy a hacerte saber lo que sucederá al término de la Indignación, porque el Fin llegará en el momento fijado.
</p><p>
20 El carnero que viste con dos cuernos representa a los reyes de los medos y de los persas;
</p><p>
21 el chivo velludo es el rey de Javán, y el gran cuerno que estaba entre sus ojos es el primer rey.
</p><p>
22 Una vez quebrado este, los cuatro que surgieron en lugar de él son los cuatro reinos que surgirán de su nación, pero no con su misma fuerza.
</p><p>
23 Al fin de su reinado, cuando los pecadores hayan colmado la medida, surgirá un rey atrevido y astuto.
</p><p>
24 Su poderío se acrecentará, pero no por su propia fuerza;causará destrucciones inauditas y tendrá éxito en lo que emprenda, destruirá a los poderosos, al pueblo de los Santos.
</p><p>
25 Gracias a su astucia, el engaño triunfará por medio de él, su corazón se ensoberbecerá y destruirá tranquilamente a muchos. Se alzará contra el Jefe de los jefes, pero luego será destrozado sin que intervenga ninguna mano.
</p><p>
26 La visión de las tardes y las mañanas, tal como ha sido expuesta, es verdad. En cuanto a ti, oculta la visión, porque es para días lejanos».
</p><p>
27 Yo, Daniel, me desvanecí y estuve enfermo varios días. Luego me levanté y me dediquéa los asuntos del rey. La visión me dejó perplejo, y no atinaba a comprender.
</p>
