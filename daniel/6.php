<h3>DANIEL</h3>
<p>Capítulo 6
</p><p>
1 Y Darío el medo lo sucedió en el trono a los sesenta y dos años de edad.
</p><p>
2 Darío consideró oportuno poner al frente del reino a ciento veinte sátrapas, distribuidos por todo el reino,
</p><p>
3 y establecer sobre ellos a tres ministros –entre los cuales estaba Daniel– a quienes esos sátrapas debían rendir cuenta, a fin de que no se atentara contra los intereses del rey.
</p><p>
4 Ahora bien, este Daniel sobresalía entre los ministros y sátrapas, porque había en él un espíritu superior, y el rey pensaba ponerlo al frente de todo el reino.
</p><p>
5 Por eso los ministros y los sátrapas trataron de encontrar un pretexto para acusar a Daniel en lo referente a los asuntos del reino. Pero no pudieron encontrar ningún pretexto ni falta, porque él era fiel y no se le descubrió ninguna negligencia ni falta.
</p><p>
6 Esos hombres, dijeron: «No encontraremos ningún motivo de acusación contra Daniel, sino es en la Ley de su Dios».
</p><p>
7 Los ministros y los sátrapas acudieron precipitadamente al rey y hablaron así: «¡Viva eternamente el rey Darío!
</p><p>
8 Todos los ministros del reino, los prefectos y los sátrapas, los familiares y los gobernadores, se han puesto de acuerdo para que el rey promulgue un edicto y ponga en vigencia una prohibición, a saber: Todo el que dentro de los próximos treinta días dirija una plegaria a cualquier dios u hombre que no seas tú, rey, será arrojado en el foso de los leones.
</p><p>
9 Por lo tanto, rey, promulga la prohibición y consígnala por escrito, para que no sea modificada, conforme a la ley de los medos y de los persas, que es irrevocable».
</p><p>
10 A causa de esto, el rey Darío puso por escrito la prohibición.
</p><p>
11 Cuando Daniel supo que el documento había sido firmado, entró en su casa. Esta tenía en el piso superior unas ventanas que se abrían en dirección a Jerusalén, y tres veces por día, él se ponía de rodillas, invocando y alabando a su Dios, como lo había hecho antes.
</p><p>
12 Aquellos hombres acudieron precipitadamente y encontraron a Daniel orando y suplicando a su Dios.
</p><p>
13 Entonces de presentaron ante el rey y, refiriéndose a la prohibición real, le dijeron: «¿Acaso no has escrito una prohibición según la cual todo el que dirija una oración dentro de los próximos treinta días, a cualquier dios u hombre que no seas tú, rey, debe ser arrojado al foso de los leones?». El rey tomó la palabra y dijo: «Así es, en efecto, según la ley de los medos y de los persas, que es irrevocable».
</p><p>
14 Entonces ellos tomaron la palabra y dijeron en presencia del rey: «Daniel, uno de los deportados de Judá, no te ha hecho caso, rey, ni a ti ni a la prohibición que tú has escrito, y tres veces al día hace su oración».
</p><p>
15 Al oír esto, el rey se apenó profundamente y puso todo su empeño por salvar a Daniel: hasta el atardecer se esforzó por librarlo.
</p><p>
16 Pero esos hombres acudieron precipitadamente al rey y le dijeron: «Tienes que saber, rey, que según la ley de los medos y de los persas, ninguna prohibición o edicto promulgado por el rey puede ser modificado».
</p><p>
17 Entonces el rey mandó traer a Daniel y arrojarlo al foso de los leones. El rey tomó la palabra y dijo a Daniel: «Tu Dios, al que sirves con tanta constancia, te salvará».
</p><p>
18 Luego trajeron una piedra y la pusieron sobre la abertura del foso; el rey la selló con su anillo y con el anillo de sus dignatarios, para que no se cambiara nada en lo concerniente a Daniel.
</p><p>
19 El rey se retiró a su palacio; ayunó toda la noche, no hizo venir a sus concubinas y se le fue el sueño.
</p><p>
20 Al amanecer, apenas despuntado el día, el rey se levantó y fue rápidamente al foso de los leones.
</p><p>
21 Cuando se acercó a él, llamó a Daniel con voz angustiosa. El rey tomó la palabra y dijo a Daniel: «Daniel, servir del Dios viviente, ¿ha podido tu Dios, al que sirves con tanta constancia, salvarte de los leones?».
</p><p>
22 Daniel dijo al rey: «¡Viva el rey eternamente!
</p><p>
23 Mi Dios ha enviado a su Angel y ha cerrado las fauces de los leones, y ellos no me han hecho ningún mal, porque yo he sido hallado inocente en su presencia; tampoco ante ti, rey, había cometido ningún mal».
</p><p>
24 El rey sintió una gran alegría a causa de Daniel, y ordenó que lo sacaran del foso. Daniel fue sacado del foso, y no se le encontró ni un rasguño, porque había confiado en su Dios.
</p><p>
25 Luego el rey mandó traer a los hombres que habían acusado a Daniel y los hizo arrojar al foso de los leones, con sus hijos y sus mujeres. Y no habían llegado aún al fondo del foso, cuando ya los leones se apoderaron de ellos y les trituraron todos los huesos.
</p><p>
26 Entonces el rey Darío escribió a todos los pueblos, naciones y lenguas que habitan sobre la tierra: «¡Tengan ustedes paz en abundancia!
</p><p>
27 Yo ordeno que en todo el dominio de mi reino se tiemble y se sienta temor ante el Dios de Daniel, porque él es el Dios viviente y subsiste para siempre; su reino no será destruido y su dominio durará hasta el fin.
</p><p>
28 El salva y libera, realiza signos y prodigios en el cielo y sobre la tierra. El ha salvado a Daniel del poder de los leones».
</p><p>
29 Así este Daniel estuvo en auge bajo el reinado de Darío y bajo el reinado de Ciro el persa.
</p>
