<h3>DANIEL</h3>
<p>Capítulo 10
</p><p>
1 El año tercero de Ciro, rey de Persia, una palabra fue revelada a Daniel, que había recibido el nombre de Beltsasar. Esta palabra es verdadera y se refiere a un gran combate. El prestó atención a la palabra y le fue dada la inteligencia en el transcurso de la visión.
</p><p>
2 En aquellos días, yo, Daniel, estuve de duelo tres semanas enteras:
</p><p>
3 no comí ningún manjar exquisito; ni la carne ni el vino entraron en mi boca, ni me hice ninguna unción, hasta que se cumplieron tres semanas enteras.
</p><p>
4 Y el día veinticuatro del primer mes, yo estaba a orillas del Gran Río, es decir, el Tigris.
</p><p>
5 Alcé mis ojos y vi a un hombre vestido de lino y ceñido con un cinturón de oro fino de Ufaz.
</p><p>
6 Su cuerpo brillaba como el crisólito, su rostro tenía el aspecto del relámpago, sus ojos eran como antorchas de fuego, sus brazos y sus piernas como el fulgor del bronce bruñido, y el sonido de sus palabras como el estruendo de una multitud.
</p><p>
7 Sólo yo, Daniel, veía la aparición; los hombres que estaban conmigo no la vieron, sino que los invadió un gran temor y huyeron a esconderse.
</p><p>
8 Así quedé yo solo contemplando esta gran aparición, y me sentí desfallecer; mi semblante se demudó hasta desfigurarse, y no pude sobreponerme.
</p><p>
9 Yo oí el sonido de sus palabras y, al oírlo, caí en trance con el rostro en tierra.
</p><p>
10 De pronto, una mano me tocó y me hizo poner, temblando, sobre mis rodillas y sobre las palmas de mis manos.
</p><p>
11 Luego me dijo: «Daniel, hombre predilecto, fíjate en las palabras que voy a decirte, y ponte de pie en el lugar donde estás, porque ahora yo he sido enviado a ti». Y mientras medecía estas palabras, yo me puse de pie, temblando.
</p><p>
12 El me dijo: «No temas, Daniel, porque desde el primer día en que te empeñaste en comprender y en humillarte delante de tu Dios, fueron oídas tus palabras, y yo he venido a causa de ellas.
</p><p>
13 El Príncipe del reino de Persia me opuso resistencia durante veintiún días, pero Miguel, uno de los primeros Príncipes, ha venido en mi ayuda. Yo lo dejé allí, junto al Príncipe de los reyes de Persia,
</p><p>
14 y vine para hacerte comprender lo que sucederá a tu pueblo en los días venideros, porque también esta es una visión para aquellos días».
</p><p>
15 Mientras él me dirigía estas palabras, volví mi rostro hacia el suelo y me quedé mudo.
</p><p>
16 De pronto, una figura como la de un hijo de hombre tocó mis labios. Yo abrí mi boca y me puse a hablar, y dije al que estaba de pie frente a mí: «Mi Señor, ante esta aparición, yo me estremecí de dolor y no pude sobreponerme.
</p><p>
17 ¿Y cómo podría este servidor hablar con mi Señor, aquí presente, si ahora me faltan las fuerzas y ya me he quedado sin aliento?».
</p><p>
18 Aquel que parecía un hombre me volvió a tocar y me fortaleció.
</p><p>
19 Luego me dijo: «No temas, hombre predilecto. ¡La paz esté contigo! ¡Sé fuerte y valeroso!». Mientras él me hablaba, recobré las fuerzas y le dije: Que hable mi Señor, ya que me has fortalecido».
</p><p>
20 El respondió: «¿Sabes por qué he venido hasta ti? Ahora vuelvo a combatir contra el Príncipe de Persia, y una vez que haya concluido, vendrá el Príncipe de Javán.
</p><p>
21 Pero yo te voy a indicar lo que está consignado en el Libro de la Verdad. No hay nadie para fortalecerme contra ellos, fuera de Miguel, el Príncipe de ustedes.
</p>
