<h3>DANIEL</h3>
<p>Capítulo 3
</p><p>
1 El rey Nabucodonosor hizo una estatua de oro, de treinta metros de alto y tres de ancho, y la erigió en la llanura de Dura, en la provincia de Babilonia.
</p><p>
2 Luego mandó reunir a los sátrapas, prefectos, gobernadores, consejeros, tesoreros, juristas, magistrados y a todos los jefes de provincia, para que asistieran a la dedicación de la estatua que había erigido el rey Nabucodonosor.
</p><p>
3 Entonces se reunieron los sátrapas, prefectos, gobernadores, consejeros, tesoreros, juristas, magistrados y todos los jefes de provincia, para la dedicación de la estatua que había erigido el rey Nabucodonosor. Y se pusieron de pie ante la estatua erigida por el rey.
</p><p>
4 El heraldo proclamó con fuerza: «A todos ustedes, pueblos, naciones y lenguas, se les ordena lo siguiente:
</p><p>
5 Apenas escuchen el sonido de la trompeta, el pífano, la cítara, la sambuca, el laúd, la cornamusa y de toda clase de instrumentos, ustedes deberán postrarse y adorar la estatua de oro que ha erigido el rey Nabucodonosor.
</p><p>
6 El que no se postre para adorarla será arrojado inmediatamente dentro de un horno de fuego ardiente».
</p><p>
7 Por tal motivo, apenas todos los pueblos oyeron el sonido de la trompeta, el pífano, la cítara, la sambuca, el laúd, la cornamusa y de toda clase de instrumentos, todos los pueblos, naciones y lenguas se postraron para adorar la estatua de oro que había erigido el rey Nabucodonosor.
</p><p>
8 En ese mismo momento, se acercaron unos caldeos y acusaron a los judíos.
</p><p>
9 Tomando la palabra, dijeron al rey Nabucodonosor: «¡Viva el rey eternamente!
</p><p>
10 Tú, rey, has ordenado que todo el que oiga el sonido de la trompeta, el pífano, la cítara, la sambuca, el laúd, la cornamusa y de toda clase de instrumentos, tiene que postrarse y adorar la estatua de oro;
</p><p>
11 y que todo el que no se postre para adorarla, debe ser arrojado dentro de un horno de fuego ardiente.
</p><p>
12 Pero hay unos judíos, Sadrac, Mesac y Abed Negó, a quienes tú has encomendado la administración de la provincia de Babilonia: esos hombres no te han hecho caso, rey; ellos no sirven a tus dioses ni adoran la estatua de oro que tú has erigido».
</p><p>
13 Entonces Nabucodonosor, lleno de indignación y de furor, mandó traer a Sadrac, Mesac y Abed Negó. Cuando esos hombres fueron traídos ante la presencia del rey,
</p><p>
14 Nabucodonosor tomó la palabra y les dijo: «¿Es verdad Sadrac, Mesac y Abed Negó, que ustedes no sirven a mis dioses y no adoran la estatua de oro que yo erigí?
</p><p>
15 ¿Están dispuestos ahora, apenas oigan el sonido de la trompeta, el pífano, la cítara, la sambuca, el laúd, la cornamusa y de toda clase de instrumentos, a postrarse y adorar la estatua que yo hice? Porque si ustedes no la adoran, serán arrojados inmediatamente dentro de un horno de fuego ardiente. ¿Y qué Dios podrá salvarlos de mi mano?».
</p><p>
16 Sadrac, Mesac y Abed Negó respondieron al rey Nabucodonosor, diciendo: «No tenemos necesidad de darte una respuesta acerca de este asunto.
</p><p>
17 Nuestro Dios, a quien servimos, puede salvarnos del horno de fuego ardiente y nos librará de tus manos.
</p><p>
18 Y aunque no lo haga, ten por sabido, rey, que nosotros no serviremos a tus dioses ni adoraremos la estatua de oro que tú has erigido».
</p><p>
19 Nabucodonosor se llenó de furor y la expresión de su rostro se alteró frente a Sadrac, Mesac y Abed Negó. El rey tomó la palabra y ordenó activar el horno siete veces más de lo habitual.
</p><p>
20 Luego ordenó a los hombres más fuertes de su ejército que ataran a Sadrac, Mesac y Abed Negó, para arrojarlos en el horno de fuego ardiente.
</p><p>
21 Entonces estos tres, con sus mantos, sus calzados, sus gorros y toda su ropa, fueron atados y arrojados dentro del horno ardiente.
</p><p>
22 Como la orden del rey era perentoria y el horno estaba muy encendido, la llamarada mató a los hombres que habían llevado a Sadrac, Mesac y Abed Negó.
</p><p>
23 En cuanto a estos tres, Sadrac, Mesac y Abed Negó, cayeron atados dentro del horno de fuego ardiente.
</p><p>
24 El rey Nabucodonosor quedó estupefacto y se levantó rápidamente. Y tomando la palabra, dijo a sus cortesanos: «¿No eran tres los hombres que fueron atados y arrojados dentro del fuego?». Ellos le respondieron, diciendo: «Así es, rey».
</p><p>
25 El replicó: «Sin embargo, yo veo cuatro hombres que caminan libremente por el fuego sin sufrir ningún daño, y el aspecto del cuarto se asemeja a un hijo de los dioses».
</p><p>
26 Entonces Nabucodonosor se acercó a la puerta del horno de fuego ardiente y, tomando la palabra, dijo: «Sadrac, Mesac y Abed Negó, servidores del Dios altísimo, salgan y vengan». Y Sadrac, Mesac y Abed Negó salieron de en medio del fuego.
</p><p>
27 Una vez reunidos los prefectos, los gobernadores y los cortesanos del rey, comprobaron que el fuego no había tenido poder sobre el cuerpo de aquellos hombres, que sus cabellos no se habían quemado, que sus mantos estaban intactos y que ni siquiera el olor del fuego se había adherido a ellos.
</p><p>
28 Nabucodonosor tomó la palabra y dijo: «Bendito sea el Dios de Sadrac, Mesac y Abed Negó, porque ha enviado a su Angel y ha salvado a sus servidores, que confiaron en él y, quebrantando la orden del rey, entregaron su cuerpo antes que servir y adorar a cualquier otro dios que no fuera su Dios.
</p><p>
29 Por eso, yo doy este decreto: «Todo pueblo, nación o lengua que hable irreverentemente contra el Dios de Sadrac, Mesac y Abed Negó, será cortado en pedazos y su casa quedará reducida a un basural, porque no hay otro dios que pueda librar de esa manera».
</p><p>
30 Entonces Nabucodonosor hizo prosperar a Sadrac, Mesac y Abed Negó en la provincia de Babilonia.
</p><p>
31 «El rey Nabucodonosor, a todos los pueblos, naciones y lenguas que habitan sobre toda la tierra: ¡Tengan ustedes paz en abundancia!
</p><p>
32 Me ha parecido bien publicar los signos y prodigios que ha realizado en mi favor el Dios Altísimo:
</p><p>
33 ¡Qué grandes son sus signos! ¡Qué poderosos sus prodigios! ¡Su reino es un reino eterno y su dominio dura de generación en generación!
</p>
