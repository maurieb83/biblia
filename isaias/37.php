<h3>ISAIAS</h3>
<p>Capítulo 37
</p><p>
1 Cuando el rey Ezequías oyó esto, rasgó sus vestiduras, se cubrió con un sayal y fue a la Casa del Señor.
</p><p>
2 Además, envió al mayordomo de palacio Eliaquím, al secretario Sebná y a los sacerdotes más ancianos, todos cubiertos de sayales, para decir al profeta Isaías, hijo de Amós:
</p><p>
3 «Así habla Ezequías: Hoy es un día de angustia, de castigo y de oprobio, porque los hijos están a punto de nacer, pero no hay fuerza para darlos a luz.
</p><p>
4 Tal vez el Señor, tu Dios, escuche las palabras del copero mayor, a quien el rey de Asiria, su señor, envió para insultar al Dios viviente, y el Señor tu Dios, lo castigue por las palabras que ha escuchado. Eleva entonces una plegaria por el resto que todavía subsiste».
</p><p>
5 Los servidores del rey Ezequías fueron a ver a Isaías,
</p><p>
6 y este les dijo: «Díganle a su señor: Así habla el Señor: No temas por las palabras que has oído y con las que me ultrajaron los lacayos del rey de Asiria.
</p><p>
7 Yo mismo pondré un espíritu en él y, apenas oiga una noticia, regresará a su país; y yo lo haré caer bajo la espada en su propio país».
</p><p>
8 El copero mayor regresó y se encontró con el rey de Asiria, que estaba atacando a Libná. El había oído, en efecto, que el rey se había retirado de Laquis,
</p><p>
9 al recibir esta noticia acerca de Tirjacá, rey de Cus: «Se ha puesto en campaña para combatirte». Al oír esto, Senaquerib envió mensajeros a Ezequías para decirle:
</p><p>
10 «Háblenle así a Ezequías, rey de Judá: Que no te engañe tu Dios, en quien confías, haciéndote pensar que Jerusalén no será entregada en manos del rey de Asiria.
</p><p>
11 Tú has oído, seguramente, lo que hicieron los reyes de Asiria a todos los países, al consagrarlos al exterminio total. ¿Y tú te vas a librar?
</p><p>
12 ¿Libraron acaso sus dioses a esas naciones que mis padres han destruido, a Gozán, Jarán, Résef, y a la gente de Edén que está en Telasar?
</p><p>
13 ¿Dónde están el rey de Jamat, el rey de Arpad, el rey de la ciudad de Sefarvaim, el de Hená y el de Ivá?».
</p><p>
14 Ezequías tomó la carta de manos de los mensajeros y la leyó. Después subió a la Casa del Señor, la desplegó delante del Señor
</p><p>
15 y oró al Señor, diciendo:
</p><p>
16 «Señor de los ejércitos, Dios de Israel, que tienes tu trono sobre los querubines: tú solo eres el Dios de todos los reinos de la tierra, tú has hecho el cielo y la tierra.
</p><p>
17 Inclina tu oído, Señor, y escucha; abre tus ojos, Señor, y mira. Escucha todas las palabras que Senaquerib ha mandado decir, para insultar al Dios viviente.
</p><p>
18 Es verdad, Señor, que los reyes de Asiria han arrasado todas las naciones y sus territorios.
</p><p>
19 Ellos han arrojado sus dioses al fuego, porque no son dioses, sino obra de las manos del hombre, nada más que madera y piedra. Por eso los hicieron desaparecer.
</p><p>
20 Pero ahora, Señor, Dios nuestro, ¡sálvanos de su mano, y que todos los reinos de la tierra reconozcan que tú sólo, Señor, eres Dios!».
</p><p>
21 Isaías, hijo de Amós, mandó a decir a Ezequías: Así habla el Señor, Dios de Israel: Tú me has dirigido una súplica acerca de Senaquerib, rey de Asiria.
</p><p>
22 Esta es la palabra que el Señor ha pronunciado contra él: Te desprecia, se burla de ti, la virgen hija de Sión; a tus espaldas mueve la cabeza la hija de Jerusalén.
</p><p>
23 ¿A quién has insultado y ultrajado? ¿Contra quién has alzado la voz y levantado bien alto tus ojos? ¡Contra el santo de Israel!
</p><p>
24 Por medio de tus servidores has insultado al Señor y has dicho: Con mis numerosos carros escalé la cima de las montañas, los rincones inaccesibles del Líbano. Talé sus cedros más altos, sus mejores cipreses; llegué hasta su último extremo, hasta lo más espeso de su bosque.
</p><p>
25 Excavé pozos y bebí todos los canales de Egipto.
</p><p>
26 ¿No lo has oído? Hace mucho tiempo que lo he preparado: lo he planeado desde los tiempos antiguos y ahora lo llevo a cabo. Así, tú has reducido a un montón de ruinas las ciudades fortificadas.
</p><p>
27 Sus habitantes, con las manos caídas, están aterrorizados, avergonzados: son como el pasto de los campos y la gramilla verde, como la hierba de los techos o el grano agostado antes de madurar.
</p><p>
28 Pero yo sé cuándo te sientas, cuándo sales y cuándo entras, y cuándo tiemblas de rabia contra mí.
</p><p>
29 Porque has temblado de rabia contra mí y tu insolencia ha subido a mis oídos, pondré mi garfio en tus narices y mi bozal en tus labios, y te haré volver por el camino por donde habías venido.
</p><p>
30 Y esto te servirá de señal: Este año se comerá del grano caído, y el año próximo, de lo que brote espontáneamente; pero al tercer año, siembren y coseche, planten viñas y coman de sus frutos.
</p><p>
31 Los sobrevivientes de la casa de Judá, los que todavía queden, echarán de nuevo raíces por debajo, y producirán frutos por arriba.
</p><p>
32 Porque de Jerusalén saldrá un resto, y del monte Sión, algunos sobrevivientes. El celo del Señor de los ejércitos hará todo esto.
</p><p>
33 Por eso, así habla el Señor acerca del rey de Asiria: El no entrará en esta ciudad, no le lanzará una flecha, no la enfrentará con el escudo, ni levantará contra ella un terraplén.
</p><p>
34 Se volverá por el mismo camino, sin entrar en esta ciudad –oráculo del Señor–,
</p><p>
35 Yo defenderé a esta ciudad para salvarla, por mi honor y el de David, mi servidor.
</p><p>
36 El Angel del Señor salió e hirió en el campamento de los Asirios a ciento ochenta y cinco mil hombres. Y cuando los demás se levantaron por la mañana, vieron que todos eran cadáveres, que estaban muertos.
</p><p>
37 Entonces Senaquerib, rey de Asiria, levantó el campamento, emprendió el regreso y se quedó en Nínive.
</p><p>
38 Un día, mientras estaba postrado en el templo de Nisroc, su dios, Adramélec y Sarecer, sus hijos, lo mataron con la espada, y se pusieron a salvo en el país de Ararat. Asarhadón, su hijo, reinó en lugar de él.
</p>
