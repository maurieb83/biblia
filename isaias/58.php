<h3>ISAIAS</h3>
<p>Capítulo 58
</p><p>
1 ¡Grita a voz en cuello, no te contengas, alza tu voz como una trompeta: denúnciale a mi pueblo su rebeldía y sus pecados a la casa de Jacob!
</p><p>
2 Ellos me consultan día tras día y quieren conocer mis caminos, como lo hará una nación que practica la justicia y no abandona el derecho de su Dios; reclaman de mí sentencias justas, les gusta estar cerca de Dios:
</p><p>
3 «¿Por qué ayunamos a tú no lo ves, nos afligimos y tú no lo reconoces?». Porque ustedes, el mismo día en que ayunan, se ocupan de negocios y maltratan a su servidumbre.
</p><p>
4 Ayunan para entregarse a pleitos y querellas y para golpear perversamente con el puño. No ayunen como en esos días, si quieren hacer oír su voz en las alturas,
</p><p>
5 ¿Es este acaso el ayuno que yo amo, el día en que el hombre se aflige a sí mismo? Doblar la cabeza como un junco, tenderse sobre el cilicio y la ceniza: ¿a eso llamas ayuno y día aceptable al Señor?
</p><p>
6 Este es el ayuno que yo amo –oráculo del Señor–: soltar las cadenas injustas, desatar los lazos del yugo, dejar en libertad a los oprimidos y romper todos los yugos;
</p><p>
7 compartir tu pan con el hambriento y albergar a los pobres sin techo; cubrir al que veas desnudo y no despreocuparte de tu propia carne.
</p><p>
8 Entonces despuntará tu luz como la aurora y tu llaga no tardará en cicatrizar; delante de ti avanzará tu justicia y detrás de ti irá la gloria del Señor.
</p><p>
9 Entonces llamarás, y el Señor responderá; pedirás auxilio, y él dirá: «¡Aquí estoy!». Si eliminas de ti todos los yugos, el gesto amenazador y la palabra maligna;
</p><p>
10 si ofreces tu pan al hambriento y sacias al que vive en la penuria, tu luz se alzará en las tinieblas y tu oscuridad será como al mediodía,
</p><p>
11 El Señor te guiará incesantemente, te saciará en los ardores del desierto y llenará tus huesos de vigor; tú serás como un jardín bien regado, como una vertiente de agua, cuyas aguas nunca se agotan.
</p><p>
12 Reconstruirás las ruinas antiguas, restaurarás los cimientos seculares, y te llamarán «Reparador de brechas», «Restaurador de moradas en ruinas».
</p><p>
13 Si dejas de pisotear el sábado, de hacer tus negocios en mi día santo; si llamas al sábado «Delicioso» y al día santo del Señor «Honorable»; si lo honras absteniéndote de traficar, de entregarte a tus negocios y de hablar ociosamente,
</p><p>
14 entonces te deleitarás en el Señor; yo te haré cabalgar sobre las alturas del país y te alimentaré con la herencia de tu padre Jacob, porque ha hablado la boca del Señor.
</p>
