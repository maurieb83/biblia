<h3>ISAIAS</h3>
<p>Capítulo 42
</p><p>
1 Este es mi Servidor, a quien yo sostengo, mi elegido, en quien se complace mi alma. Yo he puesto mi espíritu sobre él para que lleve el derecho a las naciones.
</p><p>
2 El no gritará, no levantará la voz ni la hará resonar por las calles.
</p><p>
3 No romperá la caña quebrada ni apagará la mecha que arde débilmente. Expondrá el derecho con fidelidad;
</p><p>
4 no desfallecerá ni se desalentará hasta implantar el derecho en la tierra, y las costas lejanas esperarán su Ley.
</p><p>
5 Así habla Dios, el Señor, el que creó el cielo y lo desplegó, el que extendió la tierra y lo que ella produce, el que da el aliento al pueblo que la habita y el espíritu a los que caminan por ella.
</p><p>
6 Yo, el Señor, te llamé en la justicia, te sostuve de la mano, te formé y te destiné a ser la alianza del pueblo, la luz de las naciones,
</p><p>
7 para abrir los ojos de los ciegos, para hacer salir de la prisión a los cautivos y de la cárcel a los que habitan en las tinieblas.
</p><p>
8 ¡Yo soy el Señor, este es mi Nombre! No cederé mi gloria a ningún otro ni mi alabanza a los ídolos.
</p><p>
9 Las cosas antiguas ya han sucedido y yo anuncio cosas nuevas; yo se las hago oír a ustedes.
</p><p>
10 ¡Canten al Señor un canto nuevo, alábenlo desde los confines de la tierra; resuene el mar y todo lo que hay en él, las costas lejanas y sus habitantes!
</p><p>
11 ¡Que alcen la voz el desierto y sus ciudades, los poblados donde habita Quedar! ¡Griten de alegría los habitantes de la Roca, aclamen desde la cumbre de las montañas!
</p><p>
12 ¡Den gloria al Señor, proclamen su alabanza en las costas lejanas!
</p><p>
13 El Señor irrumpe como un héroe, se enardece como un guerrero; lanza un grito de guerra, un alarido estridente, se arroja como un héroe contra sus enemigos:
</p><p>
14 «Yo permanecí callado mucho tiempo, guardé silencio y me contuve; ahora gimo como una parturienta, me sofoco y estoy jadeante.
</p><p>
15 Arrasaré montañas y colinas, y secaré todo su verdor; convertiré los ríos en tierra árida y secaré los estanques.
</p><p>
16 Conduciré a los ciegos por un camino que ignoran, los guiaré por senderos desconocidos; cambiaré las tinieblas en luz delante de ellos, y el suelo escarpado en una llanura. Estas son las cosas que haré, y no dejaré de hacerlas.
</p><p>
17 Así retrocederán llenos de vergüenza los que confían en los ídolos, los que dicen al metal fundido: «Ustedes son nuestros dioses».
</p><p>
18 ¡Oigan, ustedes, los sordos; ustedes, los ciegos, miren y vean!
</p><p>
19 ¿Quién es ciego, sino mi servidor y sordo como el mensajero que yo envío? ¿Quién es ciego, sino mi servidor y sordo como el mensajero que yo envío? ¿Quién es ciego como el que ha pactado conmigo y sordo como el servidor del Señor?
</p><p>
20 Tú has visto muchas cosas, pero sin prestar atención; has abierto los oídos, pero sin escuchar.
</p><p>
21 El Señor, a causa de su justicia, quería hacer grande y gloriosa la Ley;
</p><p>
22 pero ahora no es más que un pueblo saqueado y despojado, están todos atrapados en cuevas y encerrados en cárceles. Se los saquea, y nadie los libra, se los despoja, y nadie dice: ¡Restituye!».
</p><p>
23 ¿Quién de ustedes presta oído a esto y escucha atentamente con miras al futuro
</p><p>
24 ¿Quién entregó a Jacob al despojo, y a Israel a los expoliadores? ¿No es el Señor, contra quien hemos pecado por no querer seguir sus caminos y haber desoído su Ley?
</p><p>
25 El Señor derramó contra él el ardor de su ira y el estallido de la guerra; lo envolvió en llamas, pero él no comprendió; lo quemó, pero él no hizo caso.
