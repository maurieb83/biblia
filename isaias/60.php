<h3>ISAIAS</h3>
<p>Capítulo 60
</p><p>
1 ¡Levántate, resplandece, porque llega tu luz y la gloria del Señor brilla sobre ti!
</p><p>
2 Porque las tinieblas cubren la tierra y una densa oscuridad, a las naciones, pero sobre ti brillará el Señor y su gloria aparecerá sobre ti.
</p><p>
3 Las naciones caminarán a tu luz y los reyes, al esplendor de tu aurora.
</p><p>
4 Mira a tu alrededor y observa: todos se han reunido y vienen hacia ti; tus hijos llegan desde lejos y tus hijas son llevadas en brazos.
</p><p>
5 Al ver esto, estarás radiante, palpitará y se ensanchará tu corazón, porque se volcarán sobre ti los tesoros del mar y las riquezas de las naciones llegarán hasta ti.
</p><p>
6 Te cubrirá una multitud de camellos, de dromedarios de Madián y de Efá. Todos ellos vendrán desde Sabá, trayendo oro e incienso, y pregonarán las alabanzas del Señor.
</p><p>
7 En ti se congregarán todos los rebaños de Quedar, los carneros de Nebaiot estarán a tu servicio: subirán como ofrenda aceptable sobre mi altar y yo glorificaré mi Casa gloriosa.
</p><p>
8 ¿Quiénes son esos que vuelan como una nube, como palomas a su palomar?
</p><p>
9 Son barcos que se reúnen para mí, con naves de Tarsis al frente, para traer a tus hijos de lejos, y con ellos su oro y su plata, por el nombre del Señor, tu Dios, y por el Santo de Israel, que así te glorifica.
</p><p>
10 Gente extranjera reconstruirá tus murallas y sus reyes te servirán, porque yo te castigué con mi irritación, pero en mi benevolencia tengo piedad de ti.
</p><p>
11 Tus puertas estarán siempre abiertas, no se cerrarán ni de día ni de noche, para que te traigan las riquezas de las naciones, bajo la guía de sus reyes.
</p><p>
12 Porque la nación y el reino que no te sirvan, perecerán, y las naciones serán exterminadas.
</p><p>
13 Hasta ti llegará la gloria del Líbano, con el ciprés, el olmo y el abeto. para glorificar el lugar de mi Santuario, para honrar el lugar donde se posan mis pies.
</p><p>
14 Los hijos de tus opresores irán a inclinarse ante ti, y todos los que te despreciaban se postrarán ante la planta de tus pies y te llamarán: «Ciudad del Señor». «Sión del Santo de Israel».
</p><p>
15 Antes estuviste abandonada, aborrecida y despoblada, pero yo haré de ti el orgullo de los siglos, la alegría de todas las generaciones.
</p><p>
16 Mamarás leche de las naciones, mamarás del pecho de los reyes, y sabrás que yo, el Señor, soy tu salvador, y que tu redentor es el Fuerte de Jacob.
</p><p>
17 Haré llegar oro en lugar de bronce y plata en lugar de hierro; bronce en lugar de madera y hierro en lugar de piedra. Por magistrados te daré la Paz y por gobernantes, La Justicia.
</p><p>
18 Ya no se oirá hablar de violencia en tu país ni de expoliación y desastre en tus fronteras; a tus murallas las llamarás «Salvación» y a tus puertas, «Alabanza».
</p><p>
19 El sol ya no será tu luz durante el día, ni la claridad de la luna te alumbrará de noche: el Señor será para ti una luz eterna y tu Dios será tu esplendor.
</p><p>
20 Tu sol no se pondrá nunca más y tu luna no desaparecerá, porque el Señor será para ti una luz eterna y se habrán cumplido los días de tu duelo.
</p><p>
21 En tu pueblo, todos serán justos y poseerán la tierra para siempre: serán un retoño de mis plantaciones, obra de mis manos, para manifestar mi gloria.
</p><p>
22 El más pequeño se convertirá en un millar, el menor, en una nación poderosa. Yo, el Señor, lo haré rápidamente, a su tiempo.
