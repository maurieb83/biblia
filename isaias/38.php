<h3>ISAIAS</h3>
<p>Capítulo 38
</p><p>
1 En aquellos días, Ezequías cayó gravemente enfermo. El profeta Isaías, hijo de Amós, fue a verlo y le dijo: «Así habla el Señor: Ordena los asuntos de tu casa, porque vas a morir. Ya no vivirás más».
</p><p>
2 Ezequías volvió su rostro hacia la pared y oró al Señor.
</p><p>
3 diciendo: «¡Ah, Señor! Recuerda que yo he caminado delante de ti con fidelidad e integridad de corazón, y que hice lo que es bueno a tus ojos». Y Ezequías se deshizo en llanto.
</p><p>
4 Entonces la palabra del Señor llegó a Isaías en estos términos:
</p><p>
5 «Ve a decir a Ezequías: Así habla el Señor, el Dios de tu padre David: He oído tu súplica, he visto tus lágrimas. Yo añadiré otros quince años a tu vida;
</p><p>
6 te libraré, a ti y a esta ciudad, de manos del rey de Asiria, y defenderé a esta ciudad».
</p><p>
7 «Esta es la señal que te da el Señor para confirmar la palabra que ha pronunciado:
</p><p>
8 En el reloj de sol de Ajaz, yo haré retroceder diez grados la sombra que ha descendido». Y el sol retrocedió en el reloj los diez grados que había descendido.
</p><p>
9 Escrito de Ezequías, rey de Judá, cuando cayó enfermo y se repuso de su enfermedad:
</p><p>
10 «Yo decía: En lo mejor de mis días me tengo que ir: he sido destinado a las puertas del Abismo por el resto de mis años.
</p><p>
11 Yo decía: Ya no contemplaré al Señor en la tierra de los vivientes; no verá más a los hombres entre los habitantes del mundo.
</p><p>
12 Arrancan mi morada y me la arrebatan, como una carpa de pastores. Como un tejedor, yo enrollaba mi vida, pero él me corta de la trama: ¡de la mañana a la noche terminas conmigo!
</p><p>
13 Pido auxilio hasta la mañana; él quiebra todos mis huesos como un león: ¡de la mañana a la noche terminas conmigo!
</p><p>
14 Estoy piando como una golondrina, gimo como una paloma. Mis ojos se consumen de mirar a lo alto: ¡me oprimen, Señor, sé tú mi fiador!
</p><p>
15 ¿Qué diré para que me responda, si es él quien lo hace? Andaré errante a lo largo de mis años, con amargura en el alma.
</p><p>
16 Los que el Señor protege, vivirán, y su espíritu animará todo lo que hay en ellos: tú me restablecerás y me harás revivir.
</p><p>
17 Mi amargura se cambió en bienestar: tú has preservado mi vida de la fosa del aniquilamiento, porque has arrojado detrás de tus espaldas todos mis pecados.
</p><p>
18 No, el Abismo no te da gracias, la Muerte no te alaba, los que bajan a la Fosa no esperan en tu fidelidad.
</p><p>
19 El viviente, el que vive, te da gracias, como yo en el día de hoy. De padres a hijos, se da a conocer tu fidelidad.
</p><p>
20 Porque tú me salvaste, Señor, haremos resonar nuestras liras todos los días de nuestra vida junto a la Casa del Señor».
</p><p>
21 Luego dijo Isaías: «Traigan un emplasto de higos; aplíquenlo sobre la úlcera, y el rey sanará».
</p><p>
22 Ezequías respondió: «¿Cuál es la señal de que podré subir a la Casa del Señor?».
</p>
