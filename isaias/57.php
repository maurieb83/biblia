<h3>ISAIAS</h3>
<p>Capítulo 57
</p><p>
1 El justo desaparece y a nadie le llama la atención; los hombres de bien son arrebatados, sin que nadie comprenda que el justo es arrebatado a consecuencia de la maldad.
</p><p>
2 Pero llegará la paz: los que van por el camino recto descansarán en sus lechos.
</p><p>
3 ¡Y ustedes, acérquense aquí, hijos de una hechicera, raza de un adúltero y una prostituta!
</p><p>
4 ¿De quién se burlan? ¿Contra quién abren la boca y sacan la lengua? ¿No son ustedes hijos de la rebeldía, una raza bastarda?
</p><p>
5 ¡Ustedes, que arden de lujuria junto a los terebintos, bajo todo árbol frondoso, e inmolan niños en los torrentes, en las hendiduras de las rocas!
</p><p>
6 Las piedras lisas del torrente son tu parte: ¡sí, ellas te han tocado en suerte! En su honor has derramado libaciones, has presentado ofrendas, y yo ¿me dejaré aplacar con esas cosas?
</p><p>
7 Sobre una montaña alta y empinada has instalado tu lecho, y allí has subido a ofrecer sacrificios.
</p><p>
8 Detrás de los postes de la puerta has colocado tu memorial; y has subido al lecho que habías tendido; has hecho un trato con uno de esos con quienes te gusta acostarte, y has contemplado la insignia.
</p><p>
9 Corres hacia Mélec con el aceite, prodigas tus perfumes; envías muy lejos a tus mensajeros, los haces descender hasta el Abismo.
</p><p>
10 Te has cansado de tanto caminar, pero no dices: «¡Es inútil!». Has renovado la fuerza de tu brazo, y por eso no te has debilitado.
</p><p>
11 ¿De quién tenías miedo, a quién temías, para mentir y no acordarte de mí, para no prestarme ninguna atención? ¿Será porque me quedé callado y cerré los ojos que tú no me temes?
</p><p>
12 ¡Pero yo voy a denunciar tu justicia y tus obras: ellas no te servirán de nada!
</p><p>
13 Cuando clames, que te libren tus ídolos: ¡el viento se los llevará a todos ellos, un soplo los disipará! Pero el que se refugia en mí heredará la tierra y entrará en posesión de mi santa Montaña.
</p><p>
14 Entonces se dirá: ¡Abran paso, abran paso, preparen un camino, quiten los obstáculos del camino de mi Pueblo!
</p><p>
15 Porque así habla él que es alto y excelso, el que habita en una morada eterna, aquel cuyo Nombre es santo; Yo habito en una altura santa, pero estoy con el contrito y humillado, para reavivar los espíritus humillados, para reavivar los corazones contritos.
</p><p>
16 No, yo no recrimino para siempre ni me irrito eternamente, porque entonces desfallecerían ante mí el espíritu y el aliento de vida que yo mismo hice.
</p><p>
17 Por sus ganancias injustas, yo me irrité, y lo herí, ocultándome en mi irritación, pero el rebelde siguió el camino que quería.
</p><p>
18 Yo he visto sus caminos, pero lo sanaré, lo guiaré y lo colmaré de consuelos; y de los labios de los que están de duelo,
</p><p>
19 haré brotar la acción de gracias. ¡Paz al que está lejos, paz al que está cerca! Yo lo sanaré, dice el Señor.
</p><p>
20 Pero los impíos son como un mar agitado, que no se puede calmar y cuyas aguas arrojan fango y cieno.
</p><p>
21 ¡No hay paz para los impíos!, dice el Señor.
</p>
