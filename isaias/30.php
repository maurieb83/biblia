<h3>ISAIAS</h3>
<p>Capítulo 30
</p><p>
1 ¡Ay de los hijos rebeldes –oráculo del Señor– que hacen planes sin cortar conmigo, que concluyen pactos contrarios a mi espíritu. añadiendo así un pecado tras otro!
</p><p>
2 Se ponen en camino para bajar a Egipto sin haberme consultado, para refugiarme al amparo del Faraón y protegerse a la sombra de Egipto.
</p><p>
3 El amparo del Faraón será su vergüenza y la protección a la sombra de Egipto, su confusión.
</p><p>
4 Aunque sus jefes estén en Soán y sus mensajeros hayan llegado a Janés,
</p><p>
5 todos ellos serán defraudados por un pueblo que no sirve de nada, que no les aporta ayuda ni provecho, sino vergüenza y oprobio.
</p><p>
6 Oráculo sobre las bestias del Négueb: Por una tierra de miseria y angustia, de donde salen la leona y el león, la víbora y la serpiente voladora, ellos llevan sus riquezas a lomo de asnos y sus tesoros sobre la giba de los camellos, a un pueblo que no sirve de nada.
</p><p>
7 ¡Egipto! Su ayuda es inútil y vana; por eso yo lo llamé: «Rahab, la inmóvil».
</p><p>
8 Ahora ve, escribe esto en una tabla, delante de ellos, e inscríbelo en un libro: que sirva de testimonio perpetuo para el tiempo futuro.
</p><p>
9 Porque este es un pueblo en rebeldía, son hijos mentirosos, hijos que no quieren escuchar la enseñanza del Señor.
</p><p>
10 Ellos dicen a los videntes: «¡No tengan visiones!», y a los profetas: «¡No nos vaticinen la verdad! ¡Háblennos de cosas agradables, tengan visiones ilusorias!
</p><p>
11 ¡Apártense del camino, desvíense del sendero, dejen de ponernos por delante al Santo de Israel!».
</p><p>
12 Por eso, así habla el Santo de Israel: Porque ustedes desprecian esta palabra y confían en lo que es tortuoso y retorcido, para tener donde apoyarse,
</p><p>
13 por eso, esta falta será para ustedes como una grieta amenazadora que se va agrandando en un muro elevado, y de pronto, en un instante, sobreviene el derrumbe;
</p><p>
14 o como se quiebra una vasija de alfarero hecha añicos sin piedad, sin que se encuentre entre sus pedazos ni un trozo para sacar fuego del brasero o para extraer agua del aljibe.
</p><p>
15 Porque así habla el Señor, el Santo de Israel: En la conversión y en la calma está la salvación de ustedes; en la serenidad y la confianza está su fuerza. ¡Pero ustedes no lo han querido!
</p><p>
16 Ustedes dijeron: «¡No, huiremos a caballo!». Está bien, tendrán que huir. «¡Cabalgaremos velozmente!». Está bien, sus perseguidores serán más veloces.
</p><p>
17 Ante la amenaza de uno solo, temblarán mil; ante la amenaza de cinco, ustedes huirán, hasta que sean dejados como un mástil en la cumbre de una montaña, como señal sobre una colina.
</p><p>
18 A pesar de todo, el Señor espera para apiadarse de ustedes; a pesar de todo, él se levantará para tenerles compasión; porque el Señor es un Dios de justicia. ¡Felices todos los que esperan en él!
</p><p>
19 Sí, pueblo de Sión, que habitas en Jerusalén, ya no tendrás que llorar: él se apiadará de ti al oír tu clamor; apenas te escuche, te responderá.
</p><p>
20 Cuando el Señor les haya dado el pan de la angustia y el agua de la aflicción, aquel que te instruye no se ocultará más, sino que verás a tu maestro con tus propios ojos.
</p><p>
21 Tus oídos escucharán detrás de ti una palabra: «Este es el camino, síganlo, aunque se hayan desviado a la derecha o a la izquierda».
</p><p>
22 Tendrás por impuros a tus ídolos recubiertos de plata y a tus estatuas enchapadas en oro; los arrojarás como inmundicia, y les dirás: «¡Fuera de aquí!».
</p><p>
23 El Señor te dará lluvia para la semilla que siembres en el suelo, y el pan que produzca el terreno será rico y sustancioso. Aquel día, tu ganado pacerá en extensas praderas.
</p><p>
24 Los bueyes y los asnos que trabajen el suelo comerán forraje bien sazonado, aventado con el bieldo y la horquilla.
</p><p>
25 En todo monte elevado y en toda colina alta, habrá arroyos y corrientes de agua, el día de la gran masacre, cuando se derrumben las torres.
</p><p>
26 Entonces, la luz de la luna será como la luz del sol, y la luz del sol será siete veces más intensa –como la luz de siete días– el día en que el Señor vende la herida de su pueblo y sane las llagas de los golpes que le infligió.
</p><p>
27 ¡Miren que el nombre del Señor viene de lejos! Arde su ira y es densa la humareda; sus labios desbordan de indignación y su lengua es como fuego devorador.
</p><p>
28 Su aliento es como un torrente desbordado, que sube hasta el cuello, para zarandear a las naciones con la criba destructora y poner el freno del extravío en las quijadas de los pueblos.
</p><p>
29 Entonces, ustedes cantarán como en la noche sagrada de la fiesta, y habrá alegría en los corazones, como cuando se avanza al son de la flauta para ir a la montaña del Señor, hacia la Roca de Israel.
</p><p>
30 El Señor hará oír su voz majestuosa y mostrará su brazo que se descarga en el ardor de su ira, en la llama de un fuego devorador, en el huracán, la tormenta y el granizo.
</p><p>
31 Asiria temblará ante la voz del Señor, que golpeará con el bastón;
</p><p>
32 y cada vez que pase la vara vengadora que el Señor descargará contra ella, irá acompañada de tamboriles y cítaras, en los combates que el Señor entablará con ella, blandiendo su brazo.
</p><p>
33 Porque la hoguera está preparada hace tiempo, está dispuesta también para el rey: se ha hecho una pira profunda y ancha, con fuego y leña en abundancia, y el soplo del Señor la encenderá como un torrente de azufre.
</p>
