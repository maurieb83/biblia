<h3>ISAIAS</h3>
<p>Capítulo 47
</p><p>
1 ¡Baja y siéntate en el polvo, virgen, hija de Babilonia! ¡Siéntate en el suelo, sin trono, hija de los caldeos! Porque ya no volverán a llamarte «Delicada» y «Refinada».
</p><p>
2 ¡Toma el mortero y muele la harina; quítate el velo, levántate el vestido, descúbrete el muslo, cruza los ríos!
</p><p>
3 ¡Que se descubra tu desnudez y que se vea tu ignominia! Yo me vengaré y nadie se me opondrá,
</p><p>
4 dice nuestro redentor: su nombre es Señor de los ejércitos, el Santo de Israel.
</p><p>
5 ¡Siéntate en silencio y entra en las tinieblas, hija de los caldeos! Porque ya no volverán a llamarte «Soberana de los reinos».
</p><p>
6 Yo estaba irritado contra mi pueblo profané mi herencia, y los entregué en tus manos. Tú no les tuviste compasión: hasta al anciano lo abrumaste con el peso de tu yugo.
</p><p>
7 Tú decías: «Seré siempre soberana, a lo largo de los siglos». Pero no te preocupabas por esto, no tenías presente el futuro.
</p><p>
8 Y ahora, escucha esto, voluptuosa, tú, que reinas confiada y dices en tu corazón: «¡Yo, y nadie más que yo! ¡Nunca me quedaré viuda ni me veré privada de hijos!».
</p><p>
9 Estas dos cosas te sobrevendrán, de repente, en un solo día: la privación de tus hijos y la viudez vendrán sobre ti con todo su rigor, pese a tus muchos sortilegios y al cúmulo de tus encantamientos.
</p><p>
10 Tú te fiabas de tu maldad, pensando: «Nadie me ve». Tu sabiduría y tu ciencia te hicieron perder la cabeza, mientras decías en tu corazón: «¡Yo, y nadie más que yo!».
</p><p>
11 Pero te va a suceder una desgracia, que no sabrás conjurar; va a caer sobre ti un desastre que no podrás aplacar, te va a sobrevenir de improviso una catástrofe que no imaginabas.
</p><p>
12 Persiste en tus encantamientos y en tus muchos sortilegios, por los que has bregado desde tu juventud: ¡tal vez puedan servirte de algo, tal vez logres infundir terror!
</p><p>
13 ¡Te has cansado de recibir consejos! ¡Que se presenten y te salven los que investigan el cielo, los que observan las estrellas, los que pronostican cada luna nueva lo que te va a suceder!
</p><p>
14 Pero ellos serán como paja: el fuego los quemará; no podrán librarse a sí mismos del poder de las llamas; no serán brasas para dar calor ni fuego para sentarse ante él.
</p><p>
15 Eso son para ti tus adivinos, por los que has bregado desde tu juventud: ellos andan errantes, cada uno por su lado, no hay nadie que pueda salvarte.
</p>
