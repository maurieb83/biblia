1 Así habla el Señor: Observen el derecho y practiquen la justicia, porque muy pronto llegará mi salvación y ya está por revelarse mi justicia.
</p><p>
2 ¡Feliz el hombre que cumple estos preceptos y el mortal que se mantiene firme en ellos, observando el sábado sin profanarlo y preservando su mano de toda mala acción!
</p><p>
3 Que no diga el extranjero que se ha unido al Señor: «El Señor me excluirá de su Pueblo»; y que tampoco diga el eunuco: «Yo no soy más que un árbol seco».
</p><p>
4 Porque así habla el Señor: A los eunucos que observen mis sábados, que elijan lo que a mí me agrada y se mantengan firmes en mi alianza,
</p><p>
5 yo les daré en mi Casa y dentro de mis muros un monumento y un nombre más valioso que los hijos y las hijas: les daré un nombre perpetuo, que no se borrará.
</p><p>
6 Y a los hijos de una tierra extranjera que se han unido al Señor para servirlo, para amar el nombre del Señor y para ser sus servidores, a todos los que observen el sábado sin profanarlo y se mantengan firmes en mi alianza,
</p><p>
7 yo los conduciré hasta mi santa Montaña y los colmaré de alegría en mi Casa de oración; sus holocaustos y sus sacrificios serán aceptados sobre mi altar, porque mi Casa será llamada Casa de oración para todos los pueblos.
</p><p>
8 Oráculo del Señor, que reúne a los desterrados de Israel: Todavía reuniré a otros junto a él, además de los que ya se han reunido.
</p><p>
9 ¡Bestias del campo, fieras de la selva, vengan todas a devorar!
</p><p>
10 Sus guardianes son todos ciegos, ninguno de ellos sabe nada. Todos ellos son perros mudos, incapaces de ladrar. Desvarían acostados, les gusta dormitar.
</p><p>
11 Esos perros voraces nunca terminan de saciarse, ¡y ellos son los pastores! No saben discernir, cada uno toma por su camino, todos, hasta el último, detrás de su ganancia.
</p><p>
12 «¡Vengan! Voy en busca de vino; nos embriagaremos con bebida fuerte, y mañana será lo mismo que hoy, o más, muchísimo más».
