<h3>NUMEROS</h3>
<p>Capítulo 18
</p><p>
Los deberes de los sacerdotes y de los levitas
</p><p>
1 El Señor dijo a Aarón: Tú, tus hijos y tu casa paterna, cargarán con las faltas contra el Santuario; pero tú y tus hijos solamente cargarán con las faltas contra el ejercicio del sacerdocio.
</p><p>
2 También asociarás a tus hermanos de la tribu de Leví –tu tribu paterna– para que colaboren contigo y te sirvan como ministros, a ti y a tus hijos, en la Carpa del Testimonio.
</p><p>
3 Ellos desempeñarán tareas para ti y para toda la Carpa, pero no tendrán ningún contacto con los utensilios del Santuario o con el altar, no sea que mueran ellos y ustedes.
</p><p>
4 Deberán colaborar contigo y ejecutar las tareas de la Carpa del Encuentro, prestando toda clase de servicios. Ningún extraño se acercará a ustedes
</p><p>
5 mientras realizan las funciones del Santuario o del altar, para que la ira del Señor no se vuelva a desatar contra los israelitas.
</p><p>
6 Yo elijo a tus hermanos –los descendientes de Leví– entre todos los israelitas: ellos han sido puestos a disposición de ustedes, como dedicados al Señor, para prestar servicios en la Carpa del Encuentro.
</p><p>
7 Tú y tus hijos, en cambio, ejercerán las funciones sacerdotales en todo lo concerniente al altar y a lo que está detrás del velo. Yo hago del sacerdocio de ustedes un servicio de dedicación: el extraño que se acerque será castigado con la muerte.
</p><p>
Los derechos de los sacerdotes
</p><p>
8 El Señor dijo a Aarón: Yo te encomiendo el cuidado de mis ofrendas, es decir, de los dones sagrados de los israelitas. Te entrego todo eso, a ti y a tus hijos, como algo que les es debido, como un derecho irrevocable.
</p><p>
9 Esto es lo que te corresponde de los sacrificios más santos, de las ofrendas quemadas. Todas las ofrendas que me presentan como sacrificios santísimos, a saber, todas las oblaciones, los sacrificios por el pecado y los sacrificios de reparación, serán para ti y para tus hijos.
</p><p>
10 Tú participarás de los dones más santos. Sólo los varones podrán comerlos y deberás tratarlos como algo sagrado.
</p><p>
11 También será para ti lo que se toma de las ofrendas de los israelitas para ser ofrecido con el gesto de presentación. Yo te lo doy, a ti, a tus hijos y a tus hijas, como derecho irrevocable: podrán comerlo todos los miembros de tu casa que sean puros.
</p><p>
12 Yo te doy lo mejor del aceite, del vino y del trigo, o sea las partes escogidas que los israelitas presentan al Señor.
</p><p>
13 Las primicias de los productos de la tierra, que ellos ofrecen al Señor, serán para ti: podrán comerlas todos los miembros de tu casa que sean puros.
</p><p>
14 Todo lo que ha sido consagrado al exterminio total en Israel será para ti.
</p><p>
15 También lo serán los primogénitos, tanto de hombres como de animales, ofrecidos al Señor. Pero harás rescatar los primogénitos de los hombres y los primogénitos de los animales impuros.
</p><p>
16 Los harás rescatar dentro del mes de su nacimiento, tomando como precio por el rescate cinco siclos –en siclos de Santuario– que equivalen a veinte gueras.
</p><p>
17 Los primogénitos del ganado mayor y menor no podrán ser rescatados porque están consagrados. Por eso, derramarás su sangre contra el altar y harás arder su grasa como ofrenda que se quema con aroma agradable al Señor.
</p><p>
18 La carne, en cambio, será para ti, lo mismo que la ofrenda de presentación y la pata derecha.
</p><p>
19 Yo te doy todas las ofrendas que los israelitas ponen aparte para el Señor. Te las doy a ti, a tus hijos y a tus hijas, como derecho irrevocable. Esta será una alianza de sal –una alianza eterna– para ti y tu descendencia, delante del Señor.
</p><p>
Los derechos de los levitas
</p><p>
20 Y el Señor dijo a Aarón: Tú no recibirás una herencia en el territorio de los israelitas ni tendrás una parte entre ellos: yo soy tu parte y tu herencia.
</p><p>
21 Yo doy como herencia a los levitas todos los diezmos de Israel, a cambio de los servicios que prestan en la Carpa del Encuentro.
</p><p>
22 De ahora en adelante, los israelitas no se acercarán a la Carpa del Encuentro, porque cargarían con un pecado y morirían.
</p><p>
23 Sólo los levitas prestarán servicios en ella y cargarán con sus propias faltas. Este es un decreto válido para siempre, a lo largo de las generaciones. Pero no tendrán una herencia entre los israelitas,
</p><p>
24 porque yo les doy como herencia los diezmos que los israelitas pondrán aparte como una ofrenda para el Señor. Por eso dije, refiriéndome a ellos, que no tendrán una herencia entre los israelitas.
</p><p>
Los diezmos
</p><p>
25 El Señor dijo a Moisés:
</p><p>
26 Habla en estos términos a los levitas: Cuando ustedes reciban de los israelitas los diezmos que yo les asigné como herencia, reservarán la décima parte como una ofrenda para el Señor:
</p><p>
27 esto les será tenido en cuenta a título de contribución. Como se hace con el trigo de la era y el mosto del lagar,
</p><p>
28 también ustedes pondrán aparte para el Señor una ofrenda tomada de los diezmos que reciban de los israelitas, y se la entregarán al sacerdote Aarón, en calidad de ofrenda reservada al Señor.
</p><p>
29 De los dones que reciban, reservarán las ofrendas debidas al Señor: la mejor porción de cada cosa, o sea, la parte que debe ser consagrada.
</p><p>
30 Diles también: Una vez que hayan reservado la mejor parte –que les será tenida en cuenta como el trigo de la era y el mosto del lagar–
</p><p>
31 ustedes y sus familias podrán comerla en cualquier lugar, porque esa es su recompensa por los servicios que prestan en la Carpa del Encuentro.
</p><p>
32 Así, si ustedes reservan la mejor parte, no cargarán con un pecado, no profanarán los dones sagrados de los israelitas ni morirán.
</p>
