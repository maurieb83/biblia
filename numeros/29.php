<h3>NUMEROS</h3>
<p>Capítulo 29
</p><p>
Los sacrificios para la Fiesta de la Aclamación
</p><p>
1 El primer día del séptimo mes tendrán una asamblea litúrgica y no harán ninguna clase de trabajo. Este será para ustedes el día de la Aclamación.
</p><p>
2 En él ofrecerán, como holocausto de aroma agradable al Señor, un novillo, un carnero y siete corderos de un año y sin defecto.
</p><p>
3 También presentarán, como oblación por el novillo, tres décimas partes de una medida de harina de la mejor calidad, amasada con aceite; dos décimas partes por el carnero,
</p><p>
4 y una décima parte por cada uno de los siete corderos.
</p><p>
5 Igualmente se ofrecerá al Señor un chivo como sacrificio por el pecado, a fin de practicar el rito de expiación en favor de ustedes.
</p><p>
6 Todo esto, además del holocausto mensual y de su oblación, del holocausto perpetuo y de su oblación, y de las libaciones prescritas, como aroma agradable, como ofrenda que se quema para el Señor.
</p><p>
Los sacrificios para el Día de la Expiación
</p><p>
7 El décimo día de ese séptimo mes tendrán una asamblea litúrgica, ayunarán y no hará ninguna clase de trabajo.
</p><p>
8 Además, ofrecerán al Señor, como holocausto de aroma agradable, un novillo, un carnero y siete corderos de un año y sin defecto,
</p><p>
9 con la oblación correspondiente: tres décimas partes de una medida de harina de la mejor calidad, amasada con aceite, por el novillo; dos décimas partes por el carnero,
</p><p>
10 y una décima parte por cada uno de los siete corderos.
</p><p>
11 También se ofrecerá un chivo como sacrificio por el pecado. Todo esto, además de la víctima por el pecado ofrecida en la fiesta de la Expiación, del holocausto perpetuo, de su oblación y de sus correspondientes libaciones.
</p><p>
Los sacrificios para la Fiesta de las Chozas
</p><p>
12 El día quince del séptimo mes tendrán una asamblea litúrgica. No harán ninguna clase de trabajos y durante siete días seguidos celebrarán una fiesta de peregrinación en honor del Señor.
</p><p>
13 Ofrecerán como holocausto de aroma agradable al Señor trece novillos, dos carneros y catorce corderos de un año y sin defecto,
</p><p>
14 con su oblación de harina de la mejor calidad, amasada con aceite: tres décimas partes de una medida por cada uno de los trece novillos, dos décimas partes por cada uno de los dos carneros
</p><p>
15 y una décima parte por cada uno de los catorce corderos.
</p><p>
16 También ofrecerán un chivo como sacrificio por el pecado, además del holocausto perpetuo, de su oblación y su libación.
</p><p>
17 El segundo día ofrecerán doce novillos, dos carnero y catorce corderos de un año y sin defecto,
</p><p>
18 con las oblaciones y libaciones prescritas, según el número de novillos, de carneros y de corderos.
</p><p>
19 También ofrecerán un chivo como sacrificio por el pecado, además del holocausto perpetuo, de su oblación y sus libaciones.
</p><p>
20 El tercer día ofrecerán once novillos, dos carneros y catorce corderos de un año y sin defecto,
</p><p>
21 con las oblaciones y libaciones prescritas, según el número de novillos, de carneros y de corderos.
</p><p>
22 También ofrecerán un chivo como sacrificio por el pecado, además del holocausto perpetuo, de su oblación y su libación.
</p><p>
23 El cuarto día ofrecerán diez novillos, dos carneros y catorce corderos de un año y sin defecto,
</p><p>
24 con las oblaciones y libaciones prescritas, según el número de novillos, de carneros y de corderos.
</p><p>
25 También ofrecerán un chivo como sacrificio por el pecado, además del holocausto perpetuo, de su oblación y su libación.
</p><p>
26 El quinto día ofrecerán nueve novillos, dos carneros y catorce corderos de un año y sin defecto,
</p><p>
27 con las oblaciones y libaciones prescritas, según el número de novillos, de carneros y de corderos.
</p><p>
28 También ofrecerán un chivo como sacrificio por el pecado, además del holocausto perpetuo, de su oblación y su libación.
</p><p>
29 El sexto día ofrecerán ocho novillos, dos carneros y catorce corderos de un año y sin defecto,
</p><p>
30 con las oblaciones y libaciones prescritas, según el número de novillos, de carneros, y de corderos.
</p><p>
31 También ofrecerán un chivo como sacrificio por el pecado, además del holocausto perpetuo, de su oblación y su libación.
</p><p>
32 El séptimo día ofrecerán siete novillos, dos carneros y catorce corderos de un año y sin defecto,
</p><p>
33 con las oblaciones y libaciones prescriptas, según el número de novillos, de carneros y de corderos.
</p><p>
34 También ofrecerán un chivo como sacrificio por el pecado, además del holocausto perpetuo, de su oblación y su libación.
</p><p>
35 El octavo día tendrán una reunión solemne y no harán ninguna clase de trabajo.
</p><p>
36 Ofrecerán como holocausto, como ofrenda que se quema con aroma agradable al Señor, un novillo, un carnero y siete corderos de un año y sin defecto,
</p><p>
37 con la oblación y las libaciones prescritas, según el número de novillos, de carneros y de corderos.
</p><p>
38 También ofrecerán un chivo como sacrificio por el pecado, además del holocausto perpetuo, de su oblación y su libación.
</p><p>
39 Estos son los sacrificios que ustedes ofrecerán al Señor en sus fiestas, además de sus ofrendas votivas y voluntarias, de sus holocaustos, oblaciones y libaciones, y de sus sacrificios de comunión.
