</p><p>
10 y tu paladar como un vino delicioso, que corre suavemente hacia el amado, fluyendo entre los labios y los dientes!
</p><br><p>
 

<i>[La Amada]</i>
</p><p>
11 Yo soy para mi amado, y él se siente atraído hacia mí.
</p><p>
