</p><p>
6 De él salieron los siete Angeles que tenían las siete plagas, y estaban vestidos de lino puro y resplandeciente, y ceñidos con cinturones de oro.
</p><p>
7 Entonces, uno de los cuatro Seres Vivientes entregó a los siete Angeles siete copas colmadas de la ira del Dios que vive por los siglos de los siglos.
</p><p>
8 Y el Templo se llenó del humo que procede de la gloria de Dios y de su poder, de manera que nadie pudo entrar al Templo hasta que cesaron las siete plagas de los siete Angeles.
