<h3>NEHEMIAS</h3>
<p>Capítulo 4
</p><p>
1 Cuando Sambalat, Tobías, los árabes, los amonitas y los asdoditas se enteraron de que progresaba la reparación de las murallas de Jerusalén –porque comenzaban a cerrarse las brechas– se enfurecieron,
</p><p>
2 y se coaligaron para atacar a Jerusalén y provocar disturbios.
</p><p>
3 Entonces invocamos a nuestro Dios y montamos guardia de día y de noche para protegernos de ellos.
</p><p>
4 El pueblo de Judá decía: «Flaquea la mano de obra y hay demasiados escombros; así nosotros no podremos reconstruir la muralla»
</p><p>
5 Nuestros adversarios decían: «No sabrán ni verán nada, hasta que irrumpamos en medio de ellos. Entonces los mataremos y pondremos fin a la obra».
</p><p>
6 Y cuando llegaban los judíos que vivían cerca de ellos, nos repetían insistentemente: «Van a atacarlos desde todos los lugares donde habitan».
</p><p>
7 Yo aposté entonces a mi gente en las partes bajas, por detrás de las murallas, en los puntos desguarnecidos, disponiendo al pueblo por familias, con sus espadas, sus lanzas y sus arcos.
</p><p>
8 Y al ver que tenían miedo, me levanté y dije a los notables, a los magistrados y al resto del pueblo: «¡No les tengan miedo! Acuérdense del Señor grande y temible, y combatan por sus hermanos, sus hijos, sus hijas, sus mujeres y sus casas».
</p><p>
9 Cuando nuestros enemigos advirtieron que estábamos alerta y que Dios había desbaratado sus planes, volvimos todos a las murallas, cada uno a su trabajo.
</p><p>
10 Pero, a partir de ese día, sólo la mitad de mi gente hacía el trabajo, mientras la otra mitad tenía en la mano las lanzas, los escudos, los arcos y las corazas, y los jefes estaban detrás de toda la casa de Judá.
</p><p>
11 Los que reconstruían las murallas y los que transportaban las cargas iban armados: con una mano hacían el trabajo y con la otra empuñaban el arma;
</p><p>
12 y los que construían tenían cada uno la espada ceñida a la cintura mientras trabajaban. Además, había junto a mí un hombre encargado de hacer sonar el cuerpo.
</p><p>
13 Yo dije a los notables, a los magistrados y al resto del pueblo: «La obra es considerable y extensa, y nosotros estamos esparcidos sobre la muralla, lejos unos de otros.
</p><p>
14 Allí donde oigan el sonido del cuerno, corran a reunirse con nosotros: nuestro Dios combatirá a favor nuestro».
</p><p>
15 Así hacíamos el trabajo –mientras una mitad empuñaba las lanzas– desde que despuntaba el alba hasta que aparecían las estrellas.
</p><p>
16 En aquella oportunidad, dije también al pueblo: «Que cada uno, con su servidor, pase la noche en Jerusalén; de noche, para montar guardia, y de día, para trabajar».
</p><p>
17 Pero ni yo, ni mis hermanos, ni mi gente, ni los guardias que me seguían, nos quitábamos la ropa, y cada uno llevaba el arma en su mano derecha.
</p>
