<h3>EZEQUIEL</h3>
<p>Capítulo 35
</p><p>
1 La palabra del Señor me llegó en estos términos:
</p><p>
2 Hijo de hombre, vuelve tu rostro hacia la montaña de Seir y profetiza contra ella.
</p><p>
3 Tú le dirás: Así habla el Señor: ¡Aquí estoy contra ti, montaña de Seír! Yo extenderé mi mano contra ti y te convertiré en un desierto desolado.
</p><p>
4 Dejaré tus ciudades en ruinas y tú misma serás una desolación. Así sabrás que yo soy el Señor.
</p><p>
5 Porque has mantenido una antigua enemistad y has entregado a los israelitas al filo de la espada, en el día de su desastre, en el día de la expiación final;
</p><p>
6 por eso juro por mi vida –oráculo del Señor–: Te convertiré en sangre y la sangre te perseguirá. ¡Te has hecho reo de sangre y la sangre te perseguirá!
</p><p>
7 Yo haré de la montaña de Seír un desierto desolado, y extirparé de ella al que va y al que viene.
</p><p>
8 Llenaré sus montes de víctimas: las víctimas de las espada caerán en tus colinas, en tus valles y en todos los cauces de tus ríos.
</p><p>
9 Te convertiré en una desolación eterna y tus ciudades no volverán a ser habitadas. Así ustedes sabrán que yo soy el Señor.
</p><p>
10 Porque tú dices: «Las dos naciones y los dos territorios me pertenecen: los voy a tomar en posesión», siendo así que el Señor estaba allí,
</p><p>
11 por eso, juro por mi vida –oráculo del Señor– que voy a obrar con el mismo furor y con el mismo celo con que obraste tú, en tu odio contra ellos, y me daré a conocer a ti cuando te juzgue.
</p><p>
12 Entonces sabrás que yo, el Señor, he oído todas las blasfemias que pronunciaste contra las montañas de Israel, cuando dijiste: «¡Están devastadas; nos han sido entregadas como presa!».
</p><p>
13 Ustedes me han desafiado con su lengua y han multiplicado sus palabras contra mí, Pero yo he oído todo.
</p><p>
14 Así habla el Señor: Yo te convertiré en una desolación, para alegría de toda la tierra.
</p><p>
15 Así como tú te alegraste cuando quedó desolada la herencia de la casa de Israel, yo haré lo mismo contigo: ¡quedarás desolada, montaña de Seír, igual que todo Edom! Así se sabrá que yo soy el Señor.
</p>
