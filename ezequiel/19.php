<h3>EZEQUIEL</h3>
<p>Capítulo 19
</p><p>
1 Entona una lamentación sobre los príncipes de Israel.
</p><p>
2 Tú dirás: ¡Tu madre sí que era una leona en medio de los leones! Recostada entre los cachorros, amamantaba sus crías.
</p><p>
3 A uno de sus cachorros lo enalteció y él se convirtió en un león: aprendió a desgarrar su presa, devoró a los hombres.
</p><p>
4 Pero las naciones se concertaron contra él y quedó atrapado en su fosa: así lo llevaron con garfios a la tierra de Egipto.
</p><p>
5 Al ver que nada podía esperar, que su esperanza estaba perdida, tomó a otro de sus cachorros e hizo de él un león.
</p><p>
6 El se paseaba entre los leones, convertido en un león: aprendió a desgarrar su presa, devoró a los hombres.
</p><p>
7 Hizo estragos en sus palacios, devastó sus ciudades; la tierra y sus habitantes se espantaron por el fragor de sus rugidos.
</p><p>
8 Las naciones marcharon contra él, desde las regiones circundantes: tendieron sus redes contra él, y quedó atrapado en su fosa.
</p><p>
9 Lo encerraron con garfios en una jaula, lo llevaron al rey de Babilonia y lo pusieron en una fortaleza, para que no volviera a oírse su voz por las montañas de Israel.
</p><p>
10 Tu madre se parecía a una vid, plantada al borde de las aguas: desbordada de frutos y de hojas, porque el agua era abundante.
</p><p>
11 Le salieron unas ramas vigorosas, que fueron cetros de soberanos. Su talla se elevó por encima del follaje, era bien visible por su altura, por la abundancia de sus ramas.
</p><p>
12 Pero fue arrancada con furor y arrojada por el suelo. El viento del este secó sus frutos, que fueron cortados y se secaron; y el fuego devoró su rama vigorosa.
</p><p>
13 Ahora está plantada en el desierto, en una tierra reseca y sedienta.
</p><p>
14 De su rama ha salido un fuego que devoró sus ramas y sus frutos. Ya no hay en ella ninguna rama vigorosa, ningún cetro de soberanos. Esta es una lamentación, y se la canta como tal.
</p>
