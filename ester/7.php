<h3>ESTER</h3>
<p>Capítulo 7
</p><p>
1 El rey y Amán fueron al banquete con la reina Ester.
</p><p>
2 Aquel segundo día, en el momento de brindar, el rey volvió a decir a Ester: «¿Qué es lo que pides, reina Ester? Lo que sea, te será concedido. ¿Qué es lo que quieres? Aunque sea la mitad del reino, lo tendrás».
</p><p>
3 La reina Ester tomó la palabra y dijo: «Rey, si estás dispuesto a hacerme un favor y si esto es lo que te agrada, mi petición es que me concedas la vida y mi deseo es que perdones la vida a mi pueblo.
</p><p>
4 Porque yo y mi pueblo hemos sido entregados al exterminio, a la muerte y a la destrucción. Si hubiéramos sido vendidos como esclavos y esclavas, me habría callado, ya que esa desgracia no habría perjudicado al rey».
</p><p>
5 El rey Asuero tomó la palabra y dijo a la reina Ester: «¿Quién es y dónde está el que ha concebido semejante cosa?».
</p><p>
6 Ester respondió: «¡El enemigo y adversario es ese miserable de Amán!». Amán quedó aterrorizado ante el rey y la reina.
</p><p>
7 Y el rey, en un arrebato de ira, se retiró del banquete y fue hacia el jardín del palacio. Mientras tanto, Amán se quedó para implorar por su vida a la reina Ester, porque veía que el rey ya había decidido su ruina.
</p><p>
8 Cuando el rey volvió del jardín del palacio a la sala del banquete, Amán estaba tendido sobre el diván donde se recostaba Ester. Entonces el rey exclamó: «¡Y todavía se atreve a violar a la reina estando yo en la casa!». Apenas el rey pronunció esta palabra, se abalanzaron sobre Amán y le taparon el rostro,
</p><p>
9 y Jarboná, uno de los eunucos, dijo en presencia del rey: «Precisamente en casa de Amán se ha instalado un patíbulo de veinticinco metros; él mismo lo hizo levantar para Mardoqueo, aquel que salvó al rey con su denuncia». El rey ordenó: «¡Cuélguenlo allí!».
</p><p>
10 Así colgaron a Amán del patíbulo que él había preparado para Mardoqueo, y se calmó la furia del rey.
</p>
