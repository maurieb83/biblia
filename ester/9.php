<h3>ESTER</h3>
<p>Capítulo 9
</p><p>
1 El trece del duodécimo mes –que es el mes de Adar– día en que se debía ejecutar la orden del rey y su decreto, cuando los enemigos de los judíos esperaban dominarlos, se produjo un cambio de situación: de hecho, fueron los judíos los que se impusieron sobre sus enemigos.
</p><p>
2 Ellos se reunieron en sus respectivas ciudades, en todas las provincias del rey Asuero, para atacar a los que buscaban su ruina. Nadie les opuso resistencia, porque el temor a los judíos se había apoderado de todos los pueblos.
</p><p>
3 Todos los jefes de las provincias, los prefectos, los gobernadores y los funcionarios del rey les prestaron su apoyo, porque el temor a Mardoqueo se había apoderado de ellos,
</p><p>
4 ya que este ocupaba un alto cargo en el palacio real y su fama se extendía por todas las provincias: Mardoqueo, en efecto, se engrandecía cada vez más.
</p><p>
5 Los judíos pasaron al filo de la espada a todos sus enemigos: fue una verdadera masacre, un exterminio. Trataron como quisieron a los que los odiaban,
</p><p>
6 y en la ciudadela de Susa mataron y exterminaron a quinientos hombres,
</p><p>
7 dando muerte, especialmente, a Parsandata, Dalfón, Aspata,
</p><p>
8 Porata, Adalías, Aridata,
</p><p>
9 Parmasta, Arisai, Aridai y Vaisata,
</p><p>
10 los diez hijos de Amán, hijo de Hamdatá, el opresor de los judíos. Los mataron, pero no recogieron ningún botín.
</p><p>
11 Ese mismo día, llegó a conocimiento de rey el número de los que habían sido matados en la Ciudadela de Susa.
</p><p>
12 El rey dijo entonces a la reina Ester: «En la ciudadela de Susa, los judíos mataron y exterminaron a quinientos hombres y a los diez hijos de Amán. ¡Qué no habrán hecho en el resto de las provincias reales! Pero ¿qué es lo que pides? Lo que sea, te será concedido. ¿Qué otra cosa deseas? Se hará lo que tú digas».
</p><p>
13 Ester respondió: «Si al rey le parece bien, que a los judíos de Susa les sea permitido actual también mañana conforme al decreto válido para hoy, y que los hijos de Amán sean colgados del patíbulo».
</p><p>
14 El rey ordenó que así se hiciera: en Susa se promulgó un decreto y los diez hijos de Amán fueron colgados.
</p><p>
15 Así, los judíos de Susa se reunieron una vez más el día catorce del mes de Adar y mataron allí a trescientos hombres, pero no recogieron ningún botín.
</p><p>
16 En cuanto a los demás judíos de las provincias reales, también se reunieron para defender sus vidas y así se libraron de los ataques de sus adversarios: ellos mataron a setenta y cinco mil entre sus enemigos, pero no recogieron ningún botín.
</p><p>
17 Esto sucedió el día trece del mes de Adar; el catorce descansaron y celebraron ese día con banquetes y alegría.
</p><p>
18 Pero los judíos de Susa, que se habían concentrado el trece y el catorce, descansaron el día quince y lo festejaron con banquetes y alegría.
</p><p>
19 Por eso los judíos de las zonas rurales celebran el día catorce del mes de Adar como un día de alegría, de banquetes y fiesta, y en él se envían regalos unos a otros.
</p><p>
20 Mardoqueo consignó estas cosas por escrito y envió cartas a todos los judíos de todas las provincias del rey Asuero, tanto a los que estaban cerca como a los más alejados.
</p><p>
21 En ellas disponía que celebraran cada año el catorce y el quince del mes de Adar,
</p><p>
22 por ser esos los días en que los judíos habían quedando libres de sus enemigos, y por ser ese el mes en que la aflicción se les había cambiado en alegría y el duelo en un día de fiesta. Por eso debían festejarlos como días de banquetes y alegría, enviarse regalos unos a otros y ofrecer donativos a los pobres.
</p><p>
23 Los judíos hicieron una tradición de lo que habían comenzado a festejar y de lo que les había ordenado Mardoqueo.
</p><p>
24 Porque Amán, hijo de Hamdatá, el agaguita, el opresor de todos los judíos, había proyectado eliminar a los judíos y había echado el «Pur» –es decir, la suerte– con el fin de confundirlos y eliminarlos.
</p><p>
25 Pero, cuando se presentó ante el rey, este ordenó por escrito que recayera sobre la cabeza de Amán el plan perverso que había concebido contra los judíos, y lo colgaron del patíbulo, a él y a sus hijos.
</p><p>
26 Por eso a aquellos días se los llamó «Purím» de la palabra «Pur». De acuerdo con el contenido de aquella carta, y por lo que habían visto y les había sucedido,
</p><p>
27 los judíos instituyeron para ellos mismos, para sus descendientes y para todos sus adeptos, la tradición irrevocable de celebrar anualmente esos dos días, según lo dispuesto en aquel escrito y en la fecha fijada.
</p><p>
28 Estos días son conmemorados y celebrados de generación en generación, en cada familia, en cada provincia y en cada ciudad. Y estos días de «Purím» nunca dejarán de ser festejados entre los judíos, ni se borrará su recuerdo entre sus descendientes.
</p><p>
29 La reina Ester, hija de Abijáil, y Mardoqueo, el judío, escribieron urgiendo el cumplimiento de esta segunda carta relativa a los «Purím».
</p><p>
30 Se mandaron cartas a todos los judíos de las ciento veintisiete provincias del rey Asuero, con saludos y expresiones de lealtad,
</p><p>
31 para instituir estos días de los «Purím», en las fechas fijadas, como lo habían ordenado Mardoqueo, el judío, y la reina Ester, y como lo habían establecido para sí mismos y para sus descendientes, con algunas cláusulas sobre ayunos y lamentaciones.
</p><p>
32 Así, la orden de Ester confirmó la institución de los «Purím», y esto quedó consignado por escrito.
</p>
