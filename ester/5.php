<h3>ESTER</h3>
<p>Capítulo 5
</p><p>
1 Al tercer día, Ester se puso sus vestiduras reales y llegó hasta el patio interior del palacio, frente a la sala del rey. El rey estaba sentado en su trono, en la sala real, frente a la puerta de entrada.
</p><p>
2 Al ver a la reina Ester, que estaba de pie en el patio, el rey la miró con benevolencia y extendió hacia ella el cetro de oro que tenía en la mano. Entonces Ester se acercó y tocó la punta del cetro.
</p><p>
3 El rey dijo a Ester: «¿Qué tienes, reina Ester? ¿Qué es lo que quieres? ¡Incluso la mitad de mi reino te será concedida!».
</p><p>
4 Ester respondió: «Si al rey le parece bien, venga hoy con Amán al banquete que he preparado en su honor».
</p><p>
5 Entonces el rey ordenó: «¡Avisen inmediatamente a Amán que acepte la invitación de Ester!». El rey fue con Amán al banquete preparado por Ester
</p><p>
6 y, en el momento de brindar, le dijo: «¿Qué es lo que pides, Ester? Lo que sea, te será concedido. ¿Qué es lo que quieres? Aunque sea la mitad de mi reino, lo tendrás».
</p><p>
7 Ester respondió: «¿Sabes cuál es mi petición y mi deseo?
</p><p>
8 Si el rey me ha concedido su favor, y si a él le parece bien acceder a mi pedido y complacer mi deseo, que venga con Amán al banquete que les voy a preparar, y mañana responderé a la pregunta del rey».
</p><p>
9 Amán había salido aquel día contento y de buen humor. Pero al ver en la puerta real a Mardoqueo, que no se levantaba ni temblaba ante él, se llenó de furor.
</p><p>
10 Sin embargo, se contuvo y se fue a su casa. Luego mandó buscar a sus amigos y a Zeres, su mujer,
</p><p>
11 y les estuvo hablando del esplendor de sus riquezas, de sus muchos hijos y de lo que el rey había hecho para engrandecerlo, elevándolo por encima de los demás ministros y servidores de la corte.
</p><p>
12 Luego Amán añadió: «Además, la reina Ester preparó un banquete y me invitó a mí solo junto con el rey. Y también mañana seré su invitado en compañía del rey.
</p><p>
13 Pero todo esto no significa nada para mí, mientras vea a ese judío Mardoqueo sentado en la puerta real».
</p><p>
14 Entonces Zeres, su mujer, y todos sus amigos le dijeron: «Que hagan levantar un patíbulo de veinticinco metros, y mañana por la mañana le dirás al rey que cuelguen allí a Mardoqueo. Así irás contento al banquete con el rey». La propuesta agradó a Amán e hizo levantar el patíbulo.
</p>
