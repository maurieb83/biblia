<h3>SEGUNDO LIBRO DE LAS CRONICAS</h3>
<p>Capítulo 21
</p><p>
1 Josafat se fue a descansar con sus padres, y lo sepultaron con sus antepasados en la Ciudad de David. Su hijo Joram lo sucedió en el trono.
</p><p>
2 Joram tenía seis hermanos, hijos de Josafat, que eran: Azarías, Iejiel, Zacarías, Azariahu, Micael y Sefatías. Todos eran hijos de Josafat, rey de Israel.
</p><p>
3 Su padre les había hecho muchos regalos de oro, plata y objetos preciosos, además de algunas ciudades fortificadas de Judá. Pero él había entregado el reino a Joram porque era el hijo mayor;
</p><p>
4 y cuando Joram tomó posesión del reino de su padre y se afianzó en el poder, degolló a todos sus hermanos y a algunos de los jefes de Israel.
</p><p>
5 Joram tenía treinta y dos años cuando comenzó a reinar, y reinó ocho años en Jerusalén.
</p><p>
6 Siguió el camino de los reyes de Israel, conforme a lo que había hecho la casa de Ajab, porque se había casado con una hija de Ajab; e hizo lo que es malo a los ojos del Señor.
</p><p>
7 Pero el Señor no quiso destruir a la casa de David, en razón de la alianza que había concluido con él, y de la promesa que le había hecho de darles, a él y a sus hijos, una lámpara para siempre.
</p><p>
8 Durante el reinado de Joram, Edom se rebeló contra Judá, y se instituyó un rey.
</p><p>
9 Entonces Joram cruzó la frontera con sus jefes y todos sus carros de guerra. Durante la noche atacó a los edomitas que lo tenían cercado, a él y a los jefes de los carros de guerra.
</p><p>
10 Sin embargo, Edom se libró de la dominación de Judá hasta el día de hoy. También Libná se rebeló contra él en esa misma época, porque él había abandonado al Señor, el Dios de sus padres.
</p><p>
11 Además, Joram construyó los lugares altos en las montañas de Judá, incitó a la prostitución a los habitantes de Jerusalén e hizo extraviar a Judá.
</p><p>
12 Entonces le llegó un escrito del profeta Elías que decía: «Así habla el Señor, el Dios de tu padre David: Tú no has seguido los caminos de tu padre Josafat, ni los de Asá, rey de Judá,
</p><p>
13 sino que imitaste a los reyes de Israel e incitaste a Judá y a los habitantes de Jerusalén a que se prostituyeran como la familia de Ajab. También has asesinado a tus hermanos, la familia de tu padre, que eran mejores que tú.
</p><p>
14 Por eso el Señor infligirá un terrible castigo a tu pueblo, a tus hijos, a tus mujeres y a todos tus bienes.
</p><p>
15 Tú mismo padecerás muchas dolencias, y una enfermedad maligna te irá carcomiendo las entrañas día tras día».
</p><p>
16 El Señor excitó contra Joram la hostilidad de los filisteos y de los árabes vecinos de los cusitas,
</p><p>
17 que atacaron a Judá, la invadieron y se apoderaron de todos los tesoros que había en el palacio real, y también de sus hijos y de sus mujeres; solamente le quedó Ocozías, que era el menor de sus hijos.
</p><p>
18 Por último, el Señor lo hirió con una enfermedad incurable del vientre.
</p><p>
19 Y después de un tiempo, al cabo de dos años, la enfermedad le carcomió las entrañas, y murió en medio de terribles dolores. Su pueblo no quemó perfumes por él, como había hecho por sus padres.
</p><p>
20 Tenía treinta y dos años cuando comenzó a reinar, y reinó ocho años en Jerusalén. Se fue sin que lo lloraran, y lo sepultaron en la Ciudad de David, pero no en los sepulcros de los reyes.
</p>
