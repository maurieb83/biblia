<h3>SEGUNDO LIBRO DE LAS CRONICAS</h3>
<p>Capítulo 8
</p><p>
1 Al cabo de los veinte años que tardó Salomón en construir la Casa del Señor y su propia casa,
</p><p>
2 él reconstruyó las ciudades que le había dado Jiram y estableció en ellas a los israelitas.
</p><p>
3 Luego se dirigió a Jamat de Sobá y se apoderó de ella.
</p><p>
4 Reconstruyó Tadmor en el desierto, como asimismo todos los centros de aprovisionamiento que había edificado en Jamat.
</p><p>
5 Reconstruyó Bet Jorón de Arriba y Bet Jorón de Abajo –plazas fuertes con muros, puertas y cerrojos –
</p><p>
6 y también Baalat, todos los centros de aprovisionamiento que tenía Salomón, todas las ciudades para los carros de guerra y la caballería, y todas las demás construcciones que Salomón quiso levantar en Jerusalén, en el Líbano y en todo el país sometido a su dominio.
</p><p>
7 A los sobrevivientes de los hititas, los amorreos, los perizitas, los jivitas y los jebuseos, que no pertenecían a Israel
</p><p>
8 –es decir, a sus descendientes, que habían quedado después de ellos en el país, porque los israelitas no los habían exterminado– Salomón les impuso trabajos serviles hasta el día de hoy.
</p><p>
9 Pero no sometió a esclavitud a ningún israelita, sino que a ellos los empleó como hombres de guerra, jefes de sus escuderos y comandantes de sus carros de guerra y su caballería.
</p><p>
10 Los supervisores de los capataces puestos por Salomón eran doscientos cincuenta hombres, que dirigían al personal.
</p><p>
11 Salomón hizo subir a la hija del Faraón desde la Ciudad de David a la casa que había edificado para ella, porque él decía: «Mi mujer no puede habitar en la casa de David, el rey de Israel, ya que los lugares donde ha entrado el Arca del Señor son sagrados».
</p><p>
12 Entonces Salomón comenzó a ofrecer holocaustos al Señor sobre el altar que había erigido ante el vestíbulo del Templo.
</p><p>
13 Según el rito de cada día, ofrecía lo que había mandado Moisés para los sábados, los novilunios y las tres solemnidades anuales, a saber; la fiesta de los Acimos, la fiesta de las Semanas y la fiesta de las Chozas.
</p><p>
14 Además, conforme a las disposiciones de su padre David, asignó a las clases sacerdotales sus respectivas funciones; estableció en sus cargos a los levitas, para cantar alabanzas y oficiar en presencia de los sacerdotes, según el rito de cada día; y a los porteros, según sus clases, los encargó de cada una de las puertas, porque así lo había mandado David, el hombre de Dios.
</p><p>
15 Ellos no se apartaron en nada de lo mandado por el rey en lo referente a los sacerdotes y a los levitas, ni tampoco en lo concerniente a los tesoros.
</p><p>
16 Así se llevó a cabo toda la obra de Salomón, desde el día en que se pusieron los cimientos de la Casa del Señor hasta su terminación. La Casa del Señor quedó totalmente concluida.
</p><p>
17 Entonces Salomón se dirigió a Esión Guéber y a Elat, a orillas del mar, en el país de Edom.
</p><p>
18 Jiram, por medio de sus servidores, le envió una flota y algunos marineros, buenos conocedores del mar. Ellos fueron a Ofir con los servidores de Salomón, y trajeron de allí cuatrocientos talentos de oro, que entregaron al rey Salomón.
</p>
