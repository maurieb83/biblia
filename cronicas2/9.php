<h3>SEGUNDO LIBRO DE LAS CRONICAS</h3>
<p>Capítulo 9
</p><p>
1 La reina de Sabá oyó hablar de la fama de Salomón, y fue a ponerlo a prueba, proponiéndole unos enigmas. Llegó a Jerusalén con un séquito imponente, con camellos cargados de perfumes, de gran cantidad de oro y de piedras preciosas. Cuando se presentó ante Salomón, le expuso todo lo que tenía pensado decirle.
</p><p>
2 Salomón respondió a todas sus preguntas: no hubo para él ninguna cuestión tan oscura que no se la pudiera explicar.
</p><p>
3 Cuando la reina vio toda la sabiduría de Salomón, las casa que había construido,
</p><p>
4 los manjares de su mesa, los aposentos de sus servidores, el porte y la librea de sus camareros y de sus coperos, y los holocaustos que ofrecía en la Casa del Señor, se quedó sin aliento
</p><p>
5 y dijo al rey: «¡Realmente era verdad lo que había oído decir en mi país acerca de ti y de tu sabiduría!
</p><p>
6 Yo no lo quería creer, sin venir antes a verlo con mis propios ojos. Pero ahora compruebo que no me habían contado ni siquiera la mitad: el cúmulo de tu sabiduría supera la fama que llegó a mis oídos.
</p><p>
7 ¡Felices tus mujeres, y felices también estos servidores tuyos, que están constantemente delante de ti, escuchando tu sabiduría!
</p><p>
8 ¡Y bendito sea el Señor, tu Dios, que te ha mostrado su favor, poniéndote sobre su trono como rey, al servicio del Señor, tu Dios! ¡Sí, por su amor a Israel, y a fin de hacerlo subsistir para siempre, tu Dios te ha puesto como rey al frente de ellos, para que ejercieras el derecho y la justicia!».
</p><p>
9 La reina regaló al rey ciento veinte talentos de oro, una enorme cantidad de perfumes y piedras preciosas; nunca más hubo tantos perfumes como los que la reina de Sabá dio al rey Salomón.
</p><p>
10 Los servidores de Jiram y de Salomón, que habían traído el oro de Ofir, trajeron también madera de sándalo y piedras preciosas.
</p><p>
11 Con la madera de sándalo, el rey hizo unos tablados para la Casa del Señor y para la casa del rey, y también cítaras y arpas para los músicos. Nunca se había visto nada semejante en el país de Judá.
</p><p>
12 Por su parte, el rey Salomón dio a la reina de Sabá todo lo que a ella se le ocurrió pedir, mucho más de lo que ella misma había traído al rey. Después, la reina emprendió el camino de regreso a su país, acompañada de su séquito.
</p><p>
13 El peso del oro que recibía Salomón en un solo año ascendía a seiscientos sesenta y seis talentos.
</p><p>
14 sin contar lo que aportaba el tráfico de los viajantes y mercaderes. Todos los reyes de Arabia y los gobernadores del país llevaban oro y plata a Salomón.
</p><p>
15 El rey Salomón hizo doscientos grandes escudos de oro trabajado a martillo, empleando para cada uno seiscientos siclos de oro,
</p><p>
16 y trescientos escudos más pequeños, también de oro trabajado a martillo, empleando para cada uno trescientos siclos de oro. Luego el rey los ubicó en la sala llamada Bosque del Líbano.
</p><p>
17 El rey hizo, además, un gran trono de marfil, al que recubrió de oro puro.
</p><p>
18 El trono tenía seis gradas, una plataforma de oro, unos sostenes, y brazos a ambos lados del asiento; junto a los brazos había dos leones de pie,
</p><p>
19 y otros doce leones de pie sobre las seis gradas, a uno y otro lado. En ningún reino se había hecho nada igual.
</p><p>
20 Toda la vajilla del rey Salomón era de oro, y todo el mobiliario de la sala llamada Bosque del Líbano, de oro fino; no se usaba la plata, a la que en tiempos de Salomón no se la tenía en cuenta para nada.
</p><p>
21 Porque el rey disponía de una flota que iba a Tarsis con los servidores de Jiram, y una vez cada tres años las naves de alta mar llegaban cargadas de oro, plata, marfil, monos y pavos reales.
</p><p>
22 El rey Salomón superó a todos los reyes de la tierra en riqueza y sabiduría.
</p><p>
23 Todos los reyes de la tierra trataban de ver a Salomón para oír la sabiduría que Dios había puesto en su corazón.
</p><p>
24 Y cada uno aportaba sus presentes: objetos de plata y oro, trajes, armas, perfumes, caballos y mulas. Así, año tras año.
</p><p>
25 Salomón llegó a tener cuatro mil caballerizas para los caballos y los carros de guerra, y doce mil caballos, que acantonó en las ciudades de guarnición y en Jerusalén, junto a él.
</p><p>
26 El dominaba sobre todos los reyes, desde el Río hasta el país de los filisteos y hasta la frontera de Egipto.
</p><p>
27 El rey hizo que la plata fuera en Jerusalén tan común como las piedras, y que la madera de cedro fuera tan abundante como los sicomoros de la Sefelá.
</p><p>
28 Los caballos para Salomón se importaban de Musrí y de todos los países.
</p><p>
29 El resto de los hechos de Salomón, desde el comienzo hasta el fin, está escrito en las Crónicas del profeta Natán, en la Profecía de Ajías, el silonita, y en las Visiones del vidente Idó acerca de Jeroboam, hijo de Nebat.
</p><p>
30 Salomón reinó sobre todo Israel, en Jerusalén, durante cuarenta años.
</p><p>
31 Luego se fue a descansar con sus padres, y lo sepultaron en la Ciudad de David, su padre. Su hijo Roboam reinó en lugar de él.
</p>
