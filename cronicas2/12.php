<h3>SEGUNDO LIBRO DE LAS CRONICAS</h3>
<p>Capítulo 12
</p><p>
1 Pero cuando Roboam consolidó su reino y se hizo fuerte, abandonó la Ley del Señor, y con él todo Israel.
</p><p>
2 Y por haber sido infieles al Señor, en el año quinto del rey Roboam, subió contra Jerusalén Sisac, rey de Egipto,
</p><p>
3 con tres mil doscientos carros de guerra, sesenta mil jinetes y una multitud innumerable que venía con él de Egipto, en la que había libios, suquíes y cusitas.
</p><p>
4 Así se apoderó de las ciudades fortificadas de Judá y llegó a Jerusalén.
</p><p>
5 Entonces el profeta Semaías se presentó a Roboam y a los jefes de Judá, que se habían concentrado en Jerusalén ante el avance de Sisac, y les dijo: «Así habla el Señor: Ustedes me han abandonado en manos de Sisac».
</p><p>
6 Los jefes de Israel y el rey se humillaron y dijeron: «¡El Señor es justo!».
</p><p>
7 Cuando el Señor vio que se habían humillado, la palabra del Señor llegó a Semaías, en estos términos: «Ellos se han humillado: no los destruiré, sino que los libraré dentro de poco, y mi furor no se derramará sobre Jerusalén por la mano de Sisac.
</p><p>
8 Pero le estarán sometidos, y así sabrán lo que es servirme a mí y lo que es servir a los reyes de la tierra».
</p><p>
9 Sisac, rey de Egipto, subió contra Jerusalén y se apoderó de los tesoros de la Casa del Señor y de la casa del rey. Se apoderó de todo, incluso de los escudos de oro que había hecho Salomón.
</p><p>
10 En lugar de ellos, el rey Roboam hizo unos escudos de bronce, y se los confió a los jefes de los guardias que custodiaban el acceso a la casa del rey.
</p><p>
11 Cada vez que el rey iba a la Casa del Señor, los guardias los llevaban, y luego los volvían a dejar en la sala de guardia.
</p><p>
12 Por haberse humillado, la ira del Señor se apartó de él y no lo destruyó por completo, ya que aún había algo bueno en Judá.
</p><p>
13 El rey Roboam se reafirmó en Jerusalén y siguió reinando. Roboam tenía cuarenta y un años cuando comenzó a reinar, y reinó diecisiete años en Jerusalén, la ciudad que el Señor eligió entre todas las tribus de Israel para poner su Nombre en ella. Su madre se llamaba Naamá, la amonita.
</p><p>
14 El obró mal, porque no se empeñó en buscar al Señor de corazón.
</p><p>
15 Los hechos de Roboam, desde el principio hasta el fin, están escritos en las Crónicas del profeta Semaías y del vidente Idó, para ser registrados en las genealogías. Roboam y Jeroboam estuvieron continuamente en guerra.
</p><p>
16 Roboam se fue a descansar con sus padres, y fue sepultado en la Ciudad de David. Su hijo Abías reinó en lugar de él.
</p>
